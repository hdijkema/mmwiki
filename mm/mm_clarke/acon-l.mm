:rubric=1. Mind.
S{Mania.} S{Ferocity.} S{Laughter.} S{Distraction.} S{Instability of ideas.} S{Dread of work}


:rubric=2. Head.
S{Vertigo with nausea.} S{Lancinating pain in head extending across eyes.} S{Sensation as if a nail were driven into head when moving it.} S{Hammering in temples.} S{Pain like rheumatism in scalp 2[< ]by touch}


:rubric=3. Eyes.
S{Lancinating pain in eyes from without inward.} S{Painfulness of lids.} S{Itching in lids.} S{Pressure on lids and difficulty of raising them.} S{Smarting in canthi; dryness at inner canthus}


:rubric=4. Ears.
S{Purulent discharge from ears; redness behind them}


:rubric=5. Nose.
S{Itching on nose; sensitiveness; lancinations.} S{Compression at root of nose.} S{Skin of nose cracked.} S{Muco-purulent discharge from nose}


:rubric=6. Face.
S{Face pale; shining.} S{Pain of excoriation round mouth.} S{Pain in facial bones.} S{Tension in facial muscles.} S{Hard tuberculous pimples on face.} S{Pimples on lips.} S{Skin of face becomes light brown.} S{Numbness of jaw, like incipient paralysis, 2[> ]by compression and by drinking wine}


:rubric=7. Teeth.
S{Burning pain in upper teeth.} S{Pressing in teeth.} S{Tearing in lower teeth when opening mouth.} S{Gums bluish; ulcerated}


:rubric=8. Mouth.
S{Taste, clayey; astringent}


:rubric=10. Appetite.
S{Strong relish for dainties, fruits, cabbage.} S{Increased relish for tobacco in a smoker.} S{Aversion to food in general esp. fatty things and milk; they cause uneasiness.} S{Keen hunger very soon satisfied.} S{Constant thirst even when drinking.} S{Burning thirst.} S{Thirst at night}


:rubric=11. Stomach.
S{Risings: rancid; like rotten eggs, acid with weight on stomach.} S{Painful eructations.} S{Hiccough.} S{Inclination to vomit after eating; with shivering; with vertigo.} S{Vomiting: of mucus on getting out of bed; yellowish after drinking; with copious urination}


:rubric=12. Abdomen.
S{R. hypochondrium: bruised pain; tearing pain; starting.} S{When breathing both hypochondria feel bruised. Sensation of compression referred to diaphragm.} S{Anxiety; beatings in abdomen.} S{Lancinating pain after milk}


:rubric=13. Stool.
S{Constipation; anus feels as if strongly contracted.} S{Diarrhœa, with violent cutting after eating pork.} S{Whitish stools.} S{After stool, pain in anus as if there was a crack there; shivering.} S{Cramp at anus.} S{Itching at anus.} S{Tenesmus during night}


:rubric=14. Urinary Organs.
S{Desire to urinate with copious emissions.} S{Urging with ineffectual efforts.} S{Urine: hot; turbid, depositing a white sediment}


:rubric=16. Female Sexual Organs.
S{Itching at vulva.} S{Menstrual blood fetid.} S{Excoriation of bend of thigh after menses.} S{Leucorrhœa: viscid}


:rubric=17. Respiratory Organs.
S{Difficult respiration.} S{Slight cough with watery expectoration.} S{After a slight chill, cough leaving a taste of, blood in the mouth}


:rubric=18. Chest.
S{Swelling of the mammary glands}


:rubric=20. Neck and Back.
S{Swelling of the cervical glands.} S{The neck seems to grow larger.} S{Swelling of the neck on one side only.} S{Pressing pain at the nape of the neck.} S{Shivering in the back.} S{Jerking in the region of the kidneys}


:rubric=22. Upper Limbs.
S{Swelling of axillary glands.} S{Pressure; lancinating pains in shoulder joints.} S{Tearing pain in elbow joints.} S{Stiffness in elbows and wrists.} S{Sensation of fulness in hands.} S{Sweat on hands}


:rubric=23. Lower Limbs.
S{On stretching legs ham-muscles feel as if much shortened.} S{Jerking in legs.} S{Itching at ankles in evening.} S{Lancinating pain at instep when standing.} S{Eruption of hot, red, slightly painful spots on legs and toes}


