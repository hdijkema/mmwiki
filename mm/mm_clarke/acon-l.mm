GT{A. lycoctonum, Linn. A. telyphonum, Reich. Wolfsbane. N. O. Ranunculaceæ. Tincture of fresh plant when coming into bloom. (The only aconite which does not contain Aconitine.}


:rubric=Clinical.
S{Breasts, inflammation of. Constipation. Diarrhœa. Glands, enlargement of. Hodgkin's disease. Liver, disorders of. Pork, effects of. Strangury}


:rubric=Characteristics.
S{The symptoms of R[Acon-l], whilst resembling those of the other aconites in some respects, show a marked absence of the cutaneous-nerve affections so prominent in their provings. The most notable symptoms are swelling of the cervical, axillary, and mammary glands. I have used it with good effect in cases of scrofulous glands, and even where Hodgkin's disease has been suspected. Diarrhœa after eating pork. Itching is prominent in the proving: of eyes, nose, anus, vulva. The skin of the nose is cracked. There is a cough leaving a taste of blood in the mouth. The symptoms are 2[< ]in afternoon (like R[Lyc]); 2[< ]from mental exertion; 2[< ]during digestion, especially of pork, onions, and after wine}


:rubric=Relations.
S{2[Compare:] R[Cist], R[Lap-a], R[Con], R[Iod], R[Spong], R[Lyc], R[Carb-an]; R[Puls] (effects of pork)}


