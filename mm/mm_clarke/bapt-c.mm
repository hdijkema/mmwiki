S{Mr. J. Meredith ("Agricola") communicated to the 2[Hom. World] (xxxi. 267) his experience with this shrub. Symptoms produced on himself were: Congestion of fore part of head. Vision weak. Hectic flush after tea. Right molar felt too long,B[<] by pressure, intermittent; right jaw (later both jaws) stiff and painful at joint. Sciatica left side. Appetite increased, more energy for brain work. Sleep improved. But the most remarkable effect was the relief of a distressing symptom which had troubled him many months, and which the ordinary R[Bapt] in any preparation had failed to relieve. He describes it as "a really distressing left hypochondrium oppression, fulness and actual dyspnoea, impelling me to assume an erect position." This would continue half the night. It was uninfluenced by eating. The distress was located either in the spleen or splenic curve of the colon. The R[Bapt-c] seemed to relieve the part very rapidly, causing something to move to the left and downwards, after which he went to sleep. A case simulating typhoid was rapidly relieved by the remedy} S{showing its relation to R[Bapt].}

  
