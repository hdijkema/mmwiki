:begin[en]
GT{Benzol, Benzoline, Benzene. A product of distillation from coal tar. C6 H6. Tincture with alcohol.}


:rubric=Clinical.
S{Dysentery. Fever. Headache. Insomnia. Sweat. Typhoid fever. Vision, disorders of.}


:rubric=Characteristics.
S{Our knowledge of this medicine is derived from observations on a worker in a rubber factory, who for weeks had his hands and arms daily bathed in benzine, and drank water impregnated with it. It caused profound disturbance of mind and body; weeping at trifles; irritability; pain darting upwards in occiput. Could not turn eyes upward without severe aching and throbbing. A sort of clairvoyant state, in which a great white hand appeared to him in the darkness, coming outspread towards his face, causing him in terror to scream for the watcher. A sense of falling through the bed and floor. Insomnia and photopsic illusions. Pains travel from below upward (headache, pain in anus); also chills.}


:rubric=Relations.


:rubric2=Compare

S{R[Bry] (B[<] motion; moving eyes); R[Benz-n]; R[Ben-d] (amblyopia and disorders of vision). R[Sulph] (symptoms go from below upward).}


:head=SYMPTOMS.


:rubric=1. Mind.
S{Weeping at trifles and despairing of recovery.} S{Extremely irritable and fault-finding.}


:rubric=2. Head.
S{Severe darting pains in occiput, from below upward, recurring in paroxysms, B[<] by motion, and esp. by rising after sitting.}


:rubric=3. Eyes.
S{Could not turn eyes upward, or to one side without severe aching and throbbing.} S{A great white hand seemed to appear to him in the darkness, coming outspread towards his face, causing him in terror to scream for the watcher.} S{Photopsic illusions with wide-open eyes.}


:rubric=6. Face.
S{Occasional sudden puffing up of l. cheek and calf of l. leg, as though filled with air, going off in a few hours and returning again.}


:rubric=8. Mouth.
S{Teeth covered with sordes.} S{Soreness and sensation of looseness in upper incisors.} S{Tongue parched and brown.} S{Painful round white ulcers in the mouth, esp. on inside of cheeks.} S{Hot and very offensive breath.}


:rubric=10. Appetite.
S{Appetite lost.} S{Craving for lemons and cider.} S{Extreme thirst; for ice-water, satisfied with a sip, but wanting it again directly.}


:rubric=12. Abdomen.
S{Continual soreness to pressure in abdominal walls.} S{Heat and grinding wearing pains in lower part of bowels, B[<] just before stool.}


:rubric=13. Stool and Anus.
S{Several times an hour, a stool, smelling of benzine, of lead-coloured mucus, mixed with bright blood, accompanied with some tenesmus, and followed by throbbing in anus and rectum and lancinating pains from below upward, continuing about five minutes.}


:rubric=14. Urinary Organs.
S{Pressing pain in bladder, after passing urine, throbbing and smarting in neck of bladder and urethra for several minutes.} S{Urine: dark, offensive; sediment like red sand.}


:rubric=17. Respiratory Organs.
S{Every few days continual, dry, hacking cough.} S{Continual soreness and aching in clavicular region.}


:rubric=20. Back.
S{Continual aching and throbbing in lumbar region, B[<] by a full inspiration.} S{Extreme irritation of the kidneys.}


:rubric=22. Upper Limbs.
S{Continual soreness and aching in muscles of upper arm.}


:rubric=24. Generalities.
S{Wasted, pallid, exhausted; general prostration.} S{At one time he sank very low, approaching a typhoid condition.} S{Complained of sensation of falling through bed and floor.}


:rubric=26. Sleep.
S{For three nights, before the sweating began, complete insomnia, with unpleasant thoughts crowding the mind, and wide-open eyes, before which photopsic illusions floated continually.}


:rubric=27. Fever.
S{Chills seized remote parts and passed toward the head, from the thumbs to the elbows, and thence to shoulders; from small of back to shoulders and vertex.} S{Cold compresses came off steaming in a few minutes, smelling of benzine and stained a deep yellow, which could only be removed by long exposure to the sun.} S{For several nights copious, general, warm sweat toward morning, very exhausting, followed on several succeeding mornings by sweat only on breast, on side not lain upon, and in axillae.}

  
