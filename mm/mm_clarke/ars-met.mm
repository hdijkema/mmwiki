GT{Metallic Arsenic. As (A. W. 74.9). Trituration.}

:rubric=Clinical.
S{Constipation. Coryza. Diarrhoea. Eyes, affections of. Haemorrhoids. Headache. Itching. Sciatica. Syphilis.}

:rubric=Characteristics.
S{The symptoms of metallic arsenic are very like those of R[Ars], but not entirely. The following may be regarded as the most peculiar: Symptoms recur every 14 days; coryza every 21 days. Pains extend from one part to another; or leave one part and appear in another. Attacks come suddenly and go slowly, or 2[vice versa.] Enlarged or swollen feeling of brain, head, eyelids, hands, and fingers. Cutting, burning, stinging pains. Itching in many parts, skin peels off in small scales. Hot bathing B[<] pain in hip. B[>] Lying on l. side (heart). Washing B[>] itching of anus. Cold bathing B[>] itching of face. B[<] On waking in morning. Thinking of symptoms B[<] Many symptoms appear on r. side. R[Ars-met] lights up syphilis after being many years dormant.}

:rubric=Relations.
:rubric2=Compare
S{R[Iod], R[Merc], R[Nat-c] in syphilis; R[Nux-v] (drowsy after sound sleep); R[Rhus-t] (pain in back, hips, etc.); R[Sulph] (pulse); R[Ars] Useful in pterygium after failure of R[Nux-v] and R[Spig]. 
:rubric2=Antidoted by 
S{R[Bell] (sore throat); R[Nat-c] (syphilitic symptoms).}

:rubric=Causation.
S{Laughing (headache). Thinking (headache).}

:head=SYMPTOMS.

:rubric=1. Mind.
S{Memory weak, esp. of what is read; cannot take in what he reads.} S{Fears will be poisoned.}

:rubric=2. Head.
S{Heavy, dull feeling in forehead above frontal sinuses in morning.} S{Fulness; on waking at night.} S{As if brain were too large and it would burst.} S{Glow over l. brow.} S{Pain l. side extending to ear and eye, with nausea.} S{Headache when writing, thinking, laughing.} S{Hair falls off.}

:rubric=3. Eyes.
S{Soreness of eyes; irritated and red or sore.} S{Upper lid sore and feels swollen.} S{Pterygium (r).}

:rubric=5. Nose.
S{Coryza and hoarseness B[<] in morning.} S{Eyes red, excoriating tears, nose swollen, head feels enlarged, full as if tied up; sneezing and dyspnoea increase every second day; return every two weeks.}

:rubric=6. Face.
S{Swelling, itching, burning, stinging of forehead and face, B[<] at night, B[>] by cold washing.}

:rubric=8. Mouth.
S{Tongue coated white; imprinted with teeth.} S{Mouth sore; ulcerated.}

:rubric=9. Throat.
S{Inflamed and ulcerated, several small ulcers.} S{Violently inflamed throat, pain on swallowing, heat B[<] r. side, after coryza.}

:rubric=11, 12. Stomach and Abdomen.
 S{B[<] From brandy.} S{Takes away, or causes desire for alcohol.} S{Sore pain in liver goes through to shoulders and spine.} S{Pain in spleen down to groin, evening.} S{Pain in breast extends to region of spleen.} S{Bloated abdomen.} S{On waking, cutting, shooting, tormenting belly-ache, followed by burning, watery stool and cessation of pain.} S{Painful swelling in r. groin B[<] on extending leg.}

:rubric=13. Stool and Anus.
S{Diarrhoea, burning watery stools with, relief of pain, but leaving much debility falls asleep sitting.} S{Constipation.} S{Piles exceedingly painful, bleed a little itching in an us B[<] after rubbing; extending to perineum and scrotum; B[>] washing with very hot water.}

:rubric=14. Urinary Organs.
S{Urine hot in evening.} S{Red, sandy sediment.}

:rubric=15. Male Sexual Organs.
S{Pain in groins; morning, r., evening, l.} S{Bubo r. B[<] stretching leg; B[>] walking; B[<] sitting.} S{Itching in glans, prepuce swollen.} S{Chancres, with burning pain.} S{Offensive sweat on genitals.} S{Itching on scrotum.}

:rubric=16. Female Sexual Organs.
S{Catamenia too early and too scanty.} S{Heat in uterus and vagina; burning when touched.} S{Pain in uterus and vagina during period.}

:rubric=18. Chest.
S{Stitches in breasts; extending to hip; to spleen.} S{As if heavy load lying on chest all night; dreams of danger on water.}

:rubric=19. Heart.
S{Cutting pain in heart (4 p.m.); can only lie on l. side.} S{Pulse accelerated in morning (2[Sul.]).}

:rubric=21. Limbs.
S{Hands and fingers feel swollen; feel stiff as if they could not be closed.} S{Cracking of finger-joints.} S{Icy coldness or burning of hands and feet.} S{Lameness in r. hip-joint, and down thigh, B[<] moving from seat, 2[>c]ontinuing to walk; B[<] bathing; B[<] lying on r. side; throbbing B[<] morning, B[>] evening.} S{Sensation of dryness in r. knee-joint.}

:rubric=24. Generalities.
S{Prostration of strength, weariness.}

:rubric=25. Skin.
S{Peels off in small scales.} S{Rash on face; or abdomen.} S{Small boils on face.} S{Syphilitic symptoms reproduced.}

:rubric=26. Sleep.
S{Excessive drowsiness all day after an unusually sound sleep at night.} S{Sleepy, but can't sleep (2[Bell]).}

