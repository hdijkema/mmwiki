:rubric=Characteristics.
S{2[Chloroform] is the well-known anesthetic, the general effects of which in arresting sensation need no description. Unlike Ether and Nitrous oxide, which require to be administered without air, chloroform vapour must have abundance of air inhaled with it, or asphyxia results. Its most noteworthy effect is complete relaxation of all the voluntary muscles of the body. It may cause death by paralysing the respiration or the heart. Its use is contraindicated where there is brain softening; fatty heart; alcoholism; or albuminuria. Marcy cured with it a case of arachnitis ending in convulsions. It is especially indicated in delirium where excitement and violence predominate; desire to kill. Paralysis of voluntary and involuntary muscles and special senses. 2[Chloroform] will dissolve gall-stones, and cases have been treated by the injection of 2[Chloroform] into the gall-bladder.}


