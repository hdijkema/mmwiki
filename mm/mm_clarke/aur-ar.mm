S{The arseniate of gold was first used by Chrestien in cases of phthisis. Later it has been used by others in syphilis, especially chronic syphilitic headaches, lupus, anaemia, chlorosis. The first effect of the remedy is to cause rapid increase of appetite. The peristaltic contractions of stomach and intestines are excited and absorption accelerated.}
