:rubric=Clinical.
S{Gastric catarrh. Scrofula. Syphilis. Ulcerations.}


:rubric=Characteristics.
S{This is an unproved remedy, but clinical experience has revealed a specific relation to the manifestations of syphilis: Hunterian chancre; syphilitic nodes; falling of the hair; syphilitic and scrofulous ulcerations of scalp; syphilitic and non-syphilitic ulcerations of fauces} S{all these have been reported cured by it. Hale also commends it in: Scrofulous dyscrasia; obstinate skin diseases; cachexia of intermittents with enlargement of liver and spleen. In massive doses it has cured, according to eclectic authorities: "A derangement of the stomach, attended with profuse morbid secretion of mucus, there being always a coated tongue, with fetor of the breath, and loss of appetite and digestion." It is therefore indicated, as Hale remarks, in gastric catarrh, being an analogue, in this respect, of 2[R[Hydr]] It has not been used in the potencies; ten drops of the tincture four times a day has been a usual dose.}


