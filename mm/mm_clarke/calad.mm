:rubric=Characteristics.
S{Like the other Arums, 2[Caladium] has an intensely irritating effect on the mucous membranes and skin, and produces many burning sensations. R[Calad]. is one of the best remedies for that troublesome affection, pruritus vulvae. Sometimes this is set up by intestinal worms finding their way into the vagina, and they may then cause masturbation, and even nymphomania. There is also great disturbance in the male sexual sphere. Masturbation and its results. Nocturnal emissions occurring either without dreams or with non-sexual dreams. Spermatorrhoea. The glans penis is flabby from masturbation, prepuce if drawn back remains so from loss of contractility. Catarrhal asthma, mucus not readily raised, but giving relief when it comes up.} S{H. N. Martin gives this picture of R[Calad].: "Is similar to R[Lyc].; patient wants to lie down always; B[>] from sweat (opp. R[Merc].); B[>] after short sleep (opp. R[Lach].). Perspiration attracts flies. Genitals relaxed and perspire. Patient afraid to go to sleep, and doesn't know why. Itching of vulva with burning."} S{It is 2[suited] to persons of lax, phlegmatic temperament. There is B[<] from warmth; aversion to cold water; but bathing with cold water B[>] itching. Heat with drowsiness. Sweet odour of sweat. All symptoms B[>] after sweat; after sleeping in the daytime. Motion B[<] most symptoms. Great desire to lie down and aversion to motion; but if he makes the effort he is strong enough. Attacks like fainting after writing and thinking, when lying down or when rising.}


