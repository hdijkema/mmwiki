:rubric=9. Throat.
S{Dryness and burning in fauces and pharynx (without thirst); with aversion to cold water.}


:rubric=11. Stomach.
S{Nausea, esp. in the morning on rising.} S{Acid eructations.} S{Frequent eructations of very little wind, as if stomach were full of dry food (with asthma).} S{Burning in stomach, not B[>] by drinking.}


:rubric=12. Abdomen.
