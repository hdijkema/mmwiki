:rubric=14. Urinary Organs.
S{Bladder feels full without desire to urinate.} S{Urine fetid; with sediment (with impotence and gleet).}


:rubric=15. Male Sexual Organs.
S{Organs swollen (puffy), relaxed, and sweating.} S{Swelling of margin of prepuce with smarting during micturition.} S{After waking prepuce remains retracted; painful and swollen.} S{After coition the prepuce remains behind the glans; it is painful and swollen.} S{Glans red, dry; dotted with still redder points.} S{Glans flabby from masturbation.} S{Painful erections without sexual desire, alternating with sexual desire with relaxed penis.} S{Impotence with mental depression.} S{Frequent nocturnal emissions.} S{Nocturnal emissions either without dreams or with non-sexual dreams.} S{Imperfect erections and premature ejaculation of the semen.} S{Impotence; the penis remains relaxed, even when excited.} S{Feeling of coldness, and cold perspiration of the sexual organs.}


:rubric=16. Female Sexual Organs.
S{Itching of external genitals with voluptuousness.} S{Pruritus of vulva and vagina.} S{Pruritus vaginae; with onanism (nymphomania).} S{Worms escape into vagina and cause masturbation.} S{Cramp-like pains in uterus after midnight.}


:rubric=17. Respiratory Organs.
