S{The symptoms of R[Calc-acet]. (the first of the lime preparations proved by Hahnemann) are in the main identical with those of R[Calc]. (both being prepared from oyster shells), and are included in the pathogenesis of that medicine. Among its most marked symptoms may be named: Vertigo when walking in the open air. Stupefying, pressive pains in the forehead with compression of the whole head and obscuration of the senses 2[whilst reading:] he was obliged to stop while reading and did not know where he was. Clotar Muller cured many cases of megrim with R[Calc-acet]. The indications he relied on were: "Sour taste in mouth, sour eructations, sensation of coldness in the head, and of emptiness. Pain one-sided, affecting right eye, which was red, causing lachrymation." Sour, offensive eructations. Profuse, painless, not exhausting diarrhoea. Severe itching of anus. Allen (2[Handbook]) mentions that it has cured some marked cases of membranous dysmenia and also of violent spasmodic cough ending in the expulsion of casts of bronchial tubes. For 2[comparisons] see R[Calc]. It has been used in the excruciating pains of open cancer (compare R[Calc. oxal.]).}

  
