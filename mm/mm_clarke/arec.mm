S{The Betel-nut is in very common use in the East as a masticatory for sweetening the breath and hardening the gums. It is also used as a source of catechu, but the true catechu is an extract of I[Uncaria gambir] of the Leguminosae. The chief medical use of the I[Areca nut] has been in helminthiasis in dogs; but it is not altogether without danger. Some animals to which it was administered experienced in five minutes great difficulty of breathing, slight cough, and fell down on one side, and some died. The dose given for this purpose is one grain to each pound of the dog's weight. The I[Hydrobromate of Arecoline] (R[Arecin-br]) has been used to cause contraction of the pupil. It acts more promptly and more energetically than R[Esin], but its duration of action is shorter. According to Ricapet it surpasses R[Piloc] as a salivatory. It arrests the heart's action in diastole in poisonous doses, and in non-toxic doses increases the amplitude of the pulsations without increasing their frequency. It promotes the contractibility of the intestines, and as a teniafuge it requires no additional laxative. It should be given in pills coated with gelatin or keratin. According to F. Hobday (quoted in I[Amer. Hom.] xxiv. 123), R[Tenaline] is a much safer and more certain anthelmintic than R[Arecin-br] He advises a dose of one minim of the solution to the pound-weight of the animal, either given pure or with a little water. It is not to be given hypodermically}


