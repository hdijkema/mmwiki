:rubric=Clinical.
S{Cholera infantum. Chordee. Direction, sense of, lost. Gastric catarrh. Hysteria. Influenza. Irritation in acute eczema. Nervous excitability. Sleep, disorders of. Spermatorrhoea.}


:rubric=Characteristics.
S{Dr. Cooper has found R[camph-br]. of wide utility in all cases where nervous excitability is present. He has given it chiefly in single doses of 3x trituration. In a case reported by Dr. E. M. Beard of a young man who suffered from gastric catarrh in convulsive paroxysms which were induced by the sight of physical deformity, the touch of a cold, clammy hand, and the 2[mention] of odours, the actual presence of which did not affect him, complete relief was afforded by 3 gr. doses of R[camph-br]. every 3 or 4 hours. But the drug set up these singular symptoms: Directions appeared to be reversed; north seemed south, and east seemed west; innumerable black flies seemed to be flitting over the field of vision; he was sleepless; and when he did sleep it was restless and vexed with ghastly dreams. (R[camph]. has visions of spirits in sleep.) The reversal of directions lasted many days, and the strain on the faculty of attention was so torturing that he became prostrate in mind and body (I[C. D. P.]). Hale mentions among the conditions relieved by it: Hysteria, weeping and laughter alternately. Headache in women and young girls due to mental excitement and excessive study. Delirium tremens. Chordee. Neuralgia of testes and prostate. Impotence. Spermatorrhoea.}

  
