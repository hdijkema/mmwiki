:head=SYMPTOMS.


:rubric=1. Mind.
S{Is in much better spirits.}


:rubric=2. Head.
S{Sharp, piercing pain from l. occiput to l. temple.}


:rubric=11. Stomach.
S{Increased appetite followed by loathing of food.} S{Voracious appetite.} S{Feeling of warmth in stomach.}


:rubric=14. Urinary Organs.
S{Pricking pains from kidneys to bladder, with increased desire to urinate, stream smaller than usual.} S{Constriction along course of urethra, esp. towards neck of bladder.}


:rubric=15. Male Sexual Organs.
S{Itching of glans penis, and orifice of urethra.} S{Aching pains in perineum and spermatic vessels with feeling of weight in testes.}


:rubric=20. Back.
S{Dull, aching pain in lumbar region.}


:rubric=22. Upper Limbs.
S{Pain in shoulders, esp. on joint of r. shoulder, extending down to elbow.}


:rubric=23. Lower Limbs.
S{Pinching, drawing pain in knees and along lymphatics of legs.} S{Sharp, shooting pain in patellae and in bend of knees.} S{Tension in calves, with occasional spasmodic twitchings and general malaise.}


:rubric=27. Fever.
S{Increased heat of body without thirst.}

  
