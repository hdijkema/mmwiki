:rubric=Characteristics.
S{R[Cop] is an old-world remedy for gonorrhoea, and it has also been used for other blenorrhagic conditions. It has been proved; and observations have been made on patients who have overdosed themselves with the drug. The chief incidence of the drug's action is on the genito-urinary organs, and rectum. It is suited to the irritable bladder of old women. Irritation accompanying thickening of bladder. Burning at neck of bladder and in urethra; discharge milky, corrosive; meatus tumid and inflamed. It affects the skin and mucous membranes generally. In chronic bronchitis it is useful when there is profuse, greenish-grey, disgusting-smelling sputa. A. P. Bowie (2[Med. Adv.], xx. 14) has verified this indication: "In old persons (men, I should say) who from cold or other causes cannot urinate; or where there is frequent desire and only a few drops pass with much straining} S{frequently with a mucous discharge from the bowels, with much rumbling and rolling in the bowels." He gave 5-drop doses of the first dilution. He commends also an ointment of gtt. x of the balsam to one ounce of vaseline, "to relieve the burning and itching at the anus caused by piles."}


