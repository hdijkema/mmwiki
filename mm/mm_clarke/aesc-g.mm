S{2[Æsculus glabra,] like the Horse-chestnut, has a marked action on the rectum. It produces hard, knotty stools; very painful. Dark purple hæmorrhoidal tumours, with lame back and lower limbs. Hale gives the indication: "Very painful external tumours, dark purple, with constipation and vertigo; weakness of sacrum and lower limbs." At the same time there is fulness and heaviness of the head without pain; the sight may be dim or lost; eyes fixed and expressionless. The speech is thick and the tongue as if lame. In cattle it produces wry-neck and paresis of hind limbs. There is a cough produced by sudden irritation of throat, sensation as of a feather tickling the throat, causing hawking and raising of mucus finally streaked with blood}


