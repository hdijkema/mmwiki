:rubric=Characteristics.
R[Arum-t] is the most important of the Arums. Its irritating properties are well marked; excitable and irritable in mind and body. It is especially irritating to the nose and throat. This with its scarlet rash suggested its appropriateness in cases of scarlatina of malignant type, in which it has met with great success. An indication of its curative action in scarlatina is the urine becoming more abundant and watery. Raw, bloody surfaces appear on lips, on mouth, on nose, etc. With the rawness there is itching. "Picks lips till they bleed, corners of mouth sore, cracked, and bleeding "is a characteristic indication; also, "picking ends of fingers" and "boring nose" (especially 2[side] of nose). Tongue cracked and bleeding. Urine scanty or suppressed. Patient passes into unconsciousness and slides down in bed. 

Dr. Lippe, who is one of the greatest authorities on this remedy, said it should not be given low or repeated often. "Bores head in pillow" is an indication when the brain is involved. Kent points out that almost all the discharges are acrid and cause irritation of orifices. He defines the hoarseness as that of public speakers who, after long exertion, get cold (as from a draught), and suddenly are unable to finish. (With R[Rhus-t] the hoarseness is at the beginning and passes off by using the voice.) R[Arum-t] prominently affects the l. side. It has a headache B[<] from hot coffee and has cured headache B[<] from too hot clothing.}

