R[Cupre-l] has been proved in a fragmentary way by Burnett, who had to relinquish the proving on account of the "S{terrible pains it caused in the stomach.}" 
He concludes from his experience that the action is very like that of R[thuj], and he has successfully used it as a variant of that remedy and of R[Sabin] in the cure of tumours: S{cocks-comb growth in the mouth}; S{lipoma of thigh}; S{keloid.}

  
