:rubric=Characteristics.
S{Apomorphine contains the emetic properties of I[Opium]. It causes very rapid vomiting, most rapid when injected subcutaneously. Vomiting without previous nausea. Drowsiness. Faintness. The peculiarity of the vomiting is its suddenness, completeness, and the absence of pain or continued nausea. It has been used with success in cases of sympathetic vomiting, and from pressure of a tumour on the brain. Halbert has given R[Apom] 3x with success in a desperate case of combined alcoholism and opism. The symptoms were constant nausea; constipation; insomnia; delirious headache; extreme emaciation; pronounced hysteria}


