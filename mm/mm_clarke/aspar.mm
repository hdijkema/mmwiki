:head=SYMPTOMS.


:rubric=2. Head.
S{Vertigo in forehead; head confused, as during an attack of vertigo; dizziness in the forehead, followed by aching in the temples, chiefly the l. one; aching in the forehead over the eyes, or a sensation of confusion in the fore part of the head; aching in the temples, aggravated by pressure; weight in the forehead.}


:rubric=3. Eyes.
S{Darting and tingling in the eyes; sight more piercing.}


:rubric=6. Face.
S{Paleness of the face; increase of heat in the face; cheeks burning.}


:rubric=11. Stomach.
S{Taste insipid, sweetish (as of copper); softness of the saliva, as if it were mixed with blood.} S{Thirst increased.} S{Eructations; flatulency in the stomach; nausea in the morning, on waking, followed by vomiting of food, mixed with bile and slimy matter; afterwards diarrhoea of bile and of faecal matter.}


:rubric=12. Abdomen.
S{Sensation of fulness in the abdomen, pinching in the umbilical region, at night, with painful sensibility to the touch.} S{Inflation of the abdomen; frequent emission of wind.}


:rubric=13. Stool and Anus.
S{Bilious diarrhoea, with pain as of excoriation in the anus, colic, and dragging in the groins.}


:rubric=14. Urinary Organs.
S{Urine straw colour, scanty, soon becomes turbid, with little white specks; afterwards (about four hours), a white flaky sediment; the urine deposits a fatty sediment on the sides of the vessel; urine brown, without sediment; clear, and of a fetid smell; a peculiar smell from the urine.} S{Urgent inclination to urinate; frequent and scanty emission of urine, preceded by a sensation as if a foreign body were introducing itself into the urethra, and followed by a burning sensation; diminished secretion of urine augmentation, in the last stage of experimenting; burning in the urethra fine stitches in the orifice of the urethra; sometimes incisive pains, sometimes with dragging in the groins, colic, diarrhoea, and pain in the anus, sometimes with a sensation as though there were more urine to pass.} S{Urine loaded with phosphates and the urate of ammonia (in renal dropsy).} S{Gravel passes in small quantities with the urine.}


:rubric=15. Male Sexual Organs.
S{Excitation of sexual appetite; dartings in the glans penis.}


:rubric=16. Female Sexual Organs.
S{Catamenia continue a day beyond the usual time.}


:rubric=17. Respiratory Organs.
S{Frequent sneezing; hawking, with irresistible inclination to cough, but without power to detach mucus; paroxysms of coughing, which compel the patient to rise from his seat, abating after breakfast; violent cough, attended by oppression of the chest, and copious expectoration of mucus.} S{Violent cough, inducing retchings.} S{Abundant secretion of mucus in the throat; a constant rattling of mucus in the chest, with roughness in the throat.}


:rubric=18. Chest.
S{Oppression of the chest, esp. when writing; dyspnoea brought on by motion, on going upstairs, and sometimes in the night-time, obliging the patient to sit up in bed.} S{Pressure on the chest, sometimes after breakfast, also with tension in breathing; feeling of emptiness within, with weight on the chest; shootings at different parts of the chest, esp. below the l. shoulder-blade; sometimes in the l. side, in breathing; severe shooting pain across the r. side when sitting down.}


:rubric=19. Heart.
S{Palpitations of the heart, which are visible and audible even during very gentle movements, and which frequently recur, accompanied by agitation and anxiety during motion, and on ascending the stairs; violent when seated; irregular beating of the heart, rapid, redoubled, almost imperceptible.} S{Shooting pains in the region of the heart after a meal.} S{Pulse slightly accelerated; feeble.}


:rubric=20. Neck and Back.
S{Pain near the shoulders on touching the part; rheumatic pain between the shoulders.} S{Sensation, as if something passed through the kidneys, and penetrated to the abdominal vertebrae, on the patient sitting down.} S{Pains in the region of the false vertebrae.}


:rubric=23. Lower Limbs.
S{Pain, as of dislocation, in the right femoral joint, which causes lameness.} S{Shootings in the knees, at night, afterwards under the l. patella, on being seated.} S{Bruise-like pain in the l. thigh, with difficulty in walking, and esp. in mounting the stairs; internal pain, as of excoriation, on touching the part; also in the femoral articulation, and the knee, on bending the limb; the r. leg suffers more than the l., and is also much weaker.} S{Severe drawing pains in the calf of the r. leg, in the morning, on waking, and on extending the limb; cramps in both calves.} S{Drawing in the great toe, in consequence of a wound.}


:rubric=24. Generalities.
S{Symptoms aggravated by motion.} S{Drowsiness, with yawning; yawning in the morning.} S{Increase of natural heat; pulse quick, small, easily stopped; rapid while seated.} S{Anxiety, with palpitation of the heart, and ill-humour; calmness of mind.} S{Rheumatic pains in the back and limbs.} S{Concretions of lithic acid in the joints.} S{Great languor and disinclination to physical or mental exertion.}

  
