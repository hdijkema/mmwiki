GT{A. officinalis. N. O. Liliaceae. Tincture of young sprouts.}


:rubric=Clinical.
S{Coryza. Diabetes. Dropsies. Heart disease. Urinary disorders.}


:rubric=Characteristics.
S{Like other liliaceous plants R[Aspar] acts on the heart, kidneys, and dropsical effusions. Severe coryza and nasal catarrh, with profuse secretion of thin, whitish fluid from left nostril, afterwards right. Frequent violent sneezing. Copious discharge of tenacious mucus from the throat, not easily loosed, but brought up by hawking or paroxysms of cough. Frequent urination, with fine stinging in the meatus; followed by stitches out of the urethra. After urinating burning in urethra with sensation as if some were still passing. Increased urine beer-brown, without sediment. Urine strong-smelling; odour peculiar; deposits greasy sediment on sides of vessel; gravel. Prurigo pudendi. Fulness in chest; hydrothorax. Visible violent palpitation; while sitting; with anxious restlessness, increased by motion or ascending; with oppression of chest. Pulse weak, slow, irregular. Pain at left acromion process, under clavicle and down left arm. Constant desire to be carried about in the arms.}
:rubric=Relations.
:rubric2=Antidoted by
S{R[Acon] (prostration, feeble pulse, pain in shoulder); R[Apis].}
:rubric2=Antidote to
S{R[Coff]}

:rubric2=Compare
