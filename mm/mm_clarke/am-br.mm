:rubric=Clinical.
S{Ciliary neuralgia. Corpulency. Coryza. Cough. Epilepsy. Kidney, pain in. Larynx, catarrh of. Nails, biting. Ophthalmia, scrofulous. Ovaries, affections of. Pharyngitis. Pterygium. Whooping-cough}


:rubric=Characteristics.
S{This salt has been well proved by A. M. Cushing. The eyes and ovaries are markedly affected. In the morning the eyes are red and sore, with mucus in corners (2[<] left eye). There is pain in left ovary, dull, with hard swelling. Chronic laryngeal catarrh of speakers. A sudden desire to cough} S{so sudden it causes strangling. Sudden short cough when rising in morning, from accumulation of mucus in throat. Cough at 3 a.m. The mucus of R[Am-br] is apt to be stringy. It is indicated in epilepsy when the aura begins with a fainting or suffocating sensation at epigastrium, rising up both sides of sternum to throat. There is an irritable feeling under linger nails, 2[>] only by biting them The known conditions are: 2[>] warmth, warm drinks; 2[<] in open air, cold air. Many symptoms occur on waking, 3 a.m. Must walk about for fear of suffocation}


:rubric=Relations.
S{2[Compare:] R[Kali-br], R[Arg-n], R[Caust], R[Bufo], R[All-c] (coryza 2[<] in warm room); R[Lach] (2[<] after sleep). Cough from accumulation of mucus in throat, R[Aesc], R[Atro], R[Grat], R[Kreos], R[Seneg].}


:head=SYMPTOMS.


:rubric=1. Mind.
S{Feared he might die (with stomach symptoms).} S{Timid, discouraged; lack of self-confidence.} S{While writing mis-spelled words or numbers}


:rubric=2. Head.
S{Sensation of band round head pressing hardest just above ears.} S{Pain in side of head (l.) near eye; as if a nail driven in.} S{R.} S{sided headache 2[<] by coughing}


:rubric=3. Eyes.
S{In evening eyes (esp. r.) feet large, with constant blur before them.} S{Eyes as if sand in them.} S{R. eye feels as if bathed in hot water; stringy mucus in it.} S{Pterygium.} S{Eyes red and sore in morning, with white mucus in corners.} S{Lids swollen; droop, difficult and painful to raise; adhere.} S{Pains round eyes into head.} S{In evening, difficult vision and hemiopia}


:rubric=5. Nose.
S{Sneezing on moving about in cold room, or on raising arms; thin discharge from r. nostril (next day thicker).} S{Sneezing on going into warm room; stoppage of nose in warm room}


:rubric=8. Mouth.
S{Mouth and tongue smart severely as if just burned; morning.} S{Must hold mouth open when walking on account of heat in throat and lungs.} S{Stringy, tasteless mucus in mouth.} S{Tastes food eaten several hours before}


:rubric=9. Throat.
S{During day filled with white sticky mucus, streaked with blood.} S{Throat sore, mottled look.} S{Preparing to swallow is painful, the act is not.} S{Sensation of hot air passing up throat, r. side, though stomach feels cold.} S{Fauces and tongue feel scalded.} S{Stinging and irritation in fauces with inclination to cough 2[>] by sneezing}


:rubric=11. Stomach.
S{Belching 2[>] faint stomach.} S{Dyspnœa, with distress in upper epigastrium}


:rubric=12. Abdomen.
S{Sharp twisting pain just above crest of r. ilium}


:rubric=13. Stool and Anus.
S{Sudden urgent desire with loose stool.} S{Transient return of old hæmorrhoid}


:rubric=14. Urinary Organs.
S{Feeling over r. kidney as if something were pressed hard against it; 2[>] by pressure, but leaving a pulling sensation}


:rubric=16. Female Sexual Organs.
S{Dull, constant pain and hard swelling in l. ovary}


:rubric=17. Respiratory Organs.
S{An almost continuous cough for hours, 2[>] lying down at night; sensation of tickling irritation with heat and burning.} S{Sudden desire to cough, coming on so suddenly it strangles one.} S{Sudden short cough when rising from bed in morning, from sensation of mucus in throat.} S{Tickling in trachea and bronchial tubes, attended by cough and slight scraping in throat while coughing.} S{Waked 3 a.m. with cough}


:rubric=18. Chest.
S{Waked 3 a.m. with pain from sternum to spine, 2[>] by changing position}


:rubric=19. Heart.
