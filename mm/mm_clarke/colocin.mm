GT{An active principle (glucoside) of Citrullus colocynthis. C56 H84 O23. Dilutions in alcohol.}


:rubric=Clinical.
S{Colic. Cough. Debility. Diarrhoea. Feet, burning in. Genitals (male), congestion of; pain in. Headache. Neuralgia. Spinal pains. Travelling, effects of.}


:rubric=Characteristics.
S{Having had a separate proving, R[Colocin] must be considered apart from 2[R[Coloc]], from which it is derived, although the symptoms of the two bear a close resemblance. Neuralgic symptoms are prominent. Stitches and sensations of heat are found in many parts. Pulsation in the pit of the stomach, with prickling stitches on back in neighbourhood of middle thoracic vertebrae. Diarrhoea and colic B[>] bending forward. Congestion to male genital organs and pain in them. Burning pains in soles of the feet. B[<] Evening; by motion; riding in cars. B[>] By rest; bending over; black coffee. B[<] By motion; B[>] by rest:} S{herein appears the relationship of the glucoside to R[bry], the botanic relative of 2[R[Coloc]]}


:head=SYMPTOMS.


:rubric=1. Mind.
S{Depression.}


:rubric=2. Head.
S{Painfulness of whole head, esp. temporal and frontal regions, scalp, and eyeballs, B[<] moving eyes, straining sight, or bending forwards.} S{Headache B[<] by riding in railway cars, obliging to move slowly, and tread softly, otherwise the brain seems to shake about as if loose against the skull (in the median vertical region); accompanied by very sore pain and causing very bad humour.} S{Stitches in brain; through occiput on heavy treading; through cerebellum, like lightning, on coughing.} S{Sensitiveness of cerebellum on turning head.}


:rubric=3. Eyes.
S{Vision sharper.} S{Painfulness and aching in eyeballs B[<] straining sight, moving eyes rapidly, or turning head.}


:rubric=6. Face.
S{Pressure in left cheek-bone.}


:rubric=8. Mouth.
S{Pain in hollow molar and drawing in incisors, with sensation of swelling of l. cheek.} S{Persistent, disgusting, bitter taste.} S{Prickling and metallic taste on tip of tongue.}


:rubric=11. Stomach.
S{Continual burning pressure in stomach up into oesophagus.} S{Sticking in epigastrium; which was tense and inflated.} S{Pulsation in epigastrium accompanied by pricking stitches on back near middle thoracic vertebrae.}


:rubric=12. Abdomen.
S{Warm feeling followed by copious discharge of flatus.} S{Colic with inflation in umbilical region; colic below umbilicus.} S{Slight colic drawing from hypogastrium to hepatic region, with shuddering in scrotum, erections, and pain in buttock.} S{Tension in r. groin and drawing along spermatic cord into r. testis, which was somewhat sensitive towards evening; B[<] next morning, and B[<] walking, bending over, and going upstairs.} S{Stitches from small of back to groin when walking.}


:rubric=13. Stool and Anus.
S{Tickling in rectum.} S{Feeling of moisture at anus.} S{Irresistible inclination to stool; scanty brownish-red evacuation with painful tenesmus lasting ten minutes.} S{Brown pappy evacuations, rapidly and easily discharged in quick succession, without tenesmus.}


:rubric=14. Urinary Organs.
S{Tenesmus in urethra and rectum after urinating.}


:rubric=15. Male Sexual Organs.
S{Congestion towards genital organs, esp. scrotum, with feeling of heat and burning in small spot on same, without erections.} S{Cramp-like pain in penis with sensation as if it were bent double.} S{Dark redness and painfulness of frenum.} S{Crawling on penis with sexual desire.} S{R. testis swollen and painful to touch.} S{A pollution at night (very unusual with him).}


:rubric=17. Respiratory Organs.
S{Short hacking cough excited by tickling in throat; whitish yellow mucus.} S{Frequent cough in early morning.}


:rubric=20. Neck and Back.
S{Sharp in-pressing pain in some spots on nape, as if from a sprain B[<] by movement; sensitive to pressure.} S{Painfulness and feeling of heat along whole spine.} S{Stitches in region of lowest dorsal vertebrae.}


:rubric=22. Upper Limbs.
S{Tearing in l. shoulder.} S{Frequently repeated stitches from l. axilla down to l. elbow.} S{Paralytic weakness of r. forearm while writing.} S{Cramp-like pain in muscles of l. wrist.}


:rubric=23. Lower Limbs.
S{Stiffness and heaviness in tibiae.} S{Burning, sticking, and sensation of warmth in dorsum of r. foot; pressing digging pain in external side of second toe of l. foot.} S{Burning pain in both soles with feeling as if they were swollen.}


:rubric=24. Generalities.
S{Great desire to lie down; which relieves.} S{Unusual debility towards evening.}


:rubric=26. Sleep.
S{Restless sleep with vivid dreams.}

  
:end
