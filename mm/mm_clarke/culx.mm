R[Culx] was proved by Dr. Kent, but the only verification I have found is of this symptom: S{vertigo every time he blows his nose.} 

:div-begin=quote
W. P. Wesselhoeft cured a man, forty, stout, thickset, light brunette, who had swelling on upper jaw over first right tricuspid. Slight redness of cheek. Abscess threatened, with paroxysmal throbbing, dull pain. B[>] By warm applications. Came on after exposure to wet weather. 
R[merc]. and R[rhus-t] did no good. The patient then said that whenever he blew his nose he had vertigo, with a sense of fulness in the ears. R[Culx] promptly relieved the pain, and in forty-eight hours the swelling had gone. 
:div-end

