:rubric=Characteristics.
The well-known features of the disease of which this nosode is a product must be the guide to its use. 

It should be more efficacious in the potencies against diphtheria, both as prophylactic and cure, than the serum injections. In all probability it will be found useful in the paralysis of diphtheria and other like kinds of spinal affection. It needs clinical development and a proving in the potencies.

  
