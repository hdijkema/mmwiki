:rubric=Characteristics.
S{I[Anthracinum] is indicated in all conditions of boils and boil-like eruptions (as acne in some forms, and in carbuncles). It was introduced into homœopathic practice by Lux the veterinarian long before the experiments of Pasteur. The keynote for its employment is "succession of boils" or carbuncles, but it is also of the greatest use in other cases. "Terrible burning" with carbuncle. The potentised virus is the best remedy for the disease from which it is obtained} S{"Splenic fever" in animals, and "Malignant pustule" in human beings. Erysipelas of a foul kind, and gangrenous erysipelas; cellulitis. "Hard, stony" swelling in region of right lower jaw and submaxillary gland. Anthrax-quinsy. Whitlow and sloughing. Glands painfully swollen. Induration of cellular tissue. Black or blue blisters. Horribly offensive gangrenous ulcers. A number of symptoms have been recorded from human patients affected with the anthrax disease}


