:rubric=Clinical.
S{Abscesses. Aphonia. Boils. Cancer. Erysipelas. Glandular inflammation. Hydrophobia. Sycosis. Syphilis. Tic convulsif. Tic douloureux. Tumours. Ulcers}


:rubric=Characteristics.
S{2[Anatherum] produces a state of drunkenness. Severe neuralgic headache; as if balls moved about in head; as of steel arrows from forehead to nape. Pains in facial bones as if crushed or ulcerated. Convulsive tic. Ulcers at labial commissures (syphilis). Swelling of tonsils, constriction of throat, inability to swallow. Plugged sensation. Burning and stitches. Thirsty but cannot drink. Burning heat alternating with icy coldness in gullet: as of something alive in gullet. Involuntary urination when walking and during sleep. Venereal appetite increased by every attempt to satisfy it until driven to onanism and madness. Tumour of breast, ulcerated, indurated. Abscesses boils glandular swellings (especially of sub-maxillary and cervical glands). Herpes. Herpes and ulcers or scabs on scalp. Tumours. Wart-like growths on eyebrows. Falling off of eyebrows and beard. Chancre-like ulcers on penis. Pustules like small-pox on vulva. Abscesses, ulcers, and fissures on arms and hands. Eruptions like itch or lichen. "Erysipelas with much swelling of arms and legs, dark red or bluish eruptions, with tendency to suppurate" (Farrington). Formication as of ants. Ulcers yellowish, violet, swollen, everted, syphilitic. Discharges from ulcers are foul; foul breath and eructations. Coffee at first B[<], later B[>.] Wine and coffee B[<] toothache}


:rubric=Relations.
S{2[Compare:] R[Aven], R[Arund], R[Antho] (botan.); R[Bell], R[Cann-i], R[Stram], R[Staph], R[Merc], R[Lach], R[Thuj]. 2[Antidoted by:] Aromatic liquors. 2[Inimical:] Wines and spirits}


:head=SYMPTOMS.


:rubric=1. Mind.
S{Gay humour, with disposition to laugh and sing.} S{Blunted intellect and loss of memory}


:rubric=2. Head.
S{Vertigo with congestion, tendency to fall backward.} S{Drunkenness and staggering.} S{Burning, lancinating, pulsating headache, B[<] r. side, forehead and temples, with nausea, vomiting and great heaviness of eyes.} S{Neuralgic pain in temples, with sensation as if pointed irons were thrust in, producing fits of craziness; B[<] afternoon and night, from noise, light, and motion.} S{As if he had water in the head, B[<] walking.} S{Herpes and ulcers on scalp, with compact, thick, humid scabs and prurigo.} S{Exostoses}


:rubric=3. Eyes.
S{Muscæ volitantes and fiery circles before eyes; everything seems excessively bright and shining.} S{Wart-like growth on eyebrows}


:rubric=4. Ears.
S{Copious ear-wax.} S{Deafness, B[<] evening and damp weather}


:rubric=5. Nose.
S{Stitches in nose, with a crushing sensation at root.} S{Air inhaled seems icy cold, and causes sneezing.} S{Ulcers, greenish, badly smelling discharge.} S{Inflammation of bones.} S{Boils and small tumours on tip.} S{Nose cold pale, pointed}


:rubric=6. Face.
S{Tic douloureux, and tic convulsif.} S{Yellow and red spots; stinging pimples.} S{Ulcers, scabs, or scaly herpes; falling off of eyebrows and beard.} S{Pain in facial bones as if crushed or dislocated.} S{Jaws spasmodically clenched.} S{Submaxillary and cervical glands swell and tend to suppurate.} S{Ulcers at labial commissures; syphilitic}


:rubric=7. Teeth.
S{Digging in teeth as if forcibly pulled out.} S{Constant inclination to clench teeth.} S{Decayed, crumbling teeth; bad smell from mouth}


:rubric=8. Mouth.
S{Taste bitter, bloody, sweet or flat.} S{Speech difficult, stammering.} S{Severe pains at root of tongue as if it were cut off.} S{Tongue fissured, lacerated, as if cut on its edges, with copious salivation and debility, as from the effects of mercury.} S{Viscid saliva}


:rubric=9. Throat.
S{Inflamed throat with sensation of fulness and obstruction as if plugged.} S{Tonsillitis; burning and stitches; sometimes coldness.} S{Thirst, but throat contracts as soon as he hears water mentioned, or sees shining objects}


:rubric=11. Stomach.
S{Morbid hunger, wakes in the night to eat; as if he had tapeworm.} S{Eructations; painful; tasting of food; foul.} S{Nausea; vomiting of food, bile or blood; horrible pains in stomach.} S{Very painful pressure, like a bar, on epigastrium with short, anxious respiration.} S{Contraction, gnawing, and tearing pains; cramps and spasms.} S{Sensation as of a hard tumour starting from pylorus and extending to liver}


:rubric=12. Abdomen.
S{Inflammation and swelling of liver.} S{Burning, pulsating, and lancinating pain in region of spleen.} S{Twisting and tearing pains in bowels with nausea and vomiting.} S{Swelling in groin}


:rubric=13. Stool and Anus.
S{Stools mucous, bloody, brownish yellow; whitish, choleraic; offensive, with colic and burning in abdomen and rectum.} S{Obstinate constipation, stool hard, knotty, like sheep-dung.} S{Hæmorrhoids and abscesses.} S{Itching at anus}


:rubric=14. Urinary Organs.
S{Ardor urinæ.} S{Incontinence; when walking and during sleep}


:rubric=15. Male Sexual Organs.
S{Chancre-like sores on penis and in urethra.} S{Sexual appetite increased by every attempt to satisfy it until it drives him to onanism and madness}


:rubric=16. Female Sexual Organs.
S{Scirrhus-like swelling of cervix uteri.} S{Eruptions, whitish and reddish, in vulva; pustules.} S{Erysipelatous swelling of mammæ.} S{Ulcerated indurated tumour in breast.} S{Excoriation of nipples}


:rubric=17. Respiratory Organs.
S{Hoarseness and aphonia.} S{Difficulty of speaking with drowsiness, with glandular inflammation.} S{Corrosive sensations in larynx.} S{Liquids pass frequently into larynx and nasal fossæ.} S{Prominent swelling of laryngeal cartilages, obstinate, paroxysmal, shaking cough, B[<] in evening and night, and from dust.} S{Burning and raw feeling in chest}


:rubric=19. Heart.
S{Burning, stitches, and cramps in cardiac region, with oppression.} S{Heart seems paralysed with death-like weakness}


:rubric=20. Neck and Back.
S{Stiffness of neck and back; lumbago}


:rubric=22. Upper Limbs.
S{Rheumatic pains and swelling of joints.} S{Diseased and deformed nails.} S{Swelling of glands of axilla}


:rubric=23. Lower Limbs.
S{Stiffness, lancinating and crampy pain in sacrum and iliac bones.} S{Nails grow awry and hurt toes.} S{Offensive feet-sweats}


:rubric=25. Skin.
S{Abscesses, boils, ulcers}


