:rubric=Characteristics.
R[cypr]. has great repute as a "t[nervine|A nervine is a plant remedy that has a beneficial effect upon the nervous system in some way.]" among t[eclectics|persoon (oorspronkelijk m.n. wijsgeer) die zich niet aan een stelsel bindt, maar van alle stelsels datgene uitkiest wat hem het beste lijkt], and in domestic practice. It ranks with Scutellaria and Valerian. 

A strong infusion causes exhilaration followed by calmness; and is much resorted to by women whose nerves are shattered by long illness, or excessive indulgence in green-tea or coffee (Hale). 

S{It is suited to nervous disorders from mental over-excitement, or reflex nervous excitement; to nervous weakness following influenza.} S{It is indicated in the brain-hyperesthesia of children who wake in the night lively and full of play.} S{Ecstasy, preliminary to incipient brain disorder.} S{Epilepsy from reflex irritation.} S{t[Jactitation|spiertrekkingen, zenuwtrekkingen] and trembling; twitching of limbs.}


