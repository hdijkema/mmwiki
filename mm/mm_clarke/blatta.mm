S{Asthma. Bronchitis. Phthisis.}


:rubric=Characteristics.
S{Found accidentally to relieve asthma in a patient who took tea in which a beetle had been infused; it has since proved to have a wide range in asthmatic cases (Ray, I[Hom. Recorder], 1890, p. 254; 1891, p. 193). In the acute attack it acts better in low potencies; the higher being given in the more chronic stages. It is especially suited to corpulent people; and to malarial cases; cases B[<] in rainy weather. Has saved cases in which suffocation was threatened by great accumulation of mucus. Useful in cases of bronchitis and phthisis where there is much dyspnoea.}

  
