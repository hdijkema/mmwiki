GT{Paw Paw, or Custard Apple. N. O. Anonaceae. Tincture of ripe and unripe fruit, green leaves, bark, and root.}

:rubric=Clinical.
S{Aphthae. Carbuncle. Cramp. Diarrhoea. Fever. Scarlatina. Throat, sore.}

:rubric=Characteristics.
S{The 2[Anonaceae], to which 2[Asimina] belongs, are allied to the Magnolia family. From eating the unripe fruit five children developed high fever, sore throat, and a scarlet eruption, with diarrhoea, one of them having eventually a carbuncle. It has also been proved. The mouth, throat and stomach are irritated. Drinks much. Desire for ice-cold things and B[<] after eating.}

:rubric=Relations.
:rubric2=Compare
S{R[Caps], R[Bell], R[Ill] (colic).}

:head=SYMPTOMS.
:rubric=8, 9. Mouth and Throat.
S{Mouth feels corroded; fauces red, swollen; tonsils and submaxillary glands enlarged.}


:rubric=11. Stomach.
S{Nausea and belching; soreness in regions of stomach and abdomen on pressure.}


:rubric=12. Abdomen.
S{Colic.}


:rubric=13. Stool and Anus.
S{Diarrhoea after eating; yellowish discharges; soreness of anus when wiping it.} S{Sudden urging to stool, with sensation as if a stick the thickness of a thumb passed down the rectum; followed by a sudden diarrhoeic stool, repeated every ten or fifteen minutes, with. chilliness, drowsiness, and weak voice.}


:rubric=17. Respiratory Organs.
S{Hoarseness; the voice is weak and talking is an effort, as if the mucous membrane was thickened.}


:rubric=18. Chest.
S{Cramp in chest, lasting three hours, with blueness of face.}


:rubric=25. Skin.
S{Itching when undressing.} S{Scarlet rash followed by desquamation.}


:rubric=26. Sleep.
S{Fever, with sleepiness.} S{Drowsiness and sleeplessness alternating.}


:rubric=27. Fever.
S{Fever heat, with desire for something icy-cold; with drowsiness; with much thirst.}

  
