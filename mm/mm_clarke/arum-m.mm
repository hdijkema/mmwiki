GT{Cuckoo-pint. N. O. Araceae. Tincture of fresh tuber or corm.}

:rubric=Clinical.
S{Ascarides. Asthma. Catarrh. Coryza. Nasal polypus. Prolapsus ani. Scorbutus. Swallowing, constant.}

:rubric=Characteristics.
The leading symptoms of R[Arum-m] are as follows:

S{Inflammation and ulceration of mucous membranes.}
S{Violent irritation of nose and eyes.}
S{Pain left side of nose.}
S{Nasal polypus.}
S{Bleeding from mucous membranes and cavities.}
S{Tongue swollen, pricking, stinging, burning, excoriated.}
S{Burning, stinging pains in mouth and lips "as with a hundred needles" Tickling and burning in throat.}
S{Sensation of a swelling in left side above larynx.}
S{Hoarseness.}
S{From abdomen ascending to chest an oppression over whole chest, with hot breath; it then rose into throat.}
S{Burning, constricting pain in stomach.}
S{Ascarides have been cured by it; also prolapsus ani.}

:rubric=Relations.
:rubric2=Antidoted by
S{Sweet oil, milk, butter. Gum relieves pungent effect on mouth.}
:rubric2=Compare
S{R[Arum-t]}


:head=SYMPTOMS.

:rubric=8. Mouth.
S{Gums bleed readily.} S{Tongue so swollen he could not swallow.}

:rubric=9. Throat.
S{Pain in the throat, with difficult deglutition, as if from constriction in the oesophagus, with constant inclination to swallow; after drinking, sensation as if something were attached to the epiglottis.} S{Obstinate hoarseness.} S{Pressure in the larynx.} S{Tickling, with inclination to cough.}

:rubric=12. Abdomen.
S{Sensation of emptiness after breakfast in the abdomen, as after vomiting; with contraction in the abdomen, as from anxiety and fear.} S{Severe aching between the navel and the hip, esp. when standing upright, or lying on the side, or drawing a deep inspiration, with great sensitiveness of the part affected to external pressure.}

:rubric=14. Urinary Organs.
S{Clear, watery urine; exhaling a smell of burnt horn, and depositing a cloudy sediment.}

:rubric=16. Female Sexual Organs.
S{Catamenia profuse.}

:rubric=17. Respiratory Organs.
S{Phlegm in the respiratory organs.} S{Violent tight cough, with scanty expectoration.} S{After much coughing, expectoration of mucus streaked with yellow.} S{Sanguineous expectoration.}

:rubric=24. Generalities.
S{Violent convulsions.} S{Great physical depression and lassitude.} S{Irrepressible inclination to sleep after meals, the face becoming redder than usual during sleep.}

 
