GT{Carboneum. C. (A. W. 12). Lamp-black. Amorphous Carbon (obtained from the chimney of a coal-oil lamp). Trituration.}


:rubric=Clinical.
S{Convulsions. Sore-throat. Tetanus. Tongue, spasm of.}


:rubric=Characteristics.
S{This substance was proved by Dr. Burt in the 1x trit. It produced sore-throat, costiveness, great craving for acids, and a peculiar convulsive attack described as follows: Spasms, commencing on the tongue, passing down the trachea to the lungs which prevented him from breathing for about two moments; then gradually left and went to stomach, arms, hands, and legs. It took four persons to hold him} S{two at his hands and two at his feet. Spasms both tonic and clonic. Lasted two hours then gradually ceased. The hands had no feeling in them; they were closed and could not be opened. Wrists perfectly pulseless, hands shrivelled and cold. At 2 p.m. the spasms came on very suddenly the same as the first time but not so violent. They did not affect the lungs this time but were much harder in the stomach. Lasted three hours then gradually ceased. He seemed at the point of death. The spasms left him so weak that he could not lift his hand to his head nor sit up.}


