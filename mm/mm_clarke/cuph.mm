S{Dysentery.}


:rubric=Characteristics.
S{This is a popular remedy in some parts of U.S. for summer diarrhoea and dysentery, as one of its names, "Fluxweed," would imply.} 
The entire plant has a clammy, sticky feel, and contains tannin. 
It was first introduced to homoeopathic practice by Dr. A. A. Roth, of Frederick, Maryland, who was induced to try it in his practice by a lady patient (I[H. R.], iii. 242), and his experience has been confirmed by S. G. A. Brown (I[med. Cent.]). 
Dr. Roth gave from 5 to 10-drop doses, according to age. 
The two chief forms on which it is successful are: 
1 S{Cases arising from acidity of milk or food; vomiting of undigested food or curdled milk, with frequent green, watery, acid stools, varying in number from five to thirty a day; child fretful and feverish; can retain nothing on stomach; food seems to pass right through the child.} 
1 S{Stools decidedly dysenteric, small, frequent, bloody, with tenesmus and great pain; high fever, restlessness, and sleeplessness.} 
Dr. Roth considers it has "tonic" properties, as children rally rapidly under it. In ordinary diarrhoeas, especially diarrhoea from cold, he found it useless. 
Brown says: "If you have a child that is fretful and feverish; vomits curdled milk; from a hyper-acidity of the stomach; has frequent green, watery, acid stools or even if the stools are dysenteric, with great tenesmus and colic high fever and restlessness, give Cuphea."}

