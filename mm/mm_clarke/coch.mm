:rubric=Characteristics.
I[Armor. sat.], and also R[Coch-o], are old-world anti-scorbutics, and homeopathic provings have confirmed the old experience. 

S{R[Coch] has the anti-scorbutic properties of the other Crucifere, and is specially efficacious in scurvy from eating salt.} The essential oil is said to be identical with that of R[Sin-a]. 
S{The mucous membranes are affected: mucous stools; gonorrhśa; leucorrhśa; pituitous asthma and pituitous phthisis have been benefited by it.} 
S{There are peculiar symptoms in connection with the teeth.} S{Some violent abdominal pains were experienced. Tearing pains from the belly through to back, down the back, and settling in sacrum.} S{"Colic with backache" appears to be a leading indication.} S{Wandering chronic rheumatism.} 

S{Touch or slight pressure B[<] many symptoms.} S{Bending forward B[>]}


