Before Hahnemann's time R[Asar] was little known except as an emetic. His proving brought to light some unique characteristics. 

S{Most remarkable is a state of over-sensitiveness of the nerves, the scratching of linen or silk, or even the thought of it, is insufferable.}
S{Sensation of lightness of the limbs, thinks she is gliding through the air when she walks.}
S{Nervous irritability and exaltation.}
S{Sensation as though whole body or single parts were being pressed together.}
S{Pain as from contraction in forehead, temples, and behind ear, with watering and burning of the eyes; B[<] afternoon, B[>] when sitting, and by washing.}
S{Eyes inflamed, bleared, staring.}
S{Weak sight, B[<]bright light.}
S{When reading sensation as if eyes would be pressed asunder.}
S{Cold air and cold washing B[>].} 

With a single dose of R[Asar] 200 I cured a case of subacute inflammation of eyes and lids which had lasted some months after an attack of influenza, the indications being "B[>] in open air, and by washing in cold water." On the other hand, it is suited, according to Guernsey, to chilly persons who are always shrinking from the cold; e.g., literary, sedentary men who are sick and chilly. 


