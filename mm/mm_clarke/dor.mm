:rubric=Characteristics.
R[Dor] in its general effects bears a strong resemblance to R[canth]. An extensive proving has been made, and to this have been added the effects of poisoning from handling the insects, and from inhaling the vapour when they have been burned or scalded. 
S{Like R[canth]., R[Dor]. produces burning in mouth, throat, gullet, stomach, abdomen, rectum, and urinary passages; strangury, urethritis, and itching burning and inflammation of glans penis.} 
S{It has been successfully used in urethritis in children under ten, provoked by local irritation.} 
S{A feeling of weight or heaviness is common.} 
S{Fainting, weakness, prostration, collapse; great trembling of extremities.} 
S{Prostration after typhoid and diphtheria has been cured.} 
S{The blood will not coagulate; blood globules disorganised; enormous swelling of whole body; swelling of feet with burning; elastic swelling of whole body which will not pit on pressure (suggesting its applicability in myxoedema).}

