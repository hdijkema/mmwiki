S{I[Aran. scin.] has been proved in 1st and 2nd dilutions. One remarkable symptom noticed was: "Constant twitching of under eyelids", which brings this spider into line with the choreic R[Mygal]. Eyes inflamed, weak, watery, lids swollen. Rather profuse flow of saliva; sweet taste in mouth. Much dull, stupid headache of considerable intensity, especially in postero-superior part of head; unable to rest for it; could not collect thoughts with it. Felt as if he had been drinking. Sleepiness. All symptoms B[<] in warm room}


