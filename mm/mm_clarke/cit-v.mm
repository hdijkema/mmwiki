:rubric=Characteristics.
S{The tincture of the peel is used as a pleasant bitter tonic in the old-school practice. On sensitive individuals oranges in any form produce pathogenetic effects. One patient of my own could never eat an orange without experiencing a fearful taste at the back of the nose and throat, lasting many days and causing choking sensations and faintness. Nervous affections predominate; neuralgias of various kinds. The skin is the seat of many symptoms.}
