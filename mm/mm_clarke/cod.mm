:rubric=Characteristics.
S{R[Cod] possesses many of the properties of R[op], from which it is derived, but it has characters sufficiently distinctive to entitle it to a separate description. R[Cod] seems to possess in great degree the exhilarating properties some persons experience on taking R[op], though it is not without strong soporific effects as well. There are many disorders of sensation, notably troublesome itching. The characteristic here is "Itching with warmth." "Itching and heat of face and head." "A sensation of agreeable warmth" is another characteristic symptom. Convulsive twitchings of muscles and limbs, and especially of orbital muscles, are very marked. Twitchings prevent sleep at night. Numbness and prickling. R[Cod] has in many cases controlled diabetes. Headache is B[<] in morning; restlessness and cough B[<] at night. Symptoms in general are B[<] by motion and B[>] by rest.}


