:head=SYMPTOMS.


:rubric=1. Mind.
S{Fancies he is sea-sick.} S{Suicidal.} S{Is afraid of killing some one with a knife.} S{Disposed to strike, and inveighs against his dearest friends.} S{Weeping and singing alternately; great flow of ideas.} S{Loss of memory.}


:rubric=2. Head.
S{Vertigo; is afraid of falling if he turns his head to r. or l.} S{Sensation as if needles and nails were being driven into head (6 p.m. at evening meal).} S{Painful lancinations l. side of head with vomiting of mucus and burning along oesophagus.} S{Scalp excessively painful; slightest touch B[<] the pain.}


:rubric=3. Eyes.
S{Cannot raise lids or eyebrows.} S{Painful lancinations in both eyes, extending to bottom of orbits.} S{Swelling of l. caruncula lachrymalis with large ecchymosis of l. eye.} S{Vision dim; black points before eye.}


:rubric=4. Ears.
S{Swelling, redness and heat of auricle.} S{Hearing impaired or excessively acute.} S{Cracking in ears when swallowing.}


:rubric=5. Nose.
S{Tip of nose red.} S{Fluent coryza.} S{Sense of smell exalted; celestial odours.} S{Intolerable smells after frequent sneezing.}


:rubric=6. Face.
S{Face puffy.} S{Tic-douloureux and tic convulsive.} S{Burning in l. cheek.} S{Pain in whole lower jaw as if the teeth were being pulled out.}


:rubric=7. Teeth.
S{Teeth become loose; previously loose teeth become very painful.} S{Toothache at night, B[<] by cold, B[>] by heat.}


:rubric=8. Mouth.
S{Formication and trembling of tongue.} S{Painful, dry, cracked tongue.} S{Salivation with engorged submaxillary glands.} S{Copious salivation, with aphthae, and thready, viscid mucus.}


:rubric=9. Throat.
S{Red swelling of tonsils, uvula and velum; tickling of uvula.} S{Sensation as of ball in oesophagus with cramps there.}


:rubric=11. Stomach.
S{Bad-smelling eructations.} S{Hiccough with deep anguish and burning in stomach.}


:rubric=12. Abdomen.
S{Sensation of swelling in liver and spleen.} S{Cramp-like pains; diarrhoea with anguish.} S{Fetid flatulence.} S{Griping, B[>] lying on abdomen.}


:rubric=13. Stool and Anus.
S{Haemorrhoids; constriction; formication.} S{Sensation of foreign body in anus.} S{Diarrhoea and weakness after vomiting.} S{Involuntary stools at night, white or yellowish.} S{Burning or sharp pains with bloody stools.}


:rubric=14. Urinary Organs.
S{Pains in kidneys; in bladder; injury; sometimes incontinence.} S{Gluey, fetid urine discharged 2[guttatim].}


:rubric=16. Female Sexual Organs.
S{Swelling of uterus; weight behind uterus; feeling as if about to prolapse.} S{Leucorrhoea, with menstruation twice a month.} S{Cramp-like pains during menses.}


:rubric=17. Respiratory Organs.
S{Feeling of suffocation with fetid breath; must sit up in bed at night.} S{Cramp-like pains, and lancinations behind sternum.}


:rubric=19. Heart.
S{Violent palpitation; heart seems to be beating in water.}


:rubric=20. Neck and Back.
S{Violent pains in nape and neck with swelling.} S{Feeling as if a small stream of water was running from one ear to the other across nape of neck, causing terrible pain.}


:rubric=22. Upper Limbs.
S{Paralytic pains, commencing in entire l. upper extremity (also in r. less frequently).} S{Rheumatic pains from shoulder to finger-tips.} S{Cramp-like pains at insertion of deltoid B[>] by pressure.}


:rubric=23. Lower Limbs.
S{Electric shocks in muscles of leg.} S{Intense sciatic pains.} S{Staggers; he walks as if stepping on down.}


:rubric=25. Skin.
S{Skin of abdomen painful.}


:rubric=26. Sleep.
S{Sleep after a meal.}

  
