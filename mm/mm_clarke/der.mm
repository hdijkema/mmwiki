:rubric=Characteristics.
Dr. X. Roussel is the authority for this plant, and some of the symptoms are of a very peculiar nature. 

S{"He walks as if stepping on down."} S{"He is afraid of killing some one with a knife."} S{Both kinds of tic are noted - painful and convulsive.} 
S{Sense of smell is exalted.} 
S{"He perceives celestial odours;" also "intolerable smells, after frequent sneezing."} 
S{Viscid urine, and viscid saliva.} 
S{Electric shocks and cramps.} 

