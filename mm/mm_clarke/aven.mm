S{R[Aven] has been used empirically in substantial doses (5 to 15 drops of the tincture, preferably in hot water) in a large number of cases of nerve weakness. The leading indications are: Irregularities of the male sexual system. Nervous exhaustion. General debility. Nervous palpitation. Insomnia. Inability to keep the mind fixed on any one subject, especially when due to masturbation or sexual irregularities. It is most valuable in enabling a patient to overcome the morphine habit. It appears to exert the same kind of soothing action, without creating a habit of its own. When not more than four grains of morphine have been taken daily it may be discontinued abruptly, 15 drops of R[Aven] in a wineglass of hot water being given four times a day instead. The only symptom that has been observed to be caused by it is a pain at the base of the brain from 20-drop doses.} S{An alkaloid, R[Avenin] (C56 H21 NO18), has been isolated from oats. It is easily soluble in alcohol. No clinical observations with this have been recorded.}
 
