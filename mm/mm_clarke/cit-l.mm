:rubric=Characteristics.
S{In the observations with this plant the expressed juice of the fruit has been used containing citric acid. Lemon-juice and citric acid are traditional remedies for and preventives of sea-scurvy. That their action is homoeopathic has been proved by the occurrence of scurvy from the excessive use of the acid when taken for other purposes. Used as a local application (in one part of citric acid to eight of water) it has relieved the pains of cancer. It has a powerful effect on the circulation and on the blood itself, producing faintness, weak pulse, haemorrhages, and dropsies. Inflammation and painful enlargement of the spleen; dyspnoea. Daily headache. Stiffness in the joints, particularly in the fingers; bruised feeling, particularly in the feet. Herpetic eruptions.}


