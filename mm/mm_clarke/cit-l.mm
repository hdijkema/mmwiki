:rubric2=Compare

S{It should be compared with R[Acet-ac]; and the "bruised pain in the joints" recalls the action of its congener, Ruta; R[bell]., R[lach]. As an antidote against R[stram]. it has been employed with great success, the juice of the fruit being administered by the teaspoonful. The symptoms removed were: Convulsive fits with violent movement of the hands and of the feet, eyes convulsed, fixedness of look, salivation, pupils dilated, face red and puffed. Loss of reason, and disposition to be frightened. Pulse small and quick. Convulsions renewed or provoked by light.}

  
