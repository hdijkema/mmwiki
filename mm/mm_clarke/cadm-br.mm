:rubric=Characteristics.
S{R[Cadm-b] must be compared with R[Cadm-s]. Three persons have experimented with it. It produced a burning sensation in mouth, gullet, and stomach. In two ladies there was extreme pain and burning in stomach, and vomiting and (in one case) purging also, for five hours, pulse imperceptible. After this the symptoms were better and sleep followed, but both were confined to bed for several days, and one of them (the one who had the diarrhoea) was afterwards subject to cerebral symptoms threatening apoplexy.}

  
