:rubric=Characteristics.
S{R[Aln] is an unproved remedy. The bark-tincture of R[Aln] is used by eclectics as an "alterative" in scrofulous conditions, in chronic skin affections, rheumatism, syphilis, gravel, and gleet. The plant grows in swamps and marshes. Among its indications are: Leucorrhœa with erosions which bleed easily; amenorrhœa with burning pains from back to pubis. Hæmaturia. Cooper, who has used a fresh plant tincture of R[Aln-g], gives: "Pain in rectum after stool"; and "heaviness in head as after being drunk the night before" as having been produced by it. Hale gives as particularly indicating it: "Eruptions on the skin alternating with diseased conditions of mucous membrane.}


