:rubric=Characteristics.
S{R[Cotyledon] has had a considerable proving, but I am not aware of any clinical experience with it having been recorded. The whole houseleek family are deserving of careful study by homoeopathists. Among the most peculiar symptoms of 2[R[Cot]] are: a feeling as if some part of the body} S{a foot or the head} S{were absent. The senses of sight and hearing are dulled. A yellow patch follows the sight. Disagreeable surging in ears; ears feel stuffed. A number of pains in the region of the spleen appear. The breathing is oppressed, the heart is disturbed. Rheumatic symptoms are prominent. Weak, faint feeling. Stitching, pricking, jerking pains are predominant sensations. Cooper considers it a cancer remedy. He has cured with it sinuses in buttock. A patient to whom he gave it for polypus said it made him "stagger as if drunk." Symptoms are B[<] morning and evening.}


