:head=SYMPTOMS.


:rubric=1. Mind.
S{Sadness and indifference.} S{The slightest contradiction gives offence.} S{Disposition to be easily frightened.} S{Mania, sometimes gay, sometimes serious or furious.} S{Irresolution and uncertainty, in consequence of too fickle an imagination.} S{As if in a dream.} S{Voices, including her own, seem to come from a distance; her own voice seems strange, as if it were somebody else speaking.} S{Lectophobia.} S{In writing, many errors are made.} S{Vanishing of thoughts.} S{Want of words.} S{Ideas seem to stand still; he stares in front of him; is absorbed in higher thoughts, but is unconscious of them.}


:rubric=2. Head.
S{Attacks of vertigo on walking, or when standing for some time, to such an extent as to cause falling.} S{Sensation as if intoxicated.} S{Headache, as if there were a stone pressing upon it.} S{Sensation of a heavy weight on the vertex.} S{Pressure and tension on the temples.} S{Compression in the sinciput, from the margins of the orbit to the temples.} S{Congestion in the head, with throbbings in the brain, cheeks red and hot; with pulsation, and not unpleasant warmth in it.} S{Sensation at the scalp, as if something were creeping on it, and frequent sensation, as if drops of cold water were falling on the head.}


:rubric=3. Eyes.
S{Aching pain in the balls of the eyes.} S{Cramp-like pulling in the eyes.} S{Weakness and confusion of sight, on viewing objects either distant or near.} S{Specks, and opacity of the cornea.} S{Appearance of a denticulated circle of whitish flames before the eyes.}


:rubric=4. Ears.
S{Throbbing and pressure in the ears; disappearing when stooping, and reappearing when raising the head again.} S{Buzzing in the ears, and sensation as if a skin were before them.}


:rubric=5. Nose.
S{Swelling of the nose, with copper-like redness.} S{Stupefying pressure on the root of the nose.} S{Heat and dryness of the nose.} S{Epistaxis, preceded by a sensation of burning in the nose.}


:rubric=6. Face.
S{Paleness of the face.} S{Tingling, itching, and smarting in the face, as if from salt.} S{Palpitation of the muscles of the face.} S{Heat of the face, and redness of the cheeks.}


:rubric=8. Mouth.
S{Dryness of the mouth, with viscid saliva, and absence of thirst.} S{Eruption in the vermilion border of the lips.} S{Embarrassed speech; at one time words are wanting, at another the voice fails.} S{The speech stops with extraordinary anxiety and agony on account of pain in the back.}


:rubric=11. Stomach.
S{Empty risings.} S{Regurgitation of acrid substances of a bitter sourness.} S{Nausea, with inclination for food.} S{Vomiting with sensation of strangulation, from the epigastrium to the throat.} S{Vomiting of green bile.} S{Pain in the stomach, on being touched, as if it were ulcerated, B[>] by eating.} S{Attack of violent cramps in the stomach, with paleness, and sweat of the face; pulse almost extinct, and respiration rattling.} S{Aching, pinchings, and Cuttings in the epigastrium, and in the upper part of the stomach.}


:rubric=12. Abdomen.
S{Pain, as from a bruise, in the intestines.} S{Sensation of soreness in the abdomen (dropsy).} S{Hard and painful swelling of the hepatic region.} S{Cramp-like pains in the epigastrium.} S{Pulsation of the abdomen, as if from within outwardly.} S{Shuddering in the abdomen, as if cold water were running through it.} S{Painful jerks in the abdomen, as if it contained some living object.} S{Shaking of the intestines, as if they were detached, when the arms are moved.} S{Partial swelling of the abdomen, as from an encysted ascites.} S{Shocks and pressure towards the outside in the region of the groins.}


:rubric=13. Stool and Anus.
S{Diarrhoea, accompanied by cramp-like pains in the abdomen.} S{Pressure in the rectum towards the outside.} S{Sensation, as of a running of cold water from the anus.} S{Constipation, and hard faeces.}


:rubric=14. Urinary Organs.
S{Sensation of soreness and inflammation of the kidneys.} S{Urgent inclination to make water, with pressive pain.} S{Difficulty of making water, as if from paralysis of the bladder, and nocturnal strangury.} S{Obstinate retention of urine.} S{Stoppage of the urinary ducts by mucus and pus.} S{Urine, turbid, white, or reddish, and as if mixed with blood and pus.} S{Incontinence of urine.} S{Stream of water scattered.} S{Emission, drop by drop, of a scanty and sanguineous nature.} S{Burning pain in the urethra and in the bladder, before and during the emission of urine.} S{Urethra inflamed and painful to the touch.} S{Discharge of watery mucus from the urethra (gonorrhoea).} S{Yellow and mucous discharge from the urethra.} S{Ejection of a stone on making water.}


:rubric=15. Male Sexual Organs.
S{Genital parts cold.} S{Itching and inflammatory swelling of the prepuce, glans, and penis, with deep redness, and phimosis.} S{The penis feels sore and burnt when walking; walks with legs apart.} S{Aching in the testes, and tension in the spermatic cord, when standing upright.} S{Swelling of the prostate gland.} S{Erections, with tensive pains.} S{Repugnance to coition, or strong excitement of sexual desire.}


:rubric=16. Female Sexual Organs.
S{Profuse menstruation.} S{Sterility.} S{Miscarriage, with convulsions.} S{Threatened abortion, complicated with gonorrhoea; or on account of too frequent sexual intercourse.} S{Great excitement with sterility.}


:rubric=17. Respiratory Organs.
S{Loss of voice.} S{Accumulation of tenacious mucus in the larynx, with scraping, and difficulty of respiration.} S{Cough, violent and dry.} S{Cough with greenish and viscid expectoration.} S{Difficulty of respiration, as if there were a weight on the chest, with wheezing and rattling in the bronchia.} S{Respiration short and oppressed.} S{Respiration difficult, and possible only when standing upright.} S{Difficulty of respiration, and oppression on the chest, with uneasiness in the throat; B[<] lying down.} S{Respiration rattling.}


:rubric=18. Chest.
S{Shootings in the bottom of the chest, esp. on breathing or speaking, or during movement.} S{Inflammation of the lungs, with greenish vomiting and delirium.}


:rubric=19. Heart.
S{Painful strokes in the region of the heart.} S{Sensation as if drops were falling from the heart.} S{Inflammation of the heart.} S{Painful constraint and tension in the heart, with palpitation of the heart and anxiety.} S{Beatings of the heart, which are felt lower than their ordinary place.}


:rubric=20. Neck and Back.
S{Pains in the back, which impede speech and suspend respiration.} S{Shooting pain between the shoulder-blades.} S{Pressure towards the outside in the sacral region and coccyx.}


:rubric=22. Upper Limbs.
S{Pain, as from a bruise, in the shoulder and forearm during movement.} S{Cramps in the hands and the fingers.} S{Cramp-like contraction of the metacarpal bones, of the (r.) hand.} S{Sudden paralytic weakness of the hand, with trembling on laying hold of an object, and inability to hold it firmly.}


:rubric=23. Lower Limbs.
S{Cramps in the thighs, the calves of the legs, and the hams.} S{Weakness, staggering, and pains of the knees.} S{Displacement of the patella on going upstairs.} S{Pulsation and stretching pain in the feet, and in the joints of the foot, as after a long walk.} S{Spasmodic contraction of the the tendo Achillis, with violent pains.}


:rubric=24. Generalities.
S{Acute drawing, and contractive, pressive pains, with sensation of paralysis, or shocks and deep shootings in different parts, or else a sensation as if pinched with the fingers.} S{Rheumatic pulling during movement, apparently in the periosteum.} S{General dejection, with tottering and soreness of the knees.} S{Great fatigue, from having spoken or written.} S{Tetanus, chiefly in the upper limbs, and in the trunk.} S{Several symptoms are aggravated or provoked by touch, open air, and heat, as well as at night, and after midnight; by exercise, and after a meal.} S{Sensation, as if drops of cold water were falling (on head; from the anus; from heart).} S{Affections of ball of the foot or under part of the toes.} S{Typhoid fevers where strangury exists.}


:rubric=26. Sleep.
S{Invincible desire to sleep during the day.} S{Sensation of greater fatigue on waking in the morning, than when going to bed in the evening.} S{Nocturnal sleeplessness.} S{Great anxiety of heart.} S{At night, pricking and sensation of burning over the whole skin, as if from boiling water.}


:rubric=27. Fever.
S{Shuddering and shivering, with thirst.} S{Uneasiness and external cold.} S{Coldness of the body, with heat in the face.} S{Heat only in the face.} S{Pulse weak, slow, and scarcely perceptible.} S{Burning heat over the whole body at night.}

  
