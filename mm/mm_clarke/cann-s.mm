GT{European or American Hemp. N. O. Cannabinaceae. Tincture of male and female flowering tops.}


:rubric=Clinical.
S{Ascites. Asthma. Cataract. Cystitis. 2[Eyes; cortical opacity.] Fingers, contracted. 2[Gonorrhoea.] Headache. Hysteria. Infantile leucorrhoea. Nephritis. Nose-bleed. Palpitation. Phimosis. Pleurisy. Pneumonia. Post-partum haemorrhage. Priapism. Stammering. Tetanus. 2[Trachea, mucus in.] Urethral caruncle.}


:rubric=Characteristics.
S{I[Cannabis sativa] resembles closely Cannabis indica, but the mental symptoms and head symptoms are less pronounced and the eye and genito-urinary symptoms more so. Characteristic sensations are: "As if hot water were poured over him; over heart. As if drops of cold water were falling: on head; from anus; from heart." As if pinched with pincers in back. Pressure as with a sharp point in coccyx. Gonorrhoea, esp. when the patient walks with the legs apart. Sexual over excitement in either sex. Threatened abortion from too frequent sexual intercourse. Threatened abortion complicated with gonorrhoea. The urethra is very sensitive to touch and pressure; the patient cannot bear the legs close together. R[cann-s]. has cured many cases of urethral caruncle; and of phimosis. It resembles R[canth]. in its urethral symptoms, but has more burning and smarting (R[canth]. more tenesmus). Drawing pains in region of kidneys extending into inguinal glands, with anxious sensation of nausea at epigastrium. Unendurable fine stitching over whole body, as from a thousand needle points, at night, when sweating from warm covering, B[>] uncovering. Feet feel heavy on going upstairs.}


