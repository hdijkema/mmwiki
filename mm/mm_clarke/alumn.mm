:rubric=Characteristics.
S{The well-known astringent action of R[Alumn] gives a leading indication for its use in practice. Sensations of dryness and contraction are prominent in various parts. I have frequently removed with it constipation of the most aggravated kind, as in cases of cancer of uterus and rectum. Stools as hard as stones is the leading indication. No desire for stool for days. Contractive sensation in abdomen and pelvis, and retraction of abdomen as in lead colic. Paralytic states with the sensation as if bound with a tight band. On the other hand, it is equally available for the states of relaxation, which corresponds with its secondary action: sluggish action of muscles; hæmorrhages of various kinds; large clots in typhoid; relaxed mucous membranes; relaxed uvula; hoarseness and bronchial affections; diarrhœa. Catarrhal conditions prevail throughout the remedy. The R[Alumn] patient is very sensitive to cold; exposure to air roughens and chaps the skin. Slight scratches bleed. Bleeding after tooth extraction. There is a tendency to indurations whenever inflammations occur, hence: scirrhous indurations; indurated glands. Pains are insupportable. Attacks come and go suddenly; periodic. Many complaints come on during sleep. There is nightmare. Turning on r. side 2[> ]vertigo. Lying on r. side brings on palpitation. There is sensitiveness to all sorts of weather changes, and especially to cold. W. P. Wesselhœft has cured bronchitis characterised by scraping from upper sternum to throat, with free expectoration of thick, yellow mucus, paroxysmal cough, morning cough with gagging, aching in chest and in the lower part of the back in region of lower ribs. A peculiarity was the profuse expectoration with the scraping, for scraping coughs are usually dry. R[All-c] had helped the preliminary sneezing and coryza}


