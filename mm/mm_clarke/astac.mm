:rubric=Characteristics.
S{The well-known effects of shell-fish, as well as of other fish, in producing nettle-rash is exemplified in R[Astac] beyond all other varieties. The lacquer-workers of the East have discovered in the river-crab the best antidote to the effects of R[Rhus-t] which they employ in their craft. The liver is markedly affected, and a great characteristic is "nettle-rash with liver complaint." jaundice of children. Itching of various parts. Crusta lactea, with large lymphatic glands. Enlarged glands of neck in children and old people. Pain and tenderness of liver, jaundice, stools of pipe-clay colour. Inward chilliness and sensitiveness to air; B[<] uncovering. Violent fever with headache, glowing red face, inward chilliness. Nervous crawls over body. Tumours, recent. Gout of drinking people. Stinging pains are felt in various parts. A notable remedy.}
