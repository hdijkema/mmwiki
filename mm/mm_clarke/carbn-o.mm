:head=SYMPTOMS.


:rubric=1. Mind.
S{Depressed: stupid; apathetic.} S{Anxiety, with desire for air but too paralysed to seek it.}


:rubric=2. Head.
S{Vertigo; inclination to turn in a circle.} S{Headache, violent, persistent, with throbbing in temporal arteries.} S{Frontal headache which extends over the whole head, but chiefly felt in forehead, which seems pushed out.} S{Weight; throbbing; compression; sticking headache.}


:rubric=3. Eyes.
S{Eyes protrude; sight dim with vertigo, and fluttering and flickering before sight.}


:rubric=4. Ears.
S{Roaring in ears.}


:rubric=6. Face.
S{Trismus; jaws firmly clenched.}


:rubric=8. Mouth.
S{Paralysis of tongue.}


:rubric=11. Stomach.
S{Stomach so irritable that everything eaten was immediately vomited.}


:rubric=13. Stool.
S{Involuntary evacuations.}


:rubric=14. Urinary Organs.
S{Paralysis of bladder.} S{Urine contains sugar.}


:rubric=17. Respiratory Organs.
S{Rattling of mucus in air-passages.} S{Bloody mucus raised.} S{Expired air feels cool to back of observer's hand.} S{Breathing stertorous, slow.} S{Sense of suffocation.}


:rubric=19. Heart.
S{Intolerable pain in region of heart.} S{Violent palpitation.}


:rubric=20. Back.
S{Burning pain at right scapula.}


:rubric=23. Lower Limbs.
S{Shooting pains in r. natis and down sciatic nerve to foot, not B[<] by pressure or movement.}


:rubric=25. Skin.
S{Whole skin covered with large and small vesicles of pemphigus.} S{Purple maculation of skin.} S{Herpes zoster l. side of face along branches of trigeminus.}


:rubric=26. Sleep.
S{Great sleepiness for several days.} S{Never slept so long before.} S{Sleep deep, prolonged, interrupted by cramps in cheeks and toes.}

  
