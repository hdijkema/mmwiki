S{Cramps. Headache. Herpes zoster. Paralysis. Pemphigus. Sciatica. Trismus.}


:rubric=Characteristics.
S{The effects of R[carbn-o]. have been noted in persons poisoned by the gas. It produces a state like intoxication. The person is confused, stupid. There is loss of consciousness. Anaesthesia, but the slightest touch with a hot iron recalls sensibility. Convulsions and cramps. Great sleepiness lasting for several days; interrupted by cramps in cheeks and toes. Great coldness of surface, hands icy cold. Marked B[>] of symptoms, especially of chest, in open air.}


