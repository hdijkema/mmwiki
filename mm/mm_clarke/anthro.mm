:rubric=Clinical.
S{Caries. Cracks. Diabetes. Diuresis. Ecthyma. Eczema impetigenoides. Gout. Herpes. Impetigo. Lichen. Nostrils, cracked. Priapism. Prurigo. Rheumatism. Scabies. Scrofula. Sycosis. Urticaria}


:rubric=Characteristics.
S{"A very absurd combination," says Hering, "proved on many people, but in a very superficial way." It has been principally used in skin affections, scabies, prurigo, chronic herpes, and chronic cracks and ulcerations of nostrils. Eruptions decrease with the full of the moon. The intense thirst and diuresis suggest a place for the remedy in diabetes}


:rubric=Relations.
S{2[Compare:] The Carbons; the Antimonies; R[Rhus-t], R[Dulc], R[Ferr-i] (eczema impetigenoides)}


:head=SYMPTOMS.


:rubric=5. Nose.
S{Chronic cracks and ulcerations of nostrils}


:rubric=8. Mouth.
S{Dryness of the mouth; foul tongue}


:rubric=9. Throat.
S{Dryness of the throat; internal heat, extending to the stomach; deglutition slightly impeded}


:rubric=11. Stomach.
S{Want of appetite.} S{Surfeit.} S{Disagreeable taste in the mouth; great thirst; retching; vomiting of bile, and of black mucus; sensation of heat in the stomach; pressure within; cramps of the stomach}


:rubric=12, 13. Abdomen and Stool.
S{Inflation of the abdomen, as in tympanitis; colic.} S{Blackish stools; evacuations like pap, with colic and borborygmi; diarrhœa, which continues several days}


:rubric=14. Urinary Organs.
S{Increase in the secretion of urine; abundant flow of pale urine without sediment; burning in the urethra while passing water; or itching at the orifice; retention of urine}


:rubric=15, 16. Male and Female Sexual Organs.
S{Frequent erections.} S{Catamenia beyond the usual period}


:rubric=25. Skin.
S{Increased activity of the skin; nettle-rash.} S{Chronic erysipelas; purulent pimples, like papulæ, with considerable itching, appearing during the night and disappearing in the daytime; dropsy}


:rubric=26. Sleep.
S{Sleeplessness, with agitation and excited pulse}


:rubric=27. Fever.
S{Febrile shivering, alternating with heat, followed by gentle perspiration, and quickness of the pulse.} S{Redness and heat of the skin, disappearing on the supervention of sweat.} S{Perspiration all over the body, with pain in the head, agitation, and bruise-like sensation, and quickness of the pulse; nocturnal sweat, sometimes general, sometimes limited to the parts affected; viscid sweating of the feet; before the sweat, oppression of the chest and violent palpitations of the heart; the skin continues moist for a long time after the perspiration has ceased}


