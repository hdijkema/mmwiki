:rubric=Characteristics.
S{R[Cub] corresponds to catarrh.} 
S{It is an old-world remedy for gonorrhoea and gleet, but it affects the mucous membrane of the nose, air passages and intestines, as well as that of the urino-genital tract.} 
S{It acts well in persons of bilious temperament with tendency to constipation.} 
Some of its most characteristic symptoms are: 
S{Burning in throat with dryness, constant need to swallow saliva; burning in stomach; in abdomen; in rectum; in urethra; in fossa navicularis.} 

:div-begin=quote
Nash defines its place in gonorrhoea thus: "After the inflammatory stage is passed under the usual remedies for that stage, if there still remains burning in the urethra after micturition, and the discharge remains thick, yellow, or pus-like (with R[puls] the discharge is 2[bland]; with R[merc]. all symptoms are B[<] at night). Thin (gleety) discharge does not correspond to any of the three." 
:div-end
