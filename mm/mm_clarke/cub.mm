:head=SYMPTOMS.


:rubric=1. Mind.
S{Exaltation of temper and of the mental faculties.} S{On violent agitations he was aroused for a moment; uttered some incoherent expression and sank back into a comatose sleep.}


:rubric=3. Eyes.
S{Eyes rolled upward, injected; pupils contracted to a pin-point.}


:rubric=6. Face.
S{Face red and swollen.} S{Veins of forehead and temples turgid.} S{Lips dark purple.} S{Lips puffed, dry and shining.} S{Extraordinary twist of mouth to one side every time he attempts to speak or smile.}


:rubric=7. Teeth.
S{Teeth slightly coated with brown sordes.}


:rubric=8. Mouth.
S{Mouth containing viscid frothy saliva.} S{Aphthae of lips and gums.} S{Tongue furred and moist or very white.}


:rubric=9. Throat.
S{Constant need to swallow saliva to relieve dryness and burning in throat and larynx.}


:rubric=11. Stomach.
S{Nausea and vomiting.} S{Burning pain in stomach.}


:rubric=12. Abdomen.
S{Burning in abdomen.} S{Femoral hernia.} S{Colic.}


:rubric=13. Stool and Anus.
S{Diarrhoea, with rumbling and cutting in the abdomen and burning in the rectum.} S{Suppuration of the rectum.} S{Burning and itching of the anus.} S{Stools: blackish, yellowish, fecal; bilious; yellow transparent mucus; mingled with whitish, shining particles like rice; bloody mucous (dysenteric); involuntary; B[<] at night in bed; B[>] from rising from bed and moving about.}


:rubric=14. Urinary Organs.
S{Foaming urine.} S{Albuminous, bloody urine, with increased uroxanthine.} S{Burning and itching in the fossa navicularis.} S{Nocturnal enuresis.}


:rubric=15. Male Sexual Organs.
S{Discharge from the urethra of a dark-reddish colour (gonnorrhoea).} S{Inflammation of the penis.} S{Sensation of pressure and heaviness in the pelvis.} S{Testicles swollen.} S{Thick, yellow-green discharge, with frequent obstruction of urethra.} S{Prostatitis.}


:rubric=16. Female Sexual Organs.
S{Urethro-vaginitis, severe and of long standing, with acute pains and abundant discharge.} S{Leucorrhoea profuse, yellow, greenish, very acrid, very offensive; erythema of inner surfaces of thighs, pruritus vulvae, intense desire for coitus; small burning pimples; womb swollen and painful; menses too early, preceded and followed by leucorrhoea, or in small quantity and consisting mostly of leucorrhoea.} S{Acrid leucorrhoea of children.}


:rubric=17. Respiratory Organs.
S{Respiration slow, short and gasping; hurried and noisy.} S{Chest feels full.} S{Sense of suffocation.} S{False membranes in larynx.} S{Voice harsh and wheezing.} S{Incessant bronchial cough; B[<] evening, by heat, in open air; throat dry and parched; hurried, noisy respiration.} S{Harsh cough.} S{Barking, croupy cough.}


:rubric=19. Pulse.
S{Pulse very slow, moderately full, but extinguished by least pressure.}


:rubric=21. Limbs.
S{Sensation of heat in the palms of the hands and soles of the feet.} S{Numbness of fingers and toes.}


:rubric=25. Skin.
S{Skin warm, moist, clammy.} S{Feverish tingling heat over whole body.} S{A flush of red over whole body.} S{General eruption of roseolous papules without fever or itching, confluent on face, buttocks and upper limbs, more scant on lower.} S{Intense itching.} S{Severe urticaria febrilis.}


:rubric=27. Fever.
S{Heat flushes on face soles of feet and palms of hands.} S{Fever with heat at pit of stomach.} S{Cold sweat in chest and back; burning in abdomen (in croup).}

  
