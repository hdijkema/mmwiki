:rubric=Characteristics.
The most characteristic symptoms of R[Dol] are: 
S{a general intense itching without eruption, B[<] by scratching; and a pain in the throat on right side below angle of jaw as if a splinter were embedded vertically in it.} 
S{It is mainly a right-side medicine, and hence its relation to the liver.} 
S{It corresponds to exalted nervous sensibility, as in dentition.} 
S{Gums intensely sensitive; seem to itch.} 

:div-begin=quote
Kraft has recorded (I[N. A. J. H.], 1890, p. 20) a striking cure with R[Dol] 1,200 (Kent) of a woman who had symptoms of jaundice, white stools, and desperate itching, preventing sleep at night. There was, in addition, enormous distension of abdomen with continuous rattling of wind, intolerance of anything tight round neck or body; intense restlessness, unable to sit still a minute. 
:div-end

