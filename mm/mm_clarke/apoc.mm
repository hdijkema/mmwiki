:rubric=Characteristics.
S{The clinical effects of R[Stroph] and the symptoms produced on patients to whom it has been given in large doses, clearly show the relationship between it and R[Apoc]. Both produce intense gastric disturbance, extreme depression of the heart's action, and both control dropsical effusions and produce diuresis. R[Apoc] depresses the heart, kidneys, and intestines, relaxing sphincters. It causes an intense sinking sensation; drowsiness and a bewildered state of mind. Dizziness. Hæmorrhages. The dropsies of I[Apocynum] are general dropsies with or without organic disease; swelling of every part of the body; cardiac dropsies. The excretions are diminished, especially urine and sweat. Hydrocephalus has been cured with it: "Child lying in stupor, with constant involuntary motion of one arm and leg." "Paralysis of left side; one eye motionless, one rolling.}

S{Three cases of neuralgia of the right side} S{groin, hip, and in one case region of right kidney} S{have been cured with this remedy. All were violent, extorting cries. In one case the pain set in very suddenly and the slightest jar aggravated. Hale says the kidneys are the peculiar seat of the operation of the drug, and that it is in renal dropsies that it is especially curative. It has cured diabetes insipidus with "a sense of sinking in the pit of the stomach with great debility." Hæmorrhages, especially menorrhagia, and hæmorrhages at the change of life have been cured with it. Hale has found it restore the menses when given in cases of dropsy}

S{Diarrhœa is B[<] immediately after eating. Food or water is immediately ejected, though hungry. There is B[<] on waking in the morning. Many of the symptoms are B[<] after sleep}


