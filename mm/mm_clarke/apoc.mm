:head=SYMPTOMS.


:rubric=1. Mind.
S{Bewildered; nervous, low-spirited}


:rubric=2. Head.
S{Dizzy.} S{Very marked piercing pain in r. temple followed by vertigo.} S{Vertigo suddenly appearing and disappearing.} S{Hydrocephalus: stupor, sight of one eye totally lost, the other nearly so; forehead projecting; sutures open; stage of exudation}


:rubric=3. Eyes.
S{Eyes inflamed; B[<] morning and evening.} S{As if sand in them}


:rubric=5. Nose.
S{Violent coryza.} S{Nose and throat filled with thick yellow mucus on waking in morning.} S{("Snuffles" in infants.).} S{Peculiar dryness of mucous membranes of nose with sensation of stiffness, followed by secretion of thick yellow mucus, preceded in some cases by a thin, irritating discharge}


:rubric=6. Face.
S{Face pale, covered with cold sweat (diarrhœa).} S{Dry lips}


:rubric=8. Mouth.
S{Dry tongue, immoderate thirst.} S{Tongue coated.} S{Constant spitting, increase of mucus and saliva}


:rubric=11. Stomach.
S{Thirst on waking.} S{Great thirst; but water disagrees, causing pain, and is immediately thrown off (Ars.).} S{Cannot tolerate food or drink, which is immediately ejected.} S{Distension after a moderate meal.} S{Feels hungry, but when she eats it settles in epigastrium, turns sour, and hurts.} S{Extreme epigastric sinking.} S{Oppression at epigastrium and chest excessive, can hardly, get breath enough to smoke a cigar.} S{Ascites, often with chronic diarrhœa}


:rubric=13. Stool and Anus.
S{Gentle diarrhœa in the morning, no pain.} S{Diarrhœa explosive, involuntary immediately after eating.} S{Stool copious, yellow or brownish, like mush; sometimes containing undigested food.} S{Extreme prostration after stool.} S{Constipation though fæces not hard.} S{Piles with feeling as of a wedge being hammered into anus.} S{Tenesmus of rectum.} S{Bearing-down pain in anus.} S{Evacuations very scanty.} S{Watery stools.} S{Symptoms always immediately after eating}


:rubric=14. Urinary Organs.
S{Urinary organs torpid, urine scanty, but flows as easily as oil.} S{Severe expulsive pains of bladder.} S{Urine copious and almost involuntary from relaxed sphincters.} S{Profuse light-coloured urine; no sediment.} S{Enuresis}


:rubric=15. Male Sexual Organs.
S{Penis and scrotum swollen, dropsical}


:rubric=16. Female Sexual Organs.
S{Exhausting menorrhagia (continuous or paroxysmal).} S{Blood expelled in large clots, sometimes in fluid state.} S{Amenorrhœa in young girls; abdomen and legs bloated}


:rubric=17. Respiratory Organs.
S{Oppression; can scarcely speak; speaks in sharp staccato; must take deep breaths.} S{Violent hoarse cough, B[<] at night.} S{Hæmoptysis.} S{Cough short and dry, loose and rattling with oppression.} S{Short, unsatisfactory respirations}


:rubric=19. Heart.
S{Flutterings, dartings, prostrated feeling in cardiac region.} S{Quick, sharp, catching pain in heart with palpitation.} S{Pulse slow, laboured.} S{Irregular, intermittent, at times feeble, then slow.} S{Pulse quick, feeble when moved.} S{Fainting on moving the head.} S{Palpitation troublesome on walking; heart action scarcely perceptible}


:rubric=21. Limbs.
S{Rheumatic condition.} S{Joints stiff, esp. on moving in the morning.} S{Constant involuntary motion of one leg and one arm}


:rubric=22. Upper Limbs.
