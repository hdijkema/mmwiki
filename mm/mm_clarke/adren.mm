:rubric=Characteristics.
S{2[Adrenalin ]has cured a number of cases of Addison's disease and has arrested others. The leading features of this affection may be taken as guides for its use: Bronzing of skin; loss of strength; wasting; exceedingly rapid pulse. It has cured a case of hæmaturia accompanied by pain in the adrenal region. It has been used locally in cases of hyperæmia of the conjunctiva, dissipating it almost immediately, and thus rendering operations possible. It appears to possess a very powerful local action over dilated blood-vessels. When injected into the circulation blood pressure rises, the arteries being contracted. The general muscular system is affected, a slight stimulus producing prolonged contraction}

