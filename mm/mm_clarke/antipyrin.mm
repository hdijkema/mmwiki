:rubric=Characteristics.
S{I[Antipyrine] is known from the poisonous effects it has produced in old-school practice. Many fatal cases have been recorded, and there can be little doubt that its injudicious use has determined a fatal issue in many cases that have remained unrecorded. Its action is very like that of R[Antifeb] in producing collapse, but it has more effect on the skin, causing scarlet rash and śdema. A young lady patient of mine who took I[Antipyrin] on her own account, whenever she thought she might be going to have a headache, developed intense dysmenorrhśa (to which she was not subject), with depression. This was only removed after she had discontinued the drug. Later on she took R[Phenac], with the result that an eruption of erythema appeared on both cheeks, which peeled after a few days, and recurred persistently. It would seem from the above that the drug is homśopathic to some forms, at least, of neuralgia. One patient had Cheyne-Stokes respiration. Erythema appears first on face and arms, last on legs. Hansen mentions its use in enuresis and epistaxis. B[>] Hot drinks}


