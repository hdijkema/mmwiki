:rubric=Clinical.
S{Aphonia. Nephritis. Œsophagitis. Rheumatism. Sloughing. Thirst. Timidity. Ulcerations. Vomiting}


:rubric=Characteristics.
S{The symptoms of caustic ammonia closely resemble those of the carbonate. The caustic nature of the drug is exemplified in the burning sensations experienced, especially in throat, gullet, and rectum. White patches on tongue and inner side of cheeks. There is intense thirst, and excessively violent ejection of stomach contents. Voice low, weak. Speech broken. Aphonia, with debility; or with burning rawness in throat. (A good all-round remedy in aphonia.) Great oppression of breathing; the patient gasps for breath. As with the carbonate there is timidity} S{great tendency to take fright. Shiverings. In a case of poisoning in a man who took a mouthful of a strong solution of "household ammonia" by mistake there was great swelling of mucous membrane of mouth and throat, stertorous breathing, voice muffled and husky; later, cough with blood-stained expectoration. One night there was headache and delirium, and at the same time the urine contained albumen and hyaline casts. It has been used in muscular rheumatism of the shoulders and thighs. Hæmorrhages occur from mucous surfaces. Great excitement in evening.} S{2[<] After eating or drinking}


:rubric=Relations.
S{Compare the Ammonias, especially R[Am-c]. 2[Antidoted by:] Vinegar and vegetable acids}


