:rubric=Characteristics.
S{Boring pains are very marked in the pathogenesis of this salt; over left eye, skull, chest, tibiae, and bones generally. Symptoms B[<] cold wet weather; from October to spring (headache). Warts on tongue. White stools (Jaundice). Bright's disease. Chancres; ulcers; warts. Bubo. Enormous induration of an ovary. Induration of one part, softening of another part of the uterus. Corrosive leucorrhoea; pustules on genitals. Scirrhus or carcinoma of breast or uterus. A case of rhinoscleroma has been reported cured by it in 5x by Kranz-Busch. Old cases of rheumatism or gouty pains. Scrofula. All symptoms are B[<] by rest. According to Hale, this is the most active of all the preparations of gold. In toxic doses it causes "violent gastro-enteritis," accompanied by cramps, convulsive trembling, insomnia, priapism and insensibility. In pathogenetic doses it causes "epigastric pain, nausea, loss of appetite, and constipation." The constipation is like that of 2[Hydrastis], being accompanied with catarrh. Hale has used it with success in nervous dyspepsia with a tendency to diarrhoea after eating; in gastric and duodenal catarrh, with or without jaundice. The same authority states that the primary effect of the salt is to cause "congestion, irritation of uterus and ovaries, subacute metritis, ovaritis, profuse and premature menses, habitual abortion, nymphomania, ulceration of uterus, endocervicitis." For these the attenuations are suitable. The secondary effects are: "Atonic amenorrhoea, scanty and delaying menses, deficient sexual desire, sterility from ovarian torpor, ovarian dropsy;" for these he recommends the lowest triturations. He suggests its use in puerperal mania with sexual excitement, ovaritis, gastro-intestinal irritation, and suicidal impulses. Also in corresponding states in men. 2[Aur. mur. nat.] has been proved, but it has been chiefly used on general Aurum indications. Burnett considers it to have more power over uterine tumours than any other gold preparation.}

