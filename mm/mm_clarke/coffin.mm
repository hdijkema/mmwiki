:rubric=Characteristics.
S{A proving has been made with this alkaloid, but has brought out no symptoms not included in the pathogenesis of R[coff]. Pulsation of the arteries on head and temples was very marked. Sparks before eyes} S{roaring in the ears. Vomiting with relief of all symptoms. The R[Citrate of coffein], which is used largely in allopathic practice, has occasioned dangerous poisonous symptoms in a number of cases. These seem the most characteristic: Restlessness and dizziness; unable to keep erect. Violent precordial anxiety. Palpitation. Abdominal pulsation. Violent trembling of all the extremities. Grating of the teeth. Features expressive of the greatest anxiety. Feeling of constriction of the neck and throat, recurring periodically every fifteen minutes. Pulse very rapid.}

  
