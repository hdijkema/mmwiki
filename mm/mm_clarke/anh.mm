GT{Mescal buttons. N. O. Cactaceæ. Tincture, extract, or infusion. Havelock Ellis says: "I first cut up the buttons into small fragments and poured on boiling water twice; a single infusion is inactive.}


:rubric=Clinical.
S{Brain-fag. Delirium. Headache. Hallucinations. Megrim. Mental weakness. Neurasthenia. Paraplegia. Senses, disordered. Visions, disordered, of; coloured}


:rubric=Characteristics.
S{The plant from which the mescal buttons are obtained grows in barren and rocky soil in the valley of the Rio Grande. It is used by some Indian tribes in their religious ceremonies. It has been recently tested scientifically, a notable proving having been made by Dr. Wier Mitchell. Dr. E. M. Hale has collected the facts about the drug in an article published in the 2[Hahnemannian Monthly.] The chief feature of the drug's action is the production of coloured visions of most over-powering brilliancy, associated with moving shapes of fantastic design, the motion being regulated somewhat in time by music. In the Indian ceremonies the constant beating of tom-toms is an essential feature. Other symptoms are loss of conception of time, occipital headache, tired feeling in head, nausea. Tremor of muscles, increased knee-jerk, and loss of power of co-ordination. One prover, Havelock Ellis, noted distinct slowing of the pulse, slight faintness and shallow breathing; but there were none of the terrible heart symptoms of the other Cacti. The most prominent condition is B[<] on closing eyes. The nausea and faintness were B[<] on movement. There is great disinclination to move. B[>] Lying down}


:rubric=Relations.
S{2[Compare:] R[Cann-i] (time sense disordered; fantastic visions); R[Gels] (paralysis of accommodation); R[Bell], R[Stram], R[Op], R[Pic-ac], R[Pip-m], R[Coff], R[Coca]; R[Plat] (objects seem small and distant); R[Psor] (B[>] lying down)}


:head=SYMPTOMS.


:rubric=1. Mind.
S{Reverie.} S{Time seems long; intervals between words and sentences seem inordinately long.} S{Cannot find the right word, with difficulty of enunciation.} S{Seems to have a double personality.} S{Distrust and resentment; thinks companions are laughing at him; wants to do them violence.} S{Consciousness of unusual energy and intellectual power (which, when tested, was found not to actually exist).} S{Sense of superiority and well-being.} S{Sense of depression and inferiority}


:rubric=2. Head.
S{Frontal (l.) headache with visual zigzags.} S{Occipital headache, with disturbed vision.} S{Persistent ache and tired feeling in occipital region (lasting several days and making work impossible).} S{(It rapidly removed headache in one prover.}


:rubric=3. Eyes.
S{Visions in all colours, moving, fantastic, of surpassing brilliance, in designs (sometimes grotesque), moving scenes, dances; affected by beating time; dissipated or modified by opening the eyes; partly under control by an effort of thought.} S{Natural objects seem more brilliant, shadows deepened, flickering of lights greatly exaggerated.} S{Pupils dilated.} S{Accommodation impaired.} S{Ptosis}


:rubric=4. Ears.
S{Impressions of sound and visions heightened by any marked stimulation of skin.} S{Exaggerated reverberation of ordinary sounds}


:rubric=5. Nose.
S{The air seemed filled with vague perfume.} S{Smell blunted; could not tell whether or not tincture of asafœtida was a perfume}


:rubric=6. Face.
S{Disinclined to make the slightest movement; eyelids droop; they scarcely move the lips and jaws in articulating}


:rubric=8. Mouth.
S{Great difficulty in talking, partly from paralysis of the tongue, partly from slowness of thought}


:rubric=11. Stomach.
S{Nausea; B[<] on movement; entirely B[>] on lying down}


:rubric=19. Heart and Pulse.
S{Pulse slowed.} S{Respiration shallow.} S{Faintness}


:rubric=23. Lower Limbs.
S{A fine tremor in lower extremities}


:rubric=24. Generalities.
S{Motor inco-ordination.} S{Extreme muscular depression; don't want to stir; whole body feels relaxed.} S{Lazy contentment; "a land where it is always after noon.".} S{Fine tremor in lower extremities preluding the visions; unable to walk without assistance; can with difficulty sit up}


:rubric=26. Sleep.
S{Drowsiness; followed by consciousness of unusual energy}


