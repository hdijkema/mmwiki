GT{A. ferox, Wall. A. virosum, Don. Bisch, or Bikh, Ativisha. (The most poisonous species known, containing greatest proportion of Aconitine; from the Himalaya Mountains.) N. O. Ranunculaceæ. Tincture of root}


:rubric=Clinical.
S{Burning pains. Cheyne-Stokes breathing. Chill. Dyspnœa. Gastralgia. Neuralgia. Numbness}


:rubric=Characteristics.
S{2[A. ferox ]was proved by Dworzack, who also proved 2[Aconitine.] The Ferox developed burning pains in greater intensity than the alkaloid; more intense mental activity followed by greater depression. The 2[Aconite ]note of unendurability of suffering was marked. Anxiety and fear of suffocation from paralysis of respiratory muscles; obliged to breathe half-sitting up with head resting on palms of hands. Cold drinks 2[>;] sitting up 2[>;] warm food 2[<;] coffee 2[>]}

:rubric=Relations.
S{2[Compare:] R[Cur] and R[Phos] (respiratory paralysis; Cheyne-Stokes breathing)}


:head=SYMPTOMS.


:rubric=1. Mind.
S{Mind very active; sequence of ideas rapid; talked constantly, remembered easily the minutest circumstances of his former experiment, compared them with present one, and readily drew conclusions (6 h.). In intervals of relief from distressing symptoms laughed and joked about his very comical condition; but when the dyspnœa, anxiety, &c., returned, he could not seem to endure them, and abused heartily Aconite and toxicology in general (4 to 6 h.).} S{Incapable of any mental work, even the simplest addition; comprehension and understanding confused (2nd day)}


:rubric=8. Mouth.
S{Tongue almost insensible; feels like a piece of raw leather in mouth (6 h.).} S{Tongue covered with a thick yellowish-white fur.} S{Violent burning in mouth (in two minutes), renewed by eating (2nd day); 2[> ]by drinking cold water}


:rubric=9. Throat.
S{Violent burning in pharynx}


:rubric=11. Stomach.
S{Pressure in stomach with pain.} S{Warmth in stomach.} S{Violent drawing pain in region of stomach and sacral region, soon spreading over whole abdomen; 2[< ]by pressure on epigastrium.} S{On pressing in stomach, an internal, dull pressing pain}


:rubric=12. Abdomen.
S{Rumbling in bowels (5 m.); constant gurgling (4 h.)}


:rubric=13. Stool.
S{Two half-watery, dark, not copious stools (2nd d.)}


:rubric=14. Urinary Organs.
S{Frequent and copious evacuations of urine}


:rubric=17. Respiratory Organs.
S{Dyspnœa increased to such a degree that he was obliged to breathe half-sitting up, with head resting on palms of hands; constantly feared suffocation from respiratory paralysis}


:rubric=21. Limbs.
S{Gait tottering; remarkable weakness in lower extremities, esp. r}


:rubric=24. Generalities.
S{Formication spread over whole body, least noticeable on parts that had been cold; 2[<,] or excited by, change of temperature or motion. Having changed from one nerve to another, reached its greatest severity from 7 to 7.30 p.m. It caused a painful unrest so that he could not possibly lie quietly more than a few minutes. Getting up set up all the train of symptoms, which were 2[> ]immediately on lying down.} S{Benumbed sensation, as if he had on gloves; on pinching cheeks no pain felt; seemed to walk on woollen carpets (2nd, 3rd, and 4th d.)}


:rubric=26. Sleep.
S{Sleeplessness.} S{On waking, violent burning in mouth and throat, warmth in stomach, dull sensation in head}


:rubric=27. Fever.
S{Icy coldness of body (4 h.), objective and subjective, no amount of wraps and external warmth relieved. Desire to get warm drove him to get out of bed and sit near stove. Tottered to stove and warmth was pleasant, but vertigo, trembling, oppression, and nausea ensued and compelled him to return to bed. This experience was repeated.} S{Fierce heat (calor mordax) on forehead, cheek, and hands, with sensation as if numerous glowing hot wires were stuck into him: 2[> ]by perspiration (4 to 6 h.)}


