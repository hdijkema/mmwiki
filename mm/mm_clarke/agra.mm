S{R[Agra] is one of the remedies introduced by Dr. Cooper. It partakes of the characters of the lilies and corresponds to catarrhal conditions. Obstruction of the nostrils, especially from adenoids, and throat deafness I have frequently seen relieved by the remedy. The action of it is felt towards the root of the nose. The plant grows in sheltered places, and Dr. Cooper gives 2[> ]from shelter as a leading indication. It also corresponds to chill from cold winds, and is very like R[Sil] in this. I regard it as one of the leading remedies in cases of adenoids. Dr. Cooper gives: "Adenoids with enlarged tonsils; frequently accompanying dentition." He has cured with it mucous diarrhœa following a suppressed cold}


