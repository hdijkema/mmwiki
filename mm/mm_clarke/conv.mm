GT{Convallaria majalis. Lily of the Valley. N. O. Liliaceae.}


:rubric=Clinical.
S{Diarrhoea. Heart, affections of. Herpes. Pruritus vulvae. Vomiting (of pregnancy).}


:rubric=Characteristics.
S{R[Convallaria] is a popular remedy in Russia for heart affections, and it has been introduced into old-school medicine principally as a heart remedy. It compares closely in its action with R[Lil-t], and also resembles its other botanical relatives, R[aloe], R[All-c], and R[All-s]. It is strongly purgative and causes morning vomiting. It has been proved, and the symptoms produced snow that its nearest analogue is R[Lil-t]. Nash has used it in 30th with very satisfactory results in women complaining of great soreness in uterine region with sympathetic palpitation of the heart. He has also removed cardiac dropsy when this uterine soreness has been present as well. Berridge credits it with this symptom: "Movement in the abdomen as from the fist of a child," like 2[R[sulph]., R[thuj]., R[Croc]] In the case of 2[R[Conv]] the symptom occurs "2[when lying on the back.]" Sleepiness accompanies many symptoms. B[>] In open air, B[<] in warm room (like 2[R[All-c]]).}


:head=SYMPTOMS.


:rubric=1. Mind.
S{Depression.} S{Mind wanders from subject when reading.} S{Dull, can hardly think.} S{Irritable when asked a question.}


:rubric=2. Head.
S{Dull, heavy pains at vertex B[>] open air.} S{Headache with fever, B[<] jarring, B[>] rest.}


:rubric=3. Eyes.
S{Sees imaginary grey spots about 3 in. square in different parts of the room on coming in from a walk.} S{When reading sees small words (as "the," "it," "too,") before beginning of sentence, when they are not there.} S{P is substituted for other letters.} S{About every fifteen minutes, dull pains from r. eye over top of head and down r. side of neck.} S{Eyelids heavy.}


:rubric=6. Face.
S{Face and lips sore, crop of small hydroa, feel raw.}


:rubric=8. Mouth.
S{Fine red points on tip of tongue.} S{Intense redness and soreness of tongue, looks like raw beef; granular and quite clean, with tenderness of stomach.} S{Coppery taste in mouth.} S{Water tastes bitter.}


:rubric=11. Stomach.
S{Morning sickness and vomiting.}


:rubric=12. Abdomen.
S{Severe colic; pains come quickly and pass off slowly.} S{Dull aching pain and sore feeling in hypogastrium, B[<] when coughing.} S{Labour-like pains.} S{Movement in abdomen like the fist of a child.}


:rubric=13. Stool and Anus.
S{Tenesmus.} S{Slightly thin stool, brown, offensive with tenesmus and heat at anus after stool.} S{Sensation in rectum as if full of gas, not B[>] by passing flatus.}


:rubric=16. Female Sexual Organs.
S{Labour-like pains in sacro-iliac joints, worse r. side, running down leg intermittently.} S{Sensation as if a large cord extended from sacro-iliac synchondrosis to inguinal region, and this cord was pulled down by pelvic organs (B[<] r. side); sensation as if uterus descended and retroverted, pressing on rectum and anus, pain continuous and unbearable.} S{Soreness in uterine region with palpitation of the heart.} S{Itching at urinary meatus and orifice of vagina.}


:rubric=19. Heart.
S{When exercising, fluttering at heart, lasting about a minute, then face would get red, with sensation as if heart stopped beating and would start again very suddenly, with faint, sick feeling.} S{Pulse full, compressible, intermittent.} S{Symptoms B[>] after lying down.}


:rubric=20. Back.
S{Bruised aching in back.} S{Pain in back and wrists, gradually passing off.} S{Chills run along back at every movement.}


:rubric=21. Limbs.
S{Aching in wrists and ankles.}


:rubric=25. Skin.
S{Blotches like mosquito-bites, itching violently, on undressing scratches till the skin comes off.}

  
