:rubric=Characteristics.
S{2[H. Recorder] (Aug., 1895) reprints a paper by Dr. Bredenoll, of Erwitte, in which he gives his experience with this remedy in his own case. Following a repercussed eruption he had among a host of other troubles renal colic of great intensity, with passage of uric acid calculi. One of these was triturated in the proportion of 5 parts to 95 of sugar of milk, and of this he took 1/2 grain doses for a considerable time, with the result that the formation of calculi ceased, the tartar which had formed on the teeth dropped off, and a nodosity of stony hardness disappeared from the extensor tendon of the right middle finger.}

  
