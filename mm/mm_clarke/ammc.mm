GT{Gum-ammoniac, obtained from Dorema ammoniacum. N. O. Umbelliferæ. Trituration of the gum}


:rubric=Clinical.
S{Appendicitis. Asthenopia. Asthma. Bronchitis. Eyes, affections of. Glands, affections of. Heart, affections of. Hydrocele. Panaritium}


:rubric=Characteristics.
S{Ill-humour is the prevailing mental feature of R[Ammc] It acts prominently on mucous membranes, causing first diminished and then increased secretion. There are stars and fiery points before the sight. Sees smoke in circles. Dim sight from a blow on the head. Dim sight from reading. Hydrocele. Heart beat too strong, 2[<] lying on left side. Great accumulations of mucus. Stitches are prominent in the provings. Stitches in the cæcum at 7 p.m., alternating with pains elsewhere. This should make it appropriate in some cases of appendicitis. There are many rheumatic pains in the limbs and loins. There is marked sensitiveness to cold and 2[<] in cold weather. It is suited to bronchial affections in old people brought on by cold weather}


:rubric=Relations.
S{2[Compare:] R[Asaf], R[Con], R[Cic] (botanical relations); R[Ambr], R[Aur]; R[Arn] (effects of blow); R[Puls] (2[<] lying on left side); R[Bell] and R[Ruta] (eyes). 2[Antidoted by:] R[Arn], R[Bry].}


:head=SYMPTOMS.


:rubric=1. Mind.
S{Temper sombre and taciturn; repugnance to everything; ill-humour, which induces discontent with everything that is done; incapacity for mental application}


:rubric=2. Head.
S{Head confused, with incapability of mental labour; heaviness in the head.} S{Violent pains in the head.} S{A semi-lateral pressure in the forehead; pressure over the eyebrows.} S{A sense of confusion in the forehead, with dimness of sight; tearing sensation in the right side.} S{Pressure in the occipital region.} S{Pricking in the scalp.} S{Sensation of swelling at the occiput.} S{Itching, enforcing scratching of the parts affected, and tingling; pimples at the roots of the hair}


:rubric=3. Eyes.
S{A sense of pressure, with tickling at the upper part of the globe of the eye; throbbing and tingling at the upper part of l. eye, followed by a sense of pressure.} S{Feeling of dryness in the eyes, and as of some foreign body under the upper lid of the l. eye.} S{Stars and fiery points floating before sight.} S{Sees smoke before him (amblyopia from a blow).} S{Dimness of sight at night, and chiefly in the morning, immediately on rising, with burning heat in the eyes.} S{Reading impracticable, at least without considerable effort.} S{Photophobia, although the weather may be cloudy}


:rubric=4. Ears.
S{Tearing sensation at the exterior of r. auditory passage.} S{Gurgling in l. ear, on scratching the occiput. Roaring in the ears, with difficulty in hearing}


:rubric=5. Nose.
S{Painful dryness in the nose on rising in the morning.} S{Increase of nasal mucus.} S{Sneezing, followed by an abundant flow of mucus from the nose.} S{Constant motion of alæ nasi}


:rubric=6. Face.
S{Paleness of the face, with a sensation of uneasiness, preceded by frequent change of colour.} S{Drawing sensation in l. cheek-bone, towards the temple.} S{Throbbing in the inferior maxillary region, extending into the mouth}


:rubric=8, 9. Mouth and Throat.
S{Dryness of the mouth and throat in the morning, on awaking.} S{Sensation of fulness in the back of the throat and gullet, accompanied by nausea.} S{Sensation, as of a foreign body in the throat, which incites efforts to swallow it.} S{Scraping and burning in the gullet and œsophagus}


:rubric=11. Stomach.
S{Frequent risings.} S{Nausea, with inclination to vomit.} S{Taste clammy, insipid, or sweetish in the morning, bitter in the gullet, with loss of taste at end of tongue}


:rubric=12. Abdomen.
S{Borborygmi, with sensation of confusion in the forehead.} S{Pinching pains.} S{Violent abdominal pains.} S{Painful pressure in the region of the pubis}


:rubric=13. Stool and Anus.
S{Stools scanty; none until evening, or during two or three days.} S{Stool relaxed, of the consistence of pap; like pap after a meal.} S{Soft, with much wind, and preceded by borborygmi in the belly.} S{Mucous stools, sometimes preceded by shivering, and colicky pains which occasion writhing.} S{Liquid stools.} S{Diarrhœa, with mucous evacuations.} S{Pressure on the rectum}


:rubric=14. Urinary Organs.
S{Urine profuse, containing much lactate of urea.} S{Burning in the urethra.} S{Passing drops of urine, after having micturated}


:rubric=15. Male Sexual Organs.
S{Shooting pains in the right spermatic cord, and in its immediate neighbourhood.} S{Drawings internally.} S{Shooting pains at the root of the penis.} S{Itching in the pubic region.} S{Hydrocele}


:rubric=17. Respiratory Organs.
S{Roughness of the orifice of the throat.} S{Constant tickling, without inclination to cough.} S{Bronchial affections of old people in cold weather}


:rubric=18. Chest.
S{Respiration short, with heaving of the chest, and anxiety; quickened.} S{During inspiration, a sense of oppression, with lancinating pains in the l. side of the chest.} S{Pressure in the side.} S{Great uneasiness at the bottom of the chest, towards the back, on r. side, followed by pressure in the deepest part of the chest}


:rubric=20. Back.
S{Weight and pressure in the lumbar vertebræ.} S{Darting pains in the loins, chiefly during inspiration.} S{Rheumatic pains at the l. of the pelvis}


:rubric=22. Upper Limbs.
S{Shooting and tearing pains in the shoulder-joint.} S{Sense of weakness in l. arm.} S{Rheumatic pain.} S{A sensation of breaking, chiefly in the elbow and l. wrist; sometimes with a tearing pain in r. hand.} S{Swelling of the fingers.} S{Dragging sensation in the middle finger of r. hand.} S{Rheumatic pain in the forefinger of the r. hand.} S{Panaritium}


:rubric=23. Lower Limbs.
S{Darting pains about the hips while walking, which induce limping; also in r. hip joint, on being seated.} S{Weakness in the legs.} S{Jerking pain in l. thigh, along the crural nerve.} S{Violent pain above the knee while walking.} S{Sensation of swelling in the knee, at night, on being seated, with pinching pains in the popliteal space.} S{Shooting pains in r. knee.} S{Stiffness of the joint while walking.} S{Darting and pressive pains in the l. tibia.} S{Tearing pains in the joints of the foot.} S{Sense of pressure above r. ankle.} S{Cracking of the joints on moving.} S{Tearing, burning pains, in the metatarsal bones.} S{Heaviness in r. foot.} S{A tendency to stumble on going up stairs.} S{Pinching, burning, tearing, and drawing in l. foot.} S{Tingling in r. foot.} S{Swelling of the toes.} S{Burning and shooting pains in the great toe.} S{Drawing in the middle toe}


:rubric=24. Generalities.
S{Swelling of the joints of the upper and lower extremities.} S{Neuralgia.} S{Lassitude after the least exertion.} S{Physical dulness and lassitude, with mental depression.} S{Inclination to sleep during the day, sometimes even on rising in the morning, with listlessness; yawning from emptiness of stomach, sometimes attended by weeping; sleep unquiet during the night, disturbed by dreams, unrefreshing; sleep delayed; numerous and sometimes painful dreams.} S{Coldness and shivering, tendency to perspire.} S{Pulse small, wiry, rapid, and hard}


