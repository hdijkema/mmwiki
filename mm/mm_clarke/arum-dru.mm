S{Nettle-rash. Pricking pains.}

:rubric=Characteristics.
R[Arum-dru] is commonly cultivated in gardens. It has a fetid smell which is apt to cause headache. The following effects of pounding the root and tearing up leaves and stems have been observed:

S{Styptic metallic taste at back of mouth.}
S{Pricking, mingled with intolerable itching in fingers, as if hand plunged among nettles.}
S{On rubbing the fingers together, sensation as if a vast number of stings were driven perpendicularly into them.}

 
