:rubric=Characteristics.
S{The B[Centaureas] are allied to the Thistles. Dr. Chagon, Duc de Sorentino, experimented with the root of R[Cent] The symptoms of congestion are very marked: "Blood surges pain fully at every movement." A bright servant-girl who took it seemed stupefied for several days. "The earliest symptoms appeared a quarter of an hour after taking the medicine, viz., those of the arms together with dull pains in the hypogastrium, shiverings and general heat, especially on forehead, which was covered with sweat. These were followed by a stupefying pain in temples, frontal protuberances, orbital arches and eyes. Next came the following symptoms: Dimness of sight; appetite alternating with nausea; increase of saliva and vomiting; running of the nose; lachrymation; eructations; rapid sinking of strength; general uneasiness; confusion of mind; weariness; dull sunken eyes spasmodic sweating; itching, and pricking in the skin; drowsiness and thirst. A state of reaction, after four or six hours brought some quiet. Next day, in the forenoon or towards evening, the above symptoms reappeared but with less violence, and some of them assumed a periodic form. Then ensued coryza, colic, diarrhoea, fevers of various types, pains in the arms, dry cough, etc. The action ended with the symptoms of catarrh, of angina, and of fever in the forenoon, and with confusion and pain in the forehead" (Allen). The above gives a general idea of the action of this remedy. The symptoms are B[<] at night; in the morning on waking; by movement; during and after urinating. B[>] By eating.}


