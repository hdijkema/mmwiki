:rubric=Clinical.
S{Bright's disease. Dysmenorrhoea.}


:rubric=Characteristics.
S{R[Brachyglottis] was proved by C. L. Fischer. The Maories use the leaves to apply to old sores and ulcers. Horses eating young shrubs lose all power over hind legs and spine. It causes lassitude; weakness; loss of flesh. A large number of symptoms were produced in all regions of body, but few have been tested clinically. This appears to be the most marked: Urgency in passing urine; pain in neck of bladder; pain in bladder after urinating, and in urethra and stinging in penis; a large quantity of pale-coloured urine voided, of low specific gravity and containing albumen. Throbbing pains are very prominent in the proving. Dysmenorrhoea with fluttering in abdomen and right ovary has been cured with it. Pains in the back and limbs. Chilliness predominates.}


