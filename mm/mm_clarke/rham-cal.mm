:rubric=Characteristics.
S{I mention this well-known purgative because it has been found empirically curative in cases of subacute and chronic rheumatism with constipation. Pathogenetic effects on patients taking it for constipation have been recorded: Vomiting, severe griping, haemorrhage; in one case temporary insanity (from a home-made decoction of the bark). In two cases choleraic symptoms were produced. Only material doses have been used.}

  
