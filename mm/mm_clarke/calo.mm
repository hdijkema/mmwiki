GT{Calotropis gigantea. (E. India.) The root-bark is called Mudar, Mirdar, Yercum. N. O. Asclepiadaceae. Tincture or trituration of bark of root. Tincture of the milky juice.}


:rubric=Clinical.
S{Feet, pain in. Fever. Hands, pains in. Leprosy, tubercular. Lupus. Syphilis.}


:rubric=Characteristics.
S{The inspissated juice, root and bark have been extensively used in the East for their emetic, sudorific, alterative, and purgative qualities. The fresh juice is considered a valuable remedy in skin affections. A short proving brought out the following symptoms: Weak, tired feeling; coldness; dizziness; pain and confusion in head; nausea and vomiting of bile; frequent micturition; pain, soreness, and redness of thigh pain in both legs, swelling of knees, stiffness and inability to walk pains in hands and feet; first left then right, pains B[<] movement or putting weight on them. Dr. Gramm, of Philadelphia (I[Minn. H. Mag.], Aug., 1897), has used the tincture with great success in cases of syphilis when Mercury can no longer be given; in the anaemia of syphilis; secondary syphilis. A sense of warmth in the scrobiculus cordis is a leading indication for the remedy.} S{In one case of severe and advanced syphilis, with enormous thickening of the ends of all the nails, R[calo]. effected a cure. The remedy was given in one- to five-drop doses of the tincture, or in pellets saturated with equal parts of the tincture and 95 per cent. alcohol.}


