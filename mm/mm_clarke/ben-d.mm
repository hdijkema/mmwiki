GT{Di-Nitrobenzol. C6 H4 (NO2)2. A crystalline substance obtained from the further action of Nitric Acid on Nitrobenzol at higher temperatures. Solution in alcohol.}


:rubric=Clinical.
S{Amblyopia. Anaemia. Colour-blindness. Impotence. Peripheral neuritis. Retinitis. Spastic paralysis. Urine, black.}


:rubric=Characteristics.
S{R[Ben-d] is used in the manufacture of the higher explosives. The effects on workpeople have been recorded by Simeon Snell in the 2[British Medical Journal] of March 3, 1894. The symptoms observed strongly resemble those of 2[R[Benz-n]], viz.: Headache, giddiness, staggering; numbness and paralytic sensations. But the following:} S{Anaemia, with a murmur in the pulmonary artery. Impotence observed in men (in women there was no-alteration in menstrual functions); distaste for tobacco} S{are not mentioned under 2[R[Benz-n]], but were marked in those under the influence of R[Ben-d]: The knee-jerks were, if anything, somewhat exaggerated. Dyspnoea was marked in all, accounting partly for the inability to smoke. In the eyes the symptoms were very marked, and were very carefully observed. Ordinary vision was greatly impaired; the field of vision contracted, and partial colour-blindness, "a small central scotoma for red and green." The retinal veins were found much engorged; probably part of the general venous engorgement and cyanosis. The blood is altered and turned black, and the urine is black without containing blood.}


