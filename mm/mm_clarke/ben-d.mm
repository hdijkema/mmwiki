:head=SYMPTOMS.


:rubric=1. Mind.
S{Raving and unconscious from 5 p.m. to 10 p.m.; the doctor gave him up, but he recovered.} S{Delirious, complaining of great pain in head.}


:rubric=2. Head.
S{Giddiness, compelling to sit down.} S{Reels like one drunk; even fell off a stool when sitting; stumbled getting into a trap.} S{Could not walk many yards without falling.} S{Occipital headache; unable to work on account of headache.} S{Frontal headache with delirium.} S{Colour of hair altered from golden to a sort of red.}


:rubric=3. Eyes.
S{Blindness.} S{Colour-blindness, "central scotoma for red and green.".} S{Disc pale; greyish; edges defined; retinal vessels full, esp. the veins.} S{Retinal hyperaesthesia.} S{Veins much larger than the arteries.} S{Conjunctivae jaundiced.}


:rubric=6. Face.
S{Face pale, lips blue.} S{Face bluish, asphyxiated look.}


:rubric=11. Stomach.
S{Occasional nausea and vomiting.} S{Intolerance of alcohol, "it made him feel so bad."}


:rubric=14. Urinary Organs.
S{Urine dark, like ink; sp. gr. 1,029.}


:rubric=15. Male Sexual Organs.
S{Sexual desire lost, power diminished (restored when taking liq. strychniae after leaving off the work).}


:rubric=17. Respiratory Organs.
S{Breath very short; rendering smoking very difficult.} S{Short breath with giddiness.} S{Asphyxia.}


:rubric=19. Heart.
S{Engorgement of venous system.} S{Bruit in pulmonary artery.} S{Pulse very frequent (115), small, and compressible.} S{Anaemia.} S{Blood black.}


:rubric=21. Limbs.
S{Want of sensation in arms and legs; prickly feeling.} S{Legs numb to knees and arms to elbows.} S{Stiffness about hands and feet, but esp. the fingers.} S{Hands and feet cold; feet numb and also sore.} S{Finger-tips feel cold to the touch, though she herself is not aware of it.}

  
