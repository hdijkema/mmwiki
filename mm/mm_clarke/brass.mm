:rubric=25. Skin.
S{Sallow and muddy-looking; covered with downy hair.} S{General oedema.} S{Blotches, deep red, like burns, on backs of hands and feet and nose and forehead, varying from simple discoloration to most troublesome ulceration, causing destruction of cuticle and dropping off of nails, with a marked disposition in the aggravated cases to gangrene.}

  
