:rubric=Characteristics.
S{Our knowledge of the pathogenetic effects of 2[Brassica napus] is derived from the experience of the Irish famine, during which the people ate it freely. Dropsical swellings, scorbutic mouth, voracious appetite, tympanitic abdomen, blotches like burns, dropping off of nails, and gangrene. All the symptoms of impoverished blood appear, such as growth of downy, colourless hair.}


