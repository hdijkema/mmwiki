:rubric=Characteristics.
S{R[Berb-a] belongs to the Mahonia group of the Barberry family. It has been used by eclectics in chronic syphilis, chronic skin diseases, in scrofulous cachexia, and for removing pimples from the faces of girls. Hale reports a case of very advanced secondary syphilis cured with it, and some characteristic cases of psoriasis. In all these conditions it has been given in the Ã˜ tincture 5 to 10 drops. J. D. W. C., in 2[Recorder], 1896, gives personal experiences with doses of 10 to 15 drops a day. On the third day felt "nausea and thick-headedness." Later there was "straining to vomit without vomiting." Then a peculiar headache, strong compression, as if a band of iron two inches wide passed entirely round head just above ears. It kept growing tighter and tighter. Strong black coffee relieved this after a time, but left him tremulous in nerves and unsteady in gait. Ever after that a single drop of R[Berb-a] on the tongue was sufficient to avert an attack of biliousness when threatening, and saved him much trouble of the kind to which he had formerly been liable. R[Berb-a] has a reputation for making "a new man of an old one." A tea made of the leaves of R[Berb. mahon.] has a local reputation in the mountain typhoid of the Rockies, and the provings of R[Berb-a] show febrile symptoms and a marked action on the spleen. A systematic proving was made under Winterburn's direction. I have verified its usefulness in secondary, syphilis.}


