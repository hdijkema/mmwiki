GT{Artemisia vulgaris, Linn. Wormwood. Mugwort. Beifuss. N. O. Compositae. Tincture of fresh root.}

:rubric=Clinical.
S{Catalepsy. Chorea. Convulsions. Dysmenia. Epilepsy. Hydrocephalus. Hysteria. 2[Somnambulism.] Worms.}

:rubric=Characteristics.
S{Like the other Artemisias this remedy has a prominent place in convulsive diseases. It is indicated where the patient is excitable and irritable before attack of epilepsy. Epilepsy after fright or grief; after a blow on the head; with menstrual disturbances; with teething. Attack accompanied or followed by profuse offensive sweat; by seminal ejaculations. Profuse sweat, having a peculiar fetid, cadaverous or garlic-like odour is a characteristic of the remedy. Attacks frequently repeated, then followed by a long interval of rest. Somnambulism. Catalepsy after fright. Spasms. Chorea; swallowing difficult, hungry but cannot get the food down. Right side convulsed, left paralysed. Nocturnal pollutions. Violent cramps in abdomen with irregular, insufficient menstruation. Irregular or deficient menstruation with epileptic convulsions. Nervous chlorosis with a very dry skin. Worms.}

