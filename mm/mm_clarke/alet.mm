:rubric=Characteristics.
S{R[Alet] is said to be the most bitter of all plants. It is characterised by weariness of body and mind. Want of appetite, especially in connection with Uterine disorders. The least food distends the stomach. There are fainting attacks with vertigo. Emaciation is marked in some cases. Disgust for food, nausea; constipation. Obstinate vomiting of pregnancy. Many symptoms appear in the uterine sphere: uterine atony, menses profuse and premature with labour-like pains or amenorrhœa, debility, from loss of fluids, protracted illness. Menorrhagia, black clots, fulness and weight. Leucorrhœa, with debility. Sterility. Habitual tendency to abortion. Sensation of weight in uterine region and tendency to prolapse. Sensation of weight in occiput as if it would draw the head back. It is most suited for chlorotic girls and pregnant women. Weak, emaciated people}


