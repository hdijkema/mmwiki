S{2[Cholesterine] is a substance crystallising in leaflets with a mother-of-pearl lustre and a fatty feel. It is soluble in alcohol and ether. It occurs in the blood and brain, yolk of eggs, seeds and buds of plants, but most abundantly in the bile and biliary calculi. Ameke, who did much to introduce the proximate principles of the tissues as remedies, anticipating the practice now so much in vogue in the old school, recommended 2[Cholesterine] as a remedy in cancer of the liver. Burnett has recently adduced conclusive evidence in support of the correctness of this assertion; and I have myself cured, mainly with this, a case described to me (I did not see the patient, a man over 50) as in the last stage of liver disease. He had been given up by his medical attendant, who ordered him to make his will without delay. Burnett uses the 3x or the 3 trit. and substantial doses. He commends it in "obstinate hepatic engorgements, which by reason of their obstinacy make one think interrogatively of cancer," also in "cases in which there appears to be a semi-malignant affection, involving the left lobe of the liver and what lies between it and the pylorus and the pancreas." In such cases Burnett gives alternately R[chol] 3x and R[iodof] 3x. It has been used with success in the removal of opacities of the vitreous.}

  
