:rubric=Clinical.
S{Dyspepsia. Intermittent fever. Pneumonia.}


:rubric=Characteristics.
S{Hale commends R[Corn-f] in obstinate intermittents where quinine has been abused and where the following group of symptoms is present: For days before chill sleepiness; sluggish flow of ideas; dull, heavy headache. Paroxysms attended with nausea; vomiting and sometimes watery or bilious diarrhoea. In the chill, cold, clammy skin; in the fever, violent headache, with throbbing, stupor, confusion of intellect and vomiting. He also commends it in old cases of dyspepsia where the chief symptom is acid pyrosis. R[Corn-f], which has had a separate proving, has some very remarkable neuralgic pains in arms, chest, and trunk, and a sensation as if broken in two. It has been used successfully in a case of pneumonia on the indication of the stitches in the chest.}


