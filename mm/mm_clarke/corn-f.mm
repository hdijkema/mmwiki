:rubric=18. Chest.
S{Stitches in region of l. clavicle extending towards r. side, B[<] by taking deep breath; constant tickling in chest compelling him to cough, with difficult expectoration; constant dizziness; chilliness, followed by heat with thirst and finally sweat; drinks little and often; hunger soon after eating (inflammation of the lungs).} S{Stitch in r. chest as from a knife thrust, with vertigo.} S{Ineffectual urging to stool, followed by cutting pains in ribs, l. side, with pain under l. scapula as if a piece of flesh were being forcibly twisted out; later, copious stool.}


