S{Anaesthesia. Neuralgia.}


:rubric=Characteristics.
R[Croton chloral] received its name from the supposition that it contained Crotonic acid (C4 H6 O2). According to Hale it is said to possess the singular property of producing anaesthesia of the brain and nerves of the head without affecting the sensibility of the rest of the body. It has been given allopathically in substantial doses in cases of trifacial neuralgia. The patients who were helped by it had carious teeth and some of them anaemia. In two cases the pains were aggravated by it. In some it caused sleep; others were eased but did not sleep.

  
