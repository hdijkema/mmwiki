:begin[en]
GT{Benzoate of Ammonia. NH4 C7 H5 O2. Solution in distilled water}


:rubric=Clinical.
S{Albuminuria. Dropsy. Gout. Indigestion. Ranula. Rheumatism}


:rubric=Characteristics.
S{The chief symptoms of this salt (which has been proved) are: Head heavy, stupid. Urine scanty; smoky. Pight kidney sensitive to pressure. Gout with fluid in great toe-joint or with lithates. The right side is most affected}


:rubric=Relations.
S{It is 2[similar] in its action to the Ammonias and to R[Benz-ac]. Also to R[Gnaph] (gouty concretions), R[Caust], R[Ter], R[Urt-u]}


:head=SYMPTOMS.


:rubric=3. Eyes.
S{Feeling of swelling and soreness, apparently in the puncta and saccus lachrymalis.} S{Lids swollen}


:rubric=8. Tongue.
S{Swelling under tongue, r. side, like ranula}


:rubric=11. Stomach.
S{Eructation of food without acidity, with apparent weakness of digestion, or acid eructations.} S{Increased warmth in stomach}


:rubric=14. Urinary Organs.
S{Sensation of internal soreness on pressure in leaning the back against the cushioned back of a carriage in region of r. kidney.} S{Bloated face, eyelids swollen, head heavy, stupid; urine scanty smoky.} S{(Albuminuria.).} S{Urine smoky-looking, scanty, strong-smelling}


:rubric=17. Respiratory Organs.
S{Increased mucus in larynx causes frequent hawking and spitting}


:rubric=20. Back.
S{Pains across sacrum, with urgency to stool}


:end