S{Epilepsy. Heart, affections of. Migraine. Night terrors.}


:rubric=Characteristics.
This is an unproved salt of gold. It has been recommended by Dr. E. M. Hale as being superior to other preparations of gold, S{especially in certain nervous affections of an epileptiform character, as migraine, night terrors, somnambulism}. 

S{Also in valvular diseases of the heart and hypertrophy, attacks of faintness with coldness and feeble pulse alternating with attacks of congestion and red face with palpitation.}

  
