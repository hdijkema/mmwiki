:rubric=Clinical.
S{Asthma. Coma. Diphtheria. Epilepsy. Headache. Syncope. Tetanus. Urticaria}


:rubric=Characteristics.
S{The symptoms of R[Amyg] are not very distinguishable from those of R[Hydr-ac], but these may be taken as indicating its use: An excessively brilliant appearance of the eyes without corresponding mental expression. Heaviness in forehead (also R[Laur]). Dulness of l. half of the head (R[Hydr-ac], right). Head drawn backward. Eyes drawn to left side. Burning heat in laringo-pharyngeal region. Lancinating pains through tonsils (Farrington). Palate end fauces dark reel. Swallowing impossible. Vomiting of undigested food and bile. Convulsive and at intervals very short respiration, with fear of suffocation.} S{Jerking, hesitating speech: "What's} S{the mat} S{matter with me?" Cough with soreness of chest extending down to stomach. Stitches under left nipple, going deep in, making exhalations more difficult. Opisthotonos. Snoring. Surface cold, clammy, pallid or blue. Urticaria}


