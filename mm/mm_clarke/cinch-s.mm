GT{Sulphate of Cinchonine. (C19H22N2O) H2SO4.}


:rubric=Clinical.
S{Amblyopia. Constipation. Diarrhoea. Epistaxis. Fever. Hair falling off. Headache. Heartburn. Sexual excitement. Spinal irritation. Urinary disorders. Veins distended.}


:rubric=Characteristics.
S{R[cinch-s]. has been extensively proved, but it appears to have been little used in practice, from having been overshadowed by the better known alkaloid, R[chin-s]. The general outline of symptoms is the same in both. Some of the most peculiar symptoms of R[cinch-s]. are the following: Bursting headache on waking. Pain in anterior lobe of cerebrum, as though a net were drawn through the whole substance of the brain. Darkness before the eyes on exerting them. Sensation as if the throat were burnt with hot drink. Cold feeling at pit of stomach. Pasty taste; and pasty stools. The sexual organs are excited: voluptuous dreams and painful erections at night. Menses a week too soon and more copious than usual. The chest seems hollow and distended so that breathing seems especially easy. Many symptoms of fever are present; and there is recurrence of symptoms. Sweat between shoulder-blades in the night, followed by itching. Symptoms B[<] after eating; by motion. Weariness, sleepiness, general aching; especially in eyes, ears, and genitals.}


:head=SYMPTOMS.


:rubric=1. Mind.
S{Sort of transient intoxication; complete disinclination for work.}


:rubric=2. Head.
S{Confusion in head with pressure in eyes.} S{Morning headache; bursting headache on waking.} S{Pain in anterior lobe of cerebrum as though a net were drawn through the whole substance of the brain.} S{Profuse falling out of hair.} S{Tension in scalp, with soreness at roots of hair, as if there were pus under scalp (mostly r.).}


:rubric=3. Eyes.
S{Accumulation of mucus before eyes.} S{Attacks of darkness before eyes; on exerting them.}


:rubric=4. Ears.
S{Roaring in ears; during the night.}


:rubric=5. Nose.
S{Sudden nose-bleed of thin, bright red blood while sitting.}


:rubric=8. Mouth.
S{Ptyalism; esp. in forenoon.}


:rubric=9. Throat.
S{Sensation as if throat were burnt with hot drink, and something were sticking in pharynx.} S{Sticking pains in throat, as with fine needles, on swallowing.}


:rubric=11. Stomach.
S{Sudden heartburn.} S{Pains at pit of stomach B[<] by pressure.} S{Great heat in stomach; extending into abdomen, and up into chest, throat, and head, esp. in throat, with burning thirst.} S{Cold feeling at stomach; fulness; pressure; throbbing; sensitiveness.}


:rubric=13. Stool and Anus.
S{Dull cutting in anus, extending thence to navel, with fine stitches in pit of stomach.} S{Pasty diarrhoea.} S{Constipation; hard, scanty stools with tenesmus.} S{Pasty stool passed slowly with much pressing, sphincter very much relaxed, while the rectum is more contracted than usual.}


:rubric=14. Urinary Organs.
S{Burning sensation in urethra after passage of urine.} S{Excessive urging.} S{Urine thick sediment; brownish green; dark red; reddish yellow; white.} S{Iridescent film.}


:rubric=15. Male Sexual Organs.
S{Great excitement, erections.}


:rubric=16. Female Sexual Organs.
S{Menstruation a week too soon and much worse than usual.}


:rubric=18. Chest.
S{Chest seems hollow and distended so that breathing seems esp. easy.} S{Stitches mostly extending from r. side to pit of stomach at insertion of diaphragm.} S{Pressive pain under sternum; waking in night it seems as though lower portion of sternum were pressed inward.}


:rubric=20, 21. Neck, Back and Limbs.
S{Painful stiffness of neck; B[<] moving it.} S{First dorsal vertebrae painful to pressure.} S{Trembling of limbs and weakness.} S{Pains, drawings, bruised sensation.}


:rubric=24. Generalities.
S{Periodic sticking pains in chest, B[>] standing or sitting, B[<] lying or during deep cough.}


:rubric=26. Sleep.
S{Sleepiness.} S{Quiet sleep without dreams, but without being refreshed.} S{Voluptuous dreams and painful erections.}


:rubric=27. Fever.
S{Chilliness.} S{Shaking chill 10 a.m. to noon without subsequent fever or sweat.} S{Burning skin.} S{Great heat of body with turgescence of veins (evening).} S{Heat of head 4 p.m. with cold extremities.} S{Sweat with excessive thirst in night.} S{Skin dry, veins distended.} S{Sweat between scapulae at night, followed by itching compelling him to scratch.}

  
