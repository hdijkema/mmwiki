:rubric=Clinical.
S{Back, pains in. Coccygodynia. Corns. Feet (pains in heels). Hoarseness. Jaw, affection of. Malar bones, pain in. Neuralgia. Paralysis. Rheumatism. Spleen, affections of. Stiff-neck. Tapeworm. Toothache.}


:rubric=Characteristics.
S{R[Calc-caust]. has been extensively proved. The most marked symptoms are: Thought very difficult. Confusion in the head. Vertigo; as if room turning in a circle. Sticking, tearing, throbbing pains in various parts. Pain in right eye as if a foreign body were in it. Burning and tearing in eyes. Tearing in right cheekbone; and in right articulation of jaw. Toothache every night, 2 a.m., with feeling as if teeth were 2[fuzzy] and too large (a fuzzy sensation was noticed in other parts). Mucus in throat; burning; sensation as if a bone were sticking in it. Burning in stomach. Tapeworm passes. Hoarseness, with pain in throat; rawness in trachea and cough. Stitches in chest. Stiffness and tearing in all regions of back and in coccyx. Tearing in either shoulder down arms. Tearing in left tendo Achillis; left heel. Sticking in corns. As an external application in the form of compresses, Lime-water has an ancient repute in allaying inflammation of many kinds. It has rapidly dispelled all inflammatory action in cases of appendicitis; and has removed all suffering in an aggravated case of phagedaenic piles. As with R[Calc]., there is B[<] of most symptoms in the evening. Toothache 2 a.m. B[>] From motion. B[<] From hanging down limb. The R[Calc]. B[<] from working in water is paralleled by "pains in hands and feet after a foot-bath."}


:rubric=Relations.


:rubric2=Compare

S{Besides the Calcareas, R[Rhus-t] (pain in joint of jaw); R[Mez]. and R[Mag-c]. (pain in malar bones); R[Valer] (pain in heel); R[Pic-ac] and R[Sep]. (loins); R[Hep]. and R[Nit-ac]. (bone sensation in throat).}


:head=SYMPTOMS.


:rubric=1. Mind.
S{Thought very difficult.} S{Weary and peevish on waking; as if he had been intoxicated.}


:rubric=2. Head.
S{Confusion of the head; esp. l. side.} S{Vertigo, as if room turning in a circle; thinks she will fall from chair.} S{Throbbing in frontal sinus B[>] by contracting skin of forehead.}


:rubric=3. Eyes.
S{Burning in eyes, evening by candlelight.} S{Pain in r. eye as if foreign body under upper lid; in l. eye on rising in morning as if a thorn in it.}


:rubric=6. Face.
S{Tearing in r. cheek-bone.} S{Violent pain, swelling, stiffness of r. joint of Jaw.} S{Lips unusually red.}


:rubric=7. Teeth.
S{Severe toothache every night, 2 a.m., as if tooth was fuzzy and too large, with tensive pain in l. ear as if something was sticking in it.} S{Teeth feel too large; fuzzy.}


:rubric=8. Mouth.
S{Mouth offensive; palate feels as if mucus were upon it, but none is expectorated.}


:rubric=9. Throat.
S{Mucus in throat, difficult to raise, almost causing vomiting.} S{Hawking of granular, glutinous material like cooked rice.} S{Burning in fauces and oesophagus caused by water.} S{Sensation as of a bone sticking in pharynx.}


:rubric=11. Stomach.
S{Severe hunger three hours after eating.} S{Nausea followed by vomiting of some fluid.}


:rubric=12. Abdomen.
S{Sticking in both hypochondria, esp. severe in l.} S{Pain in l. side of region of spleen as if a ball were turning on its axis.} S{Constrictive pain in abdomen; griping; stitches.} S{Violent stitches in small intestines when bending forward.}


:rubric=13. Stool and Anus.
S{Three thin stools before breakfast and one immediately after.} S{Painful stool as if a nail were scratching in anus.} S{Thin, pasty stool with much mucus immediately after breakfast.} S{Passed many pieces of tapeworm.} S{Haemorrhoids.} S{Sticking, tearing motions in rectum extending towards anus.} S{Crawling in rectum and anus.}


:rubric=16. Female Sexual Organs.
S{Menses eight days too early.}


:rubric=17. Respiratory Organs.
S{Stitches in larynx; l. half.} S{Hoarseness, with pain in throat; feeling of rawness in trachea; cough.} S{Cough with stitches in chest, expectoration of mucus and blood.} S{Sternum: oppression; pressive sticking under; stitches across.} S{Stitches in l. chest.}


:rubric=20. Neck and Back.
S{Stiffness of neck; of back of neck; of all neck muscles.} S{Tearing in nape.} S{Violent pains in scapulae extending into small of back, when sitting; B[<] on inspiration.} S{Tearing between shoulders.} S{Tearing in both scapulae, between shoulders, then through neck and occiput as far as forehead.} S{Sensation in both shoulder-blades as if they were fuzzy and had gone to sleep (8 p.m.).} S{Pain in small of back; on waking B[>] by moving about.} S{Pressive, drawing pains; sticking; tearing, in small of back.} S{Tearing in coccyx.}


:rubric=21. Limbs.
S{Trembling in limbs; could not continue work; B[>] by eating a piece of bread.} S{Rheumatic pain alternating in all the joints.} S{Wandering pains.} S{Pains in hands and feet after a foot-bath.}


:rubric=22. Upper Limbs.
S{Tearing in shoulders, arms and axillae.} S{Paralysed condition of r. hand.} S{Stitches and tearing in l. wrist.}


:rubric=23. Lower Limbs.
S{Severe tension in lower extremities.} S{Stitches in hip-joints.} S{Tearing in knees; in r. side of r. tibia down to toes; in l. tendo Achillis; in l. heel.} S{Sticking in a corn.}


:rubric=25. Skin.
S{Rash-like eruption on forehead.} S{Biting, itching vesicles; on neck and back extending later to occiput and behind ears.}


:rubric=26. Sleep.
S{Restless; many dreams; nightmare.}


:rubric=27. Fever.
S{Violent chill with chattering teeth 7 to 7.30 p.m.}

  
