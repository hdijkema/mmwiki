:rubric=Characteristics.
S{This is a rheumatic remedy like the other Actæa, but affecting more prominently the small joints and wrists. I have several times cured wrist-rheumatism with it. The joints swell after slight fatigue. The pains are tearing, drawing. The right arm and right wrist are specially affected; and there are pains like rheumatism in face. Pains are of a violent, tearing, drawing character, 2[< ]by touch or movement. Pains of this kind running from decayed tooth to temples. Swelling of the joints from slight fatigue may be regarded as very characteristic. It is especially suited to men (Act. r. to women). 2[< ]From motion; from change of temperature; cold air}


