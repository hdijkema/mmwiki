:rubric=Clinical.
S{Urticaria.}


:rubric=Characteristics.
S{Contact with the caterpillars or the emanations from a nest of them causes intense irritation, and hard, large, areola-formed tubercles, with a red areola, so thick as to leave hardly any space between. Sometimes they are linear-formed; most marked near joints. Sensation as if a foreign body were under the skin. Itching of whole body. Itching, evenings; not relieved by anything. In one victim it caused frequent waking at night, and dreams that his arms were being cauterised, and arrows thrust into the muscles. Burning heat of skin. In one case, that of a boy who shook a large number of the caterpillars from a tree into his naked breast, it caused an itching so severe that the boy had to run home for assistance; then fever, somnolency, delirium, and finally death ensued.}


