:rubric=Clinical.
S{Epilepsy. Tetanus.}


:rubric=Characteristics.
S{The effects of the R[cic-m] are very like those of the I[Virosa], the most prominent symptom being: Falls unconscious, tetanic or clonic convulsions. Body covered with sweat. For comparisons see R[cic].}


:head=SYMPTOMS.


:rubric=1. Mind.
S{Fell on their way, and were picked up unconscious.}


:rubric=2. Head.
S{Dizziness.} S{Head hot.}


:rubric=3. Eyes.
S{Astonishing mobility of eyeballs and eyelids with widely dilated pupils.}


:rubric=6. Face.
S{Face bloated like that of a drowned person, deathly pale.} S{Face livid, even purple.} S{Jaws rigid.}


:rubric=8. Mouth.
S{Bloody foam from nose and mouth.}


:rubric=9. Throat.
S{Spasm of muscles of deglutition on presenting anything to mouth.}


:rubric=11. Stomach.
S{Nausea and vomiting; frothy, glairy fluid.}


:rubric=12, 13. Abdomen and Stool.
S{Pain in bowels; ineffectual desire for Stool.}


:rubric=20, 21. Back and Limbs.
S{Falls backward in convulsions.} S{Every muscle in the body in powerful clonic spasm.} S{Body and extremities cold.}


:rubric=26. Sleep.
S{During intervals of vomiting lay in deep sleep.}


:rubric=27. Fever.
S{Head hot.} S{Profuse sweat over whole body.}

  
