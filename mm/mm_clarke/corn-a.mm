:rubric=Clinical.
S{Eczema. Intermittent fever.}


:rubric=Characteristics.
S{F. H. Lutze communicated to 2[H. Recorder], (x. 501) a Long Island farmer's cure for eczema with a decoction of the leaves of this tree. His method was to give three times a day a wineglassful for three days. Then for the next three days apply it locally or wash the part with it; then administer it internally for another three days, and so on. He cured, thus, all his cases in eighteen days. This was the first acquaintance Lutze had with the remedy. Later on he made a proving (2[H. R.], xi. 346) on himself and another, eliciting symptoms of fever, restlessness, and sleeplessness, skin eruptions, and emaciation. A striking symptom was a cold feeling in chest as if full of ice. Symptoms B[>] after walking in open air; after supper.}


:rubric=Relations.
S{In the skin effects it compares more closely with R[Corn]. In fever with drowsiness and gastro-enteric disturbance it is practically identical with the other Cornels. All three have greatly disturbed sleep.}


