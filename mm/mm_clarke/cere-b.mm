:rubric=Characteristics.
S{The principal symptoms of R[Cere-b] are in connection with the heart, as in the case of R[Cact], with which it must he compared. The symptoms are B[<] at night; B[<] from pressure of clothing, B[>] undressing. R[Cere-b] was proved by John H. Fitch in substantial doses (2[H. P.], xii. 533). The prover lost 7 1/2 lbs. in weight. He felt weighed down by an incubus. When mental symptoms subsided, bodily symptoms increased and 2[vice versa]. Could not bear contact of clothes; felt better on undressing. Among the marked symptoms were: Wants to be at work; on something useful. Pains through globe of eyes and orbit. Pain through heart; in left great pectoral muscle; left side of chest. Expanding sensation in chest followed by sudden collapse. The effect on the nervous system passed off in four or five days. The pains of R[cere-b]. have a tendency to spread to other parts. Most appear on left side. "Itching in right popliteal space" lasted eight or nine years after the proving. "Itching with roughness of the skin like eczema above left knee anteriorly." "Itching, with eruption like herpes below left scapula remained permanent." Fitch (who regards R[cere-b]. as an "antipsoric of remarkable power") cured with it: (2[1]) a case of eczema of both hands extending up to elbows. (2[2]) Deposits in urine. (2[3]) Dropsy of cardiac and renal origin. (2[4]) Left intercostal neuralgia. (2[5]) Anterior crural neuralgia. Also two cases of insanity} S{(2[6]) one in a young woman, gloomy, restless, inclined to curse, swear, and throw things at persons, if resisted; (2[7]) the other in a married woman who had spiritual delusions} S{that she had committed the unpardonable sin, etc. This came on originally after confinement.}


