GT{Cajuput oil distilled (in water) from leaves of Melaleuca leucadendron. Variety, M. cajupute minor. (Moluccas.) N. O. Myrtaceae. Solution of the oil in rectified Spirit.}


:rubric=Clinical.
S{Deafness. Diarrhoea (night). Dropsy. Epilepsy. Gout. Headache. Heartburn. Hiccough. Hysteria. Menstruation, disorders of. Oesophagus, stricture of. Paralysis. Rheumatism. Tongue, affections of; swelling of. Toothache. Urinary disorders. Vertigo.}


:rubric=Characteristics.
S{2[Cajutut] has a reputation in the old school as a local application in rheumatism, and also in cases of dysmenorrhoea. It has had a homoeopathic proving, and some very characteristic symptoms have been developed. A feeling of enlargement; too large all over; as if head as large as half a bushel. Feeling as though could not get himself together (R[Bapt].); could not get his clothes for a time, though they were quite near. As if poisoned. As if the arms were tied to the body; heavy; useless. Numb feeling all over. A measles-like eruption; intense itching. Many nervous symptoms are developed, some reflected from the female sexual organs. There is the persistent choking sensation of hysteria; nervous dyspnoea; nervous distension of the bowels. Earache and faceache; the lobe of the ear is red; alae nasi suddenly turn red; nose appears swollen. It has cured obstinate hiccough from slightest provocation, talking, laughing, eating, or any motion. Symptoms appear and disappear suddenly; come at 5 a.m.; disappear suddenly on eating. Many symptoms are B[<] at night. Smoking 2[=] desire to vomit.}


:rubric=Relations.


:rubric2=Compare

S{R[Bov]. (swollen sensations); R[Plan] (earache, toothache); R[Colch]. (gout and rheumatism); R[Acon]. and R[Bell]. (effects of checked sweat). R[Eucal], R[Eugenia caryophyllacea] (clove), R[Eug] and the R[Brazil-nut tree] belong to the same order.}


:rubric=Causation.
S{Effects of checked perspiration.}


:head=SYMPTOMS.


:rubric=1. Mind.
S{Averse to being spoken to, though likes to hear other people talk.} S{Can't bear to look inside the books he usually studies; can think of a thousand things in a minute.} S{Prefers to walk alone, slowly.} S{Feels better in the society of ladies, and can talk with them (though naturally bashful); he does not like to talk with the men.}


:rubric=2. Head.
S{Intoxicated feeling, feels as if he would stumble over his own legs.} S{Head feels enlarged; dull and heavy; in occiput.} S{Frontal headache, esp. in eyes, B[<] on bending forward.} S{Severe headache 5 a.m., with prosopalgia and stiffness of jaws.}


:rubric=3. Eyes.
S{Heavy feeling in eyes; upper lids feel as heavy and thick as leather.}


:rubric=4. Ears.
S{Lobules of ears turn red.}


:rubric=5. Nose.
S{During day alae nasi suddenly turn red, redness suddenly disappearing.} S{Nose is most prominent; looks as if large and extending out from face on looking down.}


:rubric=6. Face.
S{Face feels all puffed up.} S{Cannot hurt skin of face by pinching it.} S{Face feels rough.} S{Stiff, dry feeling in jaws, with neuralgic pains in malar bones, and severe headache all over head on waking at 5 a.m.}


:rubric=8. Mouth.
S{Tongue feels swollen; as if it filled mouth all up, making her lisp, feels scalded; looks white and rough.} S{Profuse salivation.} S{Speech thick and slow.} S{Acrid taste.}


:rubric=9. Throat.
S{Constant inclination to spit and hawk up large quantities of tough white mucus, which he could feel drawn through the nares.} S{Feels closed up.} S{Oesophagus: paralysed; swollen sensation; constricted,B[<] attempting to swallow solid food.} S{Burning in pharynx and oesophagus.} S{Persistent sensation of choking.}


:rubric=11. Stomach.
S{Burning in throat down to stomach.} S{Nausea.}


:rubric=13. Stool and Rectum.
S{Rectum paralysed.} S{Bright yellow diarrhoea B[<] night.} S{Choleraic diarrhoea from sudden check of perspiration.}


:rubric=14. Urinary Organs.
S{Urine diminished, milky; smells like cat's urine.}


:rubric=15. Male Sexual Organs.
S{Erections with great desire.} S{Erections all night, and long after rising, but without the least desire.} S{Penis soon became shrivelled up.}


:rubric=16. Female Sexual Organs.
S{Menses suspended, or diminished and attended with pain, when caused by a cold or check of perspiration.}


:rubric=17. Respiratory Organs.
S{Warmth down trachea into lungs.} S{Hoarseness.} S{Cough causing vomiting.} S{Nervous dyspnoea.} S{Sharp pain through apices of both lungs from before backward (esp. r.).}


:rubric=20. Neck.
S{All the muscles of the neck are very sensitive to pressure.}


:rubric=21. Limbs.
S{Rheumatic symptoms (Cajuput oil is a common remedy as an application to rheumatic joints).} S{Pain as if joints were enlarged.}


:rubric=22. Upper Limbs.
S{Arms feel like soaked wood hanging to one.} S{Left arm feels out of joint.}


:rubric=23. Lower Limbs.
S{Weakness in both knees.} S{Stitching pains in knees on rising in bed.}

  
