:rubric=Characteristics.
S{The chief employment of this Euphorbian has been in violent dry cough followed by bloody expectoration. Expectoration of pure blood in the morning, and dark clotted blood in the evening. Cough most violent at night. Dulness of chest on percussion; constant severe pain in chest. Progressive emaciation. A proving by several Indian physicians has brought out a number of symptoms in the alimentary tracts characterised by: burning, sense of weight at stomach, flatulence, and sputtering diarrhœa}


