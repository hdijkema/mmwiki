:rubric=Clinical.
S{Osteitis. Pyaemia.}


:rubric=Characteristics.
S{Observations made on workers in mother-of-pearl factories show that inhalation of the dust produces catarrh of the respiratory organs, and, later on, inflammation of the ends of bones. This always begins in the diaphyses, but may spread to the epiphyses. It always attacks young subjects. First there is a more or less intense pain in the bone; comes suddenly; at first continuous; later intermittent. Soon after the appearance of the pain there is fever; thirst; loss of appetite; sleeplessness; dark-coloured urine with sediment. Then swelling occurs. The swelling always develops on one or the other end of a diaphysis, never in the middle of it nor in the epiphysis. The swelling has a clear, sharp margin; it is extremely painful to the slightest touch; at first soft, elastic and slightly fluctuating, it may become as hard as a bone. Suppuration may occur. The bones that have been affected are lower jaw, scapula, humerus, radius, ulna, tibia, fibula, tarsal, and metatarsal bones. I cured with 2[R[Conch]] 3 a delicate boy who at three different times had had pain in left knee. The left inner condyle was swollen and tender. The cure was rapid and the general health much improved at the same time. 2[R[Conch]] has, no doubt, a wide range of action in affections of bones, especially when the growing ends are affected. R[calc-p]. and R[calc]. are its nearest analogues.}

  
