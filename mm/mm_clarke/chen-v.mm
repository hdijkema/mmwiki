:rubric=Characteristics.
S{R[chen-v]. has an odour of decaying fish, and contains Trimethylamene. It constantly gives off ammonia. It has been proved, and seems to affect the left side more than the right, but the following recalls the characteristic of R[chen-a]: Pain in middle of back on right side of spinal column, increasing gradually to a pressing wedge-like pain, beneath right scapula, B[<] when sitting. There is also a pain over spine of left scapula. Pressure on the left side of the head. Constipation with external piles. Nocturnal enuresis. Sensations of dulness and fulness appear in many parts. Cold saliva; cooling sensation in abdomen. There is restlessness caused by pricking pains. Sitting B[<] pain in back; this is B[>] by bending backwards. Walking B[<] stitches in spleen and pain in right knee.}


