:head=SYMPTOMS.


:rubric=1. Mind.
S{Exhausted, apathetic, listless.} S{Anxiety and excitement, with increased heart-beat.} S{Depressed.} S{Restlessness increased to anxiety; startled by every step on the stair or knock on the door.} S{Disinclination to work.} S{Irritable, depressed.} S{Weakness and stupidity.}


:rubric=2. Head.
S{Confused head, pressure, vertigo; B[<] on looking at bright objects.} S{Great thirst, noise in ears, vertigo on rising.} S{Headache recurs every morning, esp. severe over l. supra-orbital border.} S{Slight l. sided headache every morning lasting till noon.} S{Headache reappeared at same hour, with flashing before eyes.} S{Feeling of weight about head, extreme drowsiness.} S{Violent raking pains in head, only slightly B[>] by a walk.} S{Orbital pain recurred regularly 3 p.m. to 5 p.m.} S{Numbness and fatigue in head.} S{Headache B[>] by beer.}


:rubric=3. Eyes.
S{Conjunctivae injected, lids adherent; feeling as if eyeball too big for orbit as if sand in eye.} S{Fatigued by reading.} S{Eyes feel as if not open enough as if something pinched upper lids down preventing distinct vision; flickering before eyes.} S{Intense pain in and around eyes, periodic, accompanied by chills.} S{Lachrymation, photophobia, violent paroxysms of pain daily 5 p.m.} S{Ulceration of cornea from malaria, or anaemia.} S{Trachoma, with and without pannus.} S{Eyelids heavy.} S{Weight in eyes.}


:rubric=4. Ears.
S{Noise in ears with dulness of hearing.} S{Roaring in ears with itching of skin of back.} S{After dinner, sensation in ears as if stopped with cotton wool.} S{Shooting pains in r. meatus.}


:rubric=5. Nose.
S{Epistaxis.}


:rubric=6. Face.
S{Congestion of blood to head, face red and bloated, as from alcohol.} S{Neuralgia of fifth pair of nerves; agonising 3 to 6 a.m.; the pains began in teeth, spread over head and ended in nape; increased rapidly in intensity, and were only B[>] by acupuncture and cold water.}


:rubric=8. Mouth.
S{Rheumatic toothache in sound teeth, B[<] by cold, or cold food.} S{Pain in teeth recurs, B[<] by closing jaws.} S{Tongue thickly furred, appetite bad, all food disrelished.} S{Tongue furred and pale; too large for mouth.} S{Great fetor from mouth.} S{Gums sensitive, bleed from least touch.}


:rubric=11. Stomach.
S{Voracious appetite; completely satisfied by a little piece of bread.} S{Extremely sensitive to alcohol and tobacco.} S{Easily made drunk.} S{Inability to smoke (in a great smoker).} S{Half a cigar made him feel extremely ill; cold perspiration broke out; inclination to vomit.} S{Fulness in epigastrium, colicky pains in abdomen, flatulence.} S{Appetite very bad, inclination to vomit.} S{Inclination to vomit, B[>] after dinner.} S{Thirst, satisfied with a very little drink.}


:rubric=13. Stool and Anus.
S{Diarrhoea three times a day (probably caused by too new beer).} S{After dinner liquid stool.} S{Bowels irregular; every other day opened two or three times.} S{Flatulence, constipation.} S{At noon, very hard stool, transient cutting in bowels.}


:rubric=14. Urinary Organs.
S{Frequent urging, but urine not very copious; turbid, deposits sediment.} S{Constant urging; quantity markedly increased.} S{No desire to urinate, but forced himself to do so, and passed a litre of dark urine of intensely sour smell.}


:rubric=15. Male Sexual Organs.
