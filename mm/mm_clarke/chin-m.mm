:rubric=17. Respiratory Organs.
S{Fatiguing cough with rattling in bronchi until exhaustion.}


:rubric=19. Heart.
S{During dinner transient palpitation.} S{Severe palpitation after dinner.} S{Pressure, anxiety, and palpitation in afternoon.}


:rubric=24. Generalities.
S{Intolerable restlessness; driving him out of the house.} S{Extensive prostration.} S{Symptoms recurred at regular periods; returned long after drug was discontinued.}


:rubric=25. Skin.
S{Itching of skin of chest and back.}


:rubric=26. Sleep.
S{Sleep disturbed by confused dreams.} S{Anxious dreams, woke in fright.} S{Dreams of worms, lice, vermin.} S{Strange, confused dream, then great fear.} S{Sleep disturbed by pain in head and teeth.}


