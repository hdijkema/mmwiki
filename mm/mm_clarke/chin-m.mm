:rubric=Clinical.
S{Alcoholism. Eye: neuralgia of. Gastritis. Headache. Intermittents. Iritis. Keratitis. Neuralgia. Pannus. Sclerotitis. Tobacco, effects of. Trachoma.}


:rubric=Characteristics.
S{R[chin-m]. resembles 2[R[chin-ar].] very closely. An excellent proving by old-school authorities was published in I[Virchow's Archiv.], and is translated in I[C. D. P.] It has been used in eye affections, especially when accompanied by severe neuralgic intermitting pains in and around eyes, with chills; lachrymation, photophobia, ulceration of cornea of malarial origin. Intermittent fever following broncho-pneumonia; shaking chill 5 a.m. accompanied by fatiguing cough, rattling in bronchi without expectoration. In one prover it caused an exaggerated sensitiveness to alcohol and tobacco. Two glasses of beer make him tipsy. Prostration and weakness are marked in all provers and accompany the headaches and other symptoms. There is a feeling of enlargement} S{in eyeballs, in tongue, head symptoms are B[>] after a walk.}


