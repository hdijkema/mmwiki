:head=SYMPTOMS.

:rubric=2. Head.
S{Vertigo, dulness, and stupidity, with headache (l.).} S{Violent headache between the eyes; sense of constriction across the forehead.} S{Headache from suppressed perspiration, or from the retention of effete matters in the system.} S{Nervous headaches, followed by sweating or profuse urination.} S{When the drug did not cause sweating or profuse urination, it caused violent headache with quick, full pulse and nausea.} S{A feeling (after vomiting) as if some sharp instrument were thrust from one temple to the other.}


:rubric=8. Mouth.
S{Tongue covered with a white fur.}


:rubric=9. Throat.
S{Burning and tickling in the fauces, with nausea and headache.}


:rubric=10. Appetite.
S{Great appetite even a few hours after a meal.} S{Increased appetite, notwithstanding the vomiting and headache, with constipation.}


:rubric=11. Stomach.
S{Excessive nausea; with violent headache; violent vomiting and retching.} S{Severe and long-continued vomiting, leaving behind it a sensation of rawness in the stomach and a slight pain, coldness of the surface of the skin, and feeble pulse, and a feeling as if a sharp instrument thrust through from one temple to the other.}


:rubric=13. Stool and Anus.
S{Increased secretion of bile.} S{Slight inclination to evacuate the bowels; with nausea and diuresis.} S{Diarrhoea, with nausea and vomiting, with excoriation of the anus.} S{Copious stools of soft, fluid consistence, yellowish in colour, and attended with some griping pain.} S{Constipation; pain in r. side, lower limbs, loss of appetite.}


:rubric=14. Urinary Organs.
S{Ardor urinae.} S{Increased flow of urine.} S{Pale coloured urine, with light specific gravity.} S{Increase of solid matters in urine.}


:rubric=15. Male Sexual Organs.
S{Tickling sensation at the end of the penis.}


:rubric=16. Female Sexual Organs.
S{Intermittent, bearing-down, labour-like pains (during dropsy); pressing from sacrum to hypogastrium, with scanty flow.} S{Suppression of menses (during dropsy).}


:rubric=17. Respiratory Organs.
S{Increase of the bronchial secretions.} S{Burning and tickling in fauces.} S{(Influenza; catarrhal fevers; bronchitis; hay-fever.).} S{Pain in l. side of chest on deep inspiration (pleurisy).}


:rubric=19. Heart and Pulse.
S{Action of heart lessened; pulse slow.} S{Feeble pulse after vomiting.}


:rubric=20. Back.
S{Cutting and aching about lowest dorsal vertebra.}


:rubric=21. Limbs.
S{Large joints affected.}


:rubric=26. Sleep.
S{Drowsy; sleepiness; hard sleep during the night.}


:rubric=27. Fever.
S{Coldness of surface after vomiting.} S{Diaphoresis; profuse sweat.}

 
