:rubric=Characteristics.
S{The leading features of acetic acid are excessive wasting and debility; anæmia with waxy pallor of face; intense thirst; burning in throat; nausea, retching, and sour rising as met with in cases of cancer and debility. There are gnawing, ulcerative pains in stomach; pain and burning in abdomen. Profuse, exhausting diarrhœa. It has been used in diabetes. It corresponds to putrid and hectic fevers with night sweats; hæmorrhages, varicose swellings, and dropsies. As a type of vinegar effects may be mentioned the following: A plump and blooming young woman took a small glass daily to reduce size. Soon lost flesh and colour. In a month cough set in with white concocted phlegm, fever, dyspnœa, night sweats, anorexia, dropsy, diarrhœa, death. Lungs after death were found stuffed with non-suppurating tubercles. A young German, after working twelve months in a vinegar factory, was obliged to leave from indigestion, dyspnœa, and ready fatigue. It is suited to children and old people; to lax, pale, lean persons. (Equal parts of vinegar and hot water make a refreshing sponge-down for many conditions of fever, with or without perspiration.}


