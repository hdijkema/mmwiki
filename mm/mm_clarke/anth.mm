S{I[Anthem. nob.] is the common chamomile (R[Cham]) used domestically in the form of a tea made of the flowers. It is not to be confounded with R[Matricaria chamomilla]. Burnett has recorded a case in which violent gastric disturbance and headache were caused by the "tea"; and Berridge has proved the tincture. In Burnett's case there was great coldness and chilliness inside abdomen; Berridge had feelings of coldness generally, and sensitiveness to cold air and the touch of cold things but slight exertion caused heat and perspiration}


