:head=SYMPTOMS.


:rubric=1. Mind.
S{Time seemed to pass slowly.} S{Absent-minded.}


:rubric=2. Head.
S{Vertigo on walking; seems as if he would fall to l.} S{Pains all through the head, vertex, and occiput, extending down into spine.} S{Sensation as if lobes of cerebrum were drawn forward towards frontal sinuses.} S{Headache in temples extending inwards; B[>] by hard pressure.} S{Headache, throbbing, and beating in l. half of head, B[<] by coughing or moving.} S{Pain high up in occiput extending inwards.} S{Awoke with pain low down in occiput, much B[<] on raising or accidentally moving head in morning; B[<] towards noon, and went up over vertex to forehead, and there had a strong congestive headache, with throbbing of carotids, lasting till evening and gradually disappearing.} S{Scalp feels dry and tight.}


:rubric=3. Eyes.
S{Eyes sore; and painful to touch.} S{Sensation as if brows corrugated; and as if brain were also drawn down.} S{Lids heavy.} S{Much photophobia; on waking and continuing all day; esp. from gaslight.}


:rubric=6. Face.
S{Face red, full, and bloated in morning.}


:rubric=8. Mouth.
S{Tongue coated smoothly and evenly white.} S{Smooth, dirty, white coat.} S{Sour taste.}


:rubric=9. Throat.
S{Throat red, raw; acrid feeling; hurts to swallow food or liquids.}


:rubric=11. Stomach.
S{No thirst.} S{Empty eructations.} S{Uneasy feeling as if a lump or weight in stomach.}


:rubric=12. Abdomen.
S{Several lancinations in l. hypochondrium with tympanites.} S{Burning below umbilicus.} S{Rumbling in bowels.} S{Colic B[>] bending forward; and B[>] by stool, but smarting in anus remains.} S{Burning and uncomfortable sensation in bowels.} S{Pain in bowels on walking, also painful on pressure.} S{Dull ache low down in pelvis; bearing down, not B[>] by sitting or lying, but B[>] when headache 2[>.]}


:rubric=13. Stool and Anus.
S{Throbbing, stitching pains in anus.} S{Soreness and smarting, B[<] after stool.} S{Tenesmus and straining.} S{Diarrhoeic stools; sudden, gushing, watery; thin, yellowish faecal.} S{Constipation; urgent desire, but passes only frothy mucus.} S{Dry, sticky feces.}


:rubric=14. Urinary Organs.
S{Pain and soreness in region of bladder.}


:rubric=16. Female Sexual Organs.
S{Uterus seems too low, and as if it would prolapse during stool.} S{Menses two days too early; less copious and prolonged than usual.}


:rubric=17. Respiratory Organs.
S{Stinging pains in larynx from without inwards; very sharp pricking pain and sensation of suffocation.} S{Cough B[<] morning.} S{Raising sweetish-tasting mucus quite offensive.} S{Great dyspnoea from exertion; from walking up a slight elevation.} S{Sharp pain in r. thorax running down into abdomen and up into l. shoulder.}


:rubric=19. Heart.
S{Excitability of heart's action from least motion.} S{Palpitation on going up slight elevation.} S{Pulse suddenly becomes weak and irregular.}


:rubric=20. Back.
S{Heavy pain across back and through loins; in region of kidneys.}


:rubric=22. Upper Limbs.
S{Dull, neuralgic pain in both shoulders, cannot keep them still.}


:rubric=23. Lower Limbs.
S{Muscles of thighs sore and stiff.} S{Neuralgic pains in legs.}


:rubric=26. Sleep.
S{Sleepy but unable to sleep.} S{Dreams of dead bodies.}

  
