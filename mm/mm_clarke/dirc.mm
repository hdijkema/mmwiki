:rubric=Characteristics.
R[Dirc] belongs to the same family as R[daph] and R[Mez], and is the only American member of the family. Its symptoms show its relationship to the other two, but it has not been used extensively in practice. 

S{It produces much vital depression; neuralgic pains in the head, trunk, and extremities, many of the pains proceeding from without inward.} S{Digestion is disordered, there is weight at the stomach, and a peculiar white tongue, the coating being moist and even.} S{Rumbling of flatulence.} S{Colic B[>] by bending forward, diarrhoea and tenesmus, later constipation.} S{Oppression and pains in the chest, sweetish expectoration, and excitability of the heart's action from the slightest motion.} S{Hot and restless at night; cannot sleep; dreams of dead bodies.} 

:rubric=Modalities

S{Motion B[<] most symptoms; walking B[<] vertigo and pain in bowels.}
S{Colic is B[>] by bending forward; headache B[>] by pressure.}
S{Pains in the bowels are B[>] by stool.}


