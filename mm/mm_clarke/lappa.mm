:rubric=Characteristics.
S{I[Arctium lappa] has been extensively proved, notably by Dr. Jeanes and Dr. S. A. Jones (H.R., 1893). The provings in the main confirm the teachings of antiquity in regard to this remedy. It has been given from the earliest times for prolapsus uteri, sterility, milky urine, and sores about the joints. A large number of the symptoms were observed in the skin (acne; eczema, especially scalp) and mucous membrane, eruptions and discharges. It is one of the remedies which have been successful in curing uterine prolapse. In skin affections it has been use externally as a glycerole, as well as internally. A writer in I[Amer. Homeop.] recommended the local use of the seedling roots in acne. The roots are softened by soaking in water for two hours; then the blunt end is rubbed on each individual pimple. It is not to be repeated oftener than once a week. It was one of Culpepper's remedies for fetid sweat of axillæ}


