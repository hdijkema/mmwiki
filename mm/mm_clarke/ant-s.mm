:rubric=Characteristics.
S{I[Ant. sul. aur.] has been proved. Its most notable effects were produced on the eye and chest. Burning in head (with eye complaints). Amaurosis (commencing). Spots on cornea and hypopion. Acute and chronic nasal and bronchial catarrh. Acne. Nose-bleed on washing. As with the other antimonies there is disordered digestion, pasty mouth and taste in morning. Increase of tough mucus in larynx and bronchi, respiration difficult. Dry, hard cough with passive congestion of upper lobe of left lung. Itching on hands and feet}


