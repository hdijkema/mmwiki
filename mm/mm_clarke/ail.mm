:rubric=Characteristics.
S{Corresponds to conditions characterised by certain livid or purpled appearances of the skin. Rash coming out in patches. Eruptions which are slow in appearing. Low adynamic forms of disease with extreme prostration, torpor, vomiting. Prostration at the outset of a disease is a leading indication for it. The patient is stupid, dull, delirious, weak. It has proved of signal service in cases of malignant and suppressed scarlatina. The head is burning hot, with piercing pains. The parotid glands are sensitive and enlarged. The attacks are characterised by fulness of head with heat; pains burning, darting in temples and occiput; vertigo and nausea. The rash of R[Ail] often returns annually. A feeling of fulness everywhere, and a sensation of passage of electric current from head into limbs are prominent in the provings. With the low fever of R[Ail] there is stupor or indifference with sighing. There is constant frontal headache of a neuralgic character. Paroxysmal headache with drowsiness during day, confused mind, depression. The throat is severely affected: thick, œdematous, dry, choky feeling. Dry, rough, scrapy, 2[< ]morning. Livid, swollen, with ulcers oozing a fetid discharge}

S{The symptoms are 2[> ]lying on right side (bronchial affections). Sitting up 2[= ]vomiting, dizziness. Motion 2[= ]staggering. Walking about 2[> ]toothache}

S{It is suited to nervous, sensitive persons. Bilious temperament. Stout and robust}


