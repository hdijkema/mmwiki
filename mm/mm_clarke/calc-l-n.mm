S{Alkaline blood. Asthma. Chilblains. Haemorrhage. Hay fever. Urticaria.}


:rubric=Characteristics.
S{A colloid of metallic Calcium has been prepared by the Crooke's laboratory, and its use subcutaneously has met with success in checking haemorrhages, notably of the lungs. The double lactate of Calcium and Sodium appears to possess these properties. It apparently acts on the blood, reducing its alkalinity, relieves chilblains, urticaria, asthma, and hay fever. It is given for all these conditions in old-school practice in tablets of the crude substance. Homoeopaths may use it for similar conditions in the 3x and higher. I have done this with much success with R[Calc-m]. in chilblains.}


