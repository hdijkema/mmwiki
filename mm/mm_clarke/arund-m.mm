:rubric=Characteristics.
R[Arund-m] is a reedy grass and the tincture is made from the root-shoots. It has not been much tested clinically, but its chief sphere has been proved to be in catarrhal conditions. 
S{Constant (serous) diarrhoea of nursing or teething children.}
S{Urine deposits much red sand.}
S{Mouth excoriated.}
S{Thirst.}
S{Coryza and snuffling.}
S{Scrofulous ophthalmia.}
S{Purulent discharge from the ear.} 
R[Arund-m] has cured a case of hay-fever, the symptoms beginning with "burning and itching of palate and conjunctiva." Chagon is the authority for the pathogenesis.

