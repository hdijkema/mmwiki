:rubric=Characteristics.
S{The following symptoms have been noted: Faintness, anxiety and weakness. Trembling and giddiness. Respiration quick and snoring; increased secretion of mucus (as with other chlorides), perspiration and urine. Nausea and vomiting; precordium tender; diarrhoea; pulse accelerated. Cold sweats; convulsions; paralysis. In scrofulous cases it promotes the action of mercury. Under its use glandular swellings and indurations soften and disappear. Hence its use in scrofula and boils. "C. W." has recorded a remarkable instance of the action of this remedy (2[Hom. W.], March, 1891). A scrofulous and rheumatic woman had suffered from abscess of cornea, followed by rheumatic iritis, and a dense opacity was left. An old-school practitioner had treated her with mercurials during two years in a vain endeavour to "touch her mouth." C. W. put one grain of R[Calc-m]. in an 8-oz. bottle, and directed the patient to take a tablespoonful three times a day. The medicine had not been taken ten minutes before she felt it at her fingers' ends. The mercurial action was evoked, and she was in bed a fortnight, terribly salivated. The condition of the eye was unchanged. R[Calc-m]., in the form of the liquor (1 part to 2 of distilled water), was one of Rademacher's "Stomach Remedies," and was used with great success by him in cases of obstinate vomiting. He gave 15 drops of the liquor in half a cup of water five times a day. The indications were: Vomiting of all food and drink; vomiting when no food had been taken; jaundice; pain in left hypochondrium. He also found it indicated in gastric pain; bloating and eructations.}


