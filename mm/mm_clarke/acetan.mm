GT{Acetanilid (formed from aniline). CH3 CO NH C6 H5. Exalgine is a derivative from this, Methylacetanilid, and has almost identical action. Solution and trituration}


:rubric=Clinical.
S{Asthma. Cyanosis. Fainting. Head, enlarged sensation. Palpitation. Thrombosis}


:rubric=Characteristics.
S{Given as remedies for migraine and in order to reduce fever, R[Acetan] and R[Exalg] have produced symptoms of collapse and cyanosis, in some instances fatal, with great rapidity. One patient experienced from I[Exalgine] a sensation that his head was so large that it seemed to occupy the whole room. Respiration was most difficult as in a bad attack of asthma, he seized and clenched the hands of those around him. He was not an asthmatic subject. He felt as if the diaphragm had ceased working and he must go on breathing on his own account at any cost. Several cases of thrombosis of the lower extremities from heart weakness have followed its use}


