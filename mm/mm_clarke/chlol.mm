:rubric=Characteristics.
S{Introduced as a hypnotic, R[chlol] soon showed that it possessed wide powers of disturbing the organism, and many persons (including the late Professor Tyndall) have been fatally poisoned by overdosing. The brain, eyes, urinary and sexual organs and skin are most affected. W. S. Gee cured with it a terrific headache of several days' duration: dull, heavy aching in forehead; coming on each morning 8 a.m.; B[<] sudden motion; B[<] lying down; B[>] in, open air. The 6th gave great relief though it aggravated at first, the aggravation occurring ten minutes after each dose. One dose of the 25x cured permanently. A person under the influence of R[chlol] hears voices, sees visions of arches, etc., when in the dark or when the eyes are shut. Night-terrors in children have been cured with it. The brain tissue is probably congested in the same way as the skin in the urticaria of the drug. Urticaria gives a good illustration of the effect of the drug on the minute tissues. The brain is congested; hard, full, pressing pains in head. Eyes injected; the eyeballs feel too large; lids swollen, heavy, can hardly lift them. Asthma and wheezing respirations are probably due to an urticarious condition of the lung tissue. A symptom that is very significant is this: When lying on back, inspiration was through the nose, while expiration was blown from the lips as in apoplexy. The heart's action is increased, with oppression. Palpitation, hypertrophy. Later there is paralysis of heart. Heart dilated or weakened, with peculiar fulness and lightness of chest and sense of emptiness in stomach. The sense of sinking and oppression at the pit of the stomach is marked and shows a profound action on the solar plexus. The insomnia for which R[chlol] is suited is that due to over-fatigue. Snoring in sleep. Lying down 2[<.] Wants fanning; open air 2[>.] B[<] Night and evening.}


