S{R[calc-s]. is closely related to R[hep]., which is an impure sulphide of calcium, but has a wider sphere and deeper action in suppurative processes. It is Schüssler's "Connective-tissue salt," and most of the indications for its use are derived from his clinical experience. In the last edition of his 2[Biochemic Therapy] Schüssler discards R[calc-s]. because it is not an actual constituent of the tissues, and he distributes its functions between R[sil] and R[nat-p]. Homoeopaths, having no Biochemic theory to support, may continue its use without scruple, especially as it has been proved by Hering and others. It is indicated in all kinds of cases 2[after pus has found a vent.] Nash has cured a case of suppurating kidney with it. It differs from 2[Hepar] in sensitiveness to air: 2[Hepar] cannot bear the slightest exposure; R[calc-s]. has B[>] in open air; better walking in it; desire for it. Both have B[<] from change of weather. R[calc-s]. has not the excessive sensitiveness to touch found in R[hep]. Hansen recommends it in dry eczema in children. Cystic tumours, fibromata and polypi have been cured with R[calc-s].}


