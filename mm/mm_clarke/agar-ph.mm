:rubric=Clinical.
S{Cholera. Chorea. Cramps. Diarrhœa. Gastritis. Trismus. Urine, suppression of. Vomiting}


:rubric=Characteristics.
S{Symptoms observed in poisoning cases give a complete picture of Asiatic cholera. There is extreme prostration and chilliness; the sweat is cold; the hippocratic face is marked; the tongue is cold; and there is violent thirst. Very frequent bilious vomiting. 2[Incessant cramps ]in stomach. Hard, tense abdomen. Frequent whitish watery stools; or bilious, bloody stools. The urine is suppressed; the voice is hoarse. Pulse small, intermittent, hardly perceptible. The extremities are cold, and there are violent cramps in legs, feet, and calves. Convulsions are another prominent feature. There is mental excitement, which has in some cases lasted for three days. Sometimes there is stupor, at other times consciousness remains till death}

S{The effects do not come on till ten or twelve hours after taking the drug (in this it is like 2[Colchicum)]. At times the development of choleraic symptoms saves the patient. General cholera symptoms are developed as with R[Agar] Chilliness also predominates. R[Agar-ph] is a poison of great intensity and fatality}


