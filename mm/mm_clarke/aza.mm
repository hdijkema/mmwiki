S{Constipation. Diarrhoea. Intermittent fever. Quinine, effects of. Spleen, congested.}
:rubric=Characteristics.
S{This is an Indian remedy of very ancient date. All parts of the tree are intensely bitter, and the separate parts are said to have different effects. The bark, known as 2[Margosa Bark], is the best-known medicinal portion, and the tincture for the provings was made from this by Dr. P. C. Majumdar, who is our chief authority for its effects. It contains 2[Azadirin], 2[Margosin] and 2[Catechin.] It is popularly used in a great variety of complaints, especially of the eyes, digestive derangements, and skin affections. The most peculiar feature of the proving is the fever, which commences with a very slight chill or none at all, about 3 to 4.30 p.m., and abates about 7.30 p.m. Glowing heat and burning, especially in face, eyes, palms of hands and soles of feet in open air; sweat copious, commencing on forehead, gradually extending towards trunk; no sweat on lower part of body. It is especially useful in cases previously maltreated with quinine. B[<] In open air; in afternoon.}
