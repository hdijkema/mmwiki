GT{N. O. Insecta. A tincture is made of the aphides grown on the Chenopodium glaucum}


:rubric=Clinical.
S{Colic. Coryza. Cough. Diarrhœa. Headache. Tenesmus Toothache}


:rubric=Characteristics.
S{Some of the most notable symptoms of I[Aphis] are: Coryza with burning and biting on margins of nostrils, especially of septum. Increase of most violent toothache in bed, only relieved if after awhile a general warm sweat breaks out. Cutting in abdomen and rumbling of flatus. Ineffectual urging in bladder and rectum. In the morning urging after rising, several mush-like stools with pinching, burning in anus with urging. Flatus accompanies stool. Thin stools with slime and spots of dark blood. The insect partakes of the properties of the plant it lives on, R[Chen] being a popular colic remedy, and allied to the Polygonaceæ R[Rheum] and R[Rumx]. I[Aphis] is peculiar in being the only remedy which has toothache B[>] by sweat; though R[Cham] has general B[>] by sweat}


:rubric=Relations.
S{2[Compare:] R[Aeth] (stool after rising; pinching, cutting before., urging after); R[Nat-s] (stool after rising, with discharge of wind); R[Nux-v] (frequent urging in rectum and bladder); R[Gels] (chills up and down the back)}


:head=SYMPTOMS.


:rubric=2. Head.
S{Head confused in the evening, as with coryza; with transient heat in the face, great pressure and tightening in the forehead or occiput, increased by motion, with sensation of cerebral oscillation in the head, tearing shooting in the teguments of the head}


:rubric=3. Eyes.
S{Burning heat in the eyelids during several evenings}


:rubric=4. Ears.
S{Tearing pains, sometimes in one ear, and sometimes in the other}


:rubric=5. Nose.
S{Pain of excoriation in the nostrils.} S{Violent sneezing, sometimes attended by a pain, as of excoriation, in the larynx.} S{Coryza, sometimes with burning and smarting at the edges of the nostrils, and esp. at the septum.} S{Fluent coryza, with secretion of serous mucus, sometimes also at night, with pulse accelerated to eighty-five pulsations, with coldness in the feet extending to the knees, and shuddering all down the back}


:rubric=6. Face.
S{Colour pale, yellowish; heat in the face, with head confused as by coryza, at night.} S{Dry lips, sometimes esp. in the morning}


:rubric=7. Teeth.
S{Toothache with tearing shootings, at first in a hollow molar tooth, afterwards in all the teeth of the right side; extending from the right side to the ear, the temple, and the cheek-bone.} S{Aggravation of the toothache in bed, which is mitigated only after a hot and general perspiration, which supervenes at a later period.} S{Nocturnal tearing pains in the teeth, with tearing shootings above the face towards morning}


:rubric=8, 9. Mouth and Throat.
S{Painful vesicles at the end of the tongue.} S{Dryness of the mouth and of the throat, sometimes with increased mucous secretion; increase in the secretion of saliva.} S{Great quantity of mucus in the mouth and throat, sometimes with a sickly taste, and with a constant necessity for hawking and expectoration.} S{Secretion of frothy mucus in the mouth and throat.} S{Scraping sensation in the palate; burning smarting; incisive burning, principally when inspiring; inflammatory redness.} S{In the throat, scraping and burning, as from acrid substances, sometimes with a sensation of dryness and increase of mucous secretion}


:rubric=10. Appetite.
S{Dislike to meat and bread; frequent thirst, sometimes increasing at night, or produced by a sensation of dryness in the throat.} S{Frequent risings, most frequently empty, or else with a taste of the food which has been eaten}


:rubric=12. Abdomen.
S{Frequent pinching pains in the abdomen, sometimes throughout the night, or else during the day, with necessity to go to stool, and frequent urinations.} S{Emission of wind.} S{Cutting and pinching pains, with borborygmi and emission of, wind.} S{Congestion of blood towards the abdominal viscera.} S{Rumblings of flatus in the abdomen, with frequent emission of wind}


:rubric=13. Stool and Anus.
S{Abortive inclination to go to stool, with pressure on the bladder and rectum.} S{Loose evacuations of the consistency of liquid pap, usually accompanied by burning at the anus, and a recurrence of the necessity to go to stool, or else with pinchings in the abdomen, occasioned by flatulency before and after a stool.} S{Liquid, mucus-like stools in the morning, with spots of blood, pinchings in the abdomen, pressure on the rectum, and pressive headache.} S{At a later stage the stools have more consistence, but they press painfully on the rectum and the bladder, and are sometimes attended by a flow of sanguineous mucus.} S{Tearing shooting in the rectum}


:rubric=14. Urinary Organs.
S{Tearing shooting in the bladder; pressure on the bladder, sometimes, esp. during an abortive effort to go to stool.} S{Irritation in the urethra, as from acridities, compelling frequent urination.} S{Voluptuous excitation of the glans penis.} S{Frequent and copious secretion of a frothy urine, deep yellow, sometimes with an acrid sensation in the urethra.} S{In the evening, urine red, brownish, frothy, depositing during the night a thick, yellowish sediment.} S{In urinating, burning in the urethra, esp. at the orifice}


:rubric=17. Respiratory Organs.
S{Burning scraping in the larynx, as from acrid substances.} S{Burning tickling, or lancinations, esp. in the open air, in a damp, cold atmosphere, with continual irritation in the larynx, which causes coughing, followed by expectoration of mucus.} S{Voice frequently rough and smothered, which symptoms are removed by hawking.} S{Frequent irritation in the larynx, which compels coughing.} S{Dry cough, provoked by irritation in the throat}


:rubric=20. Neck and Back.
S{Pain in the left shoulder-blade}


:rubric=21. Limbs.
S{Bruise-like pain of the limbs, with tearing shootings in the shoulders and the upper part of the arms (rheumatic pains), or else from the knees to the feet, and esp. on the tibia and at the soles of the feet}


:rubric=23. Lower Limbs.
S{Pulling and tearing above the knee in the morning, following nocturnal toothache.} S{Sensation of weariness in the legs.} S{Coldness of the feet, extending to the knees.} S{Burning lancinations in corns}


:rubric=24. Generalities.
S{Pain (rheumatic) as from weariness in the limbs, with tearing, shootings, chiefly in the temples, the ears, and teeth, which are hollow; as well as in the shoulders, the upper part of the arms, the tibia, and the soles of the feet; sensation of weariness, esp. in the legs, weariness and weakness at night.} S{The limbs feel bruised}


:rubric=26. Sleep.
S{Sleeplessness at night, without much pain; lascivious dreams with pollutions}


:rubric=27. Fever.
S{Frequent shiverings of the whole surface of the body, chiefly of the back, burning in the palms of the hands, and tendency in them to perspire in the morning; with acceleration of pulse, sometimes principally in the evening; with fluent coryza, or accelerated and wiry pulse in the morning; with hot breath and dry lips; with great tendency to perspire in the face, in the morning; in the morning, in bed, hot sweat, or general perspiration}


