GT{Serpentary. Virginia Snake-root. N. O. Aristolochiaceæ. Trituration of dried roots}


:rubric=Clinical.
S{Dyspepsia. Flatulence}


:rubric=Characteristics.
S{Serpentary was proved by Jörg's provers and others. Its chief effects were noticed in the gastro-enteric organs, nausea, vomiting, abdominal distension, flatulence, and urging to stool. Increased and afterwards lost appetite. Irritation of the urinary and genital organs with frequent desire to micturate. As with R[Arist-m], there was disturbed rest; also heat of the head}


:head=SYMPTOMS.


:rubric=1. Mind.
S{Peevish mood.} S{Disinclination for work}


:rubric=2. Head.
S{Sticking in forehead extending to base of brain.} S{Increased warmth of head}


:rubric=8. Mouth.
S{Copious salivation with frequent spitting}


:rubric=11. Stomach.
S{Increased appetite; yet able to eat little, since the first morsels satisfy.} S{Loss of appetite.} S{Nausea and vomiting; vomiting did not cease till stomach was entirely emptied.} S{Distended feeling as if too full.} S{Heaviness B[>] by emission of much flatus}


:rubric=12. Abdomen.
S{Colic in umbilical region.} S{Distension, rumbling, uneasiness, cutting pains, with at times emission of flatus and eructations which relieve}


:rubric=13. Stool and Anus.
S{Distressing itching about anus.} S{Frequent desire for stool, with evacuation of scanty, hard, tenacious fæces, with much flatulence.} S{Frequent desire with evacuation of more gas than fæces}


:rubric=14. Urinary Organs.
S{General irritation of urinary and genital organs.} S{Violent desire to urinate with great increase in quantity of urine.} S{Frequent desire but only a little brownish urine passed}


:rubric=20. Neck.
S{Pressive pain in nape of neck}


