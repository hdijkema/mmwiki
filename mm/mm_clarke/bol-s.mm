S{Diarrhoea membranosa. Dysentery.}


:rubric=Characteristics.
S{There are two fragmentary provings of this fungus, and observations on six persons poisoned. It produces gastro-enteric disturbances very similar to that of its two relatives.}


:head=SYMPTOMS.


:rubric=1. Mind.
S{Fear and restlessness.}


:rubric=3. Eyes.
S{Sparks before eyes alternating with obscuration of vision.}


:rubric=8, 9. Mouth and Throat.
S{Troublesome dryness in mouth and throat.} S{Violent burning and scraping in throat.}


:rubric=11. Stomach.
S{Unquenchable thirst.} S{Sudden desire to vomit; no nausea between attacks.} S{Vomited twenty times in three hours.} S{Frightful pain in stomach.}


:rubric=12. Abdomen.
S{Abdomen sunken and frightfully painful.} S{Obstinate tension of abdomen.}


:rubric=13. Stool.
S{A profuse diarrhoea of blood, and of the mucous lining of the intestines.} S{Watery evacuations.}


:rubric=24. Generalities.
S{Sudden sensation through all his limbs as if he would be attacked with apoplexy.} S{Violent and extremely painful cramps in the muscles of the limbs and face.} S{Faintness while vomiting.} S{Limbs cold; cold sweat over whole body.}

  
