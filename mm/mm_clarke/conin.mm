:begin[en]
GT{Conicine. An alkaloid from Conium maculatum (R[Con]). C8 H17 N. Tincture.}


:rubric=Clinical.
S{Eyes, heavy. Hearing, illusions of. Hemiplegia. Numbness. Tongue, paralysis of. Vertigo. Vision, disordered.}


:rubric=Characteristics.
S{2[R[Conin]] is a poison rivalling prussic acid in the intensity of action. It has had rather an extensive proving. It causes numbness, muscular paralysis, vertigo, disorders of sight, hearing, and touch, lethargy and overpowering sleepiness. A feeling as if one had been up all night, or after a debauch. Cramp in calves on ascending steps or drawing off boots. Vertigo is B[>] in open air.}


:head=SYMPTOMS.


:rubric=1. Mind.
S{Calm and tranquil; slowness of mental process.} S{Inability to think or fix the attention.} S{Loss of consciousness.}


:rubric=2. Head.
S{Vertigo and confusion with prostration B[>] in open air.} S{Weight, fulness, and pressure in head.} S{Open air caused pain in supra-orbital and cutaneous malae nerves.}


:rubric=3. Eyes.
S{Lids heavy.} S{Vision confused; objects swim.} S{Dazzling of sight.} S{Complete blindness.} S{Objects seem large; one thinks he sees his own nose a shapeless mass.}


:rubric=4. Ears.
S{Hearing weakened; ears feel as if stopped with cotton wool.} S{Illusions of hearing.}


:rubric=6. Face.
S{Face sunken, pale.} S{Numbness of muscles about the jaws.} S{Head and face very warm.}


:rubric=8. Mouth.
S{Tongue pale, paralysed, and numb.} S{Epithelium denuded in many places, papillae very prominent.} S{Violent burning in mouth.} S{Accumulation of saliva.}


:rubric=9. Throat.
S{Scraping in throat.}


:rubric=11. Stomach.
S{Eructations; nausea; vomiting.}


:rubric=12. Abdomen.
S{Rumbling with distension of abdomen.} S{Tendency to diarrhoea.}


:rubric=17. Respiratory Organs.
S{Voice thick; muddled words when speaking.} S{Respirations frequently yawning.}


:rubric=20. Neck and Back.
S{Spasmodic distortion of neck with spasms of face which draw the mouth up to a point.} S{Pain in l. lumbar region.}


:rubric=21. Limbs.
S{Heaviness of arms and legs, esp. l. arm.}


:rubric=22. Upper Limbs.
S{Numbness with tingling of arms.} S{Tense cramp in muscles of ball of thumb with painful sensation on strongly adducting thumb.} S{Hands bluish, cold.} S{Moisture on finger-tips.} S{Fingers and hands wet.}


:rubric=23. Lower Limbs.
S{Gait unsteady; weakness esp. marked on going out to walk.} S{Carries body forward.} S{Legs felt independent as if they did not care for him or anybody else.} S{Cramp in calves on ascending steps.}

  
:end
