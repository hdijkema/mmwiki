:rubric=Characteristics.
S{Originally introduced by Sir James Simpson as a remedy for the morning sickness of pregnancy and chronic cases of vomiting, which he supposed to be due to some eruption on the mucous membrane of the stomach, R[Cer-ox] has fairly maintained its reputation. Hale found it of most use in cases of vomiting of half-digested food. It has mostly been given in the lower triturations by homoeopaths, and in five to ten grain doses by allopaths. In the latter doses it has produced decidedly narcotic effects in two cases. Some have obtained great success with it in sea-sickness. Spasmodic coughs of nervous or reflex origin have been successfully treated with it; and dysmenorrhoea in fleshy and robust women, scanty flow, pain coming before, or at commencement, with feeling of tenesmus, B[>] when the flow is established (like R[lach].).}

  
