:rubric=Characteristics.
S{R[Asc-t] causes sharp, stitching, pricking pains; B[<] by motion. It is of the hydrogenoid type, corresponding to catarrhal complaints from cold and damp weather. Rheumatic pains affect the body diagonally, l. upper and r. lower, or the opposite. Muscular and articular rheumatism with stitching pains, dark red urine and hot, perspiring skin. Sensitive to tobacco. Pain in forehead and abdomen from coughing. Griping and sharp peritoneal pains B[<] by pressure. Dysentery in autumn; and painful diarrhoea with griping and tenesmus. Warm feeling in chest. Dyspnoea. Cough hard and dry; or hoarse, croupy with tight breathing and constriction of larynx, and acute pleuritic pains. Pains in lungs B[>] bending forward. Stitches in l. side shooting over to r. and up to l. shoulder. Influenza with pleuritic pains. Cutting pain behind sternum; intercostal spaces near sternum tender to pressure. Pain like pricking of a needle in region of heart. Contracting pain in heart. Sharp pains, starting from l. nipple downward with stiffness of l. side of neck. Excessive weakness, walking seems impossible. Bilious marsh-fever on rice plantations. High fever with hot sweat. Catarrhal complaints from cold and damp weather. Diarrhoea is B[<] in winter. Symptoms B[<] in morning; on rising; by motion; by coughing; by tobacco. Chest pains B[<] 4 a.m. and 4 p.m.}


