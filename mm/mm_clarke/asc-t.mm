:head=SYMPTOMS.


:rubric=1. Mind.
S{Weakness of memory thinking difficult.} S{At 9 p.m. cheerful, mood changed, and without any cause he became fretful and feverish.}


:rubric=2. Head.
S{Feeling of drunkenness with weakness of sight after smoking a very little.} S{Swimming of the head with dulness behind forehead.} S{Dull headache in the forehead and vertex, B[<] by motion; B[>] by lying down.} S{Headache in the morning on rising.} S{Headache B[>] after a foot-bath.} S{Headache pressing deeply on, the base of the skull.} S{Pain in the forehead from coughing.} S{Alopecia.}


:rubric=3. Eyes.
S{Eyes look dull, fatigued, and heavy (as after long illness).} S{Ophthalmia, with itching and pain in eyes.} S{Feeling of sand in the eyes.} S{Vision disturbed large spots before the eyes.}


:rubric=5. Nose.
S{Fluent coryza, with much sneezing.} S{Blowing of blood from l. nostril.} S{Itching of the nose.} S{Snuffles of children.}


:rubric=6. Face.
S{Hippocratic face after violent diarrhoea.} S{Yellow complexion.} S{Vesicles on lips.} S{Itching of lips.}


:rubric=8. Mouth.
S{Teeth yellow-coated.} S{Bleeding of the gums.} S{Tongue covered with a tough yellow coating.} S{Taste putrid.} S{Taste of blood.}


:rubric=9. Throat.
S{Slight constriction of throat, and pricking in larynx.}


:rubric=11. Stomach.
S{Loss of appetite; esp. in morning.} S{Insatiable hunger.} S{Sensitive to tobacco.} S{Eructations.} S{Nausea, and efforts to vomit in the morning when rising.} S{In stomach: nervous pains; disagreeable weight; burning.} S{In evening, violent gastralgia after supper; it seems as if coughing would bruise the stomach.} S{Cramp.}


:rubric=12. Abdomen.
S{Grumbling in r. hypochondrium.} S{Throbbing in l. hypochondrium.} S{Rumbling in the bowels, with uneasiness, or sharp, cutting pains and soreness.} S{Burning borborygmi.} S{Colic pains from flatulence.} S{Colic on going upstairs.} S{Dull pain in bowels on pressure.}


:rubric=13. Stool and Anus.
S{Emission of fetid flatulence.} S{Soft and fetid stool at 11 a.m., preceded by rumbling in the bowels.} S{Early morning diarrhoea (1 to 3 a.m.), sudden discharge.} S{Winter diarrhoea.} S{Autumnal dysentery.} S{Tenesmus.} S{At 11 a.m. another stool, almost black, with many ascarides and yellow spots like fat, with a feeling as if a stream of fire passed through abdomen.} S{Stools: dysenteric; like white of eggs; yellow; green; clammy; smelling like rotten eggs; enveloped in froth; like moss.} S{With stool: colic.} S{After stool: colic continues long.} S{Constipation after diarrhoea.}


:rubric=14. Urinary Organs.
S{Lancinating pain in urethra, excoriation of glans penis in several places, with commencement of sanious and purulent secretion.} S{Urine very red: looks as if blood were in it.}


:rubric=15. Male Sexual Organs.
S{Flabbiness, perspiration, and insipid smell of genitals.} S{Excoriation of glans penis in several places, with commencement of sanious discharge (chancre); B[>] by bathing with urine.} S{Weakness; erection without desire.}


:rubric=16. Female Sexual Organs.
S{Menorrhagia, with violent bearing down.}


:rubric=17. Respiratory Organs.
S{Dry cough, with constriction of throat; causing pain in forehead and abdomen; dry, hacking cough, B[<] night and morning; from irritation of larynx or bronchia.} S{Respiration painful, esp. at base of l. lung.} S{Breath smells like pepper.} S{Humid asthma; B[<] after eating: B[<] after smoking a little.} S{Oppression and difficulty of breathing; in paroxysms, like asthma.}


:rubric=18. Chest.
S{Warm feeling in chest.} S{Sharp pains shooting from l. nipple downward, with stiffness of l. side of neck.} S{Pain moves up to behind the sternum, and becomes more sharp and cutting, B[<] drawing a long breath, or moving the arms (as in triturating).} S{The spaces between the ribs, close to the sternum, are sensitive to pressure, and the pain, which is quick and darting, shoots over from l. to r. side, and up to l. shoulder.} S{Pain in chest relieved by bending forward.} S{Acute pleuritic pain in r. side, with dry, hacking cough, and scanty mucous expectoration.} S{Pain in chest low down, in diaphragm.} S{Sharp pains from l. nipple downward, with stiffness l. side neck.}


:rubric=19. Heart and Pulse.
S{Constrictive pain in the heart.} S{Tenderness to pressure over heart.} S{Lancinating pains; like pricking of a needle in region of heart.} S{Pain beneath l. nipple with palpitation.} S{Pulse slightly accelerated.}


:rubric=20. Neck and Back.
S{Stiff-neck (l.); on waking.} S{Lancinating pains in back and between the shoulders.} S{Pains shooting from l. chest into l. shoulder.} S{Sharp pain in loins near sacrum; lumbago.}


:rubric=21. Limbs.
S{Rheumatic pain in all the joints.}


:rubric=22. Upper Limbs.
S{Sharp, shooting pains in r. shoulder.} S{Pain in l. shoulder, shooting from l. chest.} S{Pains near wrists.}


:rubric=23. Lower Limbs.
S{Itching of thighs and nates but no eruption.} S{Coxalgia in r. hip.} S{Corns painful.}


:rubric=24. Generalities.
S{Weak and languid, as if he had been sick a long time.} S{Emaciation.} S{Numbness of whole body.}


:rubric=25. Skin.
S{Vesicles, pimples, and pustules all over the body, esp. on arms, legs, and face; itching.}


:rubric=26. Sleep.
S{Drowsy.} S{Uneasy, restless sleep.} S{Troublesome dreams.}


:rubric=27. Fever.
S{Chill towards noon; with cold feet though room is warm.} S{Feverish afternoon.} S{High fever with hot sweat.} S{Rheumatic; catarrhal; bilious marsh fevers of rice plantations.}

  
