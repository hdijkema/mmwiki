S{2[Cistus] is a very ancient remedy for scrofulous affections, and also in scorbutic states and gangrenous ulcerations. The provings and homoeopathic uses confirm its suitability in these respects and bring out clearer indications. It is most suited to scrofulous subjects who are very sensitive to cold air. "Sensitive to cold air" runs through the pathogenesis and also feelings of coldness, of which its peculiar property of favouring the formation of ice about its roots in early winter is strangely suggestive. Cold feelings: in larynx; in abdomen. "Inhaling the slightest cold air causes sore-throat, which he has not when inhaling in a warm room." Inhaled air feels cool. Cold breath. Stitches and cold feelings are very prominent; there is also a soft or spongy feeling which is very characteristic. (I cured with 2[Cistus] a case of sore-throat that had lasted for years under allopathic treatment with local swabbings. There was an accumulation of thick yellow, leathery phlegm that had to be cleared away by local means. The patient complained of a "spongy" feeling in the throat, and that led me to the remedy.) Trembling (with the fever). Sensation as if ants were running through the whole body (in evening, with anxious, difficult breathing). There is also great sensitiveness to touch: B[<] from touch. Throat symptoms B[>] by swallowing. B[<] By motion. B[<] Evening; night; morning. B[<] By drinking coffee (diarrhoea). B[>] By expectoration.}


