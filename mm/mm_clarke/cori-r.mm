:rubric=Clinical.
S{Delirium tremens. Mania. Memory lost. Vomiting.}


:rubric=Characteristics.
S{R[Coriaria] produces intoxication very like that of alcohol. Great muscular activity as in acute mania, requiring several persons to hold a person when under the influence of the drug. The most marked feature is complete loss of memory, which ensues after coming out of the acute stage of the poisoning. A prover who ate 1 1/2 gr. of the resin experienced the following: Disagreeable, irritating sensation in throat, extending to stomach, with pain across region of stomach, accompanied by nausea (in five min). In a quarter of an hour vomiting came on, continuing more or less for two hours. Very unpleasant sensations continued for two hours more, when, after great flushing of the face, with all but intolerable heat, the effects passed away.}

  
