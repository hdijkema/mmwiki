:head=SYMPTOMS.


:rubric=1. Mind.
S{Forgetful.} S{Indifferent; with drowsiness.} S{Indolence with loss of mental and physical energy.} S{Unable to concentrate attention.} S{Mind confused, particularly on rising in the morning.} S{Depression and petulance.}


:rubric=2. Head.
S{Dulness and weight in head, esp. temples, B[>] by coffee.} S{Heaviness of head, with nausea.} S{Aching and throbbing pain over l. eyebrow.} S{Dull pains over eyeballs; unusual pulsation extending to back of head.} S{Pulsating headache in temples.} S{Headaches, with drowsiness and confusion of ideas; semi-lateral headaches; deep-seated pains under vertex; dull, heavy pains over whole head, with drowsiness, lassitude, nausea, and general sweat.} S{Headache; B[<] by walking, stooping, shaking head; B[>] by coffee.}


:rubric=3. Eyes.
S{Eyes sunken, hollow, as after a debauch; dark circles under them, conjunctiva tinged yellow.} S{Soreness of eyeballs; aching pains through them.} S{Eyeballs and lids heavy, as if pressed down by a weight; inclination to close eyes in sleep.} S{Herpes of lids.}


:rubric=4. Ears.
S{Ringing in ears.}


:rubric=5. Nose.
S{Prickling in nose and nasal bones.} S{Coryza early in morning.}


:rubric=6. Face.
S{Heat and burning in face; without redness.} S{Countenance yellow, or pale, sallow, and sunken, indicating suffering and debility.}


:rubric=8. Mouth.
S{Pungent, bitter, or insipid taste; yellowish or white fur on tongue.} S{Ulceration of tongue, gums, and mouth; from cold, or gastric derangement; aphthae.} S{Sweating and burning in mouth, throat, and stomach, with desire for stool.}


:rubric=11. Stomach.
S{Thirst for cold drinks.} S{Eructations.} S{Acid pyrosis; painful and slow digestion.} S{Gagging as if to vomit in morning.} S{Nausea, with confused and heavy pain in head.} S{Nausea with bitter taste and aversion to all kinds of food, and desire for sour drinks.} S{Sense of faintness and emptiness in stomach and bowels.} S{Distension; burning; oppression: pulsating pain in stomach.}


:rubric=12. Abdomen.
S{Chronic hepatitis and bilious derangement.} S{Constant working in bowels as if all in motion.} S{Borborygmus.} S{Distension B[>] after loose stool.} S{Griping in umbilical region; B[<] during stool.}


:rubric=13. Stool and Anus.
S{Urgency to stool; early in morning in bed; with fulness and uneasiness of bowels; frequent but ineffectual.} S{Diarrhoea with excessive debility and nervous irritability.} S{Stools: dark, bilious, watery, mucous, with griping, burning, and tenesmus, nausea, drowsiness, dulness of the head, and general perspiration; dark green, thin and offensive; greenish slimy; mucous, bilious or watery; frequent and scanty; offensive flatus.} S{Bearing-down pains in rectum and bowels, with urgent desire for stool.} S{Hard, dry, scanty stool, with pressing in rectum.} S{Ulceration of rectum.} S{Bowel complaints with pains before, during, and after discharges.} S{Dysenteries and diarrhoeas with inactivity of liver.}


:rubric=14. Urinary Organs.
S{Frequent desire to pass water.} S{Urine scanty and high-coloured.}


:rubric=15. Male Sexual Organs.
S{Increased desire, but lack of power.} S{Frequent strong and persistent erections during the night.} S{Pruritus.}


:rubric=16. Female Sexual Organs.
S{Leucorrhoea.} S{Pruritus.}


:rubric=17. Respiratory Organs.
S{Frequent inclination to expand chest by taking a long breath.} S{Cough with sharp stitches in chest.} S{Dry, spasmodic cough, or tedious, chronic cough, with mucous expectoration.}


:rubric=18. Chest.
S{Choking sensation in upper part of thorax.} S{Soreness of chest on rising in morning.} S{Stitches; bruised feeling; in chest and back.} S{Dragging down on each side of chest.} S{Shooting pains from centre of thorax to lower abdomen.} S{Intermittent shooting pains in chest and abdomen.} S{Rheumatic or neuralgic pains in chest, back, and limbs.} S{Fine scarlet rash on chest, with, itching.}


:rubric=19. Heart.
S{Perceptible pulsations in chest.} S{Palpitation of heart.} S{Circulation accelerated.}


:rubric=20. Neck and Back.
S{Drawing pains at nape of neck.} S{Dull pains in back.} S{Sore pains in lumbar region, B[<] bending forward.}


:rubric=21. Limbs.
S{Weakness of extremities.} S{Coldness of hands and feet after a loose stool.}


:rubric=22. Upper Limbs.
S{Weakness and fatigue of arms.} S{Burning and itching sensation in hands and arms.} S{Coldness of hands following a loose stool.}


:rubric=23. Lower Limbs.
S{Pain in r. hip while lying in bed.} S{Legs weak and tremulous, particularly when ascending.} S{Itching on legs and thighs; burning sensation in feet.}


:rubric=24. Generalities.
S{General debility and impaired mental energy, with great drowsiness; in heat of summer.}


:rubric=25. Skin.
S{Yellow or earthy appearance of skin.} S{Heat of whole surface with itching, burning, or prickling sensations; B[<] by scratching or rubbing.} S{Eczema, pruritus and similar affections of external genitals.} S{Itching in paroxysms B[<] at night.} S{Dry or moist eruptions; with cough.} S{Vesicular eruptions; urticaria; miliaria; roseola.}


:rubric=26. Sleep.
S{Drowsiness with entire loss of mental and physical energy; heavy feeling in head; depression of spirits; tendency to perspire.} S{Sleep unrefreshing and disturbed by unpleasant dreams.}


:rubric=27. Fever.
S{Chilliness, with nausea, dull pain in head, debility, and languor.} S{Chilly sensation, followed by transient flushing.} S{Flushes of heat and coldness in alternation, followed by cold perspiration.} S{Transient flushes of heat pervading whole body, with shooting pains through brain.} S{Paroxysm preceded for days by sleepiness, dull, heavy headache, sluggish flow of ideas; slight exercise causes sweat and great fatigue: during apyrexia, debility and painful diarrhoea; first moderate heat, then light sweat, ending with a crawling sort of chill; when all the stages seem aborted, and the patient says the chills amount to nothing; weak, languid, and loss of appetite.} S{General clammy sweat, with headache, nausea, pain in back, lassitude, and confusion of ideas.}

  
