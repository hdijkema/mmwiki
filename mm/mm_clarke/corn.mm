:rubric=Characteristics.
S{A popular remedy for aphthous and ulcerated conditions of mucous membranes. Hale says he has seen it cure chronic ulceration of mouth and throat, which had recurred for many years. R[Corn] has had an extensive proving. It has caused liver derangement with aching eyeballs. Disturbed sleep. The symptoms are B[<] at night; on waking; by motion of any kind; from catching cold; from summer heat; B[>] by coffee. The fever symptoms are identical with those of 2[R[Corn-f]]}


