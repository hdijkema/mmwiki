S{Dr. Cartier gave an account of this nosode in his paper read at the International Homoeopathic Congress, 1896 (2[Transactions], Part "Essays and Communications," p. 187). R[Tub-a] acts most prominently on the apices of the lungs, and it corresponds most closely to the bronchitis of influenza, which simulates tuberculosis, having cured several hopeless-looking cases. It has also done excellently in some cases of bronchitis following measles. The bacillus of avian tuberculosis has been identified with that of human tuberculosis, but the clinical properties of the two nosodes are not identical.}


