GT{Cinchona Boliviana (a variety of Cinchona calisaya growing further north). N. O. Rubiaceae. Tincture of bark.}


:rubric=Clinical.
S{Aphthae. Corns. Coryza. Hands, red. Mouth, commissures ulcerated. Pneumonia. Wry-neck.}


:rubric=Characteristics.
S{R[chin-b]. was proved by Modiedo, and the proving is accepted by Hering. It presents the main features of R[chin]., but some are peculiar and deserve separate mention. Among these are: Ulceration of commissures of mouth. Cough with dagger-like pains in base of right lung. Rigidity of nape of neck. Hands red; hot; or cold and sticky. There is also sticky sweat about anus. Flatulence, disturbed and dreamful sleep, and sensitiveness to touch are common to both.}


:head=SYMPTOMS.


:rubric=1. Mind.
S{Cowardice.} S{Gloom alternating with serenity.} S{Alternation of bad and good humour.} S{Fits of rage, blasphemes.}


:rubric=2. Head.
S{Vertigo as after a drunken fit, voracious appetite.} S{Headache in morning on awaking; leaves during the day, or lasts all day and leaves during sleep.}


:rubric=3. Eyes.
S{Violent pains like a stab in l. eye, followed by itching and tears.}


:rubric=4. Ears.
S{Detonation as of a cannon in r. ear.} S{Ringing, humming, whistling sounds.}


:rubric=5. Nose.
S{Much sneezing on awaking with smell of fresh pus.} S{Sensation of incipient cold with tight feeling and pain in throat.} S{Violent cold in head.} S{Mucus from posterior nares.}


:rubric=6. Face.
S{Aphthae on inner surface of lips; ulceration of commissures.}


:rubric=8. Mouth.
S{Mouth bitter, morning on waking.}


:rubric=9. Throat.
S{Constriction and burning in throat.}


:rubric=10. Appetite.
S{Hunger, or lack of appetite; repugnance to meat.} S{After eating: weight in stomach; cold; sneezing; sleepy and desire to sleep in evening.}


:rubric=11. Stomach.
S{Much gas rises from stomach.}


:rubric=12. Abdomen.
S{Pain in both hypochondria and under ribs on waking in the morning.} S{Pain in splenic region.}


:rubric=13. Anus.
S{Biting in anus as from worms; excoriated sensation.} S{Viscous sweat on anus.}


:rubric=15. Male Sexual Organs.
S{Pollutions exceedingly weakening.} S{Excoriation on inner and lower part of prepuce.} S{Gleet and ulceration of prepuce.}


:rubric=17. Respiratory Organs.
S{Cough with pain in r. lung, as from a dagger buried there; at times makes breathing laborious; burning in eyes; extremities cold.} S{Unbearable pain in r. lung, extends to region of liver, sensitive to touch.}


:rubric=20. Neck and Back.
S{Nape of neck rigid, cannot turn head to r.; any sudden movement causes a snap or crack.} S{Wry-neck.} S{Pain in: l. shoulder; in l. buttock, and also in r. hip-bone on sitting up in bed.}


:rubric=22. Upper Limbs.
S{Hands red; vessels distended.} S{Hands hot, even in cold and rainy weather.} S{Hands cold, sticky.}


:rubric=23. Lower Limbs.
S{Legs and feet icy cold, as if blown on by wintry wind.} S{Painful corns.}


:rubric=26. Sleep.
S{Dreams: of sea; of fishes; of swimming; of bathing; of journeys by land and sea; of vomiting worms.}

  
:end
