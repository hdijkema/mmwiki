:rubric=Characteristics.
S{Like 2[R[Jug-r]] and 2[R[Jug-c], R[Cary]] appears to possess antiscorbutic properties. An exclusive diet of the nuts for three or four weeks, in a girl seven years old, produced pronounced symptoms of scurvy; haemorrhages from all surfaces; on the least excoriation of skin very dark blood gushed out. Blood gushed from gums, nose, ears, and other parts.}


