:rubric=Characteristics.
R[Abies-c] has been only imperfectly proved, but it has marked symptoms which will serve to indicate it in any case of disease in which they may be prominent: 


S{Great appetite, tendency to over-eat; gnawing, hungry, faint feeling in epigastrium.} 

According to Hale it has cured: "A light-headed feeling, attended with a gnawing, hungry, faint feeling at epigastrium, craving hunger which, if gratified, was followed by distension of the stomach and hard beating of the heart." Among the peculiar sensations are: A feeling as if the right lung and liver were small and hard; pain beneath right scapula. The patient lies with the legs drawn up. Shivering as if the blood turned to cold water}


