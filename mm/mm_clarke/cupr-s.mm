:rubric=Characteristics.
Sulphate of Copper is a well-known emetic and caustic. 

S{It causes 2[forcible] vomiting and much nausea.} S{Paleness of face; or jaundiced appearance; pale skin generally; a peculiar diarrhoea; enlarged liver.} 

:div-begin=quote
J. D. Tyrrell (Med. Adv.], xix. 14) mentions the case of a lady who could not use R[Cupr-s] for "stamping" fancywork, because it "made her face hurt and swell so she could scarcely see, and her lips became everted, "turned inside out," she said." It is a well-known application in allopathic practice for stimulating flabby granulations, and it has cured itching eruptions, and manifestations of syphilis. 
:div-end

