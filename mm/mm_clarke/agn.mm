:rubric=Clinical.
S{Agalactia. Anus, excoriation and chaps of. Ascites. Dislocations, Gonorrhœa, secondary. Gouty joints. Gums, ulcerated. Impotence. Knees, coldness of. Leucorrhœa. Mouth, ulcers in. Rheumatic nodes. Spleen, swelling of; induration of. Sprains. Sterility. Testicles, swelling of; induration of. Toothache}


:rubric=Characteristics.
S{As its name implies, I[Agnus Castus ]produces its chief effects in the sexual sphere, causing depression of function. General debility and depression of vital power are marked. There ire very characteristic symptoms in the mental sphere. Great sadness with a fixed idea of approaching death. This fear is not of 2[immediate ]death as with 2[Aconite,] but the patient thinks it is sure to come after a while, and there is no use in doing anything. When this mental state is found after confinement and the milk fails to appear, R[Agn] is the remedy. Absent-mindedness. Drowsiness. It corresponds to lymphatic constitutions. Premature old age, with apathy and melancholy, self-contempt from sexual abuse. Nervous debility in unmarried persons. Impotence and gleet in old offenders. Among other peculiar symptoms are: Illusion of smell as of herrings; or musk; ulcers in mouth and on gums. Tearing pain in lower jaw. Toothache from hot food or drink. Swelling and induration of spleen. Rumbling of flatus during sleep. Discharge of prostatic fluid whilst straining at stool. Suppressed menses with abdominal pain. Leucorrhœa staining yellow. Sterility. Deficiency of milk, with despair of recovery. Pain as if dislocated in joints. Joints easily twisted. Rheumatic and gouty nodosities on joints}


:rubric=Relations.
S{Agnus is the only proved member of the Verbena family, though the Labiatæ or Mint family are closely allied. 2[It is antidoted by: ]R[Camph], R[Nat-m] (headache); strong solutions of table-salt. 2[It is followed well by: ]R[Ars], R[Bry], R[Ign], R[Lyc], R[Puls], R[Sulph], Selen. 2[Compare: ](Leucorrhœa staining yellow), R[Nux-v], R[Chel], R[Carb-an], R[Kreos] Teste groups it with R[Mur-ac] and R[Hyos].}


:rubric=Causation.
S{Sexual excesses. Repeated attacks of gonorrhœa or gleet. Sprains or over-lifting}


:head=SYMPTOMS.


:rubric=1. Mind.
S{Extreme absence of mind; unable to recollect things; finds it difficult to read or keep up the train of thought.} S{Despairing sadness; low-spirited; fears of death; keeps repeating that she will soon die}


:rubric=2. Head.
S{Tearing pain, with pressure in the temples and forehead (in the brain); 2[< ]during motion.} S{Pain in the temple, as from a blow.} S{Heaviness in the head, and pressure, as if the head would fall forward.} S{Pain in the vertex, as from staying in a room filled with a thick and smoky atmosphere.} S{Tension and chilliness in the scalp, which is warm to the touch}


:rubric=3. Eyes.
S{Corrosive itching or gnawing itching over and on the eyebrows, on the eyelids and under the eyes; 2[> ]by scratching, but it soon returns.} S{Dilated pupils (and photophobia)}


:rubric=4. Ears.
S{Roaring in the ears.} S{Hardness of hearing}


:rubric=5. Nose.
S{Odour before the nose, like herrings or musk.} S{Hard, aching pressure on the dorsum of the nose; 2[> ]by pressure}


:rubric=6. Face.
S{Corrosive itching of the cheeks, under the eyes, and on the chin.} S{Formication in the cheeks.} S{Rending, tearing pain under the alveoli of the r. lower jaw}


:rubric=8. Mouth.
S{Ulcers in the mouth and on the gums.} S{The teeth are painful when touched by warm food or drink}


:rubric=10. Appetite and Taste.
S{Thirstlessness and aversion to drink.} S{Metallic, coppery taste}


:rubric=11. Stomach.
S{Nausea in pit of stomach when standing; later in the abdomen, with a sensation as if the intestines were pressing downwards; constant inclination to support bowels with the hands}


:rubric=12. Abdomen.
S{Abdomen sore to touch.} S{Swelling and induration of the spleen, esp. after intermittent fevers.} S{Ascites.} S{Rumbling of flatulence during sleep.} S{Fear as if entrails were sinking down; constantly wants to support bowels with hands}


:rubric=13. Stool and Anus.
S{Difficulty of passing soft stools.} S{When pressing at stool, discharge of prostatic fluid.} S{Sensation as of subcutaneous ulceration near the anus, only when walking.} S{Corrosive itching of the perineum.} S{Rhagades at the anus.} S{Deep fissures of the anus, often giving pain when walking}


:rubric=14. Urinary Organs.
S{Frequent micturition}


:rubric=15. Male Sexual Organs.
S{Diminution of sexual power.} S{The penis is small and flaccid; so relaxed that voluptuous fancies excite no erection.} S{Feeble erections without sexual desire.} S{The testicles are cold, swollen, hard, and painful.} S{Impotence, with gleet (esp. with those who have frequently had gonorrhœa).} S{Gleet, without sexual desire or erections.} S{Emission of prostatic fluid when straining at stool; during micturition.} S{Drawing along the spermatic cords.} S{Pollutions from irritable weakness with prostatorrhœa.} S{Itching of the genital organs.} S{Yellow discharges from the urethra.} S{Gonorrhœa, with suppressed sexual desire}


:rubric=16. Female Sexual Organs.
S{Transparent leucorrhœa; parts very relaxed.} S{Leucorrhœa spotting linen yellow.} S{Sterility, with suppressed menses, and want of sexual desire.} S{Milk scanty or entirely suppressed.} S{Deficient, secretion of milk in lying-in women.} S{Swelling and inflammation of the uterus.} S{Retained placenta}


:rubric=18. Chest.
S{Hard pressure in the region of the sternum, esp. during a deep inspiration.} S{Cough in the evening in bed, before going to sleep}


:rubric=22. Upper Limbs.
S{Hard pressure in right axilla and upper arm; worse from touch and motion.} S{Swelling of the finger joints, with arthritic, tearing pains}


:rubric=23. Lower Limbs.
S{Lancinating pain in the right hip-joint.} S{Heaviness of right foot, as from a weight.} S{Stitches in the legs (left big toes).} S{Tearing pain in joints of toes; worse when walking}


:rubric=24. Generalities.
S{Great debility.} S{Inflammatory, rheumatic swelling of the joints.} S{Gouty nodosities.} S{Sprains, bad results from lifting too much.} S{Feet turn under when walking}


:rubric=25. Skin.
S{Corrosive itching on different parts of the body, 2[> ]by scratching, but it soon returns.} S{Itching around the ulcers, in the evening}


:rubric=27. Fever.
S{Pulse small, slow, imperceptible.} S{Chilliness, internal with trembling, the external skin is warm.} S{Much chilliness, with cold hands.} S{Flushes of burning heat, principally in the face, with cold knees in the evening in bed.} S{Perspiration almost only on the hands, when walking in the open air}

