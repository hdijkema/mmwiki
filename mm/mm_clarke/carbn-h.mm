:rubric=Characteristics.
S{This gas, which was experimented with by Sir Humphrey Davy, produces a condition almost resembling an apoplectic attack. Stupefaction: comatose; surface cold and completely insensible. Spasm as in lock-jaw. Arms flexed at elbows. One mental symptom is peculiar: an extraordinary sensation of contentment, so that life seems exalted; all his thoughts appear in a moment as if seen in an inner mirror. Many eye symptoms were noted: steady and extreme oscillation of the eyeballs. Eyes distorted. Lids half closed. Dark bodies moving before eyes. B[>] By open air (vertigo, convulsions). It is a popular custom to take children suffering from whooping-cough to breathe the air at a gas-works. A case has been recorded in which an accidental escape of gas in a child's bedroom affected a prompt cure of its whooping-cough. Illuminating gas is a complex gas, carburetted hydrogen being the leading ingredient. In the Schema are included some symptoms produced by illuminating coal gas.}


