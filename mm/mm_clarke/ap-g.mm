:rubric=Characteristics.
S{A proving of tincture of seeds produced the following symptoms: Unpleasant feeling at stomach, with belchings tasting of celery. Heartburn. Spitting up of food. Gone feeling at pit of stomach lasting for hours and partially relieved by eating.} S{Effects of eating the root: during the following day and night, most obstinate retention of urine, which could only be relieved by the catheter. On another person this was observed: Inability to sit still or lie still in bed from feeling of "fidgets"; cannot keep mind from I thinking. In a proving under the direction of W. P. Wesselhœft (I[Med. Advance], April, 1886) a remarkable symptom was: a dull ache in sacrum B[<] when lying down, B[>] when moving about. Another symptom was "sensation as if both eyes were sunken back into their sockets." Throbbing headaches, B[<] by slightest motion; B[>] by rest, mostly left-sided. Urticaria, always appearing with shuddering; intense stinging itching with rapid change of location. Pressure in stomach preceding urticaria, and relieved when it appears. Toothache in left molars B[>] by holding cold water in the mouth. Hering cured with R[Ap-g] an old abscess near the umbilicus. Allen mentions: "Profuse discharge from granulating ulcers, intense constriction over sternum, with drawing feeling through to back on lying down," as having been cured by it}


