:rubric=Characteristics.
S{2[Absinthium ]has been proved, but a number of the symptoms are taken from observations made on absinthe drinkers. The convulsions of 2[Absinth.] are preceded by trembling; the patient makes grimaces; bites tongue; foams. Halbert regards R[Absin] as of especial service in cases of minor epilepsy, where consciousness is not entirely lost. The characteristic is "a peculiar vertigo on rising, with a tendency to fall backward." In a case of epileptoid vertigo, signs of constant cerebral and spinal congestion; nausea and tendency to frequent vomiting, persistent tremors; epileptoid attacks of hysterical character and opisthotonos, R[Absin] first in Ø, and later in 3, completely cured. Tremor is a marked feature of the remedy: tremor of tongue; of heart. Magnan, who has studied absinthism, says the characteristic symptoms of absinthe are: Sudden and severe giddiness, epileptiform seizures, delirium with hallucinations, and loss of consciousness. For some time after the attack there is loss of memory. The giddiness and epileptiform attacks are the most important of the symptoms. He adds that those who take absinthe are liable to hysterical manifestations. There is exhilaration followed by horrible delirium (R[Bell]); patient obliged to walk about (R[Art-v], R[Cham], R[Cina] have 2[> ]moving about). Patient walks about in distress, seeing all sorts of demons. Sleeplessness; typhoid with congestion of base of brain. It corresponds to nervousness, excitement, and sleeplessness in children}


