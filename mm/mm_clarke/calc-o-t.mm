:rubric=Characteristics.
S{This preparation of I[Calcarea] has been used in preference to others in cases of simple bland leucorrhoea. Water in which eggs have been boiled is popularly supposed to have the property of causing warts if brought in contact with the hands. This suggests R[Calc-o-t]. as possibly the best I[Calcarea] preparation to prescribe in cases of warts. No doubt R[Calc-o-t]. has the general properties of R[Calc-c ostrear]. (i.e., ordinary R[Calc].), but it has been little used clinically, "Backache, as if broken in two," is a keynote.}

  
