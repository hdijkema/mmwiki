:head=SYMPTOMS.


:rubric=1. Mind.
S{Memory lost.} S{Lethargy.} S{Anxiety, feels she will die suddenly.} S{Alternating moods.} S{Dreamy, absent-minded.} S{Took the wrong car without realising where she was going.} S{When riding in the car rode past the place she intended to get off at.} S{Suspicious, thinks her husband is going to put her in an insane asylum; every day, 3 to 8 p.m., for ten days, yet she knew it was a delusion.}


:rubric=2. Head.
S{Hard aching pain, commencing l. frontal eminence, spreading down l. side to teeth, thence to r. frontal eminence, then to teeth r. side.}


:rubric=3. Eyes.
S{Swelling like bags over eyes and under brows.} S{Aching and itching in eyes; dim vision, redness of margins and twitching.} S{L. eye waters; from concussive cough.}


:rubric=5. Nose.
S{Catarrh; scabs; obstruction.}


:rubric=6. Face.
S{Expression bloated, besotted.} S{Face sallow.} S{Burning face.} S{Blue circles round eyes.}


:rubric=9. Throat.
S{Accumulation of mucus; glossy, thick, tough.} S{Difficult empty swallowing, but easy swallowing of solids and liquids.} S{Warm drinks grateful.}


:rubric=11. Stomach.
S{Nausea B[>] by ice; B[<] by water which 2[=] vomiting.}


:rubric=12. Abdomen.
S{Intolerance of tight clothing.}


:rubric=13. Stool.
S{Diarrhoea early in morning, on, waking.} S{Stool, gushing, frequent, watery, at first without pain; after several hours, great pain before the motions.}


:rubric=15. Male Sexual Organs.
S{Violent sexual desire.}


:rubric=16. Female Sexual Organs.
S{Increased desire.} S{Yellow leucorrhoea.} S{Pain in r. ovary.} S{Herpetic eruption on labia.} S{Menstrual flow very profuse; light with dark clots.}


:rubric=19. Heart.
S{Feels as if heart were distended, or swelled to fill the whole chest.} S{Strongly conscious of heart.} S{Hard aching and sudden sharp stitching in heart.} S{Throbbing or fluttering under l. scapula.} S{At 3 p.m. sensation of fluttering followed by feeling that heart fell down into abdomen; then pulse became feeble, with heat lasting until after midnight.}


:rubric=17. Respiratory Organs.
S{Hard, dry, tickling cough, B[<] 3 p.m., even causing escape of urine.}


:rubric=27. Fever.
S{Chill or fever beginning in afternoon.}

  
