:rubric=Characteristics.
S{B[Cenchris] has the main features of all the serpent poisons: Coma; semi-consciousness; insensibility of cornea; swelling of upper lip; general swelling; paralysis; cold, clammy sweat. Distinctive symptoms are: a marked alternation of moods; and dreamy absent-mindedness. The dreams of B[Cenchris] are very vivid and horrible, cannot be shaken off during waking hours; often lascivious. Swelling above eyes, below brow, like an over-hanging bag of waters. Diarrhoea has been cured having the following characters: "Pain before stool; stool papescent cold but not chilly." Yellow Leucorrhoea; pain in right ovary herpetic eruption on labia. Hard, dry tickling cough, B[<] 3 p.m. Restlessness and sense of suffocation; palpitation; sensation of dying. Must lie with head drawn back, she chokes so. Tight clothing unbearable. Chill or fever beginning in afternoon. Feeling as if whole body were enlarged to bursting; B[<] in region of heart. "Awoke with throbbing in vulva and anus, followed by a dull aching in the sacral region, B[>] by walking about." Symptoms B[<] lying down; B[<] afternoon; B[<] evening and all night; B[<] on waking.}


:rubric=Relations.

S{R[Lach] is nearest, but affects left ovary more than right; R[Cench] has difficult empty swallowing with easy swallowing of solids and liquids; R[lach]. can swallow solids but not liquids; R[crot-h]. (lost sense of position and direction); Crocus (alternating moods); R[kali-c]. (swelling of upper lids).}

