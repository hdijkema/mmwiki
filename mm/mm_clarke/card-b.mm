:rubric=Characteristics.
S{The proving of R[card-b]. shows a strong action on the eyes: twitchings, and disturbance of vision; flickerings; muscae volitantes. The prover had "blackness before eyes for a short time." Eyeballs feel larger. "Burning" is a symptom which runs through the proving. There is a "bitter burning" in the stomach, as if it were much deranged; burning in hands after sweat; burning in the arms on exerting them. The gastro-abdominal disturbances of R[card-m]. are observed, in less degree, in R[card-b]. There is disordered taste, dysphagia; yawning and hiccough; cutting pains in abdomen, vomiting and diarrhoea. The respiratory sphere is strongly affected and there is pain and contractive sensation in the trachea with hoarseness. Inspired air feels cold. Other symptoms are: Feeling as if buccal cavity were contracted. Tension in tendons. Aching and cracking in joints. Aching, cutting pain, slight swelling of veins. Red and afterwards yellow spots on skin. The most prominent of all the symptoms in the proving is the sensation of contraction which occurs in many parts. Symptoms are B[<] by motion, by touch, by walking, by stretching the limbs.} 
S{![In two fatal cases of poisoning, of a boy and a girl, from eating the root of the 2[Carline] Thistle (2[Carlina gummifera, Atractylis gummifera]), which has an enormous root, purple composite flowers, surrounded by radical spinous leaves, there was stertorous breathing, drowsiness, retching without actual vomiting, asphyxia and coma. After death the tongue protruded slightly between the lips; pupils were greatly dilated; there was evidence of general venous congestion. In one case (that of the boy) the veins of the brain were extremely congested.]}


