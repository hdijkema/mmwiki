:head=SYMPTOMS.


:rubric=1. Mind.
S{Anxiety, fear, starting at every noise, frequently breaking into cold sweat.} S{Fretful; during fever.} S{Cross humour.}


:rubric=2. Head.
S{Confusion of the head, interrupted stitches in temples, four times in succession (more l. than r.).} S{Vertigo on raising head, B[<] on stooping.} S{Pressure in occiput from crown; for several days pressure on eyeball as if it would come out.} S{Pressure on forehead esp. when stooping.} S{Heaviness in head.}


:rubric=3. Eyes.
S{Sensation as if (l.) eyeball would be pressed outwards, rather a pleasant than a painful sensation.} S{Quivering movement in l. canthus, tickling in l. eye, very painful, with flow of tears; stitches in inner canthus.} S{Eyeballs feel larger.} S{Dimness and whirling before eyes.} S{Grey spots pass before eyes.} S{Blackness before eyes, for a short time.} S{Flickering before l. eye as if small fiery stars passed in front of it, removed by winking.}


:rubric=4. Ears.
S{Ringing in ears.} S{Roaring in r. ear, soon going off.} S{Feeling as if something were in ear., also as if something burst like a bladder, and then humming.}


:rubric=5. Nose.
S{Continued violent griping and tickling in nose as if coryza were coming on.}


:rubric=6. Face.
S{Flushes of heat in face.}


:rubric=8. Mouth.
S{Continued feeling, lasting for 8 to 10 days, as if buccal cavity contracted and narrowed, beginning and declining slowly; B[>] after eating.} S{Much mucus in mouth.} S{Constant overflow of saliva.} S{Sour taste for six days, always getting worse and almost sulphurous, B[>] for awhile by eating.} S{Mouth dry, morning and evening, with thirst.} S{Tickling on surface of tongue near root, lasts five minutes and changes to shoots like electric sparks; toothache r. side in periods, drawing more than shooting.} S{Flat taste, tongue sensitive as if swollen; much furred.}


:rubric=9. Throat.
S{Swallowing difficult.}


:rubric=11. Stomach.
S{Bitter burning, as though stomach very much deranged.} S{After eating, eructation.} S{Constant yawning and hiccough.} S{Though stomach is full it feels empty.} S{Appetite bad; nausea.} S{Vomiting.}


:rubric=12. Abdomen.
S{Constant drawing, cutting pain in abdomen.} S{Slight pinching in abdomen; curious sounds r. side of navel.}


:rubric=13. Stool.
S{Diarrhoea.}


:rubric=17. Respiratory Organs.
S{Hoarseness.} S{Pain in trachea as if inflamed; as if fatigued by long coughing; inspired air feels cold; coughs frequently, it sounds rough, like hissing.} S{Dyspnoea.} S{Breathes quickly, must draw in air with an effort, as the trachea feels closed up, each inspiration is like a low whistle, esp. evenings.} S{Feels as if too tightly laced; unable to sing or talk long for several days.} S{Constant dry cough.}


:rubric=18. Chest.
S{Throbbing below l. breast.}


:rubric=20. Neck, Back, and Trunk.
S{Dull shooting in sides over hip, now r. now l., at first only on stooping or moving, but afterwards at all times.}


:rubric=21. Limbs.
S{Cracking in joints, motion difficult.} S{Limbs heavy as if paralysed; B[<] during the fever.} S{Sensation when touching the limbs as if there were bruised spots (veins painful and prominent).} S{Pains in all the bones, esp. when the limbs have been stretched.} S{Tension in tendons.}


:rubric=22. Upper Limbs.
S{When leaning on arms burning in them, but they are not hot to the touch.} S{Contraction in elbows; and arms.} S{Feeling in forearm, as if in the blood-vessels, like a long knife-stab, with continued burning.} S{Trembling of hands.}


:rubric=23. Lower Limbs.
S{Weakness and giving way of knees when walking, esp. during the fever.} S{Feet weak after sitting, soles feel sore when treading.}


:rubric=24. Generalities.
S{Aching and slight swelling of veins.} S{Long, knife-like stab in veins of forearm.}


:rubric=25. Skin.
S{Painless rash, like nettle-rash; a goose-skin, rather pointed, hard, preceded by cold creeping over the whole body, accompanied by feverish symptoms, lasting several days and slowly disappearing.} S{Small red spots on the finger, lasting several days, followed by a yellow spot lasting a long time.}


:rubric=27. Fever.
S{Febrile chill with goose-flesh several days from noon to evening, at times appearing earlier.} S{Fever consisting of flushes of heat in face; after eating, over whole body, without thirst, with dimness of vision and pressure in the eyes, hot breath, hot lips, hands, and feet.} S{Burning on exerting arms.} S{Burning under skin on face, afterwards in other parts of body.} S{Slight general sweat.} S{Sweat followed by burning heat in hands.}

  
