:rubric=Clinical.
S{Amaurosis. Convulsions. Cyanosis. Diuresis. Epilepsy. Nystagmus. Respiration, slow. Strabismus. Tetanus. Trismus.}


:rubric=Characteristics.
S{2[Nitro-benzene] is a very poisonous substance, and our knowledge of its action is due to numerous cases of poisoning that have occurred. It produces faintness, sinking, convulsions, twitchings, stupor. It slows the respiration until death occurs. Rolling of the eyeballs in their vertical axis is a prominent feature, and the pupils are dilated. Blueness of lips, face, and finger-nails. Flapping of alae nasi. Head drawn backward and to the left side in spasms. Involuntary evacuations. Paralysis of all limbs. Punctiform ecchymoses. The odour of bitter almonds is strong in the secretions.}


:rubric=Relations.


