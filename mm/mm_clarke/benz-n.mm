:head=SYMPTOMS.


:rubric=1. Mind.
S{Extreme mental excitement.} S{Loquacity, followed by drowsiness, soon passing into stupor.} S{Soon became unconscious with inarticulate, confused speech.}


:rubric=2. Head.
S{Vertigo; and headache.} S{Trembles; staggers; falls unconscious.} S{Sensation of formication under scalp, or as if the hair was bristling up.}


:rubric=3. Eyes.
S{Rolling of eyeballs.} S{Constant slow movement of eyeballs from l. to r. under closed lids.} S{The eyeballs showed a constant turning inward and outward in a slow, regular motion, with the visual axes perfectly parallel.} S{Constantly rolling in vertical axes.} S{Strabismus.} S{Eyes staring.} S{Great injection of conjunctiva of bulbs.} S{Eyeballs seemed enlarged.} S{Pupils dilated; insensible to light.} S{Distorted vision and flashes of strange light and colours.}


:rubric=4. Ears.
S{Violent roaring in ears, and sounds.}


:rubric=6. Face.
S{Stupid expression.} S{Cyanosis.} S{Oedema.} S{Covered with cold sweat.} S{Trismus.}


:rubric=8. Mouth.
S{Tongue white and swollen; thick and soft.} S{Burning taste, followed by numbness and tingling of tongue and lips.} S{Stammering speech.} S{Tenacious saliva flowed from the mouth.}


:rubric=11, 12, 13. Stomach, Abdomen, Stool.
S{Burning in throat and stomach.} S{Nausea, vomiting, of food eaten.} S{Pains in abdomen.} S{Involuntary evacuations.}


:rubric=14. Urinary Organs.
S{Diuresis; urine brownish red; odour of bitter almonds.} S{Urine and faeces passed involuntarily.}


:rubric=17. Respiratory Organs.
S{Sighing respiration.} S{Respiration frequently interrupted.} S{Dyspnoea.} S{Respiration exceedingly slow; shallow.} S{Snoring.} S{Respiration difficult, catching, accelerated.}


:rubric=19. Heart.
S{Pulse rapid, weak, irregular.} S{Pulse full and slow.} S{Pulse scarcely perceptible.}


:rubric=20. Neck.
S{Stiff-neck; trismus and fibrillary twitchings in masseter muscles.}


:rubric=21. Limbs.
S{Twitchings in hands and feet.} S{Arms spasmodically flexed, sometimes extended.} S{Arms spasmodically flexed at first, afterwards relaxed.}


:rubric=24. Generalities.
S{Gait as if intoxicated; staggering here and there.} S{Violent convulsions; with unconsciousness.} S{Fell unconscious; extremities paralysed; evacuations involuntary.} S{Fainted from inhaling vapours.} S{Over-powering odour of bitter almonds.}


:rubric=25. Skin.
S{Punctiform ecchymoses.} S{Skin livid; bluish-grey.} S{Ecchymosed spots, bluish-black, beneath scalp and on gluteal muscles.}


:rubric=27. Fever.
S{Skin cold.} S{Sweat on forehead and face.} S{Whole body covered with profuse cold sweat.}

  
