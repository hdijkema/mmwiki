:rubric=Characteristics.
S{R[Cascarilla] has a family resemblance to the other Euphorbians. It acts prominently on the digestive tract. Its leading symptoms are: Sensation of swelling deep down in throat. Accumulation of mucus in throat. Hunger after meals and feeling of emptiness. Aversion to smell of tobacco smoke. The concussion of every step is felt painfully in stomach. Griping preceding morning stool. The abdominal symptoms are B[>] by warm drinks. Movings about in abdomen as if hot water undulating there. Boils in inguinal region. Diarrhoea with backache and lassitude. Lassitude and muscular weakness are marked throughout. Constant slight urging with gnawing pain high up in rectum. Constipation, stools hard, in pieces, covered with mucus. Passes bright blood with or without stool, in large quantities, causing weakness. Short, dry cough from tickling in trachea. Haemoptysis. Stitch upward in l. side of chest. Backache and weakness. Inclination to lie down. Sleep with clear consciousness. Heat with thirst for warm drinks (intermittents).}


