S{Evacuations difficult, hard, broken, and covered with mucus, preceded by pinchings in the abdomen.} S{Discharge of clear blood from the rectum, with the evacuations, during and after hard, brown stool in large lumps; and without stool.} S{Diarrhoea alternating with hard, lumpy stool.}


