:rubric=Characteristics.
S{This salt has been proved. The symptoms are in the main identical with those of the metal, but some are peculiar or more pronounced in the one than in the other. The Muriatic element is seen in the heart symptoms; violent palpitations, sore aching, heaviness and sensation of rigidity in heart, hyperaemia from heart disease. Catarrhal and glandular affections are marked. Warts appear in various parts: on the tongue, on the genitals. Digestion is slow. Diarrhoea after eating. R[Aur-m] is a sycotic remedy causing suppressed discharges to reappear. Halbert has given R[Aur-m] 2x with much success in cases of sclerotic and exudative degeneration of the nervous system. He narrates a case of disseminated sclerosis, the result of a fall; one of exudative localised meningitis; and one of Morvan's disease greatly improved under its use. This patient, a man cook, had hypertrophy of all the fingers. In some of them this had progressed till painless whitlows appeared. Analgesia and anaesthesia were present, and some atrophy of the muscles of the hand and arm. Brachial neuritis appeared to be the causative factor in the case. Remarkable improvement occurred under R[Aur-m] 2x. R[Aur-m] is particularly valuable in haemorrhages from the womb at climacteric and after, which are frequently of sycotic origin. Violent chill and fever. Hectic fever. Ascending stairs B[<] Cold washing and cold weather 2[>.] Warmth B[<]; throws off bed-covers.}
:rubric=Suited to: 
S{Lymphatic, scrofulous constitutions. Gold salts, like mercury salts, dissolve organic tissues; they stimulate the heart more, but act on fluid tissues less powerfully than mercury.}


