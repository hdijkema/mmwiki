:head=SYMPTOMS.
:rubric=1. Mind.
S{Frenzied with excitement.} S{Spectral illusions; sees insects and crawling things and tries to catch them.} S{Mania, tries to escape.} S{When spoken to often turns his head to wrong side.}


:rubric=2. Head.
S{Vertigo on turning head suddenly.} S{Feeling as if head were screwed up; walking caused the most severe sticking pains; B[>] towards 11 a.m., and disappeared by evening.} S{Head very hot.} S{Headaches of epileptics.} S{Sticking pains in back of skull and over eyes, B[<] every motion and stepping.} S{Sticking l. temple on waking, extending into ear and eye; B[>] moving in open air.} S{Headache, with flushed face, blindness, and delirium.}


:rubric=3. Eyes.
S{Objects appear large, with red halo.} S{Everything seems elongated, objects appear in a cloud.} S{Clouds of flies; motes; bright spots; stars; flashes.} S{Figures on carpet appear constantly and successively to rise in her face.} S{Diplopia.} S{Sharp neuralgic pains in and about eyes.} S{At 9 p.m. eyelids heavy, difficult to keep open.}


:rubric=6. Face.
S{Face hot and very red.} S{Deathly pallor.}


:rubric=8. Mouth.
S{Mouth dry.} S{Tongue thick; cannot articulate distinctly.} S{Tongue semi-paralysed.} S{Speech slow, embarrassed, stuttering, chattering.}


:rubric=9. Throat.
S{Burning caused by coughing.} S{Dryness.} S{Dysphagia; swallowing causes paroxysms of suffocation.} S{Throat dark red.}


:rubric=11. Stomach.
S{Vomiting of food; after hot drinks; with severe sticking pains in umbilical region.} S{B[>] After vomiting.} S{Region of stomach very sensitive; swelling in pyloric region.}


:rubric=14. Urinary Organs.
S{Frequent micturition.} S{Involuntary, but scanty; nocturnal enuresis.} S{Cutting, drawing pains in l. ovary, extorting screams, B[>] bending; ovary swollen and tender during menses.} S{Epilepsy from ovarian irritation.}


:rubric=19. Heart.
S{Failure of action from chloroform and other cardiac paralyzers.} S{Accelerated pulse.}


:rubric=20. Back.
S{Burning in back under sternum and in region of stomach.} S{Spinal irritation, pressure causes pallor, nausea, belching, and retching.}


:rubric=21. Limbs.
S{Numbness; heaviness; paralysis of limbs.}


:rubric=24. Generalities.
S{Hyperaesthesia of some nerves: ophthalmic, auditory, olfactory, vaginal, spinal, solar-plexus, nerves of uterus and bladder.} S{Great desire for open air (faintness).} S{Very weak in the open air.} S{Giddiness and staggering.} S{B[>] From motion.}


:rubric=26. Sleep.
S{Disturbed by gastric pains.} S{Restless through night.} S{Waked by frightful dreams.}

  
