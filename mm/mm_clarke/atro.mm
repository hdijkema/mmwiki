:rubric=Characteristics.
S{Atropine, as the most active principle in R[Bell], produces, as we should expect, most of the characteristic effects of that drug; but at the same time the two are not identical. R[Atro] produces hyperaesthesia of the sensory nerves. The most numerous symptoms have been observed in the eyes. All kinds of illusions of vision. Everything appears large. Motes over everything. I once cured with it a lady who had an appearance of shadows passing over whatever she looked at. It has shown an affinity for the pancreas, which relates it to R[Iod], R[Kali-i], and R[Iris] A case of chronic wryneck, of three years' standing, was relieved and finally cured within two weeks by injections of 1/120 into the platysma, the dose being increased up to 1/20th. This was in old-school practice. It has caused difficulty of speech: frequent stuttering, especially at words difficult to pronounce; articulation indistinct, rapid and chattering.}
