:rubric=Clinical.
S{Face, neuralgia of. Hydrophobia. Tic douloureux. Toothache.}


:rubric=Characteristics.
S{Most of the symptoms of R[Coccinella] appear in the teeth and nerves of the face and mouth. It has been used in hydrophobia, and therein its actions would appear to be analogous to that of R[Canth]. Symptoms indicating it are: B[<] by any bright object. Is awakened by profuse accumulation of Sativa; vomits and complains of sore throat. The neuralgia of R[Cocci-s] is B[<] at night, accompanied by icy cold extremities and cold, moist skin; the right side is most affected. Pains extend from one part to another, and may jump from side to side. Its attacks recur periodically; are often of brief duration but quickly recurring, generally B[<] at night; but may be entirely absent in the night. Returning every eight, ten, twelve, fourteen, or twenty days.}


:rubric=Relations.


:rubric2=Compare

S{in tic douloureux, R[spig]., R[thuj].}


:head=SYMPTOMS.


:rubric=2. Head.
S{Dull headache, as from an enlargement of the brain towards the occiput.} S{Semi-lateral, tearing and shooting pains in the sinciput.}


:rubric=6. Face.
S{Faceache, the disappearance of which is attended by faintness, oppressed breathing, and intermittent pulse.} S{Flush of blood (congestion) to face, like a transient heat; cheeks red, esp. r.} S{Pressure and heaviness, esp. over r. eye, which is sensitive to least touch.} S{During paroxysm, cannot open eyes, pain B[<] by any bright object.} S{Tearing, rhythmical with pulse, extending from r. upper jaw to the lobule of ear.}


:rubric=7. Teeth.
S{Chronic pain extending from superior molars to forehead; affected part paler; attack lasts only a minute but is instantly repeated, B[<] at night, preventing sleep.} S{Sensation of coldness in all the teeth; in the entire buccal cavity.} S{Rhythmical, violent drawing in both rows of teeth while eating.} S{Sensation as though the teeth would be drawn out; severe drawing in jerks.} S{Jerking and tearing with stitches, extending to occiput with warmth through whole head.} S{Painful sensation in back teeth as if they were hollow and air were forced into them.} S{Pulsative jerking in back teeth.} S{Swollen gum.}


:rubric=8. Mouth.
S{Tobacco bites the tongue severely.}


:rubric=9. Throat.
S{Sensation in the throat as though the uvula was too long (Nux vom. has this same sensation, when the uvula is bluish and inflamed).}


:rubric=10. Appetite.
S{Appetite lost.}


:rubric=17. Respiratory Organs.
S{With disappearance of pain in face respiration becomes oppressed.}


:rubric=26. Sleep.
S{Nocturnal attacks of pain entirely preventing sleep, but could not sleep even when pain did not appear.} S{Attack ends in a general heat and sleep of two or three hours, from which he wakes without any pain.}

  
