S{The most peculiar symptoms of R[Both-l], for which Ozanam is the authority, are these: Amaurosis. Day-blindness.} S{"can scarcely see her way after sunrise." Inability to articulate without any affection of the tongue. Haemorrhages, the blood being fluid and black. All the symptoms of pulmonary congestion, oppressed breathing and bloody expectoration, more or less profuse. Paralysis of one arm or one leg only. After being bitten in the little finger of one hand, paralysis began in the finger-tips of the other hand and extended over the whole of that side. Deep gangrene, bones laid bare and necrotic. Intolerable pain in right great toe (patient bitten in left thumb). The diagonal course of symptoms is marked. Hemiplegia. Dissecting gangrene. Slight shivering followed by very profuse cold sweat.}


