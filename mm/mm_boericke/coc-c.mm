:rubric=generals
The clinical application of the symptoms of this remedy, place it among the medicines for spasmodic and whooping coughs, and catarrhal conditions of the bladder; spasmodic pains in kidneys, with visceral tenesmus. Anuria, anasarca, ascites.

:rubric=mind
Early morning or afternoon sadness.

:rubric=head
Suboccipital soreness; worse after sleep and exertion. Headache, worse from lying on back, better with the head high. Dull pain over right eye in morning. 2[Sensation of a foreign body between upper lid and eyeball]. Distress from cinders lodged in eye.

:rubric=respiratory
Constant hawking from enlarged uvula; coryza, with inflamed fauces; 2[accumulation of thick viscid mucus], which is expectorated with great difficulty. 2[Tickling in larynx]. Sensation of a crumb behind larynx, must swallow continually; brushing teeth causes cough. Fauces very sensitive. Suffocative cough; worse, first waking, with tough, white mucus, which strangles. Spasmodic morning cough. 2[Whooping cough attacks end with vomiting of this tough mucus]. Chronic bronchitis complicated with gravel; large quantities of albuminous, tenacious mucus, are expectorated. Walking against wind takes breath away.

:rubric=heart
Sensation as if everything were pressed toward the heart.

:rubric=urinary
Urging to urinate; 2[brick-red sediment. Urinary calculi, haematuria, urates], and uric acid; lancinating pains from kidney to bladder. Deep-colored, thick urine. Dysuria.

:rubric=female
Menses too early, profuse, 2[black] and thick; 2[dark clots], with dysuria. Intermittent menstruation; flow only in evening and at night. 2[Large clots] escape when passing water. 2[Labia] inflamed.

:rubric=modalities
M[Worse], left side, after sleep, touch, pressure of clothing, brushing teeth, slightest exertion. M[Better], walking.

