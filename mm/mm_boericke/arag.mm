:local-name=White Loco Weed-Rattle Weed

:rubric=generals
Acts principally on nervous system, producing a bewildered, confused state. Symptoms of incoordination and paralysis. Locomotor ataxia. Tired in the morning.

:rubric=mind
Great depression; worse in morning or evening. Cannot study. Cross, irritable, restless. 2[Bewildered]. Mental confusion and apathy. Desires to be alone. Difficulty in concentrating mind, absent-minded. Lack of ambition. Defective expression in writing. Restlessness and aimless wandering. Must concentrate his mind on walking.

:rubric=head
Diplopia. Burning in eyes. Cracking of lower lip.

:rubric=throat
Aches. Feels dull. Sore with nausea. Pharynx dark, swollen, glazed.

:rubric=respiratory
Weight on chest in region of ensiform cartilage. Constriction as of a wide band. Soreness of chest under sternum. Oppression.

:rubric=extremities
Weakness of limbs. Pain in left sciatic nerve. Cramps of muscles on front of leg while walking.

:rubric=relations
:rubric2=compare
R[Astragallus] and R[Oxytropis], two varieties of Loco Weed; also R[Baryta].

:rubric=dose
Sixth and two hundredth potencies.

