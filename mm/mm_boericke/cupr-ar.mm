:rubric=generals
A remedy for symptoms depending on deficient kidney action, various intestinal affections, cholera morbus and infantum; entero-colitis, diarrhoea, and dysentery. Gastro-intestinal disturbances of influenza and typhoid. 2[Uraemic convulsions], headache, vertigo and unconscious conditions resulting from brain oedema. Nephritis of pregnancy. Convulsions preceded by gastro-intestinal symptoms. Chlorosis. Bronchial asthma and with emphysema. Purulent endocarditis (Royal). Painful neuroses, enteroptosis. Delirium and tremor cordis.

:rubric=mouth
Tongue thickly coated, dirty brown, white, metallic taste; thirst. Dry mouth.

:rubric=heart
Cardiac rhythm and force altered due to defective elimination.

:rubric=abdomen
Gastro-enteritis. Violent abdominal pain. Diarrhoea in phthisis. Cholera (2[Ars; Verat; Camph]). Rumbling and sharp cutting pain. Dark liquid stools.

:rubric=back
Persistent lameness. Pain in lumbar region and in lower left shoulder-blade; chest feels tight.

:rubric=urinary
Renal inefficiency and uraemia. 2[Garlicky odor]. Diabetes. Urine of high specific gravity; increased, acetones and diacetic acid.

:rubric=male
Perspiration of scrotum; is constantly damp and moist. Boils on scrotum. Purulent discharge of a 2[white] color from urethra; tingling and burning in urethra; pain in prostate; pains in penis.

:rubric=extremities
2[Cramps in calves of legs], worse after midnight, only relieved by getting out of bed and standing. Ulcers; gangrene.

:rubric=skin
Icy cold. Sweat, and skin even when dry. Cold, clammy perspiration of an intermittent nature. Acne, pustules on face and in the cruro-genital region; ulcers look like chancre. Gangrene; carbuncles.

:rubric=dose
Third trituration.

