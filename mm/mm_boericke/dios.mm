:local-name=Wild Yam

:rubric=generals
As a remedy for many kinds of 2[pain], especially colic, and in severe, painful affections of abdominal and pelvic viscera; it ranks with the polychrests of the Materia Medica. Persons of feeble digestive powers; tea-drinkers, with much flatulence. 2[Gall stone colic].

:rubric=mind
Calls things by the wrong name.

:rubric=head
Dull pain in both temples; better pressure, but worse afterwards. Buzzing in head.

:rubric=stomach
Mouth dry and bitter in morning, tongue coated, no thirst. Belching of large quantities of offensive gas. Neuralgia of stomach. 2[Sinking at the pit of the stomach]; pyrosis. 2[Pain along sternum and extending into arms]. Eructations of sour, bitter wind, 2[with hiccough]. Sharp pain in epigastrium, relieved by standing erect.

:rubric=abdomen
Pains suddenly shift to different parts; 2[appear in remote localities, as fingers and toes]. Rumbling, with emission of much flatus. Griping, cutting in hypogastric region, with intermittent cutting in stomach and small intestines. Colic; better walking about; pains radiate from abdomen, to back, chest, arms; worse, bending forwards and while lying. 2[Sharp pains from liver, shooting upward to right nipple]. Pain from gall-bladder to chest, back, and arms. Renal colic, with pain in extremities. Hurried desire for stool.

:rubric=heart
Angina pectoris; pain back of sternum into arms; labored breathing; 2[feeble action of heart]. Especially with flatulence and pain through chest and tightness across.

:rubric=rectum
Haemorrhoids, with darting pains to liver; look like bunches or grapes or red cherries; protrude after stool, with pain in anus. Diarrhoea (worse in morning), yellowish, followed by exhaustion, as if flatus and feces were hot.

:rubric=male
2[Relaxation and coldness of organs]. Pains shoot into testicles from region of kidneys. 2[Strong-smelling sweat] on scrotum and pubes. Emissions in sleep, or from sexual atony, 2[with weak knees].

:rubric=female
Uterine colic; pains radiate from uterus. Vivid dreams.

:rubric=respiratory
Tight feeling all along sternum. Chest does not seem to expand on breathing. Short-winded.

:rubric=extremities
Lameness in back; worse, stooping. Aching and stiffness in joints. Sciatica; pains shoot down thigh; worse, right side; better, when perfectly still. 2[Felons] in beginning, when pricking is first felt. Nails brittle. Cramps in flexors of fingers and toes.

:rubric=modalities
M[Worse], evening and night, 2[lying down], and doubling up. M[Better], standing erect, motion in open air; pressure.

