:rubric=generals
Symptoms found in munition workers handling T. N. T who inhale and ingest it and also absorb some through the skin. They were compiled by Dr. Conrad Wesselhoeft and published in the December, 1926 number of the Journal of the American Institute of Homeopathy.

The destructive action of T. N. T on the red blood corpuscles is responsible for the anaemia and the jaundice with their secondary symptoms. The hemoglobin is changed so it cannot act satisfactorily as an oxygen carrier and as a result we have breathlessness, dizziness, headache, faintness, palpitation, undue fatigue, muscle cramps and cyanosis; also drowsiness, depression and insomnia. Later stages of the poisoning produce toxic jaundice and aplastic anaemia. The jaundice is the result of cellular destruction in contrast to obstructive jaundice.

:rubric=head
Depression and headache (frontal). Aversion to company, apathetic and weeps easily. Faintness, dizziness, mental sluggishness; delirium, convulsions, coma. Face very dark.

:rubric=respiratory
Nose dry with stuffed sensation. Sneezing, coryza, burning of trachea, choking weight on chest; dry, convulsive cough, raising mucous plugs.

Gastro-Intestinal. - Bitter taste, much thirst, sour regurgitation; dull burning behind the ensiform; nausea, vomiting, constipation followed by diarrhoea with cramps.

Cardio-Vascular. - Palpitation, tachycardia, bradycardia, intermittent pulse.

:rubric=urinary
High colored urine, burning on urination, sudden desire, incontinence and retention.

:rubric=skin
Hands stained yellow. Dermatitis, nodular erythema, vesicles, itching and burning; puffiness. 2[Tendency to haemorrhage] under the skin and from the nose. 2[Tired pain in back of knees].

