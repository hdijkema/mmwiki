:rubric=generals
A deep-acting anti-psoric remedy, with marked action in glandular affections, herpetic eruptions, chronic swellings, when patient 2[is extremely sensitive to cold. Sensation of coldness in various parts]. Scrofulous ophthalmia. Poisoned wounds, bites, phagedenic ulcers. 2[Malignant disease of the glands of the neck]. Cistus has affinity for naso-pharynx; aborts colds that center in posterior nose. Sniffling.

:rubric=face
Itching, burning, and crusts on right zygoma. Lupus, caries; open, bleeding T[cancer]. Tip of nose painful.

:rubric=mouth
Scorbutic swollen gums. 2[Mouth feels cold]; putrid, impure breath. 2[Pyorrhea] (R[Merc-c]; R[Caust]; R[Staph]; R[Kreos]). 2[Hurts to protrude the tongue].

:rubric=ears
Watery discharge; also fetid pus. Tetter on and around ears, extending to external meatus.

:rubric=throat
Spongy feeling; 2[very dry] and 2[cold air passing over parts causes pain]. Breath, tongue, and throat feel cold. Uvula and tonsils swollen. A small, dry spot in throat; must sip water frequently. Hawking of mucus. Swelling and suppuration of glands of throat. Head drawn to one side by swellings in neck. Sore throat from inhaling the least 2[cold air]. Heat and itching in throat.

:rubric=stomach
2[Cool feeling] in stomach before and after eating. 2[Cool feeling] in whole abdomen. Desire for cheese.

:rubric=stool
Diarrhoea from coffee and fruit, thin, yellow, urgent; worse in morning.

:rubric=chest
Coldness in chest. The neck is studded with tumors. Induration of mammae. Haemorrhage from lungs.

:rubric=extremities
Sprained pain in wrist. Tips of fingers sensitive to cold. Tetter on hands. Cold feet. T[Syphilitic] ulcers on lower limbs, with hard swelling around. White swelling.

:rubric=sleep
Cannot sleep from coldness in throat.

:rubric=female
Induration and inflammation of mammae. Sensitive to cold air. Bad smelling leucorrhoea.

:rubric=respiratory
Asthmatic after lying down (trachea feels narrow), preceded by formication.

:rubric=skin
Itching all over. Small, painful pimples; lupus. 2[Glands inflamed and indurated]. C[Mercurio-syphilitic] ulcers. Skin of hands hard, thick, dry, fissured; deep cracks. Itching of swollen hands and arms; general itching which presents sleep. Hemicrania.

:rubric=modalities
M[Worse], slightest exposure to cold air; mental exertion, excitement. M[Better] after eating.

