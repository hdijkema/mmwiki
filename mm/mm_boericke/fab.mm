A South American shrub cultivated in Southern California. It is a terebrinthine diuretic. It has also tonic and chologogue properties, used in the treatment of nasal catarrh, jaundice, dyspepsia and to increase the secretion of bile (Albert Schneider). Useful in the uric acid diathesis, cystitis, gonorrhoea, 2[prostatitis], dysuria, vesical catarrh with suppurative prostatic conditions; post-gonorrhoeal urinary conditions; cholethiasis and liver affections. Vesical tenesmus and burning after urination. Excoriating urine and calculi.

:rubric=dose
Ten to twenty drops of the tincture.

