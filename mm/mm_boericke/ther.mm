:rubric=generals
Nervous hyperaesthesia. Has affinity for the tubercular diathesis. Vertigo, sick headache, peculiar pain around heart region, phthisis florida, scrofula have all been treated successfully with this remedy. 2[Sensitive to noise; it penetrates the body, especially teeth]. Noises seem to strike on painful spots over the body. Rachitis, caries, necrosis. Phthisis, stitch high up in left apex (2[Anthrax]). Where the indicated remedy does not hold long.

:rubric=mind
Restless; finds pleasure in nothing. Time passes too quickly.

:rubric=head
Pain worse anyone walking over floor. 2[Vertigo, with nausea and vomiting on least motion], particularly when closing eyes.

:rubric=eyes
Luminous vibrations before eyes; sensitive to light. Pressure behind eyeballs. Throbbing over left eye.

:rubric=nose
Discharge yellowish, thick, offensive; ozaena (2[Puls; Thuja]).

:rubric=stomach
Seasickness. Nausea and vomiting when closing eyes and on motion (2[Tabac]). Stinging pain on left side over anterior aspect of spleen. Burning in liver region.

:rubric=respiratory
Pain in upper left chest (2[Myrt; Pix; Anis]). 2[Pain in left floating ribs. Cardiac anxiety and pain]. Pinching in left pectoral muscle.

:rubric=back
Sensitiveness between vertebrae; avoids pressure on spine. Stinging pains.

:rubric=skin
2[Stinging thrusts everywhere]. Sensitive skin in thighs. Itching sensations.

:rubric=modalities
M[Worse], touch; pressure; on shipboard; riding in carriage; closing eyes; jar; noise, coitus; left side.

