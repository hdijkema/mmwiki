:rubric=generals
The rectal and anal symptoms are most important. Chronic ulcers on lower parts of body, leg, foot, toe, also breast, rectum.

:rubric=head
Rush of blood to head and face. Nervous. Vertigo when moving. Burning in eyes and ringing in ears.

:rubric=rectum
Biting, 2[itching in anus]; orifice swollen. Burning in anus after stool; then internal chilliness. Fistula ani, diarrhoea, with anal burning and internal chilliness. Painful ulcer, oozing offensive moisture on perineum. 2[Haemorrhoids, fissures, ulceration of anus and perineum, purple, covered with crusts]. Atrocious pains with and after each stool. Sudden, pasty diarrhoea, with faintness in abdomen.

:rubric=chest
Sticking pain in left chest. Heat in chest. Dull shooting from front to back through heart.

:rubric=extremities
Pain in wrist and fingers; knees and toes. Weakness of legs, inhibiting walking.

:rubric=sleep
Terrifying dreams, nightmare.

:rubric=skin
Sensitive, painful. Ulcers below coccyx, around sacrum; varicose veins. Ulcers in general, from pressure, bedsores, etc. Itching, burning, as from nettles.

