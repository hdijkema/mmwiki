:rubric=generals
Causes glycosuria and increased urine. Is known to produce nephritis, diabetes, degeneration of the liver, high blood pressure and dropsy. Its therapeutic keynote is 2[great emaciation, debility and tendency to ascites and general dropsy]. Backache and delayed menses. Dry mucous membranes and skin.

:rubric=head
Ill-tempered; dull, heavy pain. Nostrils sore, with purulent, acrid discharge. Mental depression.

:rubric=eyes
Lids inflamed and agglutinated; 2[styes].

:rubric=stomach
Excessive thirst; nausea; 2[vomiting. Ravenous appetite]; eating followed by flatulence. 2[Boring pain in pyloric region. Gastric and duodenal ulcers.] Burning pain. 2[Abdomen bloated]. Gas, second only to Lycop.

:rubric=urinary
Copious urination. 2[Diuresis]. Incontinence of urine. 2[Diabetes]. Emaciation and tympanites. 2[Burning in urethra], with very acid urine. 2[Unable] to retain urine without pain. 2[Enuresis] (2[Mullein oil]).

:rubric=male
Complete impotency, with nocturnal emissions. Organs cold, relaxed sweaty.

:rubric=relations
:rubric2=compare
R[Syzygium], R[Phos ac], R[Lact ac], R[Arg-n], R[Kali bich], R[Ars], R[Phloridzin] (a glucosidal principle obtained from the bark of the root of the apple and other fruit trees. Produces diabetes and fatty degeneration of the liver; intermittent fever. Daily doses, 15 grains. Phlorizin causes glycosuria. No hyperglycemia results. It compels the secretory epithelium of the kidney to break down serum albumin into sugar. There is no increase in blood sugar).

:rubric=dose
Second trituration.

