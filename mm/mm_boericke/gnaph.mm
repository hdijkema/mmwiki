:rubric=generals
A remedy of unquestioned benefit in sciatica, when pain is associated with 2[numbness] of the part affected. Rheumatism and morning diarrhoea. Polyuria.

:rubric=face
Intermittent pains of superior maxillary of both sides.

:rubric=abdomen
Borborygmus. Colic; pain in various parts of the abdomen. Irritated prostate. First stage of cholera infantum; vomiting and purging.

:rubric=female
Weight and fullness in pelvis. 2[Dysmenorrhoea], with scanty and painful menses.

:rubric=back
Chronic backache in lumbar region; better resting on back. Lumbago with numbness in lower part of back and weight in pelvis.

:rubric=extremities
Cramps in calves of legs and feet when in bed. Rheumatic pain in ankle joints and legs. 2[Intense pain along the sciatic nerve; numbness alternates with pain]. Frequent pains in calves and feet. Gouty pains in big toes. Better, drawing limbs up, flexing thigh on abdomen. Gouty concretions (2[Ammon benz]). Anterior crural neuralgia (2[Staph]). Pain in joints as if they lacked oil. Chronic muscular rheumatism of back and neck.

