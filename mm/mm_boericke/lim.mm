:rubric=generals
Limulus was introduced by C. Hering and partially proved by him and Lippe. Hering was surprised to see the blood of the King-crab that he dissected, blue, which on investigation, was found to contain copper as he had surmised and which he thought would prove to be another medicine for Cholera. Further provings are necessary to establish this, though symptoms so far observed make this probable. Hering's fertile mind always lead him to pioneer paths into practical therapeutics.

Bodily and mental exhaustion; drowsiness 2[after sea bathing]. Gastro-enteric symptoms. Painful fullness of whole right side of body.

:rubric=head
Mental depression. Difficult to remember names, confused with heat of face, rush of blood to face, worse when meditating. Pain behind left eye-ball.

:rubric=nose
Fluent coryza. Sneezing worse drinking water. Constant nasal dropping. Pressure above nose and behind eyes.

:rubric=abdomen
Colic with heat. Cramp-like pain with watery stools. Abdomen hot and constricted. Piles, constriction of anus.

:rubric=respiratory
Husky voice. Dyspnoea after drinking water. Oppression of chest.

:rubric=extremities
Crural neuralgia. Soles of feet ache, feel numb. Pain in right hip-joint. Heels sore.

:rubric=skin
2[Itching spots and vesicles] on face 2[and hands]. Burning in palms.

:rubric=relations
:rubric2=compare
R[Asterias], R[Homarus], R[Cuprum].

:rubric=dose
Sixth potency.

