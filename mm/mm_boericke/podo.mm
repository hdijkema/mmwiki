:local-name=(PODOPHYLLUM)

:rubric=generals
Is especially adapted to persons of bilious temperament. It affects chiefly the 2[duodenum], small intestines, liver, and 2[rectum] The Podophyllum disease is a gastro-enteritis with colicky pain and bilious vomiting. Stool is watery with jelly-like mucus, painless, 2[profuse]. Gushing and offensive. Many troubles during pregnancy; pendulous abdomen after confinement; prolapsus uteri; painless cholera morbus. Torpidity of the liver; portal engorgement with a tendency to haemorrhoids, hypogastric pain, fullness of superficial veins, jaundice.

:rubric=mind
Loquacity and delirium from eating acid fruits. Depression of spirits.

:rubric=head
Vertigo, with tendency to fall forward. Headache, dull pressure, worse morning, with heated face and bitter taste; 2[alternating with diarrhoea. Rolling of head from side to side], moaning and vomiting and eyelids half closed. Child perspires on head during sleep.

:rubric=mouth
Grinding the teeth at night; 2[intense desire to press the gums together] (2[Phytol]). Difficult dentition. 2[Tongue broad, large, moist]. Foul, putrid taste. 2[Burning sensation of tongue].

:rubric=stomach
Hot, sour belching; nausea and vomiting. Thirst for large quantities of cold water (2[Bry]). Vomiting of hot, frothy mucus. Heartburn; gagging or empty retching. Vomiting of milk.

:rubric=abdomen
Distended; heat and emptiness. 2[Sensation of weakness or sinking.] Can lie comfortably only on stomach. Liver region painful, 2[better rubbing part]. Rumbling and shifting of flatus in ascending colon.

:rubric=rectum
Cholera infantum and morbus. Diarrhoea of long standing; 2[early in morning; during teething, with hot, glowing cheeks] while being bathed or washed; in hot weather after acid fruits. Morning, painless diarrhoea when not due to venous stasis or intestinal ulceration. Green, watery, 2[fetid, profuse], gushing. 2[Prolapse of rectum] before or with stool. Constipation; clay-colored, hard, dry, difficult. Constipation alternating with diarrhoea (2[Ant crud]). Internal and external piles.

:rubric=female
Pain in uterus and 2[right ovary, with shifting noises along ascending colon]. Suppressed menses, with pelvic tenesmus. 2[Prolapsed uteri], especially after parturition. Haemorrhoids, with prolapsus ani during pregnancy.

:rubric=extremities
Pain between shoulders, under right scapula, in loins and lumbar region. Pain in right inguinal region; shoots down inner thigh to knees. Paralytic weakness on left side.

:rubric=fever
Chill at 7 am, with pain in hypochondria, and knees, ankles, wrists, 2[Great loquacity] during fever. Profuse sweat.

:rubric=modalities
M[Worse], in early morning, in hot weather, during dentition.

:rubric=relations
:rubric2=compare
R[Mandragora]-also called mandrake - (must not be confounded with R[Podoph]. Great desire for sleep; exaggeration of sounds and enlarged vision. Bowels inactive; stools large, white and hard). R[Aloe], R[Chel], R[Merc], R[Nux-v], R[Sulph. Prunella]-Self-head - (Colitis).

:rubric=dose
Tincture to sixth potency. The 200th and 1000th seem to do good work in cholera infantum, when indicated.

