:rubric=generals
Has a special action on the uterus; also upon serous and fibrous membranes; hence its use in gout. 2[Pain from sacrum to the pubis. Haemorrhages, where blood is fluid and clots together]. Tendency to miscarriage, especially at third month. 2[Violent pulsations]; wants windows open.

:rubric=mind
2[Music is intolerable], produces nervousness.

:rubric=head
Vertigo with suppressed menses. Bursting headache, suddenly coming and going slowly. Rush of blood to head and face. Drawing pains in masseter muscles. Teeth ache when chewing.

:rubric=stomach
Heartburn. Desire for lemonade. Bitter taste (R[Rhus-t]). Lancinating pain from pit of stomach across back.

:rubric=abdomen
Bearing-down, constrictive pain. Colic, mostly in hypogastric region. Tympanitic distention.

:rubric=rectum
Sense of fullness. Constipation. 2[Pain from back to pubis]. Haemorrhoids, with bright red blood; bleed copiously.

:rubric=urine
Burning and throbbing in region of kidneys. Bloody urine; much urging. Bladder inflamed with throbbing all over. Inflammation of urethra.

:rubric=male
Inflammatory gonorrhoea, with pus-like discharge. Sycotic excrescences. Burning, sore pain in glans. Prepuce painful with difficulty in retracting it. Increased desire.

:rubric=female
2[Menses profuse, bright]. Uterine pains extend into thighs. Threatened miscarriage. Sexual desire increased. Leucorrhoea after menses, corrosive, offensive. Discharge of blood between periods, with sexual excitement (2[Ambr]). Retained placenta; intense after-pains. Menorrhagia in women who aborted readily. Inflammation of ovaries and uterus after abortion. Promotes expulsion of moles from uterus (2[Canth]). 2[Pain from sacrum to pubis, and from below upwards shooting up the vagina]. Haemorrhage; partly clotted; worse 2[from least motion]. Atony of uterus.

:rubric=back
2[Pain between sacrum and pubis from one bone to another]. Paralytic pain in small back.

:rubric=extremities
Bruised pains in anterior portion of thighs. Shooting in heels and metatarsal bones. 2[Arthritic pain in joints]. Gout; worse, in heated room. Red, shining swelling. Gouty nodosities (2[Ammon phos]).

:rubric=skin
Fig-warts, with intolerable itching and burning. Exuberant granulations (2[Thuj; Nit ac]). 2[Warts]. Black pores in skin.

:rubric=modalities
M[Worse], from least motion, heat, warm air. M[Better], in cool fresh air.

