:local-name=A Night-blooming Cereus

:rubric=generals
:rubric=mind
Great desire to work and to be doing something useful.

:rubric=head
Occipital headache and 2[pain through the globe of the eyes and orbits] (2[Cedron; Onos]). Pain across the brain from left to right. Pain along right malar bone running to temple.

:rubric=chest
Convulsive pains at the heart; feels as if transfixed. Pain in chest through heart, with pain running toward spleen. Pain in left pectoral muscle and cartilages of left lower ribs. Sensation of a great weight on heart, and pricking pain. Hypertrophy of heart. Difficult, sighing respiration, as from some compression of chest.

:rubric=skin
Itching of skin (2[Dolich; Sulph]).

:rubric=extremities
Pain in neck, back, shoulders, down arms, hands and fingers. Pain in knees and joints of lower extremities.

:rubric=relations
:rubric2=compare
R[Cactus], R[Spig], R[Kalmia], R[Cereus serpentinus] (Very irritable with tendency to swear; wild anger and low morals. Disturbance in speech; in writing leaves off the last syllable. Paralyzed feeling. Pains in heart, and dwindling of sexual organs. Emissions, followed by pain in testicles).

:rubric=dose
Third to sixth attenuation.

