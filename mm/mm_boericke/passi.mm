An efficient anti-spasmodic. Whooping-cough. Morphine habit. Delirium tremens. Convulsions in children; neuralgia. Has a quieting effect on the nervous system. Insomnia, produces normal sleep, no disturbance of cerebral functions, neuroses of children, worm-fever, teething, spasms. 2[Tetanus]. Hysteria; puerperal convulsions. Painful diarrhoea. Acute mania. 2[Atonic condition] generally present. 2[Asthma], 10-30 gtt every ten minutes for a few doses. Locally, in erysipelas.

