:rubric=generals
We are indebted to the Eclectic school for this remarkable medicine as a "corrector of blood dyscrasia". Acute auto-infection. Symptoms of 2[blood poisoning, septic conditions generally]. Diarrhoea in typhoid. Gonorrhoea. Boils. 2[Erysipelas] and foul ulcers. 2[Gangrene]. Goitre with exophthalmic symptoms; full doses, also injecting 5-10 drops into thyroid gland. Tendency to malignancy in acute and subacute disorders. Last stages of cancer to ease pain. 2[Venom infection]. Cerebro-spinal meningitis. Puerperal infections. 2[Tired feeling]. Piles. Pustules. Acts on vermiform appendix thus has been used for appendicitis, but remember it promotes suppuration and a neglected appendicitis with pus formation would probably rupture sooner under its use. Lymphatic inflammation; crushing injuries. 2[Snake bites] and bites and stings generally. Foul discharges with emaciation and great debility.

:rubric=head
Confused, depressed. Aches with a peculiar periodical flushing of the face, even to the neck; dizziness and profound prostration.

:rubric=nose
Foul-smelling discharge, membranous formations protruding. Post-nasal catarrh with ulceration and fetor. Nose feels stuffed up. Right nostril raw, bleeding.

:rubric=mouth
Canker; gums recede and bleed easily; corners of mouth and lips crack; tongue dry and swollen; sores; dirty brownish. Tongue, lips, and fauces 2[tingle], with sense of fear about heart (2[Acon]). White coating of tongue, with red edges. Promotes the flow of saliva.

:rubric=throat
Tonsils purple or black, gray exudation extending to posterior nares and air-passages. Ulcerated sore throat.

:rubric=stomach
Sour belching and heartburn. Nausea; better lying down.

:rubric=chest
Pain as of a lump in chest and under sternum. Pain in pectoral muscles (2[Aristolochia]).

:rubric=urine
Albuminous, scanty, frequent, and involuntary.

:rubric=female
2[Puerperal septicaemia]; discharges suppressed; abdomen sensitive and tympanitic; offensive, excoriating leucorrhoea.

:rubric=extremities
2[Aching in limbs] and general lassitude.

:rubric=skin
Recurring boils. Carbuncles. Irritations from insect bites and poisonous plants. Lymphatics enlarged. Old tibial ulcers. Gangrene.

:rubric=fever
2[Chilliness, with nausea]. Cold flashes all over back. 2[Malarial fever].

