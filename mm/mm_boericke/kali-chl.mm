:rubric=generals
Acts very destructively upon the kidneys, producing a croupous nephritis, hemoglobinuria, etc. Parenchymatous nephritis with stomatitis. Produces most acute ulcerative and follicular stomatitis. 2[Noma. Toxaemic conditions of pregnancy] (urinary symptoms). Chronic nephritis; hepatitis. Septicaemia. Anaemia.

:rubric=mouth
Profuse secretion of acid saliva. Whole mucous surface red, tumid, with gray-based ulcers. Tongue swollen. 2[Stomatitis]-aphthous and 2[gangrenous]. Fetor. Mercurial stomatitis (as a mouth wash).

:rubric=stomach
Feeling of weight in epigastric and umbilical region. Flatulence. Vomiting of greenish-black matter.

:rubric=stool
Diarrhoea; profuse, greenish mucus.

:rubric=urine
Albuminous, scanty, suppressed. Haematuria; diuresis. Nucleo-albumin and bile, high Phosphoric acid, with low total solids.

:rubric=skin
Jaundice. Itching miliary or papular eruptions. Discolored; chocolate tint.

:rubric=dose
Second to sixth potency. Use cautiously locally as it is poisonous.

