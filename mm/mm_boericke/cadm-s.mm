:rubric=generals
Its pathogenesis gives symptoms corresponding to very low forms of disease, as in cholera, 2[yellow fever], where, with exhaustion, vomiting, and 2[extreme prostration], the disease runs deathward. Important gastric symptoms. Carcinoma ventriculi; persistent vomiting.

The attack is upon the stomach more especially. Patients must keep quiet. 2[Chilliness and coldness] even when near the fire.

Mind and Head. - Unconscious. Vertigo; room and bed seem to spin around. Hammering in head. Heat in head.

:rubric=nose
2[Ozaena]. Tightness at root. Nose obstructed; 2[polypus]. Caries of nasal bones. Boils on nose. Nostrils ulcerated.

:rubric=eyes
2[Opacity of cornea]. Blue circle around eyes. One pupil dilated. Night blindness.

:rubric=face
Distortion of mouth. Trembling of jaw. 2[Facial paralysis]; more left side.

:rubric=mouth
Difficult swallowing. Oesophagus constricted (R[Bapt]). Salty belching. Intense nausea, with pain and cold. Stringy, offensive exudation on mucous membrane. Salty taste.

:rubric=throat
Sore throat, constant tickling; gagging and nausea, worse deep breathing; chilliness and aching.

:rubric=stomach
Soreness in pit of stomach on pressure. Violent 2[nausea]; retching. 2[Black vomit]. Vomiting of mucus, green slime, blood, with great prostration, and great tenderness over the stomach. Burning and cutting pains in stomach. Carcinoma, helps the persistent vomiting. Coffee ground vomiting.

:rubric=abdomen
Sore, tender, tympanitic. Region of liver sore. Coldness. Black, offensive clots of blood from bowels. Pain in abdomen, with vomiting. Tenderness and tympanites.

:rubric=stool
Bloody, black, and offensive. Gelatinous, yellowish green; semi-fluid, with urinary suppression.

:rubric=urine
Rawness and soreness in urethra, urine mixed with pus and blood.

:rubric=heart
Palpitation, with constriction of chest.

:rubric=fever
2[Icy coldness] (R[Camph]; R[Verat]; R[Helo]). 2[Yellow fever] (R[Crot-h]; R[Carb-v]).]

:rubric=skin
Blue, yellow, sallow, scaly, cracking. Itching; better scratching. Chloasma, yellowish stains on nose and cheeks; worse exposure to sun and wind. Chilblains.

:rubric=sleep
Stops breathing on going to sleep. Wakes up suffocating. Fears to go to sleep again. Protracted sleeplessness.

:rubric=modalities
M[Worse], walking or carrying burdens; after sleep; from open air, stimulants. M[Better], eating and rest.

