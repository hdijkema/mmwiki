:rubric=generals
Is used in various forms of asthma and chronic bronchitis Catarrh of bladder. Catarrhal influenza. Dysentery; attack preceded by constipated stools covered with white mucus.

:rubric=respiratory
Cough with expectoration 2[profuse], stringy, frothy, light-colored. Excited by sense of mucus rattling in chest and worse by drafts of air. Constriction of lungs. Catarrh, with copious, stringy, mucous discharges. Desire to hawk and scrape throat. Irritation of posterior nares, involving mucous membranes of nasal passages with constriction of supra-orbital region.

:rubric=relations
:rubric2=compare
R[Aral], R[Copaiv], R[Tereb], R[Cubeb], R[Samb], R[Silphion cyrenaicum] (phthisis pulmonum, with incessant cough, profuse night-sweats, emaciation, etc); R[Polygonum aviculare] (has been found useful in phthisis, when given in material doses of the mother tincture); R[Salvia] (tickling cough). R[Arum dracontium] (loose cough at night on lying down). R[Justicia adhatoda] (bronchial catarrh, hoarseness, oversensitive).

:rubric=dose
Third potency. Lower 2[triturations] preferred by some.

