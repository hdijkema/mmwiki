:rubric=generals
Has considerable clinical reputation in the treatment of earache, toothache, and enuresis. Sharp pain in eyes, reflex from decayed teeth or inflammation of middle ear. Eyeball very tender to touch. Pain plays between teeth and ears. Pyorrhea alveolaris. Depression and insomnia of chronic Nicotinism. Causes an aversion to tobacco.

:rubric=head
Periodical prosopalgia, worse 7 am to 2 pm, accompanied with flow of tears, photophobia; pains radiate to temples and lower face.

:rubric=ears
Hearing acute; noise painful. Sticking pain in ears. Neuralgic earache; 2[pain goes from one ear to the other through the head]. Otalgia, with toothache. Loud noises go through one.

:rubric=nose
Sudden, yellowish, watery discharge.

:rubric=mouth
Teeth ache and are sensitive and sore to touch. Swelling of cheeks. Salivation; teeth feel too long; worse, cold air and contact. Toothache, better while eating. Profuse of saliva. Toothache, with reflex neuralgia of eyelids.

:rubric=stool
Wants to defecate; goes often, but cannot bad, can hardly stand. Diarrhoea, with brown watery

:rubric=urine
Profuse flow; 2[nocturnal enuresis] (2[Rhus arom; Caust; Bellad]).

:rubric=skin
Itching and burning; papulae. Urticaria, chilblains (2[Agar; Tamus]).

:rubric=relations
:rubric2=compare
R[Kalm], R[Cham], R[Puls].

:rubric=dose
Tincture, and lower potencies. Local use in toothache in hollow teeth, otorrhoea, pruritus, and Poison-oak. Incised wounds.

