:rubric=generals
Vertigo of cerebral origin, cerebellar disease, ataxic symptoms and chorea, present a field for the homeopathic employment of this drug. 2[Profound weakness], gone, faint feeling, and despondency. Loss of control of sphincter. Muscular inco-ordination.

:rubric=mind
Mental confusion, incoherency, illusions; apathetic. 2[Alternation of happy, hopeful states with depression and weakness]. Extreme irritability.

:rubric=head
Dropsy, stupid; pain on attempting to raise head. Double vision; heavy look about eyes; tinnitus, aphasia; 2[tongue as if paralyzed. Eyes bloodshot and restless]. Vertigo, unable to rise. Double vision; ptosis; tinnitus; dysphagia, difficult speech.

:rubric=urinary
Albuminuria, with casts. Scanty. Pink color. Constant desire to urinate; scanty, brownish red. Haemato-porphyrinuria.

:rubric=respiratory
Congestion of lungs; stertorous breathing. Sighing dyspnoea.

:rubric=extremities
Ataxic movements, 2[staggering gait]; cold, weak, trembling; legs seem too heavy. Extreme restlessness; muscular twitchings. Knee-jerks disappear. Stiffness and paralysis of both legs. Anaesthesia of legs.

:rubric=sleep
Fidgety, wakeful, drowsy. Insomnia.

:rubric=skin
Itching, bluish purpura. Erythema.

:rubric=relations
R[Trional]; insomnia associated with physical excitement; (vertigo, loss of equilibrium, ataxia, nausea, vomiting, diarrhoea, stertorous breathing, cyanosis, tinnitus, hallucinations).

:rubric=dose
Third trituration.

2[Non-Homeopathic Uses. - ]As a hypnotic. Dose, ten to thirty grains in hot water. Takes about two hours to act.

