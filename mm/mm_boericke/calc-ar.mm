Anger, anxiety. Desire for company. Confusion, delusions, illusions. Great depression.

:rubric=head
Violent rush of blood to head with vertigo. Pain in head 2[better by lying on painful side]. Weekly headache. Benumbling headache mostly around ears.

:rubric=stomach
Region of stomach distended. Enlarged liver and spleen in children. Pancreatic disease; relieves burning pain in cancer of pancreas. Belching with saliva and beating of heart.

:rubric=urinary
2[Kidney region sensitive to pressure]. Albuminuria, passes urine every hour.

:rubric=heart
Constriction and pain in region of heart, suffocating feeling, 2[palpitation], oppression and throbbing and pain in back extending to arms.

:rubric=female
Offensive, bloody leucorrhoea. Cancer of uterus; burning pain in uterus and vagina.

:rubric=back
Pain and stiffness near nape of neck. Violent backache, throbbing, drives out of bed.

:rubric=extremities
Removes inflammatory products in veins of lower extremities. Weariness and lameness of lower limbs.

