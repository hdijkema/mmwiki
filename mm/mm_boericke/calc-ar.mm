:rubric=generals
Epilepsy with rush of blood to the head before attack; aura felt in region of heart; flying sensation. Complaints in fat women around climacteric. Chronic malaria. Infantile enlarged liver and spleen. 2[Nephritis], with great sensitiveness in kidney region. Complaints of drunkards after abstaining (2[Carbon sulph]). Fleshy women at climacteric, 2[slightest emotion causing palpitation]. Dyspnoea, with feeble heart. 2[Chilliness]. Albuminuria. Dropsy. Affections of spleen and mesenteric glands. Hemoglobin and red corpuscles are low.

