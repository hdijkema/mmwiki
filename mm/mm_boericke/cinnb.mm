:local-name=(MERCURIUS SULPHURATUS RUBER)

:rubric=generals
For certain forms of ciliary neuralgia and ulceration upon a syphilitic base, this remedy is most effective. Sleepless during night.

:rubric=head
Congestion to head; face purple red.

:rubric=eyes
2[Pains from lachrymal duct around eye to temple, from inner canthus across brows to ear. Severe shooting pain in bones of orbit, especially running from inner to outer canthus in the bone. Redness of whole eye]. Lids granulated; canthi and lids red.

:rubric=nose
Pressive sensation, as from heavy spectacles. Pain about root, extending into bones on each side (2[Aur; Kal hyd]).

:rubric=throat
Stringy mucus passed through posterior nares into throat. Dryness of mouth and throat; must rinse the mouth. Fiery-looking ulcers in mouth and throat.

:rubric=male
Prepuce swollen; 2[warts] on it which bleed easily; testicles enlarged; buboes; angry-looking chancres. Syphilides, squamous and vesicular.

:rubric=female
Leucorrhoea. Feeling of pressure in vagina.

:rubric=extremities
Pain in forearm from elbow down, including hands. Pain in long bones when barometer lowers; coldness of joints.

:rubric=skin
2[Very fiery-red] looking ulcers. Nodes on shin-bones. Buboes. Condyloma, easily bleeding.

:rubric=modalities
M[Worse], lying on right side (feels as if contents of body were being dragged over to that side).

