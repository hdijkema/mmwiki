Painful erections; swelling of the prepuce. Thick, yellowish, muco-purulent discharge. Deep pain in perineum.

:rubric=urine
Frequent, burning, smarting swelling, and redness of meatus. Stream small and slow. 2[Acute aching in kidney region]. Sensation of a ball pressing against the urethra; worse, standing. Gleet, with profuse, thick discharge; chronic cystitis.

