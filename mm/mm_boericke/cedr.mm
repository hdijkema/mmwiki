:rubric=generals
Periodicity is the most marked characteristic of this drug. Its particularly useful in tropical or in damp, warm, marshy countries. It has been found curative in malarial affections, especially neuralgia. Adapted to persons of a voluptuous disposition, excitable, nervous temperament. Has powers of antidoting snake-bites and stings of insects. Tincture of pure bean scraped on wound. Mania.

:rubric=head
Pain from temple to temple across eyes. Pain over whole right side of face, coming on about 9 am.Crazy feeling from pain across forehead; worse, working on black. Roaring in ears produced by Cinchona. Whole body seems numb with headache.

:rubric=eyes
Shooting over left eye. Severe 2[pain in eyeball, with radiating pains around eye], shooting into nose. Scalding lachrymation. 2[Supra-orbital neuralgia periodic]. Iritis, choroiditis.

:rubric=extremities
Lancinating pain in joints; worse, feet and hands. Sudden pain in ball of right thumb, extending up arm to shoulder. Pain in ball of right foot, extending to knee. Shingles, with radiating pain. Dropsy of knee-joint.

:rubric=fever
Chilliness towards evening; then frontal headache extending into parietal region. Red eyes. Heat, with itching of eyes, tearing in limbs, 2[numbness of limbs].

