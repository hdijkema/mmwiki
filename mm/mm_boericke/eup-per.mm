:rubric=generals
S{Known as "Bone-set", from the prompt manner in which it relieves pain in limbs and muscles that accompanies some forms of febrile disease, like malaria and influenza.}
S{Eupatorium acts principally upon the gastro-hepatic organs and bronchial mucous membrane.}
S{It is a boon in miasmatic districts, along rivers, marshes, etc, and in all conditions where there is a great deal of bone-pain.}
S{Cachexia from old chronic, bilious intermittents.}
S{Worn-out constitutions from inebriety.}
S{Sluggishness of all organs and functions.}
S{Bone-pains, general and severe.}
S{Soreness.}
S{Marked periodicity (R[Ars]; R[Chin]; R[Cedr]).}

:rubric=head
S{Throbbing pain.}
S{Pressure as if a cap of lead pressed over the whole skull.}
S{Vertigo; sensation of falling to 2[left].}
S{Vomiting of bile.}
S{Top and back of head with pain and 2[soreness of eyeballs].}
S{Periodical headache, every third and seventh day.}
S{2[Occipital pain after lying down, with sense of weight].}

:rubric=mouth
S{Cracks in corners of mouth, yellow coated tongue, thirst.}

:rubric=stomach
S{Tongue yellow.}
S{Taste bitter.}
S{Hepatic region sore.}
S{Great thirst.}
S{Vomiting and purging of bile, of green liquid several quarts at a time.}
S{Vomiting preceded by thirst.}
S{2[Hiccough] (R[Sul-ac]; R[Hydr-ac]).}
S{Avoids tight clothing.}

:rubric=stool
S{Frequent, green watery.}
S{Cramps.}
S{Constipated, with sore liver.}

:rubric=respiratory
S{Coryza, with sneezing.}
S{2[Hoarseness and cough, with soreness in chest]; must support it. 2[Influenza], with great soreness of muscles and bones.}
S{2[Chronic] loose cough, chest sore; 2[worse at night].}
S{Cough relieved by getting on hands and knees.}

:rubric=fever
S{Perspiration relieves all symptoms except headache.}
S{Chill between 7 and 9 am, 2[preceded by thirst with great soreness and aching of bones].}
S{Nausea, vomiting of bile at close of chill or hot stage; throbbing headache.}
S{Knows chill is coming on because he cannot drink enough.}

:rubric=extremities
S{2[Aching pain in back.}
S{Aching in bones of extremities with soreness of flesh.}
S{Aching in arms and wrists].}
S{Swelling of left great toe.}
S{Gouty soreness and inflamed nodosities of joints, associated with headache.}
S{Dropsical swelling.}

:rubric=modalities
S{M[Worse], periodically.}
S{M[Better], by conversation, by getting on hands and knees.}

