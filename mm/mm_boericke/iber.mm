:rubric=generals
State of nervous excitement. Has marked action upon the heart. Possesses great efficacy in cardiac diseases. Control vascular excitement in hypertrophy with thickening of the heart's walls. Cardiac debility after influenza. Liver region full and painful. White stools.

:rubric=mind
Sad and sighing; fearful and trembling. Irritable.

:rubric=head
Vertigo and pains around heart. Constant hawking of thick, stringy mucus until after a meal. Hot, flushed face. Vertigo, 2[as if occiput were turning around]; eyes feel forced outwards.

:rubric=heart
Conscious of heart's action. On turning on left side, stitching pain as of needles through ventricles felt at each systole. Palpitation, 2[with vertigo and choking in throat]. Stitching pains in cardiac region. 2[Pulse full, irregular, intermittent]. Worse, least motion and in warm room. Sensation of weight and pressure, with occasional sharp, stinging pains. Dropsy, with enlarged heart. Violent palpitation 2[induced by slightest exertion, or by laughing, or coughing. Darting pains through heart. Cardiac dyspnoea]. Dilation of heart. Wakes with palpitation about 2 am. Throat and trachea fills up with mucus. Cough causes redness of face. 2[Tachycardia].

:rubric=extremities
Numbness and tingling in left hand and arm. Whole body sore, lame and trembling.

:rubric=modalities
M[Worse], lying down; on left side; motion, exertion; warm room.

