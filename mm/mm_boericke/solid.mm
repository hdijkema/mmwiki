:rubric=nose
Nares irritated with abundant mucus secretion; paroxysms of sneezing.

:rubric=stomach
Bitter taste, especially at night; coated tongue with very scanty brown and sour urine.

:rubric=respiratory
Bronchitis, cough with much purulent expectoration, blood-streaked; oppressed breathing. Continuous dyspnoea. Asthma, with nightly dysuria.

:rubric=female
Uterine enlargement, organ pressed down upon the bladder. 2[Fibroid tumors].

:rubric=urine
Scanty, reddish brown, thick sediment, dysuria, gravel. 2[Difficult and scanty]. Albumen, blood, and slime in urine. Pain in kidneys extend forward to abdomen and bladder (2[Berb]). 2[Clear and offensive urine]. Sometimes makes the use of the catheter unnecessary.

:rubric=back
Backache of congested kidneys (2[Senec aur]).

:rubric=skin
Blotches, especially on lower extremities; 2[itch]. Exanthema of lower extremities, with urinary disturbances, dropsy and threatened gangrene.

:rubric=relations
R[Iodoform] 2x antidotes poison of Golden-rod. R[Arsenic. Agrimonia] (Pain in region of kidneys).

:rubric=dose
Tincture, to third potency. Oil of Solidago, 1 oz to 8 oz. Alcohol. 15 drops doses to promote expectoration in bronchitis and bronchial asthma in old people (Eli G. Jones).

