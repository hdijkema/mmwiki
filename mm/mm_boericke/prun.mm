:rubric=generals
Special action on the urinary organs and head. Very valuable in certain neuralgias, anasarca, and especially oedema pedum. Ankle and foot feel sprained. 2[Ciliary neuralgia] (2[Spig]).

:rubric=head
Pressing-asunder pain beneath skull. 2[Shooting from right frontal bone through brain to occiput. Pain in right eyeball, as if it would burst]. Piercing toothache, as if teeth were pulled out; worse, taking anything warm.

:rubric=eyes
2[Ciliary neuralgia]. Bursting pain in right eyeball shooting like lightning through the brain to occiput. 2[Sudden pain in left eye as if it would burst], better by lachrymation. Irido-choroiditis. Opacity of vitreous humor. Eyes feel as if bursting.

:rubric=abdomen
Ascites. Cramp-like pain in bladder region; worse, walking.

:rubric=rectum
Hard, nodular stool, with rectal pain, as if angular body were pressed inward. Burning in anus after slimy diarrhoea.

:rubric=urine
Tenesmus of bladder. Ineffectual effort to urinate. 2[Hurriedly impelled to urinate; the urine seems to pass as far as glans, and then returns and causes pain in urethra]. Neuralgic dysuria. 2[Must press a long time before urine appears].

:rubric=respiratory
Wheezing when walking. Oppression of chest; anxious, short respiration. Angina pectoris. Furious beating of heart; worse, slightest motion.

:rubric=skin
Herpes zoster. 2[Dropsy]. Itching on tips of fingers, as if frozen.

