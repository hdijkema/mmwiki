Dr. Sylwestrowicz of the Hering Research Laboratory of Hahnemann College, Philadelphia contributes his experience with R[Form-ac], as follows:

"The best field for the formic acid treatment are cases of atypical gout. Under this classification are to be mentioned disturbances in the muscles such as myositis, periostitic processes of the bones in form of doughy swellings, changes of the fascias such as Dupyutren's contraction, skin troubles such as chronic eczema, psoriasis and loss of hair, kidney disturbances such as subacute and chronic nephritis. In these cases R[Form-ac] in 12x and 30x, hypodermically 1 cc is indicated at intervals of 2-4 weeks. Eight till twelve days after the first injection an aggravation is often noticed.

In acute rheumatic fever and acute gonorrhoeic arthritis R[Form-ac] 6x, every six days 1 cc, sometimes 12x in sensitive patients shows often splendid results abolishing the pains and preventing reoccurrence.

Chronic arthritis needs a special discussion. Clinical experiments of the Hering research Laboratory of the Hahnemann Medical College of Philadelphia on a great number of cases of arthritis with R[Form-ac] showed that it preferably acts on the ligaments, capsula and bursa of the joints. Such kind of cases respond very readily to treatment.

The prognosis depends to a large extent upon the etiology of the case. The most satisfactory cases are chronic arthritis in connection with gouty diathesis. Chronic arthritis following an attack of acute rheumatic fever shows also remarkable results although often pains of a neuralgic character persisting in certain spots are very stubborn. Finally chronic arthritis of traumatic nature can be cured by formic acid. In the latter case R[Form-ac] 6x showed quicker and better results than 12x or 30x which are indicated in the previous cases. In general the disappearance of the stiffness of the joint is the first sign of improvement. Then the pain and swelling cease gradually in 1-6 months time.

The prognosis of the formic acid treatment is not so favorably in chronic arthritis in which deformans processes have already taken place on the articular surfaces. Such processes in the beginning can be checked completely, advanced cases frequently show an improvement. But there is always the possibility that this improvement is only temporary. This is particularly to be expected in the cases of the so-called arthritis deformans in which even the inflammations on the ligaments and capsula are of a very progressive character".

