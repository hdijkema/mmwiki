:rubric=generals
The respiratory symptoms are most important, and have been frequently verified clinically. A very good remedy for the 2[offensive expectoration and cough in phthisis], bronchitis, and emphysema. Tuberculosis, affecting generally the middle lobes. 2[Everything tastes sweet]. Haemoptysis, hectic and colliquative diarrhoea.

:rubric=head
2[Weight on vertex; aching and burning in temples and above eyes]. Crushing feeling in vertex. Vertigo, dizzy when lying down.

:rubric=eyes
Ciliary neuralgia; worse any attempt to use eyes: burning in eyes. Lachrymation. Cannot bear light. Headache; involving nerves going to eye.

:rubric=female
2[Pain in milk ducts]; intolerable between nursing. Pain in nipples.

:rubric=chest
2[Sticking pain through right breast near sternum, extending to back near shoulders]. Dyspnoea, and continuous cough, early in morning. Cough, with profuse and fetid expectoration; compels him to sit up. Hoarseness.

:rubric=fever
Hectic; profuse and debilitating perspiration; intermittent, with pain in arms. Desire for acids.

:rubric=extremities
Tired feeling when walking.

