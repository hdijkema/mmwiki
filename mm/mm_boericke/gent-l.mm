:local-name=Yellow Gentian

:rubric=generals
Stomach symptoms marked. Acts as a tonic, increasing appetite.

:rubric=head
Vertigo, worse, rising or motion; better open air. Frontal headache, better eating and open air. Brain feels loose, head tender. Aching in eyes.

:rubric=throat
Dry. Thick saliva.

:rubric=stomach
Acid risings, ravenous hunger, nausea, weight and aching in stomach. Inflation and tension of stomach and abdomen (2[Pothos]). Colic, umbilical region sensitive to touch. Flatulence.

:rubric=relations
:rubric2=compare
R[Gentiana quinque flora] (intermittent fever; dyspepsia, cholera infantum, weakness); R[Gentiana cruciata] (throat symptoms in addition to similar stomach symptoms; dysphagia; vertigo with headache; pressing inward sensation in eyes; constricted throat and head and abdomen. Distention, fullness and tightness in abdomen. Creeping over body as from fleas). R[Hydrast], R[Nux-v].

:rubric=dose
First to third attenuation.

