Urinary symptoms most important. Cystitis, with bloody urine. Uterine haemorrhage. Chronic vesical irritation, with pain, tenesmus, and catarrhal discharges. 2[Burning after the discharge of slimy urine. Pyelitis]. Calculous inflammation. Dyspnoea, nausea, vomiting, pulse small and irregular. Cyanosis. Urticaria without itching.

:rubric=urinary
Frequent urging, with severe spasms of bladder; burning and tearing pain. Urine contains blood, pus, and much tenacious mucus, with clots in large masses. Involuntary; green urine. Painful dysuria.

:rubric=relations
:rubric2=compare
R[Arbutin] (a crystallized glucoside of Uva; found also in Kalmia, Gaultheria and other genera of the family of R[Eriaceae]; given in doses of 3 to 8 grains with sugar three times a day. Used as an urinary antiseptic and diuretic). R[Arctosphylos manzanita] (acts on renal and reproductive organs. Gonorrhoea, vesical catarrh, diabetes menorrhagia. Tincture of leaves). R[Vaccinum myrtillus]-Huckleberries - (dysentery; typhoid, keeps intestines aseptic and prevents absorption and reinfection).

:rubric=dose
Tincture, five to thirty drops. In pyelitis a trituration of the leaves. 

