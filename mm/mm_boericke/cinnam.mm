:rubric=dose
Tincture to third potency. For cancer, strong decoction, one-half pint in a day. 2[Oil of cinnamon] in aqueous solution best local disinfectant. 3-4 drops in two quarts of water as a douche, wherever a germicide and disinfectant is needed. Three drops on sugar for hiccough.

