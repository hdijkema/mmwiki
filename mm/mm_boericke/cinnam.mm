Cancer where pain and fetor are present. Best when skin is intact. Its use in haemorrhages has abundant clinical verification. Nosebleed. Haemorrhages from bowels, haemoptysis, etc. A strain in loins or false step brings on a profuse flow of bright blood. 2[Post-partum haemorrhage]. Flatulency and diarrhoea. Feeble patients with languid circulation.

:rubric=female
Bearing-down sensation. Menses 2[early, profuse, prolonged, bright red]. Sleepy. No desire for anything. Fingers seem swollen. Uterine haemorrhages caused by overlifting, during puerperal state; menorrhagia.

