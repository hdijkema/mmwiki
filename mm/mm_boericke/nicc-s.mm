Useful in climacteric disturbances. Periodic neuralgias of malarial origin. Urine and saliva increased. Coppery taste. Weak, asthenopic literally persons with weak digestion and constipation, are worse in morning and suffer from periodic headaches and hoarseness.

:rubric=head
Nervous, uneasy, desire to recline, tired, cannot settle down to any occupation. Periodic headaches, occipital pain, extending down to spine, worse lying on back; sore pain in eyes.

:rubric=back
Stiff, numb sensation, worse in neck. Spine sore. Awakened in morning with burning soles, Spinal pains, legs and arms heavy and weak, cannot lie on back.

:rubric=female
Dull aching in ovaries, with sensation as if menses appear. 2[Hot flashes], followed by perspiration on parts touching each other, when separated become dry.

