:rubric=generals
Acts directly on intestinal mucous membrane increasing peristalsis. Colitis, with pathological flora. Has vaso-dilatory properties. Arterial hypotension begins usually in 30 to 45 minutes after twenty to forty drop doses of the tincture.

Adapted to fleshy subjects with dyspepsia and catarrhal affections. High livers. Patients who eat a great deal more, 2[especially meat], than they drink. 2[Pain in hip, pain in psoas and iliac muscles. Pulmonary tuberculosis].

Cough and expectoration diminishes, temperature becomes normal, weight is gained, and sleep becomes regular. 2[Haemoptysis].

:rubric=head
Heavy; pulsation in temples; catarrhal deafness.

:rubric=mouth
Much sweetish saliva after meals and at night. Sensation of a hair on tongue or throat.

:rubric=stomach
Voracious appetite. Burning eructations. Least change in diet causes trouble. Constipation, with constant dull pains in bowels. 2[Tongue pale, red papillae].

:rubric=respiratory
Constant rattling of mucus in bronchi. Cough in the morning after leaving bedroom, with mucous expectoration, which is tenacious and difficult to raise. Sensitive to cold air. Dilated bronchi, with fetid expectoration. Darting pain in chest.

:rubric=female
Pain in swelling of breasts. Eruption in vagina and on breasts and vulva during menses.

:rubric=relations
