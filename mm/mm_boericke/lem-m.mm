:rubric=generals
A catarrhal remedy. Acts especially upon the nostrils. 2[Nasal polypi; swollen turbinates. Atrophic rhinitis]. Asthma from nasal obstruction; worse in wet weather.

:rubric=nose
2[Putrid smell]; loss of smell. Crusts and muco-purulent discharge very abundant. Post-nasal dropping. Pain like a string from nostrils to ear. Reduces nasal obstruction when it is an oedematous condition. Dryness of naso-pharynx.

:rubric=mouth
2[Putrid taste] on rising in the morning. Dry pharynx and larynx.

:rubric=abdomen
Disposition to noisy diarrhoea.

:rubric=modalities
M[Worse], in damp, rainy weather, especially 2[heavy rains].

:rubric=relations
:rubric2=compare
R[Dulc] (damp surroundings and foggy weather). R[Calc], R[Teucr], R[Calend], R[Nat-s].

:rubric=dose
Third to thirtieth potency.

