:rubric=generals
Of frequent use in children with sour diarrhoea; difficult dentition. 2[Whole child smells sour].

:rubric=mind
Impatient and vehement; desires many things and cries (2[Cina]).

:rubric=head
Sweat on hairy scalp; constant and profuse. 2[Cool sweat on the face, especially about mouth and nose].

:rubric=mouth
Much saliva. Sensation of coolness in teeth. Difficult teething; restless and irritable. Breath smells sour (2[Cham]).

:rubric=stomach
Desire for various kinds of food, but soon tires of all. Throbbing in pit. Feels full.

:rubric=abdomen
Colicky pain about navel. Colic when uncovering. Wind seems to rise up to chest.

:rubric=rectum
Before stool, unsuccessful urging to urinate. 2[Stools smell sour], pasty, with shivering and tenesmus, and burning in anus. Sour diarrhoea during dentition. Colicky, even ineffectual urging to evacuate altered fecal stools.

