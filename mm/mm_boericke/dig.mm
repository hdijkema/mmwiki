:rubric=dose
The third to thirtieth attenuation will bring about reaction when the drug is homeopathically indicated; but for palliative purposes the physiological dosage is required. For this purpose, the tincture made from the 2[fresh] plant, in doses of five to twenty drops, when the cardiac stimulation is desired, or the infusion of 1 1/2 per cent. Dose, one-half to one ounce if the diuretic action is wanted. The tincture may be given on sugar or bread, and nothing liquid be taken for twenty minutes before or after its administration. Of the powdered leaves, 1/2 to 2 grains in capsules. Digitoxin 1-250 grain. No matter what form of digitalis is given the dose should be reduced as soon as the pulse rate has been lowered to 80 beats a minute and the normal rhythm has been partially or completely restored. Under such conditions a good rule is to cut the dose in half and still more if there be a sudden falling off of the urinary output.