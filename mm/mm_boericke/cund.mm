:rubric=generals
Stimulates the digestive functions and thus improves the general health. Allays the pain in gastralgia accompanying cancer of stomach. Modifies secretions of digestive glands. Varicose ulcers. Lupus.

2[Painful cracks in corner of mouth] is a guiding symptom of this drug. Chronic gastric catarrh, syphilis, and cancer. Tumors; stricture of oesophagus. The active principle (2[Condurangin]) produces locomotor ataxia.

:rubric=stomach
Painful affections of the stomach; ulceration. Vomiting of food and indurations, constant burning pain. Stricture of oesophagus, with burning pains behind sternum, where food seems to stick. Vomiting of food, and indurations in left hypochondrium with constant burning pain.

:rubric=skin
Fissures form about the muco-cutaneous outlets. Epithelioma of lips or anus. Ulcerative stage of carcinoma cutis when fissures form.

