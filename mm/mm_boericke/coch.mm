:rubric=eyes
2[Sore and scrofulous]; traumatic inflammation of eyes, blearedness and cataract. Copious running from eyes.

:rubric=stomach
Pain towards back; worse, pressure on dorsal vertebrae. Belching and cramps. Colic with backache. 2[Violent cramp from stomach through both sides around to back]. Griping around navel.

