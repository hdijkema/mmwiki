:rubric=generals
A remedy for asthmatic and bronchial affections. Bronchial phthisis, with night-sweats and emaciation. Asthma relieved by expectoration. 2[Cough after influenza]. Furthers absorption of effusion in plural cavity. Appetite poor and impaired digestion. Whooping cough.

:rubric=head
Dizzy, feels intoxicated. Pressure outwards; worse, occiput. Pain in ears. Coryza. Burning in throat. Foul mouth in morning. Coryza with dizziness and sneezing.

:rubric=respiratory
Wheezing; asthma, with coryza and 2[mucous secretions]. Dull pain in right lung. Burning in fauces. Chronic bronchitis, bronchial tuberculosis, with profuse, easily raised bronchial secretion, giving relief.

:rubric=male
Sore, dragging in testicle, could not bear any pressure; better gentle support.

