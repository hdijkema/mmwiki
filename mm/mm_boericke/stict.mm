:rubric=generals
Offers a set of symptoms like coryza, bronchial catarrh and influenza, together with nervous and 2[rheumatic] disturbances. There is a general feeling of dullness and malaise, as when a cold is coming on; dull, heavy pressure in forehead, catarrhal conjunctivitis, etc. 2[Rheumatic stiffness of neck].

:rubric=mind
2[Feels as if floating in air] (2[Datura arborea; Lac. Can]). Confusion of ideas; 2[patient must talk].

:rubric=head
Dull headache, with dull heavy pressure in forehead and 2[root of nose. Catarrhal headache before discharge appears]. Burning in eyes and soreness of balls. Sensation as if scalp were too small. Burning in eyelids.

:rubric=nose
2[Feeling of fullness at the root of the nose] (2[Nux]). Atrophic rhinitis (2[Calc fluor]). 2[Dryness of nasal membrane. Constant need to blow the nose, but no discharge]. Dry scabs, especially in evening and night. 2[Hay-fever]; incessant sneezing (2[Sabad]).

:rubric=female
Scanty flow of milk.

:rubric=abdomen
Diarrhoea; stools profuse, frothy; worse, morning. Urine increased, with soreness and aching in bladder.

:rubric=respiratory
Throat raw; dropping of mucus posteriorly. 2[Dry, hacking cough during night; worse, inspiration]. Tracheitis, facilitates expectoration. Loose cough in morning. Pain through chest from sternum to spiral column. Cough after measles (2[Sang]); 2[worse towards evening and when tired. Pulsation from right side of sternum down to abdomen].

:rubric=extremities
Rheumatic pain in right shoulder joint, deltoid, and biceps. Swelling, heat, redness of joints. 2[Spot of inflammation and redness over affected joint]. Pain severe and drawing. Chorea-like spasms; legs feel floating in air. 2[Housemaid's knee] (2[Rhus; Kali hyd; Slag]). Shooting pains in knees. Joints and neighboring muscles red, swollen, painful. Rheumatic pains precede catarrhal symptoms.

:rubric=modalities
M[Worse], sudden changes of temperature.

