Affections of breast, heart, lancinating pains.

With heart symptoms, numbness and pain in left arm, sensation of sinking in pit of stomach.

Left side of head, face, chest, hip to knee, all have lancinating pain.

A streak of pain from the temple to the chin, as if the face were cut with a razor. Burning in throat, roaring in ears. Sensation of a tight girdle around chest, as of a knife piercing the heart. Pain in neck, back, and extremities. Compare: 2[Arnica; Lachesis].

