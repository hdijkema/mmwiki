:rubric=generals
A mucous membrane medicine. Bearing-down sensations in pelvic organs and bronchial symptoms are most marked. Substernal pain. Diabetes.

:rubric=head
Vertigo on stooping; throbbing after eating, pressure in temples and forehead.

:rubric=respiratory
Dry cough; worse at night and lying down; larynx painful. Chronic bronchitis; cough, with much thick expectoration, with languor and weak digestion. Stitches behind sternum. Teasing cough with much and free expectoration. Palliative in tubercular laryngitis.

:rubric=female
Menses too early and painful. Labor-like pains; urging to stool; dragging in genitals, with violent backache. Itching of legs during menses, chattering of teeth from cold during menstruation. Moving about in abdomen, stitches in genitals. Chronic metritis.

:rubric=rectum
Pressing toward rectum as of something extruding.

:rubric=urinary
Frequent urging to urinate; passes only in drops. Violet odor (2[Tereb]).

:rubric=extremities
Pain in right shoulder and wrist; tearing in left palm, unable to double fingers; pain in lower limbs, feet and ankles.

:rubric=relations
:rubric2=compare
R[Crocus], R[Ignatia], R[Arum dracontium] (loose cough worse at night on lying down).

:rubric=dose
First to third potency.

