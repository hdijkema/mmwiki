:rubric=generals
Skin and female symptoms marked. Indurated feeling. Vasomotor symptoms of skin and mucous membranes.

Dizzy, better lying on back. Fainting. Numbness and blindness. Nausea and vomiting, followed by creeping chills from feet upward. Cramping pain in bowels. Axillary glands swollen with soreness of upper arms. Perspiration on parts laid on with itching.

:rubric=face
Painful and swollen. Erysipelatous inflammation of lids. 2[Chemosis of conjunctiva]. Swelling of mouth and throat, with violent burning pains.

:rubric=urinary
2[Burning] with micturition; also itching.

:rubric=female
Menstruation, preceded by depression, pain, pressure, and constipation. 2[Left ovary markedly affected], with 2[frequent burning, micturition]; sacral pains extending up back. 2[Erosion around external os].

:rubric=skin
Erythema; 2[intense itching]; burning. 2[Boils]; stinging and soreness, relieved by bathing with vinegar. Wheals, macules and swellings with burning, stinging and soreness. Erythema multiforme, 2[relieved] by bathing with vinegar.

