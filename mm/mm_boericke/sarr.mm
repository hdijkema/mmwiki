A remedy for variola. Visual disorders. Congestion to head, with irregular heart action. Chlorosis. Contains a very active proteolytic enzyme. Sick headache; throbbing in various parts, especially in neck, shoulders and head, which feels full to bursting.

:rubric=eyes
Photophobia. Eyes feel swollen and sore. Pain in orbits. Black objects move with the eye.

:rubric=stomach
Hungry all the time, even after a meal. Sleepy during meals. Copious, painful vomiting.

:rubric=back
Pains shooting in 2[zig-zag] course from lumbar region to middle of scapula.

:rubric=extremities
Limbs weak; bruised pain in knees and hip-joints. Bones in arm pain. 2[Weak between shoulders].

:rubric=skin
Variola, aborts the disease, arrests pustulation.

