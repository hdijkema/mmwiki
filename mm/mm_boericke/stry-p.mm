:rubric=generals
This drug acts through the cerebro-spinal system upon muscles, causing twitching, stiffness, weakness and loss of power; upon circulation, producing irregularity of pulse, and upon the mind, producing lack of control, 2[uncontrollable desire to laugh] and disinclination to use the brain. Very irregular pulse. Tachycardia. Rapid and weak pulse. Useful in chorea, hysteria, acute asthenia after acute fevers. Symptoms M[Worse] motion, M[Better] rest and in open air. An excellent remedy in anaemia of spinal cord; paralysis; burning, aching, and weakness of spine; pain extends to front of chest; tenderness on pressure in mid-dorsal region; cold, clammy feet; 2[hands and axillae covered with clammy perspiration]. Atelectasis and break in the compensation of a hypertrophied heart; the beginning of fatty degeneration of the heart muscle (Royal).

:rubric=dose
Third trituration.

