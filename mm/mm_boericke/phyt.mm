:rubric=generals
Aching, soreness, restlessness, prostration, are general symptoms guiding to Phytolacca. Pre-eminently a glandular remedy. Glandular swellings with heat and inflammation. Has a powerful effect on fibrous and osseous tissues; fasciae and muscle sheaths; acts on scar tissue. Syphilitic bone pains; chronic rheumatism. Sore throat, quinsy, and diphtheria. Tetanus and opisthotonos. Decrease of weight. Retarded dentition.

:rubric=mind
Loss of personal delicacy, disregard of surrounding objects. Indifferent to life.

:rubric=head
Vertigo on rising. Brain feels sore. Pain from frontal region backward. Pressure in temples and over eyes. Rheumatism of scalp; pains come on every time it rains. Scaly eruption on scalp.

:rubric=eyes
2[Smarting]. Feeling of sand under lids. Tarsal edges feel hot. Fistula lachrymalis (R[Fl-ac]). 2[Abundant lachrymation, hot].

:rubric=nose
Coryza; flow of mucus from 2[one nostril and from posterior nares].

:rubric=mouth
Teething children with irresistible desire 2[to bite the teeth together]. Teeth clenched; lower lip drawn down; lips everted; jaws firmly set; chin drawn down on sternum. Tongue 2[red tip], feels rough and scalded; bleeding from mouth; blisters on side. Mapped, indented, fissured, with yellow patch down center. Much stringy saliva.

:rubric=throat
2[Dark red or bluish red]. Much pain at root of tongue; soft palate and tonsils swollen. Sensation of a lump in throat (2[Bell; Lach]). 2[Throat feels rough, narrow, hot. Tonsils swollen], especially right; dark-red appearance. 2[Shooting pain into ears on swallowing]. Pseudo-membranous exudation, grayish white; thick, tenacious yellowish mucus, difficult to dislodge. 2[Cannot swallow anything hot] (2[Lach]). Tension and pressure in parotid gland. Ulcerated sore throat and diphtheria; 2[throat feels very hot; pain at root of tongue extending to ear]. Uvula large, dropsical. 2[Quinsy; tonsils and fauces swollen], with burning pain; cannot swallow even water. 2[Mumps. Follicular pharyngitis].

:rubric=abdomen
Sore spot in right hypochondrium. Rheumatism of abdominal muscles. Colic at navel. Burning griping pains. Bruised feeling through epigastrium and abdomen. Constipation of the aged and those with weak heart. Bleeding from rectum.

:rubric=urine
Scanty, suppressed, 2[with pain in kidney region]. Nephritis.

:rubric=female
Mastitis; 2[mammae hard and very sensitive]. Tumors of the breasts with enlarged axillary glands. Cancer of breast. Breast is hard, painful and of purple hue. Mammary abscess. When child nurses, 2[pain goes from nipple all over body]. Cracks and small ulcers about nipples. Irritable breasts, before and during menses. Galactorrhoea (2[Calc]). Menses too copious and frequent. Ovarian neuralgia of right side.

:rubric=male
Painful induration of testicles. 2[Shooting along perineum to penis].

:rubric=heart
Feeling as if heart leaped into throat (2[Pod]). Shock of pain in cardiac region alternating with pain in right arm.

:rubric=respiratory
Aphonia. Difficult breathing; dry hacking, tickling cough; worse at night (2[Mentha; Bellad]). Aching pains in chest, through mid-sternum; with cough. Rheumatism of lower intercostals.

:rubric=back
Aching pains in lumbar region; pains streaking up and down spine into sacrum. Weakness and dull pain in region of kidneys. Back stiff, especially in morning on rising and during damp weather.

:rubric=extremities
Shooting pain in right shoulder, with stiffness and inability to raise arm (2[see Heart]). Rheumatism pains; worse in morning. 2[Pains fly like electric shocks], shooting, lancinating, shifting rapidly (2[Puls; Kali bich]). Pain in under side of thighs. Syphilitic sciatica. 2[Aching of heels]; relieved by elevating feet. Pains like shocks. Pain in legs, patient dreads to get up. Feet puffed; pain in ankles and feet. Neuralgia in toes.

:rubric=fever
High fever, alternating with chilliness and great prostration.

:rubric=skin
Itches, becomes dry, shrunken, pale. Papular and pustular lesions. Most useful in early stages of cutaneous diseases. 2[Disposition to boils], and when sloughing occurs. Squamous eruptions. Syphilitic eruptions. Swelling and induration of glands. 2[Venereal buboes]. Scarlatina-like rash. Warts and moles.

:rubric=modalities
M[Worse], sensitive to electric changes. Effects of a wetting, when it 2[rains], exposure to 2[damp], cold weather, night exposure, motion, right side. M[Better], warmth, dry weather, rest.

