:rubric=generals
Manifests its action mainly in chronic rheumatic, arthritic and paralytic affections, indicated by the tearing, drawing pains in the muscular and fibrous tissues, with deformities about the joints; progressive loss of muscular strength, tendinous contractures. Broken down seniles. In catarrhal affections of the air passages, and seems to choose preferably dark-complexioned and rigid-fibered persons. Restlessness at night, with tearing pains in joints and bones, and faint-like sinking of strength. This weakness progresses until we have gradually appearing paralysis. Local paralysis, vocal cords, muscles of deglutition, of tongue, eyelids, face, bladder and extremities. Children are slow to walk. The skin of a Causticum person is of a 2[dirty white sallow], with warts, especially on the face. Emaciation due to disease, worry, etc, and of long standing. 2[Burning, rawness, and soreness] are characteristic.

:rubric=mind
S{Child does not want to go to bed alone}. S{Least thing makes it cry}. S{Sad, hopeless}. S{2[Intensely sympathetic]}. S{Ailments from long-lasting grief, sudden emotions}. S{Thinking of complaints, aggravates, especially haemorrhoids}.

:rubric=head
Sensation of empty space between forehead and brain. Pain in right frontal eminence.

:rubric=face
Paralysis of right side. Warts. Pain in facial bones. Dental fistula. Pain in jaws, with difficulty in opening mouth.

:rubric=eyes
Cataract with motor disturbances. Inflammation of eyelids; ulceration. Sparks and dark spots before eyes. 2[Ptosis] (R[Gels]). Vision impaired, as if film were before eyes. Paralysis of ocular muscles after exposure to cold.

:rubric=ears
Ringing, roaring, pulsating, with deafness; words and steps re-echo; chronic middle-ear catarrh; accumulation of ear-wax.

:rubric=nose
2[Coryza, with hoarseness]. Scaly nose. Nostrils ulcerated. 2[Pimples and warts].

:rubric=mouth
Bites inside of cheek from chewing. Paralysis of tongue, with indistinct speech. Rheumatism of articulation of lower jaw. Gums bleed easily.

:rubric=stomach
Greasy taste. Aversion to sweets. Feels as if lime were burned in stomach. Worse after eating fresh meat; smoked meat agrees. Sensation of ball rising in throat. 2[Acid] dyspepsia.

:rubric=stool
Soft and small, size of goose-quill (R[Phos]). Hard, tough, covered with mucus; shines like grease; small-shaped; expelled with much straining, or only on standing up. Pruritus. Partial paralysis of rectum. Rectum sore and burns. Fistula and large piles.

:rubric=urine
Involuntary when coughing, sneezing (R[Puls]). Expelled very slowly, and sometimes retained. Involuntary during first sleep at night; also from slightest excitement. 2[Retention] after surgical operations. Loss of sensibility on passing urine.

:rubric=female
2[Uterine inertia during labor]. Menses cease at night; 2[flow only during day] (R[Cycl]; R[Puls]). Leucorrhoea at night, with great weakness (R[Nat-m]). Menses delay, late (R[Con]; R[Graph]; R[Puls]).

:rubric=respiratory
2[Hoarseness] with pain in chest; 2[aphonia]. Larynx sore. 2[Cough, with raw soreness of chest]. Expectoration scanty; 2[must be swallowed]. Cough 2[with pain in hip], especially left worse in evening; 2[better, drinking cold water]; worse, warmth of bed. Sore streak down trachea. Mucus under sternum, which he cannot 2[quite reach]. Pain in chest, with palpitation. Cannot lie down at night. Voice re-echoes. Own voice roars in ears and distresses. Difficulty of voice of singers and public speakers (Royal).

:rubric=back
Stiffness between shoulders. Dull pain in nape of neck.

:rubric=extremities
Left-sided sciatica, with numbness. Paralysis of single parts. Dull, tearing pain in hands and arms. Heaviness and weakness. Tearing joints. Unsteadiness of 2[muscles of forearm] and hand. Numbness; loss of sensation in hands. 2[Contracted tendons]. Weak ankles. Cannot walk without suffering. 2[Rheumatic tearing in limbs; better by warmth, especially heat of bed]. Burning in joints. Slow in learning to walk. Unsteady walking and easily falling. 2[Restless legs at night]. Cracking and tension in knees; stiffness in hollow of knee. Itching on dorsum of feet.

:rubric=skin
Soreness in folds of skin, back of ears, between thighs. 2[Warts] large, jagged, bleeding easily, on tips of fingers and nose. Old burns that do not get well, and ill effects from burns. Pains of burns. Cicatrices freshen up; old injuries reopen. Skin prone to intertrigo during dentition.

:rubric=sleep
Very drowsy; can hardly keep awake. Nocturnal sleeplessness, with dry heat, inquietude.

:rubric=modalities
M[Worse], dry, cold winds, in 2[clear fine] weather, 2[cold] air; from motion of carriage. M[Better], 2[in damp, wet weather; warmth]. Heat of bed.

:rubric=relations
According to the careful investigations of Dr. Wagner of Basel, Causticum corresponds to Ammon causticum 4x. R[Caust] does not agree with R[Phos]; the remedies should not be used after each other. R[Diphtherotoxin] follows, causticum in chronic bronchitis.

Antidote: Paralysis from lead-poisoning.

