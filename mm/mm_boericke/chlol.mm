:rubric=generals
This drug, used in physiological doses, is a powerful hypnotic and cardiac depressant. It has a marked effect on the skin, producing erythema, ecchymosis, etc, which symptoms have been utilized homeopathically with much success, especially in the treatment of hives. Emotional excitability, hallucinations. Night terrors in children. Muscular prostration.

:rubric=head
Morning headache; worse in forehead, also in occiput, on motion; better in open air. Passive cerebral hyperaemia (use 30th). Feeling as if hot band were drawn from temple to temple. Hears voices.

:rubric=eyes
Eyes blood-shot and watery. Circles of light, black spots. Illusions of sight where eyes are closed or at night. Dim vision. Conjunctivitis, burning in eye and lids; eyeball feels too large; everything looks 2[white].

:rubric=skin
Red blotches, like measles. 2[Urticaria, worse, spirituous liquors, hot drinks]. Erythema aggravated by alcoholic drinks, with palpitation; causes pain in tendons and extensors. Intense itching. Surface of body 2[stone-cold]. Wheals come on from a chill; better, warmth. Purpura (2[Phos; Crotal]).

:rubric=respiratory
Extreme dyspnoea, with sensation of weight and constriction of chest. Asthma, with sleeplessness.

:rubric=sleep
Insomnia, hallucinations, horrid dreams. Somnolence.

:rubric=modalities
M[Worse], after hot drinks, stimulants, eating, night.

:rubric=relations
:rubric2=antidotes
R[Ammon], R[Atrop], R[Dig], R[Mosch].

:rubric2=compare
R[Bell], R[Op], R[Apis], R[Veronal] - (a dangerous drug made by the action of alcohol upon urea and contains the same radical that alcohol does. Makes a man just as drunk as pure alcohol. Staggers, cannot stand up) (Dr. Varney). (Confluent reddish spots; dermatitis, itching of glans and prepuce; circumscribed dermatitis patch on first metacarpal phalangeal joint). R[Luminal] - (Sleeplessness with skin symptoms in migraine; lethargy like epidemic encephalitis) (Dr. Royal).

:rubric=dose
First trituration in hives, otherwise, higher potencies. Locally, in offensive foot-sweat, bathe with one per cent solution. For its physiological effects, five to twenty grains. Use cautiously.

