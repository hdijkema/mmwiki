:rubric=generals
In congested and atonic states of the uterus and its ligaments, with hepatic disorders. Chronic catarrhal diseases of the middle ear. 2[Flabby, debilitated constitution]. Both hands swollen in morning. Phlegmatic. Depressed, faint.

:rubric=head
Vertigo, with aching across forehead. Swimming feeling, as if top of head would float off. Bleeding of nose in clots.

:rubric=mouth
Sore irritable spots along sides of tongue and throat, gums sore, tongue swollen; aphthous ulceration. Putrid taste. Furred tongue, large, flabby, intended. Cough with aphonia.

:rubric=stomach
2[Drowsy after meals].

:rubric=urine
Dark, with albumen and casts. Diffuse nephritis. Much pain across small of back.

:rubric=female
Feeling as if uterus were pushed up on sitting down (2[Ferr iod]). Feels as if it opened and shut. Violent metrorrhagia. Leucorrhoea and backache. Passive, bearing-down from heavy condition of uterus. Womb is heavy, sodden, with tendency to prolapse. Subinvolution.

:rubric=extremities
Hands swollen every morning. Extreme weakness in ankles and knees.

:rubric=relations
:rubric2=compare
R[Aur mur nat], R[Calc], R[Sep], R[Heliotropium] (uterine displacement, with active bearing-down sensation and loss of voice; membranous dysmenorrhoea).

Antidote: R[Puls], R[Guaiacum].

:rubric=dose
Fifteen to twenty drops of Labarraque's solution in water. Third attenuation made with dilute alcohol, lower with water.

