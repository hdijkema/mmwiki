:rubric=generals
Weakness, palpitation, nervousness, fear, like other spider preparations. Chorea is the principal therapeutic field of this. Sexual symptoms are important.

:rubric=mind
Delirious, restless, sad; fears death; despondent.

:rubric=face
2[Twitching of facial muscles]. Mouth and eyes open in rapid succession. Hot and 2[flushed]. Tongue dry and parched; put out with difficulty. Head jerked to one side. Grating of teeth at night.

:rubric=stomach
Nausea, with dim sight. Aversion to food. Excessive thirst.

:rubric=male
Violent erections. Chordee (2[Kali brom; Camph]).

:rubric=extremities
Unsteady gait. 2[Constant motion of whole body]. Tremulous. Intense redness in streaks, following course of lymphatics. Twitching of limbs. Restless hands. Convulsive, 2[uncontrollable movements of arms and legs]. Limbs drag while walking.

