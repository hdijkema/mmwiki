:rubric=generals
S{Acts on circular muscular fibers, hence constrictions.}
S{It is the heart and arteries especially that at once respond to the influence of Cactus, producing very characteristic 2[constrictions] as of an iron band.}
S{This sensation is found in various places, oesophagus, bladder, etc.}
S{The mental symptoms produced correspond to those found when there are heart affections, sadness, and melancholy.}
S{2[Haemorrhage, constrictions, periodicity, and spasmodic pains].}
S{Whole body feels as if caged, each wire being twisted tighter.}
S{Atheromatous arteries and weak heart.}
S{Congestions; irregular distribution of blood.}
S{2[Favors formation of clots speedily].}
S{Great periodicity.}
S{Toxic goitre with cardiac symptoms.}
S{Cactus is pulseless, panting and prostrated.}

