:local-name=(TONGO - DIPTRIX ODORATA)

:rubric=generals
Useful in neuralgia; pertussis.

:rubric=head
Tearing pain in supra-orbital nerve, with heat and throbbing pain in head and epiphora. Confused, especially the occiput, with somnolence and a sort of intoxication. Trembling in right upper lid. Coryza; nose stopped, must breathe through mouth.

:rubric=extremities
Tearing pains in hip-joints, femur, and knee, especially left side.

:rubric=relations
R[Melilotus. Anthoxanthum], R[Asperula], and R[Tonga] contain R[Coumarin], the active principle. Compare them in hay-fever; also, R[Trifol], R[Napth], R[Sabad].

:rubric=dose
Tincture and lower potencies.

