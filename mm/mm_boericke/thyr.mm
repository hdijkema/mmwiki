:rubric=dose
Crude Thyroid at times; better sixth to thirtieth potency. If the crude Thyroid is taken (two to three grains or more daily); the pulse should be watched. Must not be given in physiological doses where with feeble heart there is high blood pressure and not in tubercular patients. 

