:rubric=generals
Thyroid produces anaemia, emaciation, muscular weakness, sweating, headache, nervous tremor of face and limbs, tingling sensations, paralysis. Heart rate increased, exophthalmus and dilation of pupils. 2[In myxoedema and cretinism] its effects are striking. Rheumatoid arthritis. Infantile wasting. Rickets. Delayed union of fractures. In half grain doses twice a day over a considerable period said to be 2[effective in undescended testicle] in boys. Thyroid exercises a general regulating influence over the mechanism of the organs of nutrition, growth and development. Thyroid weakness causes decided craving for large amount of sweets.

Of use in psoriasis; and 2[tachycardia]. Arrested development in children. Improves the memory. 2[Goitre]. Excessive obesity. Acts better with pale patients, rather than those of high color. Amblyopia. 2[Mammary tumor. Uterine fibroid]. Great weakness and hunger, yet loses flesh. 2[Nocturnal enuresis. Agalactea]. Begin treatment early in pregnancy. Dose 1 1/2 gr. 2 to 3 times daily. 2[Vomiting of pregnancy] (give early in morning before patient gets up). 2[Fibroid tumors of the breast], 2x trit. Dilates arterioles (2[Adrenaline contacts them]). Sensation of faintness and nausea. Marked sensitiveness to cold Hypothyroidism after acute diseases, i.e, weakness. Easy fatigue, weak pulse, tendency to fainting, palpitation, cold hands and feet, low blood pressure, chilliness and sensitive to cold (Thyroid 1x3 times daily). Has a powerful diuretic action in myxodema and various types of oedema.

:rubric=mind
Stupor, alternating with restless melancholy. Irritable, worse least opposition; goes into a rage over trifles.

:rubric=head
Feeling of lightness in brain. 2[Persistent frontal] headache. Eyeballs prominent. Face flushed; lips burn. Tongue thickly coated. Fullness and heat. Face flushed. Bad taste in mouth.

:rubric=heart
Weak, frequent pulse, with inability to lie down. 2[Tachycardia] (2[Naja]). Anxiety about chest, 2[as if constricted]. 2[Palpitation from least exertion]. Severe heart pain; Ready excitability of heart. Heart's action weak, with numbness of fingers.

:rubric=eyes
Progressive diminution of sight with central scotoma (2[Carbon sulf]).

:rubric=throat
Dry, congested, raw, burning; worse left side.

:rubric=stomach
Desire for sweets and thirst for cold water. Nausea worse riding in car. Flatulence, much flatus in abdomen.

:rubric=urinary
Increased flow; polyuria; some albumen and sugar. 2[Enuresis] in weakly children who are nervous and irritable (1/2 gr night and morning). Urine smells of violets, burning along urethra, increase of uric acid.

:rubric=extremities
Rheumative arthritis with tendency to obesity, coldness and cramps of extremities. Peeling of skin of lower limbs. Cold extremities. Aching pains. Oedema of legs. Trembling of limbs and entire body.

:rubric=respiratory
Dry, painful cough with scanty, difficult expectoration and burning in pharynx.

:rubric=skin
2[Psoriasis] associated with adiposity (2[not] in developing stage). Skin 2[dry], impoverished. Cold hands and feet. 2[Eczema]. Uterine fibroids. 2[Browny swelling]. Swelling of glands of stony hardness. Sluggish cases. Jaundice with pruritus. Ichtyosis, lupus. Itching without eruption, worse night.

