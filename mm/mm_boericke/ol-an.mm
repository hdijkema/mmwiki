:rubric=generals
Acts on the nervous system, especially on pneumo-gastric region. Useful in migraine and neuralgia of spermatic cord. Burning pains and stitches. "2[Pulled upward]" and "2[from behind forward]" pains.

:rubric=head
Tearing pain, with sadness and irritability; worse after dinner; relieved by rubbing. Itching, burning vesicles; better, friction. Malar bones feel pulled forcibly upward. Migraine with polyuria.

:rubric=eyes
Smarting in eyes; misty vision. Glistening bodies before eyes. Lachrymation when eating. Short-sighted. Twitching of lids (2[Agar]).

:rubric=nose
Watery, excoriating discharge; worse in open air.

:rubric=face
Feels drawn. Cramp-like pains. 2[Twitching of lips]. Malar bone feels pulled upward. Toothache, 2[better pressing teeth together].

:rubric=mouth
Bites cheek while eating (2[Caust]). Tongue feels sore. Greasy feeling in mouth.

:rubric=throat
Sore, dry, constricted. Air feels cold.

:rubric=stomach
2[Sensation as if water were in stomach]; of coldness, of constriction, and of burning; better, eructations.

:rubric=abdomen
Flatulence and rumbling. Ineffectual urging stool, with burning in anus. After stool, bruised pain in abdomen.

:rubric=urine
2[Polyuria]. Greenish urine, frequent and urgent want to urinate, with tenesmus and scanty emission. Itching in urethra.

:rubric=male
Desire increased; ejaculation too soon. Pain along spermatic cord to testicles. 2[Testicles feel seized and pulled forcibly upward]; worse, right. 2[Pressure in the perineum]. Prostatic hypertrophy.

:rubric=female
2[Early and scanty menstruation]; flow black.

:rubric=respiratory
Chest feels constricted. Asthma from suppressed foot-sweat. Oppression. 2[Stitches in breast from behind forward].

:rubric=extremities
Sprained feeling in small of back. Cracking of vertebrae on raising head (2[Aloe, Nat c, Thuj]). Restlessness. Rheumatic pain in shoulders. Fish-brine odor of sweat of heels.

:rubric=modalities
M[Worse], after eating, from 2 to 9 pm. M[Better], by rubbing, eructation, open air.

