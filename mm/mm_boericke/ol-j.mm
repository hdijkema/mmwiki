:local-name=Cod-liver Oil

:rubric=generals
Internally, a nutrient and a hepatic and pancreatic remedy (Burnett). Emaciation, lassitude, scrofulous diseases, rheumatic affections. 2[Atrophy of infants]; emaciation with hot hands and head; restless and feverish at night. 2[Pains in liver region]. Tuberculosis in the beginning.

:rubric=chest
Hoarseness. Sharp stitching pains. Burning spots. 2[Dry, hacking, tickling cough], especially at night. Whooping-cough in miserable, scrofulous children. Here give drop doses, increasing daily one drop up to twelve, then descend in the same way (Dahlke). 2[Soreness through chest]. Haemoptysis (2[Acalypha; Millef]). 2[Palpitation], accompanies other symptoms. 2[Yellowness]. Children who cannot take milk.

:rubric=extremities
Aching in elbows and knees, in 2[sacrum]. Chronic rheumatism, with rigid muscles and tendons. 2[Burning in palms].

:rubric=fever
Constantly chilly towards evening. 2[Hectic fever]. Night-sweats.

:rubric=relations
:rubric2=compare
R[Cholesterine], R[Tubercul], R[Phosph], R[Iod]. One liter of Ol. Jecoris contains 0. 4 gram Iod. R[Gadus morrhua] - Cod - (frequent breathing, with flapping of alae nasi; rush of blood to chest; pain in lungs and cough; dry heat in palms).

:rubric=dose
First to third trituration. Locally in ringworm, and nightly rubbing, for dwarfish, emaciated babies.

