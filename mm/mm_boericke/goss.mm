:rubric=generals
A powerful emmenagogue, used in physiological doses. Homeopathically, it corresponds to many reflex conditions, depending on disturbed uterine function and pregnancy. Gossypium will relieve tardy menses, especially with sensation that the flow is about to start and yet does not do so. Tall, bloodless patients, with nervous chills.

:rubric=head
Pain in cervical region with tendency for head to draw backward with nervousness.

:rubric=stomach
Nausea, with inclination to vomit before breakfast. Anorexia, with uneasy feeling at scrobiculum at time of menses.

:rubric=female
Labia swollen and itching. 2[Intermittent pain in ovaries]. Retained placenta. Tumor of the breast with swelling of axillary glands. Morning sickness, with sensitive uterine region. Suppressed menstruation. Menses too watery. Backache, weight and dragging in pelvis. Uterine sub-involution and fibroids, with gastric pain and debility.

