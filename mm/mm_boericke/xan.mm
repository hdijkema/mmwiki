:rubric=generals
Its specific action is on the nervous system and mucous membranes. Paralysis, especially 2[hemiplegia]. Painful haemorrhages, after-pains, 2[neuralgic dysmenorrhoea], and rheumatic affections, offer a therapeutic field for this remedy, especially in patients of spare habit and nervous, delicate organization. Indigestion from over-eating or from too much fluid. Sluggish capillary circulation. Neurasthenia, poor assimilation, insomnia, occipital headache. Increases mucous secretion of mouth and stimulates the secretion from all glands with ducts opening in the mouth.

:rubric=mind
Nervous, frightened. Mental depression.

:rubric=head
Feels full. Weight and pain on vertex. Pain over eyes, throbbing pressure over nose, pressure in forehead; head seems divided; ringing in ears. Occipital headache. Sick headache with dizziness and flatulence.

:rubric=face
Neuralgia of lower jaw. Dryness of mouth and fauces. Pharyngitis (2[Wyethia]).

:rubric=abdomen
Griping and diarrhoea. Dysentery, with 2[tympanites], tenesmus; inodorous discharges.

:rubric=female
Menses too early and painful. Ovarian neuralgia, with pain in loins and lower abdomen; worse, 2[left side], extending down the thigh, along genito-crural nerves. 2[Neuralgic dysmenorrhoea], with neuralgic headaches; 2[pain in back and down legs]. Menses thick, almost black. 2[After-pains] (2[Arnica; Cup; Cham]). Leucorrhoea at time of menses. Neurasthenic patients who are thin, emaciated; poor assimilation with insomnia and occipital headache.

:rubric=respiratory
Aphonia. Constant desire to take a long breath; oppression of chest. Dry cough, day and night.

:rubric=extremities
Paralysis of left side following spinal disorders. Numbness of left side; impairment of nerves of motion. Hemiplegia. Pain in nape, extending down back. Sciatica; worse, hot weather. Anterior, crural neuralgia (2[Staph]). Left arm numb. Neuralgic shooting pain, as from electricity, all over limb.

:rubric=sleep
Hard and unrefreshing; dreams of flying. Sleeplessness in neurasthenics.

