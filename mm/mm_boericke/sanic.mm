:local-name=(SANICULA)

:rubric=generals
Has been found a useful remedy in enuresis, seasickness, constipation, etc. Rickets.

:rubric=head
Dread of downward motion (2[Borax]). 2[Profuse sweat on occiput] and in nape of neck, during sleep (2[Calc; Sil]). Photophobia. Lachrymation in cold air or from cold application. Profuse scaly dandruff. Soreness behind ears.

:rubric=throat
Thick, ropy, tenacious mucus.

:rubric=mouth
Tongue large, flabby, burning; must protrude it to keep cool. Ringworm on tongue.

:rubric=stomach
Nausea and vomiting from car-riding. Thirst; drink little and often (2[Ars; Chin]). Is vomited as soon as it reaches the stomach.

:rubric=rectum
Stools large, heavy and painful. 2[Pain in whole perineum]. No desire until a large accumulation. After great straining only partially expelled; recedes, crumbles at verge of anus (2[Mag mur]). Very offensive odor. Excoriation of skin about anus, perineum, and genitals. Diarrhoea; changeable in character and color; after eating.

:rubric=female
Bearing-down, as if contents of pelvis would escape; better, rest. Desire to support parts. Soreness of uterus. Leucorrhoea with 2[odor of fish-brine or cold cheese] (2[Hepar]). Vagina feels large.

:rubric=back
Dislocated feeling in sacrum and better lying on right side.

:rubric=extremities
Burning of soles of feet (2[Sulph; Lach]). Offensive foot-sweat (2[Sil; Psor]). Cold, clammy sweat of extremities.

:rubric=skin
Dirty, greasy, brownish, wrinkled. Eczema, fissured hands and fingers (2[Petrol; Graph]).

:rubric=modalities
2[Worse, moving arms backward].

:rubric=relations
:rubric2=compare
R[Abrot], R[Alum], R[Calc], R[Sil], R[Sulph]. Sanicula Aqua must not be confounded with the Sanicle (pool-root or wood marsh), also called R[Sanicula]. This is used in various nervous affections, resembling Valeriana. It is used as a vulnerary, resolvent for sanguineous extravasations, and as an astringent (Has not been proved).

:rubric=dose
Thirtieth potency.

