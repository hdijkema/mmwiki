Seems to act especially on nervous system and urinary organs. A remedy for dropsy, hepatic, renal or cardiac and post-scarlatinal; causes diaphoresis and augments the urinary secretion. Acute rheumatic inflammation of large joints. Intermittent, pressing-down uterine pains.

:rubric=head
Feels as if 2[a sharp instrument were thrust through from temple to temple]. Constriction across forehead. Nervous headache, after suppressed perspiration, followed by 2[increased urine, with increase of specific gravity]. Headache from retention of effete matters in system.

