:rubric=generals
Spasmodic tickling cough, especially in cardiac patients, is often magically influenced by this drug. 2[Lack of reaction], especially in chest and heart affections. 2[Drink rolls audibly through oesophagus and intestines]. General coldness, not ameliorated by warmth. Violent pain in stomach with loss of speech. Spasm of facial muscles and oesophagus. Asphyxia neonatorum.

:rubric=fever
Coldness; chills and heat alternate. Thirst, with dry mouth in afternoon.

:rubric=respiratory
Cyanosis and dyspnoea; worse, sitting up. Patient puts hands on heart. Cough, with valvular disease. Exercise causes pain around heart. Tickling, 2[dry cough]. Dyspnoea. Constriction of chest. Cough, with copious, jelly-like, or bloody expectoration. Small and feeble pulse. Threatening paralysis of lungs. 2[Gasping for breath]; clutches at heart.

:rubric=heart
Mitral regurgitation. Clutching at heart and palpitation. Cyanosis neonatorum.

:rubric=sleep
Spells of deep sleep, with snoring and stertorous breathing.

:rubric=extremities
Toe and finger nails become knotty. Skin blue. Sprained pains in hips, thighs and heels. Cold, clammy feet and legs. Clubbing of fingers. Veins of hands distended.

