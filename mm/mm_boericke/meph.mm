:rubric=generals
A great medicine for 2[whooping-cough]. In order to insure its full success, it should be given in the lower dilutions from 1x to 3x. Suffocative feeling, asthmatic paroxysms, spasmodic cough; cough so violent, seems as if each spell would terminate life. Child must be raised up, gets blue in face, cannot exhale, Mucous rales through upper part of chest. Patient wants to bathe in ice-cold water.

:rubric=mind
Excited, full of fancies. Can neither sleep nor work.

:rubric=eyes
Pain from overexertion; blur; unable to distinguish letters; conjunctiva red; eyes hot and painful.

:rubric=mouth
Painful jerks in root of teeth. Bloated face. Coppery taste, as after eating onions.

:rubric=respiratory
Sudden contraction of glottis, when drinking or talking. 2[Food goes down wrong way]. False croup; 2[cannot exhale. Spasmodic and whooping-cough. Few paroxysms in day-time, but many at night]; with vomiting after eating. Asthma, as if inhaling sulphur; cough from talking; hollow, deep, with rawness, hoarseness, and pains through chest. 2[Violent spasmodic cough; worse] at night.

:rubric=sleep
Awakes at night with rush of blood to lower legs. Vivid dream of water, fire, etc.

:rubric=relations
:rubric2=compare
R[Dros], R[Coral], R[Sticta].

:rubric=dose
First to third potency. Has a very short action.

