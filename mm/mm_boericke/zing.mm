:local-name=(ZINGIBER)

:rubric=generals
States of debility in the digestive tract, and sexual system and respiratory troubles, call for this remedy. Complete cessation of function of kidneys.

:rubric=head
Hemicrania; sudden glimmering before eyes; feels confused and empty. Pain over eyebrows.

:rubric=nose
Feels obstructed and dry. Intolerable itching; red pimples.

:rubric=stomach
Taste of food remains long, especially of bread and toast. Feels heavy, like from a stone. 2[Complaints from eating melons and drinking impure water. Acidity] (2[Calc; Robinia]). Heaviness in stomach on awakening with wind and rumbling, great thirst and emptiness. Pain from pit to under sternum, worse eating.

:rubric=abdomen
Colic, diarrhoea, extremely loose bowels. Diarrhoea from drinking bad water, with much flatulence, cutting pain, relaxation of sphincter. Hot, sore, painful anus during pregnancy. Chronic intestinal catarrh. Anus red and inflamed. Haemorrhoids hot, painful, sore. (2[Aloe]).

:rubric=urinary
Frequent desire tu urinate. Stinging, burning in orifice. Yellow discharge from urethra. Urine thick, turbid, of strong odor, suppressed. Complete suppression after typhoid. After urinating, continues to ooze in drops.

:rubric=male
Itching of prepuce. Sexual desire excited; painful erections. Emissions.

:rubric=respiratory
Hoarseness. 2[Smarting below larynx]; breathing difficult. 2[Asthma], without anxiety, M[Worse] toward morning. Scratching sensation in throat; stitches in chest. Cough dry, hacking; copious morning sputa.

:rubric=extremities
Very weak in all joints. Back lame. Cramps in soles and palms.

:rubric=relations
:rubric2=compare
R[Calad].

Antidote: R[Nux-v].

:rubric=dose
First to sixth potency.

