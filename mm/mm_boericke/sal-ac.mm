:local-name=Salicylic Acid

:rubric=generals
The symptoms point to its use in rheumatism, dyspepsia, and 2[Meniere's disease]. Prostration after influenza; also tinnitus aurium and deafness. Haematuria.

:rubric=head
Vertigo; tendency to fall to left side. Headache; confusion in head on rising suddenly. Incipient coryza. Piercing pain in temples.

:rubric=eyes
Retinal haemorrhage. Retinitis after influenza, also albuminuric.

:rubric=ears
2[Roaring and ringing in ears]. Deafness, with vertigo.

:rubric=throat
Sore, red and swollen. Pharyngitis; swallowing difficult.

:rubric=stomach
2[Canker sores], with burning soreness and fetid breath. 2[Flatulence; hot, sour belching]. Putrid fermentation. 2[Fermentative dyspepsia]. Tongue purplish, leaden-colored; foul breath.

:rubric=stools
Putrid diarrhoea; gastro-intestinal derangements, especially in children; stools like green frog's spawn (2[Magn carb]). Pruritus ani.

:rubric=extremities
Knees swollen and painful. Acute articular rheumatism; worse, touch and motion, profuse sweat. Pain shifts. Sciatica, burning pain; worse at night. Copious foot-sweat and ill affects where suppressed.

:rubric=skin
Itching vesicles and pustules; better by scratching. Sweat without sleep. Urticaria. Hot and burning skin. Purpura. Herpes zoster. Necrosis and softening of bones.

:rubric=relations
:rubric2=compare
R[Salol] (rheumatic pain in joints, with soreness and stiffness, headache over eyes; urine violet-smelling); R[Colch], R[Chin], R[Lact ac]. Spiraea and Gaultheria contain salicyl acid.

:rubric=dose
Third decimal trituration. In acute articular rheumatism, 5 grains every 3 hours (Old school dose).

