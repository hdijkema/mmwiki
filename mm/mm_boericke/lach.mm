:local-name=(LACHESIS)

:rubric=generals
Like all snake poisons, Lachesis decomposes the blood, rendering it more fluid; hence a haemorrhagic tendency is marked. Purpura, septic states, diphtheria, and other low forms of disease, when the system is thoroughly poisoned and the prostration is profound. The modalities are most important in guiding to the remedy. Delirium tremens with much trembling and confusion. Very important during the climacteric and for patients of a melancholic disposition. Ill effects of suppressed discharges. Diphtheritic paralysis (2[Botulinum]). Diphtheria carriers. Sensation of tension in various parts. Cannot bear anything tight anywhere.

:rubric=mind
Great 2[loquacity]. Amative. Sad in the morning; no desire to mix with the world. Restless and uneasy; does not wish to attend to business; wants to be off somewhere all the time. Jealous (R[Hyos]). Mental labor best performed at night. Euthanasia. Suspicious; nightly delusion of fire. Religious insanity (R[Verat]; R[Stram]). Derangement of the 2[time sense].

:rubric=head
Pain through head on awaking. Pain at root of nose. Pressure and burning on vertex. Waves of pain; worse after moving. Sun headaches. With headache, flickerings, dim vision, very pale face. Vertigo. Relieved by onset of a discharge (menses or nasal catarrh).

:rubric=eyes
Defective vision after diphtheria, extrinsic muscles too weak to maintain focus. Sensation as if eyes were drawn together by cords which were tied in a knot at root of nose.

:rubric=ears
Tearing pain from zygoma into ear; also with sore throat. Ear-wax hard, dry.

:rubric=nose
Bleeding, nostrils sensitive. Coryza, preceded by headache. Hay asthma; paroxysms of sneezing (2[Silica; Sabad]).

:rubric=face
Pale. Trifacial neuralgia, left side, heat running up into head (2[Phos]). Tearing pain in jaw-bones (2[Amphisbaena; Phos]). Purple, mottled, puffed; looks swollen, bloated, jaundiced, chlorotic.

:rubric=mouth
Gums swollen, spongy, bleed. Tongue swollen, burns, trembles, red, dry and cracked at tip, catches on teeth. 2[Aphthous and denuded spots] with burning and rawness. Nauseous taste. 2[Teeth ache, pain extends to ears]. Pain in facial bones.

:rubric=throat
Sore, 2[worse left side, swallowing liquids. Quinsy]. Septic parotiditis. Dry, intensely swollen, externally and internally. Diphtheria; membrane dusky, blackish; 2[pain aggravated by hot drinks]; chronic sore throat, with much hawking; mucus sticks, 2[and cannot be forced up or down. Very painful; worse slightest pressure, touch is even more annoying]. In diphtheria, etc, the trouble 2[began] on the left side. Tonsils purplish. Purple, livid color of throat. Feeling as if something was swollen which must be swallowed; 2[worse, swallowing saliva or liquids. Pain into ear. Collar and neck-band must be very loose].

:rubric=stomach
Craving for alcohol, oysters. Any food causes distress. Pit of stomach painful to touch. Hungry, cannot wait for food. Gnawing pressure 2[made better by eating], but returning in a few hours. Perceptible trembling movement in the epigastric region. Empty swallowing more painful than swallowing solids.

:rubric=abdomen
Liver region sensitive, 2[cannot bear anything around waist]. Especially suitable to drunkards. Abdomen tympanitic, sensitive, painful (2[Bell]).

:rubric=stool
Constipated, 2[offensive] stool. Anus 2[feels tight], as if nothing could go through it. Pain darting up the rectum every time be sneezes or coughs. Haemorrhage from bowels like charred straw, 2[black particles]. Haemorrhoids protrude, become 2[constricted, purplish]. Stitches in them on sneezing or coughing. Constant urging in rectum, not for stool.

:rubric=female
Climacteric troubles, palpitation, flashes of heat, haemorrhages, vertex headache, fainting spells; worse, pressure of clothes. Menses too short, too feeble; 2[pains all relieved by the flow] (2[Eupion]). Left ovary very painful and swollen, indurated. Mammae inflamed, bluish. Coccyx and sacrum pain, especially on 2[rising] from sitting posture. Acts especially well at beginning and close of menstruation.

:rubric=male
Intense excitement of sexual organs.

:rubric=respiratory
Upper part of windpipe very susceptible to touch. Sensation of suffocation and strangulation on lying down, particularly 2[when anything is around throat]; compels patient to spring from bed and rush for open window. Spasm of glottis; feels as if something ran from neck to larynx. Feels 2[he must take a deep breath]. Cramp-like distress in praecordial region. Cough; dry, suffocative fits, tickling. Little secretion and much sensitiveness; worse, pressure on larynx, 2[after sleep], open air. 2[Breathing almost stops on falling asleep] (2[Grind]). Larynx painful to touch. Sensation as of a plug (2[Anac]) which moves up and down, with a short cough.

:rubric=heart
Palpitation, with fainting spells, especially during climacteric. Constricted feeling causing palpitation, with anxiety. Cyanosis. Irregular beats.

:rubric=back
Neuralgia of coccyx, 2[worse rising from sitting posture]; must sit perfectly still. Pain in neck, worse cervical region. Sensation of threads stretched from back to arms, legs, eyes, etc.

:rubric=extremities
Sciatica, right side, better lying down. 2[Pain in tibia] (may follow sore throat). Shortening of tendons.

:rubric=sleep
Patient 2[sleeps into an aggravation]. Sudden starting when falling asleep. Sleepiness, yet cannot sleep (2[Bell; Op]). Wide-awake in evening.

:rubric=fever
Chilly in back; feet icy cold; hot flushes and hot perspiration. Paroxysm returns after acids. Intermittent fever every spring.

:rubric=skin
Hot perspiration, 2[bluish, purplish appearance]. Boils, carbuncles, ulcers, with bluish, purple surroundings. Dark blisters. Bed-sores, with black edges. Blue-black swellings. Pyemia; dissecting wounds. Purpura, with intense prostration. 2[Senile erysipelas]. Wens. Cellulitis. Varicose ulcers.

:rubric=modalities
M[Worse], after sleep, (2[Kali bich]). Lachesis sleeps 2[into] aggravation; ailments that come on during sleep (2[Calc]); left side, in the spring, warm bath, pressure or constriction, hot drinks. Closing eyes. M[Better], appearance of discharges, warm applications.

:rubric=relations
