:rubric2=complementary
R[Crotalus cascavella] often completes curative work of Lachesis (R[Mure], R[Lyc], R[Hep], R[Salamandra]).

Incompatible: R[Acet ac], R[Carb ac].

:rubric2=compare
R[Cotyledon] (climacteric troubles); R[Nat m], R[Nit-ac], R[Crotal], R[Amphisboena] -snake lizard - (right jaw swollen and painful, lancinating pains; headaches, lancinating pains. Eruption of vesicles and pimples); R[Naja], R[Lepidium].

:rubric=dose
Eighth to 200th potency. Doses ought not be repeated too frequently. If well indicated, a single dose should be allowed to exhaust its action.

