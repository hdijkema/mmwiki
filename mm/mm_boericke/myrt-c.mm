:rubric=generals
The leaves contain Myrtol, an active antiseptic. Chest pains, as found often in consumptives, call for this remedy. Incipient phthisis. Nerve sedative and stimulant to mucous membranes, bronchitis, cystitis and pyelitis.

:rubric=chest
Stitching pain 2[in left breast, running through to shoulder-blade] (2[Illic; Therid; Pix]). Dry, hollow cough, with tickling in chest. Worse in the morning. Sensation of burning in left chest.

:rubric=relations
:rubric2=compare
