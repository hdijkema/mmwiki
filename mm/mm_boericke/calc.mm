:rubric=generals
This great Hahnemannian anti-psoric is a constitutional remedy 2[par excellence]. Its chief action is centered in the vegetative sphere, impaired nutrition being the keynote of its action, the glands, skin, and bones, being instrumental in the changes wrought. Increased local and general perspiration, swelling of glands, scrofulous and rachitic conditions generally offer numerous opportunities for the exhibition of Calcarea. Incipient phthisis (R[Ars-i]; R[Tub]). It covers the tickling cough, fleeting chest pains, nausea, acidity and dislike of fat. Gets out of breath easily. 2[A jaded state, mental or physical, due to overwork. Abscesses in deep muscles; polypi and exostoses]. Pituitary and thyroid disfunction.

Raised blood coagulability (R[Stront]). Is a definite stimulant to the periosteum. Is a haemostatic and gives this power probably to the gelatine injections.

Easy relapses, interrupted convalescence. Persons of scrofulous type, who take cold easily, with increased mucous secretions, children who grow fat, are large-bellied, with large head, pale skin, chalky look, the so-called leuco-phlegmatic temperament; affections caused by working in water. Great sensitiveness to cold; partial sweats. Children crave eggs and eat dirt and other indigestible things; are prone to diarrhoea. Calcarea patient is fat, fair, flabby and perspiring and cold, damp and sour.

