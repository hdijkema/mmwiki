Antidote: Acute Opium poisoning. R[Atropin] and Black Coffee. Chronic Opium poisoning. R[Ipecac], R[Nux-v], R[Passiflora. Berberis] is useful to counteract opium habit.

:rubric=dose
Third to thirtieth and 200th potency.

Non-Homeopathic Preparations and Uses. - 2[Palliative only] in great pain, sleeplessness, peritonitis, and to check excess secretion in diarrhoea, diabetes, etc.

