:rubric=generals
Hahnemann says that it is much more difficult to estimate the action of Opium than of almost any other drug. The effects of Opium as shown in the insensibility of the nervous system, the depression, drowsy stupor, painlessness, and torpor, the general sluggishness and lack of vital reaction, constitute the main indications for the drug when used homeopathically. All complaints are characterized by 2[sopor]. They are 2[painless], and are accompanied by 2[heavy, stupid sleep, stertorous breathing. Sweaty skin]. Dark, mahogany-brown face. Serous apoplexy-venous, passive congestion. Want of sensitiveness to the action of medicines. Reappearance and aggravation from becoming heated. Opium lessens voluntary movements, contracts pupils, depresses higher intellectual powers, lessens self-control and power of concentration, judgment; stimulates the imagination, checks all secretions except that of the skin. Want of susceptibility to remedies even though indicated. Diseases that originate from fright.

:rubric=mind
Patient wants nothing. 2[Complete loss of consciousness; apoplectic state]. Frightful fancies, daring, gay, bright. Unable to understand or appreciate his sufferings. Thinks he is not at home. Delirious talking, with wide open eyes.

:rubric=head
Vertigo; 2[lightness of head in old people]. Dull, heavy, stupid. Delirium. Vertigo after fright. Pain in back of head; great weight there (2[Gels]). Bursting feeling. Complete insensibility; no mental grasp for anything. Paralysis of brain.

:rubric=eyes
Half-closed, dilated; pupils insensible, 2[contracted]. Ptosis (2[Gels; Caust]). Staring glassy.

:rubric=face
Red, bloated, 2[swollen, dark suffused, hot]. Looks intoxicated, besotted (2[Bapt; Lach]). Spasmodic facial twitching, especially corners of mouth. Veins of face distended. 2[Hanging down of lower jaw]. Distorted.

:rubric=mouth
Dry. Tongue black, 2[paralyzed] bloody froth. Intense thirst. Blubbering op lips. Difficult articulation and swallowing.

:rubric=stomach
Vomiting, with colic and convulsions. Fecal vomiting. Incarcerated hernia. Hungry; no desire to eat.

:rubric=abdomen
Hard, bloated, tympanitic. Lead colic during colic, urging to stool and discharge of hard feces.

:rubric=stool
Obstinate constipation; no desire to go to stool. 2[Round, hard, black balls]. Feces protrude and recede (2[Thuj; Sil]). Spasmodic retention of feces in small intestines. Stools involuntary, black, offensive, frothy. Violent pain in rectum, as if pressed asunder.

:rubric=urine
Slow to start; feeble stream. 2[Retained] or involuntary, after fright. Loss of power or sensibility of bladder.

:rubric=female
Suppressed menses from fright. Cessation of labor-pains with coma between paroxysms. Threatened abortion and suppression of lochia, from fright, with sopor. Horrible labor-like pains in uterus, with urging to stool.

:rubric=respiratory
Breathing stops on going to sleep; must be shaken to start it again (2[Grindelia]). Hoarse. 2[Deep snoring; rattling, stertorous breathing]. Difficult, intermittent, deep, unequal respiration. Heat in chest; burning about heart. Cough, with dyspnoea and blue face; with bloody expectoration.

:rubric=sleep
Great drowsiness (2[Gels.; Nux mosch]). Falls into a heavy stupid sleep. Profound coma. Loss of breath on falling asleep (2[Grind]). Coma vigil. Picking at bedclothes. Very sleepy, but cannot go to sleep. Distant noise, cocks crowing, etc, keep him awake. Child dreams of cats, dogs, black forms. Bed feels so hot cannot lie on it. Pleasant, fantastic, amorous dreams. Shaking chill; then heat, with sleep and sweat. Thirst only during heat.

:rubric=fever
Pulse 2[full] and 2[slow]. Heat extending over body. 2[Hot] perspiration. Fever characterized by stupor, snoring respiration, twitching of limbs, intense thirst and sleepiness. General low temperature with inclination to stupor.

Back and Extremities. - Opisthotonos. Swollen veins of neck. Painless paralysis (2[Oleand]). 2[Twitching of limbs]. Numbness. Jerks as if flexors were overacting. Convulsions; worse from glare of light; coldness of limbs.

:rubric=skin
Hot, damp, sweating, Constant desire to uncover. 2[Hot perspiration over whole body except lower limbs].

:rubric=modalities
M[Worse], heat, during and after sleep (2[Apis; Lach.]). M[Better], cold things, constant walking.

