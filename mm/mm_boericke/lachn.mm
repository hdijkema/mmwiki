:rubric=generals
Head, chest and circulation are affected. Bridge of nose as if pinched. A remedy for torticollis, rheumatic symptoms about neck. 2[Tuberculosis]-light-complexioned people. Early stages, and established chest cases, with much coldness. Produces a desire to talk-a flow of language and the courage to make a speech.

:rubric=head
Right-sided pain, extending down to jaw; 2[head feels enlarged]; worse, least noise. Scalp painful. Sleepless. Circumscribed red cheeks; scalp feels sore, as if hair was standing on end; burning in palms and soles. Bridge of nose feels as if pinched.

:rubric=chest
Sensation of heat-bubbling and boiling around heart region rising to head.

:rubric=back
2[Chilliness between the shoulder-blades]; pain and stiffness in back.

:rubric=neck
2[Drawn over to one side] in sore throat. Rheumatism of the neck. Stiffness of neck. Pain in nape, as if dislocated.

:rubric=skin
Body icy cold; face yellow; tendency to sweat.

:rubric=relations
:rubric2=compare
R[Dulc], R[Bry], R[Puls]; also R[Fel tauri] (nape of neck pains, and great tension there).

:rubric=dose
Third potency. Tincture in phthisis, unit doses, once or twice a week, or three drops every four hours.

