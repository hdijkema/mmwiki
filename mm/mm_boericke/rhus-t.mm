:rubric=generals
The effects on the skin, rheumatic pains, mucous membrane affections, and a typhoid type of fever, make this remedy frequently indicated. Rhus affects fibrous tissue markedly-joints, tendons, sheaths-aponeurosis, etc, producing pains and stiffness. Post-operative complications. 2[Tearing asunder pains]. Motion always "limbers up" the Rhus patient, and hence he feels better for a time from a change of position. Ailments from strains, overlifting, getting wet while perspiring. Septic conditions. Cellulitis and infections, carbuncles in early stages (2[Echinac]). Rheumatism in the cold season. 2[Septicaemia].

:rubric=mind
Listless, sad. Thoughts of suicide. 2[Extreme restlessness, with continued change of position]. Delirium, with fear of being poisoned (2[Hyos]). 2[Sensorium becomes cloudy. Great apprehension at night, cannot remain in bed].

:rubric=head
Feels as if a board were strapped on the forehead. Vertigo when rising. 2[Heavy] head. Brain feels loose and as if struck against skull on walking or rising. Scalp sensitive; worse on side lain on. Headache in occiput (2[Rhus rad]); painful to touch. Pain in forehead and proceeds thence backward. Humid eruptions on scalp; itching greatly.

:rubric=eyes
Swollen, red, oedematous; 2[orbital cellulitis]. 2[Pustular inflammations]. Photophobia; profuse flow of yellow pus. Oedema of lids, suppurative iritis. Lids inflamed, agglutinated swollen. Old injured eyes. Circumscribed corneal injection. Intensive ulceration of the cornea. Iritis, after exposure to cold and dampness, and of rheumatic origin. Eye painful on turning it or pressing, can hardly move it, as in acute retrobulbar neuritis. Profuse gush of hot, scalding tears upon opening lids.

:rubric=ears
Pain in ears, with sensation as if something were in them. Lobules swollen. Discharge of bloody pus.

:rubric=nose
Sneezing; coryza from getting wet. Tip of nose red, sore, ulcerated. Swelling of nose. Nosebleed on stooping.

:rubric=face
2[Jaws crack when chewing]. Easy dislocation of jaw (2[Ign; Petrol]). 2[Swollen face], erysipelas. Cheek bones sensitive to touch. Parotitis. Facial neuralgia, with chilliness; worse, evening. 2[Crusta lactea] (2[Calc; Viol tric]).

:rubric=mouth
Teeth feel loose and long; gums sore. Tongue red and cracked; 2[coated, except red triangular space at the tip]; dry and red at edges. Corners of mouth ulcerated; fever-blisters around mouth and chin (2[Nat mur]). 2[Pain in maxillary joint].

:rubric=throat
Sore, with 2[swollen glands]. Sticking pain on swallowing. Parotitis; left side.

:rubric=stomach
Want of appetite for any kind of food, with unquenchable thirst. 2[Bitter taste] (2[Cupr]). Nausea, vertigo, and bloated abdomen after eating. 2[Desire for milk]. Great thirst, with dry mouth and throat. Pressure as from a stone. (2[Bry; Ars]) 2[Drowsy after eating].

:rubric=abdomen
Violent pains, relieved by lying on abdomen. Swelling of inguinal glands. Pain in region of ascending colon. Colic, compelling to walk bent. Excessive distention after eating. Rumbling of flatus on first rising, but disappears with continued motion.

:rubric=rectum
Diarrhoea of blood, slime, and reddish mucus. Dysentery, with tearing pains down thighs. Stools of cadaverous odor. Frothy, painless stools. Will often abort a beginning suppurative process near the rectum. Dysentery.

:rubric=urinary
Dark, turbid, high-colored, scanty urine, with white sediment. Dysuria, with loss of blood.

:rubric=male
Swelling of glands and prepuce-dark-red erysipelatous; scrotum thick, swollen, 2[oedematous. Itching intense].

:rubric=female
Swelling, with intense itching of vulva. Pelvic articulations stiff when beginning to move. Menses early, profuse, and prolonged, acrid. 2[Lochia thin, protracted, offensive diminished (Puls; Secale), with shooting upwards in vagina (Sep).]

:rubric=respiratory
Tickling behind upper sternum. 2[Dry, teasing cough] from midnight until morning, 2[during a chill, or when putting hands out of bed]. Haemoptysis from overexertion; blood bright red. Influenza, with aching in all bones (2[Eup perf]). Hoarseness from overtraining voice (R[Arn]). Oppression of the chest, cannot get breath with sticking pains. Bronchial coughs in old people, worse on awaking and with expectoration of small plugs of mucus.

:rubric=heart
Hypertrophy from overexertion. Pulse quick, weak, irregular, intermittent, with numbness of left arm. 2[Trembling and palpitation when sitting still].

:rubric=back
Pain between shoulders on swallowing. 2[Pain and stiffness in small of back; better, motion, or lying on something hard]; worse, while sitting. Stiffness of the nape of the neck.

:rubric=extremities
Hot, painful swelling of joints. 2[Pains tearing in tendons, ligaments, and fasciae]. Rheumatic pains spread over a large surface at nape of neck, loins, and extremities; better motion (2[Agaric]). Soreness of condyles of bones. 2[Limbs stiff paralyzed. The cold fresh air is not tolerated; it makes the skin painful]. Pain along ulnar nerve. Tearing down thighs. 2[Sciatica]; worse, cold, damp weather, at night. Numbness and formication, after overwork and exposure. Paralysis; trembling after exertion. Tenderness about knee-joint. Loss of power in forearm and fingers; crawling sensation in the tips of fingers. Tingling in feet.

:rubric=fever
Adynamic; restless, trembling. Typhoid; tongue dry and brown; sordes; bowels loose; great restlessness. Intermittent; chill, with dry cough and restlessness. During heat, urticaria. Hydroa. Chilly, as if cold water were poured over him, followed by heat and inclination to stretch the limbs.

:rubric=skin
Red, swollen; 2[itching intense]. Vesicles, herpes; 2[urticaria]; pemphigus; erysipelas; vesicular suppurative forms. Glands swollen. 2[Cellulitis]. Burning eczematous eruptions with tendency to scale formation.

:rubric=sleep
2[Dreams of great exertion]. Heavy sleep, as from stupor. Sleepless before midnight.

:rubric=modalities
M[Worse], during sleep, cold, wet rainy weather and after rain; at night, 2[during rest], drenching, when lying on back or right side. M[Better], warm, dry weather, motion; walking, change of position, rubbing, warm applications, from stretching out limbs.

