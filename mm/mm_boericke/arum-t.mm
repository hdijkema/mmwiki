:rubric=generals
Arum maculatum, Italicum, Dracontium, have the same action as the Triphyllum. They all contain an irritant poison, causing inflammation of mucous surfaces and destruction of tissue. 2[Acridity] is the keynote of the kind of action characteristic of Arum.

:rubric=head
Bores head in pillow. Headache from too warm clothing, from hot coffee.

:rubric=eyes
Quivering of upper eyelids, especially left.

:rubric=nose
Soreness of nostrils. 2[Acrid, excoriating discharge], producing raw sores. 2[Nose obstructed; must breathe through mouth. Boring in the nose]. Coryza; discharge blood-streaked, watery. Nose completely stopped, with fluent, acrid discharge. Hay-fever, with pain over root of nose. Large scabs high up on right side of nose. Face feels chapped, as if from cold wind; feels hot. 2[Constant picking at nose until it bleeds].

:rubric=mouth
2[Raw feeling at roof and palate]. Lips and soft palate sore and burning. Lips chapped and burning. 2[Corners of mouth sore and cracked]. Tongue red, sore; whole mouth raw. Picking lips until they bleed. Saliva profuse, acrid, corroding.

:rubric=throat
Swelling of sub-maxillary glands. 2[Constricted and swollen; burns]; raw. Constant hawking. 2[Hoarseness]. Expectoration of much mucus. Lungs feel sore. Clergyman's sore throat. Voice uncertain, uncontrollable. Worse, talking, singing.

:rubric=skin
Scarlet rash; 2[raw, bloody surfaces] anywhere. Impetigo contagiosa.

:rubric=modalities
2[Worse, northwest wind]; lying down.

