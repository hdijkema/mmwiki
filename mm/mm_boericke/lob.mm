:rubric=dose
Tincture, to thirtieth potency. Locally the tincture is antidotal to Poison-oak. Often the 2[Acetum Lobelia] acts better than any other preparation. Lobelia hypodermically acts clinically almost precisely as the antitoxin of diphtheria does upon the infection and renders the system stronger to resist future infections (F. Ellingwood).