:local-name=Elder

:rubric=generals
Acts especially on the respiratory organs. Dry coryza of infants, snuffles, oedematous swellings. 2[Profuse sweat] accompanies many affections.

:rubric=mind
Sees images when shutting eyes. 2[Constant fretfulness]. Very easily frightened. Fright followed by suffocative attacks.

:rubric=face
Turns blue with cough. Red, burning spots on cheeks. Heat and perspiration of face.

:rubric=abdomen
Colic, with nausea and flatulence; frequent watery, slimy stools.

:rubric=urine
Profuse urine with dry heat of skin. Frequent micturition, with scanty urine. Acute nephritis; dropsical symptoms, with vomiting.

:rubric=respiratory
Chest oppressed with pressure in stomach, and nausea Hoarseness with tenacious mucus in larynx. Paroxysmal, 2[suffocative cough, coming on about midnight], with crying and dyspnoea. Spasmodic croup. Dry coryza. 2[Sniffles of infants]; nose dry and obstructed. Loose choking cough. When nursing child must let go of nipple, nose blocked up, cannot breathe. 2[Child awakes suddenly, nearly suffocating, sits up, turns blue. Cannot expire] (2[Meph]). Millar's asthma.

:rubric=extremities
Hands turn blue. Oedematous swelling in legs, insteps, and feet. Feet icy cold. Debilitating night-sweats (2[Salvia; Acet ac]).

:rubric=fever
Dry heat while sleeping. 2[Dreads uncovering. Profuse sweat over entire body during waking hours]. Dry, deep cough precedes the fever paroxysm.

:rubric=skin
Dry heat of skin during sleep. Bloated and swollen; general dropsy; 2[profuse sweat on waking].

:rubric=modalities
M[Worse], sleep, during rest, after eating fruit. M[Better], sitting up in bed, motion.

:rubric=relations
:rubric2=compare
R[Ipec], R[Meph], R[Op], R[Sambucus Canadensis] (great value in dropsies; large doses required-fluid extract, 1/4 to 1 teaspoonful three times daily).

:rubric2=antidotes
R[Ars], R[Camph].

:rubric=dose
Tincture, to sixth potency.

