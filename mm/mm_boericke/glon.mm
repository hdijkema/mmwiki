:rubric=dose
Sixth to thirtieth potency.

For palliative (non-homeopathic) purposes, in angina pectoris, asthma, heart-failure, etc, physiological doses-i.e, 1-100 of drop-must be given. Here it is the great emergency remedy. The conditions calling for it are small, wiry pulse, pallor, arterial spasm, anaemia of brain, 2[collapse], feeble heart, syncope, dicrotic pulse, vertigo,-the opposite of those indicating a homeopathic dosage. Often thus used to lower the arterial tension in chronic interstitial nephritis.