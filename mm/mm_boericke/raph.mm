:rubric=generals
Produces pain and stitches in liver and spleen. Increases of bile and salivary secretion. Symptoms will not appear if salt is used with the Radish. Great accumulation and incarceration of flatulence. "Globus" symptoms. Seborrhoea, with greasy skin. Pemphigus. Hysteria; chilliness in back and arms. Sexual insomnia (R[Kali-br]). Nymphomania. 2[Post-operative gas pains].

:rubric=head
Sadness, aversion to children, especially girls. Headache, brain feels tender and sore. Oedema of lower eyelids. Mucus in posterior nares.

:rubric=throat
Hot-ball feeling from uterus to throat, stopping there. Heat and burning in throat.

:rubric=stomach
Putrid eructations. Burning in epigastrium, followed by hot eructation.

:rubric=abdomen
Retching and vomiting, loss of appetite. Distended, 2[tympanitic, hard. No flatus emitted upward or downward]. Griping about navel. Stool liquid, frothy, profuse, brown, with colic, and pad-like swelling of intestines. Vomiting of fecal matter.

:rubric=female
Nervous irritation of genitals. Menses very profuse and long-lasting. 2[Nymphomania], with aversion to her own sex and to children, and sexual insomnia.

:rubric=urine
Turbid, with yeast-like sediment. Urine more copious, thick like milk.

:rubric=chest
Pain in chest extends to back and to throat. Heavy lump and coldness in center of chest.

