:rubric=generals
An ovarian remedy; produces the symptom-complex of chronic Oophoritis. Useful where the parenchyma of the gland is not totally destroyed. Acts also on mind and skin. Motor weakness, averse to exercise.

:rubric=mind
Weeping mood. 2[Love of approbation]. Pride; 2[easily offended]. Inclined to use violent language. 2[Keeps up brightly when in company], much exhausted afterwards, and pains aggravated.

:rubric=head
Feels as if swung backward and forward. Temporo-parietal neuralgia with pain in shoulder. 2[Pain across top of head from ear to ear]; worse after an evening's entertainment, with irritability and sour eructations. Sallow complexion.

:rubric=abdomen
Shooting pain from navel to pelvis. Sensation as if intestines were bitten off. Intestines feel strangulated. Soreness of abdomen, swelling in right groin. Flatulency.

:rubric=female
Uterine prolapse and retroversion. Subacute pelvic peritonitis, with right-sided pain and backache; menorrhagia. Cutting pain in uterus; relieved after stool. 2[Pain and swelling in region of right ovary]. Shooting or burning pain in pelvis and bearing-down; relieved by rubbing. Soreness and shooting pain from navel of breast. Glairy leucorrhoea. Menstrual discharge while nursing. Stitches in right breast near nipple. It is indicated in that gynaecological condition where the disease had its inception in the right ovary, the uterine prolapse and retroversion, the subacute pelvic peritonitis and concomitant symptoms being secondary (F. Aguilar, M. D).

:rubric=extremities
Pruritus. Tired feeling in small of back. Fleeting, neuralgic pains in extremities, Heavy and tired in limbs. Darting pain from toes to hips. Rheumatic pain in right shoulder; in right hip. Sciatica.

:rubric=relations
Complementary; R[Plat].

