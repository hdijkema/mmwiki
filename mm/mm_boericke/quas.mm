:local-name=(QUASSIA - PICRAENA EXCELSA)

:rubric=generals
Acts on gastric organs as a tonic (2[Gentian; Hydr]). Seems to possess marked action on eyes, producing amblyopia and cataract. Pain in right intercostal muscles above the liver. Pressure and stitches in liver, and sympathetically in spleen.

:rubric=stomach
Atonic dyspepsia, with gas and acidity. Heart-burn and gastralgia. Regurgitation of food. Abdomen feels empty and retracted. Dyspepsia after infectious diseases; especially grip, dysentery. Tongue dry or with brown sticky coating. Cirrhosis of liver with ascites.

:rubric=urinary
Excessive desire-impossible to retain urine; copious micturition day and night. As soon as the child wakes up the bed is drenched.

:rubric=extremities
Inclination to yawn and stretch (R[Rhus-t]). Sensation of coldness over back. Prostration, with hunger. Cold extremities, with sensation of internal coldness (2[Helo-derma]).

:rubric=dose
First to third potency, or spoonful doses of Aqua Quassiae.

