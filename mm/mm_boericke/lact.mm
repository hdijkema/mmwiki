:rubric=generals
This remedy acts principally upon the brain and circulatory system. Delirium tremens with sleeplessness, coldness, and tremor. Hydrothorax and ascites. Impotence. Sense of lightness and 2[tightness] affecting whole body, especially chest. Seems to be a 2[true galactogogue]. Marked action on extremities.

:rubric=mind
Stupefaction of sense. Great restlessness.

:rubric=head
Dull, heavy, confused, dizzy. Heat of face and headache, with general coldness. Headache, with affections of respiratory organs.

:rubric=abdomen
Sensation of weight, of fullness; borborygmi; abundant emission of wind. Colic in early morning, abdomen tense, relieved somewhat by evacuation and passing of wind.

:rubric=chest
Difficult breathing. Suffocative breathing from dropsy of the chest. Constant tickling cough. Incessant, spasmodic cough, as if chest would fly to pieces. Squeezing sensation in lower chest.

:rubric=female
Promotes catamenia. 2[Increase of milk] in breasts (2[Asafoet]).

:rubric=sleep
Restless; impossible to get to sleep. Deep, comatose sleep.

:rubric=extremities
Lame hip down left side; worse walking. Coldness and numbness of feet and legs. Tremor of hands and arms. Cramps in shin bones, extending to toes and side of leg involving calves.

