:rubric=generals
Kreosotum is a mixture of phenols obtained from this distillation.

Pulsations all over the body, and profuse bleeding from small wounds. Very severe, old neuralgic affections; pains rather aggravated by rest. 2[Excoriating], burning, and offensive discharges. Haemorrhages, ulcerations, cancerous affections. Rapid decomposition of fluids and secretions, and burning pains. Overgrown, poorly developed children. Post-climacteric diseases. Tumefaction, puffiness, gangrene. Ailings of teething children.

:rubric=mental
Music causes weeping and palpitation. Vanishing of thought; stupid, forgetful, peevish, irritable. Child wants everything but throws it away when given.

:rubric=head
Dull pain, as from a board pressing against forehead. Menstrual headache. Occipital pain (R[Gels]; R[Zinc] R[Pic-ac]).

:rubric=eyes
Salty lachrymation. Lids red and swollen.

:rubric=ears
Eruption around and pimples within. Difficult hearing and buzzing.

:rubric=face
Sick, suffering expression; hot, cheeks red.

:rubric=mouth
Lips red, bleeding. 2[Very painful dentition]; child will not sleep. 2[Very rapid decay of teeth, with spongy, bleeding gums]; teeth dark and crumbly (2[Staph; Ant c]). Putrid odor and bitter taste.

:rubric=nose
Offensive smell and discharge. Chronic catarrh of old people. Acrid rawness. Lupus (2[Ars]).

:rubric=throat
Burning, choking sensation. 2[Putrid odor].

:rubric=stomach
Nausea; vomiting of food several hours after eating; of sweetish water in the morning. Feeling of coldness, as of ice water in stomach. Soreness; better eating. Painful hard spot. Haematemesis. Bitter taste after a swallow of water.

:rubric=abdomen
Distended. Burning haemorrhoids. Diarrhoea; very offensive; dark brown. Bloody, fetid stools. 2[Cholera infantum] in connection with painful dentition, green stools, nausea, dry skin, exhaustion, etc.

:rubric=urine
2[Offensive]. Violent itching of vulva and vagina, worse when urinating. Can urinate only when lying; cannot get out of bed quick enough during first sleep. Dreams of urinating. Enuresis in the first part of night. 2[Must hurry when desire comes to urinate].

:rubric=female
Corrosive itching within vulva, burning and swelling of labia; violent itching between labia and thighs. During menses, 2[difficult hearing]; buzzing and roaring; eruption after. Burning and soreness in external and internal parts. Leucorrhoea, yellow, acrid; odor of green corn; worse between periods. Haemorrhage after coition. Menses too early, prolonged. Vomiting of pregnancy, with ptyalism. 2[Menstrual flow intermits] (2[Puls]); ceases on sitting or walking; reappears on lying down. Pain worse after menses. Lochia offensive; 2[intermits].

:rubric=respiratory
2[Hoarse, with pain in larynx]. Cough; worse evening, with efforts to vomit, with pain in chest. Raw burning in chest; pains and oppression. Cough after influenza (2[Eriodyction]). Winter coughs of old people, 2[with heavy pressure on sternum]. Gangrene of lungs. After every cough, 2[copious, purulent expectoration]. Haemoptysis; periodic attacks. Sternum feels pressed in.

:rubric=back
Dragging backache, extending to genitals and down thighs. Great debility.

:rubric=extremities
Pain in joints, hip and knee. Boring pain in hip-joints. Scapulae sore.

:rubric=skin
Itching, worse towards evening. Burning in soles. Senile gangrene. Small wounds bleed freely (R[Crot]; R[Lach]; R[Phos]). Pustules and herpes. Ecchymosis; dorsal surface of fingers and hands eczematous.

:rubric=sleep
Disturbed with tossing. Paralytic sensation in limbs on waking. Anxious dreams of pursuit, fire, erections, etc.

:rubric=modalities
M[Worse], in open air, cold rest, when lying; after menstruation. M[Better], from warmth, motion, warm diet.

:rubric=relations
