:rubric=generals
Selenium is a constant constituent of bones and teeth.

Marked effects on the genito-urinary organs, and often indicated in elderly men, especially for prostatitis and sexual atony. 2[Great debility]; worse, heat. Easy exhaustion, mental and physical, in old age. Debility after exhausting diseases.

:rubric=mind
Lascivious thoughts, with impotency. Mental labor fatigues. 2[Extreme sadness]. Abject despair, uncompromising melancholy.

:rubric=head
Hair falls out. 2[Pain over left eye; worse walking in sun, strong odors and tea]. Scalp feels tense. Headache from tea drinking.

:rubric=throat
Incipient tubercular laryngitis. Hawking and raising transparent lumps of mucus every morning. 2[Hoarseness]. Cough in morning, with expectoration of bloody mucus. Hoarseness of singers. Much clear, starchy mucus (2[Stann]).

:rubric=stomach
Desire for brandy and other strong drink. Sweetish taste. Hiccough and eructations after smoking. After eating, pulsation all over, especially abdomen.

:rubric=abdomen
Chronic liver affections; liver painful, 2[enlarged, with fine rash over liver region]. Stool constipated, hard and accumulated in rectum.

:rubric=urinary
Sensation in the tip of urethra as if a biting drop were forcing its way out. Involuntary dribbling.

:rubric=male
Dribbling of semen during sleep. Dribbling of prostatic fluid. Irritability after coitus. 2[Loss of sexual power], with lascivious fancies. 2[Increases desire, decreases ability]. Semen thin, odorless. Sexual neurasthenia. On attempting coition, penis relaxes. 2[Hydrocele].

:rubric=skin
Dry, scaly eruption in palms, with itching, 2[Itching about the ankles] and folds of skin, between fingers. Hair falls out from brows, beard, and genitals. Itching about finger-joints and between fingers; in palms. Vesicular eruption between fingers (2[Rhus; Anac]). Seborrhoea oleosa; comedones 2[with an oily surface of the skin]; alopecia. 2[Acne].

:rubric=extremities
Paralytic pains in small of back in the morning. Tearing pain in hands, at night.

:rubric=sleep
2[Sleep prevented by pulsation in all vessels], worse abdomen. Sleepless until midnight, awakens early and always same hour.

:rubric=modalities
M[Worse], after sleep, in hot weather, from Cinchona, draught of air, coition.

:rubric=relations
Incompatible: R[Chin], R[Wine].

