:rubric=generals
Arouses latent syphilis. Periodicity very marked; symptoms recur every two and three weeks. Weakness. Swollen feeling of parts.

:rubric=head
Low spirited, memory weak. 2[Desire to be alone]. Annoyed by visions, causing her to cry. Head feels too large. Left-sided headache up to eyes and into ears. Headache worse stooping and lying down. Oedematous swelling of forehead.

:rubric=face
Red, itching, burning and bloated. Eyes swelled and watery, burn with coryza. Eyes weak, day and gas light unpleasant.

:rubric=mouth
The tongue coated white, and shows imprint of the teeth. Mouth sore and ulcerated.

:rubric=abdomen
Sore pain in liver goes through to shoulders and spine. Pain in spleen down to groin. Pain in breast extends to hip and spleen. Diarrhoea, burning watery stools with relief of pain.

