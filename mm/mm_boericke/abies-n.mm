:local-name=Black Spruce

:rubric=generals
A powerful and long-acting remedy, in various forms of disease, whenever the characteristic stomach symptoms are present. Most of the symptoms are associated with the gastric disturbances.

S{2[In dyspeptic troubles of the aged], with functional heart symptoms; also after tea or tobacco}. S{2[Constipation]}. S{Pain in external meatus}.

:rubric=head
S{Hot, with flushed cheeks}. S{Low-spirited}. S{Dull during the day, wakeful at night}. S{Unable to think}.

:rubric=stomach
S{2[Pain in stomach always comes on after eating]}. S{Sensation of a lump that hurts, 2[as if a hard-boiled egg had lodged in cardiac end of stomach]; continual distressing constriction just above the pit of the stomach, as if everything were knotted up}. S{Total loss of appetite in morning, but great craving for food at noon and night}. S{Offensive breath}. S{Eructations}.

:rubric=chest
S{Painful sensation, as if something were lodged in the chest and had to be coughed up; lungs feel compressed}. S{Cannot be fully expanded}. S{M[Worse] coughing; waterbrash succeeds cough}. S{Choking sensation in throat}. S{Dyspnoea; worse lying down; sharp, cutting pain in heart; 2[heart's action heavy and slow; tachycardia], bradycardia}.

:rubric=back
S{Pain in small of back}. S{Rheumatic pains and aching in bones}.

:rubric=sleep
S{Wakeful and restless at night, with hunger}. S{Bad dreams}.

:rubric=fever
S{Alternate heat and cold; chronic intermittent fever, with pain in stomach}.

:rubric=modalities
S{M[Worse] after eating}.

:rubric=relations
:rubric2=compare
Lump in stomach - R[Chin], R[Bry], R[Puls]; also other Conifers - R[Thuj], R[Sabin], R[Cupre-s] (Cupressus) (painful indigestion) also R[Nux-v], R[Kali-c].

:rubric=dose
First to thirtieth potency.