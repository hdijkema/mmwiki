:rubric=generals
Has some reputation as a remedy for epileptic conditions, and convulsive diseases of childhood and girls at puberty. Locally and internally is injurious to eyes. 2[Petit mal]. Epilepsy without aura; after fright and other violent emotions and after masturbation. Several convulsions close together. Somnambulism. Gets up at night and works, remembers nothing in the morning (R[Kali-p]).

:rubric=head
Drawn back by spasmodic twitchings. Mouth drawn to left. Congestion of brain.

:rubric=eyes
2[Colored light produces dizziness]. Pain and blurring of vision; better; rubbing; worse, using eyes.

:rubric=female
Profuse menses. Violent uterine contractions. Spasms during menses.

:rubric=fever
Profuse sweat, 2[smelling like garlic].

:rubric=relations
:rubric2=compare
R[Absinth], R[Cina], R[Cicuta].

:rubric=dose
First to third potency. Said to act better when given with wine.

