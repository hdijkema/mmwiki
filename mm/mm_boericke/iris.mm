:rubric=generals
Thyroid, 2[pancreas], salivary, intestinal glands, and gastro-intestinal mucous membrane, are especially affected. Increases the flow of bile. Sick headaches and cholera morbus are a special therapeutic field for its action.

:rubric=head
Frontal headache, with nausea. Scalp feels constricted. Right temples especially affected. Sick headache, worse rest; begins with a blur before eyes, after relaxing from a mental strain. Pustular eruption on scalp.

:rubric=ears
Roaring, buzzing, 2[ringing] in ears, with deafness. Aural vertigo, with intense noises in ears.

:rubric=face
Neuralgia after breakfast, beginning in infra-orbital nerve, and involving whole face.

:rubric=throat
Mouth and tongue feel scalded. Heat and smarting in throat. 2[Burning]. Profuse flow of saliva; ropy. 2[Goitre].

:rubric=stomach
2[Burning of whole alimentary canal. Vomiting], sour bloody, biliary. Nausea. Profuse flow of saliva (2[Merc; Ipec; Kali iod]). 2[Deficient appetite].

:rubric=abdomen
Liver sore. Cutting pain. Flatulent colic. Diarrhoea; stools watery, with 2[burning at anus] and through intestinal canal. Periodical night diarrhoea, with pain and green discharges. 2[Constipation] (give 30th)

:rubric=extremities
Shifting pains. Sciatica, as if left hip-joint were wrenched. Pain extends to popliteal space. Gonorrhoeal rheumatism (use 2[Irisin]).

:rubric=skin
Herpes zoster, associated with gastric derangements. Pustular eruptions. Psoriasis; irregular patches with shining scales. Eczema, with nightly itching.

:rubric=modalities
M[Worse], in evening and at night, from rest. M[Better], from continued motion.

