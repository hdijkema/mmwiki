:rubric=generals
Highly efficacious medicine for acute catarrhal conditions of the respiratory tract (used in the beginning)

:rubric=head
Irritable, sensitive to external impressions; hot, full and heavy head; lachrymation, with 2[coryza, profuse], fluent, with constant sneezing; loss of smell and taste; 2[coryza with cough].

:rubric=throat
Dry, pain during empty swallowing, tenacious mucus. Mouth dry.

:rubric=respiratory
Dry cough from sternal region all over chest. Hoarseness, larynx painful. 2[Paroxysmal cough], with suffocative obstruction of respiration. 2[Cough with sneezing]. Severe dyspnoea with cough. 2[Tightness across chest]. Asthmatic attacks, cannot endure a close, warm room. 2[Whooping-cough].

:rubric=relations
Seems to come between R[Cepa] and R[Euphrasia], which compare.

:rubric=dose
Third potency and higher. Severe aggravation have been noticed from lower potencies. 

