:rubric=generals
A remedy for nasal catarrh, with headache, pain at root of nose, dry and painful eyes. Psoriasis (2[Ars; Chrysoph ac; Thyroid]). Bronchitis of children over seven years. Facilitates the termination of the cold and conserves strength and appetite (Cartier).

:rubric=head
Floating sensation on turning head quickly; aching in frontal region and root of nose, over orbits. Headache; worse pressure and tobacco smoke.

:rubric=nose
Watery discharge; drops into throat. 2[Feels stopped; pain at root]. Dry crusts, on removal, leave mucous membrane raw. Post-nasal dropping of thick, bland, yellowish mucus. 2[Crusts in nose].

:rubric=ears
Catarrhal conjunctivitis and blepharitis marginalis. Eyes feel weak, stiffness of balls and tendency of lids to close. Feel heavy and droop. Lachrymation in wind. Agglutination in morning. Dry, painful, burning; soon tire. Oedema of orbital region. Supraorbital pain.

:rubric=throat
Dark, 2[purplish, swollen, oedematous]; red and glassy.

:rubric=respiratory
Racking cough, with profuse greenish expectoration. 2[Oppression of chest and about heart], and also larynx. Miner's asthma. Lungs feel as though smoke had been inhaled.

:rubric=extremities
Aching in arms; worse in shoulder. Pain in anterior crural nerves. Joints stiff. Feels tired all over. Knee-joints crack.

