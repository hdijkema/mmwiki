:rubric=generals
Has been used for milk-flow in nursing women, lumbago and rheumatism, colic, with drowsiness and spasmodic yawning. Is an anti-phlogistic remedy akin to Acon its symptoms showing congestion and inflammation. Acidity and burning in oesophagus with eructation.

:rubric=female
Pruritus; vulva red; dryness and burning in vagina. Burning pain in vagina during coition. Leucorrhoea, bloody.

:rubric=extremities
Sciatic pain, especially right side. Pain in shoulders. Swelling of veins of hands. Pain in all articulations of hands. Coldness of feet and toes.

:rubric=fever
Flushes of heat. Sweat on palms. Hands alternately hot and cold.

:rubric=dose
Third potency.

