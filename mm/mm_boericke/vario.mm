:local-name=Lymph from Small-pox Pustule

:rubric=generals
Used for "internal vaccination." Seems to be efficacious in protecting against, modifying and aiding in the cure of smallpox.

:rubric=head
Morbid fear of small-pox. Deafness. Pain in occiput. Inflamed eyelids.

:rubric=respiratory
Oppressed breathing. Throat feels closed. Cough with thick viscid, bloody mucus. Feeling of a lump in right side of throat.

:rubric=relations
R[Excruciating backache]. R[Aching in legs]. Tired all over with restlessness. Wrists pain. Pains shift from back to abdomen.

:rubric=fever
Hot fever, with intense radiating heat. Profuse, bad-smelling sweat.

:rubric=skin
Hot, dry. Eruption of pustules. 2[Shingles].

:rubric=relations
:rubric2=compare
Vaccin (same action); R[Malandrinum] - the morbid product of the grease of the horse (a prophylactic of small-pox and a remedy for the ill-effects of vaccination; chronic eczema following vaccination).

:rubric=dose
Sixth to thirtieth potency.

