:rubric=generals
Want of power of concentration and co-ordination. Vertigo, numbness and muscular prostration. Marked association of head and eye symptoms, with muscular tiredness and weariness.

A remedy for 2[migraine]. Headaches from eyestrain and sexual weakness. It produces diminution of sexual desire in both sexes; hence its homeopathicity, in 2[sexual neurasthenia]. Depressed or lost sexual life in women. Neuralgic pains. General prostration. Acts as if born tired.

:rubric=head
Loss of memory. Nose feels dry. Confused. Dull, heavy, dizzy, pressing upward in occiput. Occipito-frontal pain in morning on waking, 2[chiefly left side]. Pain in temples and mastoid (2[Capsic]).

:rubric=eyes
Vision blurred; optic disc hyperaemic, and retinal vessels enlarged. Strained feeling in eyes; worse, using eyes. Eyes heavy and dull, muscular asthenopia; 2[ocular muscles tense]. Internal eye muscles paretic. 2[Pain in eyeballs] between orbit and ball, extending to left temple.

:rubric=throat
Severe dryness. Discharge from posterior nares. Raw, scraping. Stuffed feeling in posterior nares. Symptoms worse by cold drinks.

:rubric=abdomen
Craving for ice-water and cold drinks; wants to drink often. Abdomen feels bloated

:rubric=back
Pain in dorsal and lumbar regions. Numbness and tingling in feet and legs.

:rubric=chest
Sore, aching in breasts; feels swollen and sore. Pain in heart; pulse, rapid, irregular, weak.

:rubric=male
Constant sexual excitement. 2[Psychical impotence]. Loss of desire. Speedy emissions. Deficient erections.

:rubric=female
Severe uterine pains; bearing-down pains; old pains return. 2[Sexual desire completely destroyed]. Feels as if menses would appear. Aching in breasts. Nipples itch. Menses too early and too prolonged. Soreness in uterine region. Leucorrhoea, yellow, acrid, profuse.

:rubric=extremities
Pain in back. 2[Tired and numb] feeling in legs, popliteal spaces, and below knees. 2[Staggering gait]. Sidewalk seems too high. Pain in left scapular region. Great muscular weakness and weariness.

:rubric=modalities
M[Worse], from motion, jar, and tight clothing. M[Better], when undressed, when lying down on back, from cold drinks, and eating.

