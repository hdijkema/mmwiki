:rubric=generals
Of value in cholera and diarrhoea. The abdominal symptoms are most important. Suppressed measles (H. Farrington).

:rubric=stomach
Hiccough, followed by copious vomiting. Nausea and vomiting, brought on by drinking, with acrid feeling from throat. Great thirst. 2[Very easy vomiting]. Heat and burning in stomach, with crampy, constrictive pain in epigastrium.

:rubric=abdomen
Distended, with gurgling noises. Pain in hypochondria. Pain in region of liver and under right scapula to shoulder. Violent urging to urinate.

:rubric=stool
Sudden, profuse, watery, like rice-water. 2[Diarrhoea; forced discharge; loud noise in abdomen like gurgling of water coming out of a bung-hole], associated with coldness, cramps, nausea, and vomiting.

:rubric=extremities
Cramps in muscles, especially calves, legs, and feet. 2[Coldness] of whole body. Pain in ankles, feet and toes. Heels sensitive.

:rubric=modalities
M[Better], by placing hands in cold water.

