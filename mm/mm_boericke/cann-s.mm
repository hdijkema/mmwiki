:rubric=generals
Seems to affect especially the urinary, sexual, and respiratory organs. It has characteristic sensations as of dropping water. Great fatigue, as from over-exertion; weary after meals. Choking in swallowing; things go down the wrong way. 2[Stuttering]. Confusion of thought and speech. Wavering speech. Wavering speech, hasty, incoherent.

:rubric=head
Lectophobia. Vertigo; sensation of dropping water on head. Pressure on root of nose.

:rubric=eyes
2[Opacity of cornea]. Cataract from nervous disturbances, abuse of alcohol and tobacco; patient feels deeply approaching blindness. Misty sight. Pressure from back of eyes, forward. Gonorrhoeal ophthalmia. Eyeballs ache. Scrofulous eye troubles (2[Sulph; Calc]).

:rubric=urine
Retained, with obstinate constipation. Painful urging. Micturition in split stream. Stitches in urethra. Inflamed sensation, with soreness to touch. 2[Burning while urinating, extending to bladder]. Urine scalding, with spasmodic closure of sphincter. Gonorrhoea, acute stage; urethra very sensitive. Walks with legs apart. Dragging in testicles. Zigzag pain along urethra. Sexual overexcitement. Urethral caruncle (2[Eucalypt]), phimosis. Stoppage of urethra by mucus and pus.

:rubric=female
Amenorrhoea when physical powers have been overtaxed, also with constipation.

:rubric=respiratory
Oppression of breathing and palpitation; 2[must stand up]. Weight on chest; rattling wheezing breathing. Cough, with green viscid, also bloody, expectoration.

:rubric=heart
Sensation as if drops were falling from the heart. Painful strokes and tension with palpitation. Pericarditis.

:rubric=sleep
Frightful dreams. More tired in morning. Sleepy during day.

:rubric=extremities
Contraction of fingers after a sprain. Dislocation of patella on going upstairs. Feet feel heavy on going upstairs. Paralytic tearing pains. Affections of the ball of the foot and under part of toes.

:rubric=modalities
M[Worse], lying down; going upstairs.

