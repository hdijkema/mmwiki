:rubric=generals
The urinary symptoms are most important. Useful in renal colic, prostatic affections, and catarrh of bladder. Sensation as if bladder were distended, with pain. 2[Pain going down thigh].

:rubric=urinary
Black, bloody, thick mucous urine. 2[Constant urging; great straining; pain down thighs during efforts to urinate]. Can emit urine only when he goes on his knees, pressing head firmly against the floor. Feeling of the bladder being distended and neuralgic pain in the anterior crural region. (2[Staph].) Dribbling after micturition. (2[Selen].) 2[Violent pain in glans penis]. Itching along urethra; urethritis, with prostatic trouble. Inflammation of urethra; becomes almost cartilaginous.

