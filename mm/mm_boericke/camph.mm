Hahnemann says: "The action of this substance is very puzzling and difficult of investigation, even in the healthy organism because its 2[primary action], more frequently than with any other remedy, alternates and becomes intermixed with the vital reactions (after effects) of the organism. On this account it is often difficult to determine what belongs to the vital reactions of the body and what to the alternating effects due to the primary action of the camphor."

Pictures a state of collapse. 2[Icy coldness] of the whole body; sudden sinking of strength; pulse small and weak. After operations, if temperature is subnormal, low blood pressure, 3 doses camph. 1x, 15-minute intervals. This condition is met with in cholera, and here it is that Camphor has achieved classical fame. 2[First stages of a cold, with chilliness and sneezing]. Subsultus and extreme restlessness. Cracking of joints. Epileptiform convulsions. Camphor has a direct relationship to muscles and fascia. In local rheumatic affections in cold climates necessary. Distention of veins. As a heart stimulant for emergency use of Camphor is the most satisfactory remedy. Drop doses on sugar as often as every five minutes.

It is characteristic of Camphor that the patient 2[will not be covered], notwithstanding the icy coldness of the body. One of the main remedies in shock. 2[Pain better while thinking of it]. Very sensitive to cold and to touch. Sequelae of measles. 2[Violent convulsion], with wandering and hysterical excitement. Tetanic spasms. Scrofulous children and irritable, weakly blondes especially affected.

