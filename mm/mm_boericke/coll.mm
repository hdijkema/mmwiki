:local-name=Stone-Root

:rubric=generals
Pelvic and portal congestion, resulting haemorrhoids and constipation, especially in females. Depressed arterial tension, general atony of muscular fiber. 2[Chronic nasal, gastric, and pharyngeal catarrh], due to portal obstruction. Dropsy from cardiac disease. Pruritus in pregnancy, with piles. 2[Constipation of children from intestinal atony]. Said to be of special value when given before operations, for rectal diseases. Sense of weight and constriction. Venous engorgement.

:rubric=head
Dull frontal headache; from suppressed haemorrhoids. Chronic catarrh. Yellow-coated tongue. Bitter taste (2[Colocy; Bry]).

:rubric=rectum
2[Sensation of sharp sticks in rectum. Sense of constriction]. Vascular engorgement of rectum. Dry feces. 2[Most obstinate constipation], with protruding haemorrhoids. Aching in anus and hypogastrium. Constipation during pregnancy; with membranous dysmenorrhoea, following labor (2[Nux]). Painful bleeding piles. Dysentery, with tenesmus. 2[Alternate constipation and diarrhoea], and great flatulence. 2[Itching of anus] (2[Teucrium; Ratanh]).

:rubric=female
Dysmenorrhoea; 2[pruritus of vulva]; prolapse of womb; swelling and dark redness of genitals; pain on sitting down. Membranous dysmenorrhoea, with constipation. Pruritus. Cold feeling in thighs after menstruation. Sensation of swelling of labia and of clitoris.

:rubric=respiratory
2[Cough from excessive use of voice]; "minister's sore throat"; sharp pain in larynx. Hoarseness. Harassing, dry cough.

:rubric=heart
Palpitation; rapid but weak. Dropsy. After heart symptoms relieved, piles or menses return. Chest-pains alternate with haemorrhoids. Oppression, faintness, and dyspnoea (2[Acon ferox]).

:rubric=modalities
M[Worse], from the slightest mental emotion or excitement; cold. M[Better], heat.

:rubric=relations
Antidote: Nux.

:rubric2=compare
R[Aescul], R[Aloes], R[Hamam], R[Lycopus], R[Negundo], R[Sulph], R[Nux-v].

:rubric=dose
Tincture, to third attenuation. Higher potencies where there is organic heart affection.

