:rubric=generals
Marked action on heart; oppression of chest; fullness in throat. Epilepsy. Numb aching in muscular and fibrous tissue. Sciatica. Well-marked pains through the breast to scapula. Catarrh of larynx and trachea. Hysterical joint.

:rubric=mind
Lost, confused feeling. Could not articulate for some time on awaking. Pressing vertex headache. Ailments from suppressed emotion. 2[Feeling as if a part of the body were absent].

:rubric=breast
Pain under left nipple, and aching in right breast. Pain through to scapula from region of left breast. Pain at angles of scapulae. Full, bursting feeling, as if from obstruction at heart. Choking fullness in throat. Breathing oppressed.

:rubric=extremities
Aching in back and thighs. Aching in all joints. Skin sensitive, rubbing of trousers causes an acute sting. Legs and arms feel heavy and sore.

