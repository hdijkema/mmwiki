:local-name=Smooth Sumach

:rubric=generals
Epistaxis and 2[occipital headache. Fetid flatus]. Ulceration of mouth. 2[Dreams of flying through the air] (2[Sticta]). 2[Profuse perspiration arising from debility] (2[China]). It is claimed that this remedy will so disinfect the bowels that the flatus and stools will be free from odor. It acts well in putrescent conditions with tendency to ulceration.

:rubric=mouth
Scurvy; nursing sore mouth (2[Veronica]). Aphthous stomatitis.

:rubric=relations
Said to be antidotal to the action of Mercury, and has been employed in the treatment of secondary syphilis after mercurialization.

:rubric=dose
Tincture. Usually locally to soft, spongy gums, aphthae, pharyngitis, etc. Internally, first potency.

