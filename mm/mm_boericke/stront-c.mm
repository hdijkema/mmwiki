:rubric=generals
Rheumatic pains, chronic sprains, stenosis of oesophagus. Pains make patient faint or sick all over. Chronic 2[sequelae of haemorrhages], after operations with much oozing of blood and coldness and prostration. Arterio-sclerosis. High blood pressure with flushed face pulsating arteries, threatened apoplexy. Violent involuntary starts. Affections of bones, especially femur. Restlessness at night, smothering feeling. 2[For shock after surgical operations]. 2[Neuritis], great sensitiveness to cold.

:rubric=head
2[Vertigo with headache and nausea]. Distensive pressure. Aches from nape of neck, spreading upwards; better wrapping head up warmly (R[Sil]). Flushes in face; violent pulsating. Supraorbital neuralgia; pains increase and decrease slowly (R[Stann]). Bloody crusts in nose. Face red; burns, itches. Itching, redness and burning of nose.

:rubric=eyes
Burning and redness of eyes. Pain and lachrymation on using eyes, with dancing and chromatic alterations of objects looked at.

:rubric=stomach
Loss of appetite, aversion to meat, craves bread and beer. Food tasteless. Eructations after eating. Hiccough causes chest pains; cardialgia.

:rubric=abdomen
Sticking in abdominal ring. Diarrhoea; 2[worse at night; continuous urging]; better towards morning. Burning in anus lasts a long time after stool (R[Rat]). Uncomfortable fullness and swelling of abdomen.

:rubric=extremities
Sciatica with oedema of ankle. Rheumatic pain in right shoulder. Rheumatism with diarrhoea. Gnawing as if in marrow of bones. Cramps in calves and soles. 2[Chronic] spasms, particularly of ankle-joint. Oedematous swelling. Icy-cold feet. Rheumatic pains, especially in joints. Veins of hands engorged.

:rubric=fever
Heat, with aversion to uncover or undress.

:rubric=skin
Moist, itching, burning eruption; better in open air, especially warm sunshine. 2[Sprains of ankle-joint, with oedema]. Violent perspiration at night.

:rubric=modalities
M[Better] immersing in 2[hot water]; M[worse], change of weather; from being quiet; when beginning to move; great sensitiveness to cold.

:rubric=relations
:rubric2=compare
R[Arn], R[Ruta], R[Sil], R[Bar-c], R[Carb-an], R[Carb-v], R[Stront jodat] (arterio-sclerosis). R[Strontium brom] (often gives excellent results where a bromide is indicated. Vomiting of pregnancy. Nervous dyspepsia. It is anti-fermentative and neutralizes excessive acidity). R[Stront nit] (Morbid cravings; headache and eczema behind ears).

:rubric=dose
Sixth trituration and thirtieth potency.

