:rubric=generals
Is of much use in infantile colics when the child seems to be 2[full of wind]. Oxaluria, with excess of urea; increased specific gravity. Where the system is broken down, bowels constipated, muscular weakness, and waste of nitrogenous materials, Senna will act as a tonic. Ebullitions of blood at night. 2[Acetonaemia], prostration, fainting, constipation with colic a flatulence. Liver enlarged and tender.

:rubric=stool
Fluid yellowish, with pinching pains before. Greenish mucus; never-get-done sensation (2[Merc]). Burning in rectum, with strangury of bladder. 2[Constipation], with colic and flatulence. Liver enlarged and tender, stools hard and dark, with loss appetite, coated tongue, bad taste, and 2[weakness].

:rubric=urine
Specific gravity and density increased; hyperazoturia, oxaluria, phosphaturia, and acetonuria.

