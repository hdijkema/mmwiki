:rubric=female
Menses too early; pale, thin, watery. Sterility, with acid secretions from vagina. Leucorrhoea; discharge creamy or honey-colored, or acid and watery. Sour-smelling discharges from uterus. Morning sickness, with sour vomiting.

:rubric=extremities
Rheumatism of the knee-joint.

:rubric=back
Weariness; aching in wrists and finger-joints. Hamstrings sore. 2[Synovial crepitation]. Rheumatic arthritis.

:rubric=skin
Yellow. Itching in various parts, 2[especially of ankles. Hives]. Smooth, red, shining. Erysipelas. Feet icy cold in daytime, burn at night. Swelling of lymphatic glands.

