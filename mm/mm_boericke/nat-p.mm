Imagines, on waking at night, that pieces of furniture are persons; that the hears footsteps in next room. 2[Fear.]

:rubric=head
Feels dull in the morning, full feeling and throbbing.

:rubric=eyes
2[Discharge of golden-yellow], 2[creamy matter] from the eyes. Dilation of one pupil. Whites of eyes dirty yellow.

