:rubric=generals
Irritation and catarrh of respiratory organs. Eczema. Albuminuria. 2[Pain in trachea]. Increases and gives odor to local perspiration. Causes 2[adhesion of the nail fold].

:rubric=head
Feels as if a band around head. Falling off of hair (2[Kali carb; Fl-ac]).

:rubric=nose
Coryza, with full feeling in nose. Nose and larynx sensitive to air. Small lumps of phlegm from posterior nares.

:rubric=eyes
2[Glaucoma]; with iridescent vision. Violent supra and infra-orbital neuralgia; violent pains and lachrymation. 2[Green colors surround candle-light]. Conjunctivitis. Increase in intra-ocular tension, dim sight, photophobia.

:rubric=respiratory
Acute laryngitis; cough and expectoration of tough, stringy mucus. Convulsive cough; feels as though membrane were torn from larynx. Noisy, 2[dry, hard, cough], in violent short bursts, coming from low down, shaking the whole body. Talking causes pain in larynx. Hoarse; 2[pain in larynx]; sore sternum. Twitching of fingers, with spasmodic cough.

:rubric=skin
Eczema, with pruritus. Irritated skin. Itching pimples. Bromidrosis, sweat in axilla smelling of garlic, worse evening and night. Fold remains attached to growing nail.

