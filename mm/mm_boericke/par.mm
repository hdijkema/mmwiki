:rubric=generals
Head symptoms marked and verified. Sensation of expansion and consequent tension. Coldness of right side of body, left hot. Catarrhal complaints, stuffed feeling at root of nose. Disorder of sense of touch.

:rubric=mind
Imaginary foul smells. Feels too large. 2[Garrulous], prattling, vivacious.

:rubric=head
Sensation as if scalp were contracted and bones scraped. Soreness of top of head; cannot brush hair. Aches, as from 2[pulling a string from eyes to occiput]. Occipital headache, with a feeling of weight. Head feels very large, expanded. Scalp sensitive. Numb feeling on left side of head.

:rubric=eyes
Affections of the eyebrows. Eyes feel heavy, as if they were projected; 2[sensation of a string through eyeballs]. Expanded, as though lids did not cover.

:rubric=face
Neuralgia; hot stitches in left malar bone, which is very sore. Has relieved in inflammation of the antrum, where eye symptoms co-existed.

:rubric=mouth
2[Tongue dry when awaking]-Coated white, 2[without thirst], with bitter or diminished taste.

:rubric=respiratory
Stuffed condition and fullness at root of nose. Periodical, 2[painless hoarseness]. Cough as from vapor of sulphur in trachea. Constant hawking, on account of viscid, green mucus in larynx and trachea.

:rubric=extremities
Sense of 2[weight and weariness in nape of neck] and across shoulders. Neuralgia, beginning in left intercostal region, and extending into left arm. Arm becomes stiff, fingers clenched. Neuralgia of coccyx; pulsating, sticking, when sitting. 2[Fingers often feel numb]. Numbness of upper limbs. Everything feels rough.

