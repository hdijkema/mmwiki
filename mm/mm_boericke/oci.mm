:rubric=generals
Is to be remembered in diseases of the kidneys, bladder and urethra. Uric acid diathesis. Red sand in the urine is its chief characteristic, and frequently verified. Swelling of glands, inguinal and mammary. Renal colic, especially right side. Symptoms of renal calculus are pronounced.

:rubric=urine
High acidity, formation of spike crystals of uric acid. Turbid, thick, purulent, bloody; 2[brick-dust red] or yellow 2[sediment. Odor of musk. Pain in ureters]. Cramps in kidneys.

:rubric=male
Heat and swelling of left testicle.

:rubric=female
Valva swollen; darting pain in labia. Nipples painful to least contact. Breasts feel full and tense; itching. Prolapsus vaginae.

