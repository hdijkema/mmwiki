:rubric=generals
An excellent remedy in excessive irritability of the bladder, dysuria, cystitis, gonorrhoea.

:rubric=nose
Always blowing nose.

:rubric=urinary
Frequent, 2[difficult], and painful urination (2[Pop]). Gravelly deposits. Catarrhal and purulent discharges (2[Pareira]). Strangury, pyelitis; enlarged prostate. Chronic cystic irritability. Incontinence; constant desire. Urine is dense and causes irritation of the mucous surfaces.

:rubric=relations
:rubric2=compare
R[Tradescantia]; (Haemorrhage from ear and upper air passages; painful urination, urethral discharge; scrotum inflamed). R[Chimaph], R[Senecio], R[Populus trem], R[Buchu], R[Uva].

R[Polytrichum Juniperinum]-Ground Moss - (Painful urination of old people; dropsy, urinary obstruction and suppression).

:rubric=dose
Tincture or infusion by boiling two ounces in a quart of water until it is reduced to a pint. To be taken in four doses in 24 hours.

