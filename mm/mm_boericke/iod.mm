:rubric=generals
Rapid metabolism: 2[Loss of flesh] great appetite. Hungry with much thirst. Better after eating. 2[Great debility, the slightest effort induces perspiration]. Iod individual is exceedingly thin, dark complexioned, with enlarged lymphatic glands, has voracious appetite but gets thin. Tubercular type.

All glandular structures, respiratory organs, circulatory system are especially affected; they atrophy. Iodine arouses the defensive apparatus of the system by assembling the mononuclear leucocytes whose phagocytic action is marked, at a given point. Lead poisoning. Tremor. Iodine craves cold air.

2[Acute exacerbation of chronic inflammation]. Arthritis deformans. 2[Acts prominently on connective tissue. The plague. Goitre]. Abnormal vaso-constriction, capillary congestion followed by oedema, ecchymosis, haemorrhages, and nutritive disturbances are the pathological conditions at the basis of its symptomatology. Sluggish vital reaction, hence chronicity in many of its aspects. Acute catarrh of all mucous membranes, rapid emaciation, notwithstanding good appetite, and glandular atrophy call for this remedy, in numerous wasting diseases and in scrofulous patients. Acute affections of the respiratory organs. 2[Pneumonia], rapid extension. Iodine is warm, and wants cool surroundings. Weakness and loss of breath going upstairs. 2[Adenoid vegetations]. Tincture internally and locally to swollen glands and rattlesnake bites.

