:rubric=generals
Marked action on nervous system. Trembling, sensation of emptiness. Walks backwards. Congestion of spine and paralysis. Pains come and go quickly. Sphincters relaxed. Staggering gait. Reflexes lost.

:rubric=mind
Desires to be alone. Disinclined to work or talk. Worse, thinking of symptoms (2[Oxalic ac]). 2[Mental depression]. Vertigo (2[Granatum]).

:rubric=head
Vertigo. Full, warm feeling about head. Feeling of intoxication, with loss of vision. Pain in maxillary bones and masseter muscles. Mouth and nose dry.

:rubric=eyes
Sight obscured; pupils contracted; do not respond to light. Paralysis of nerves and muscles of eyes.

:rubric=stomach
Eructations with colicky pains. Epigastrium tender.

:rubric=rectum
Sphincter seems relaxed. Stools slip from anus, 2[like lumps of jelly], mushy.

:rubric=urine
Urging to urinate when thinking of it. 2[Profuse flow]. Pain in kidneys (2[Berberis]).

:rubric=male
No desire or ability. Pain in testicles and along spermatic cord and down thighs.

:rubric=extremities
Pain along ulnar nerve. Numb feeling about spine. 2[Staggering gait]. Loss of co-ordination. Patellar tendon reflex lost. Pains come and go quickly, but muscles remain sore and stiff.

:rubric=sleep
Restless, dreams of quarrel.

:rubric=modalities
M[Worse], thinking of symptoms (mono-maniac tendency). M[Worse], every other day. M[Better], after sleep.

:rubric=relations
:rubric2=compare
R[Astrag], R[Lathyr], R[Oxal ac], R[Baryta] (Loco plant is rich in Baryta). R[Lolium].

:rubric=dose
Third potency and higher. 

