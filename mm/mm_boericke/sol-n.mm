:local-name=Black Nightshade

:rubric=generals
Used with success in ergotism, with tetanic spasms and stiffness of whole body, with mania. Marked action on head and eyes. 2[Meningitis]. Chronic intestinal toxaemia. Brain irritation during dentition. Restlessness of a violent and convulsive nature. Formication with contraction of extremities.

:rubric=head
Furious delirium. Vertigo; terrible headache and complete cessation of the mental faculties. Night terrors. 2[Congestive] headache.

:rubric=nose
Acute coryza; 2[profuse, watery discharge from right nostril]; left stopped up, with chilly sensation, alternating with heat.

:rubric=eyes
Pain over both eyes. Alternate dilatation and contraction of pupils; weak sight; floating spots.

:rubric=respiratory
Constructive feeling in chest, with difficult breathing; cough with tickling in throat. Expectoration 2[thick, yellow]. Pain in 2[left] chest, sore to touch.

:rubric=fever
Alternation of coldness and heat. Scarlet fever; eruption in spots, large and vivid.

:rubric=relations
:rubric2=compare
R[Bellad], R[Solanum Carolinense]-Horse-nettle - (conclusions and epilepsy, twenty to forty-drop doses; is of great value in grand mal of idiopathic type, where the disease has begun beyond age of childhood; hystero-epilepsy, also in whooping-cough); R[Solan mammosum]-Apple of Sodom - (pain in left hip-joint); R[Solan oleraceum] (swelling of mammary gland, with profuse secretion of milk); R[Solan tuberosum] (cramps in calves and contraction of fingers; spitting through closed teeth); R[Solan vesicarium] (recommended in facial paralysis); R[Solaninum aceticum] (threatening paralysis of the lungs in the course of bronchitis in the aged and children must cough a long time before able to raise expectoration); R[Solan pseudocaps] (acute pains, in lower abdomen); R[Solan tuberos aegrotans]-Diseased potato - (prolapse of the rectum, patulous anus; offensive breath and odor of body; tumors of rectum look like decayed potato; dreams of pools of blood); R[Solanum tuberosum]-Potato berries - (cramps in the calves of the legs and fingers).

:rubric=dose
Second to thirtieth potency.

