:local-name=High Cranberry

:rubric=generals
A general remedy for cramps. Colicky pains in pelvic organs. Superconscious of internal sexual organs. Female symptoms most important. 2[Often prevents miscarriage]. False labor-pains. Spasmodic and congestive affections, dependent upon ovarian or uterine origin.

:rubric=head
Irritable. Vertigo; feels as if falling forward. Severe pain in temporal region. Sore feeling in eyeballs.

:rubric=stomach
Constant nausea; relieved by eating. No appetite.

:rubric=abdomen
2[Sudden cramps and colic pains]. Tender to pressure about umbilicus.

:rubric=female
Menses 2[too late, scanty, lasting a few hours], offensive in odor, with crampy pains, cramps extend down thighs (2[Bell]). Bearing-down pains before. Ovarian region feels heavy and congested. Aching in sacrum and pubes, with pain in anterior muscles of thighs (2[Xanthox]); 2[spasmodic and membranous dysmenorrhoea] (2[Borax]). Leucorrhoea, excoriating. Smarting and itching of genitals. Faint on attempting to sit up. 2[Frequent and very early miscarriage], causing seeming sterility. Pains from back to loins and womb worse early morning.

:rubric=urinary
Frequent urging. Copious, pale, light-colored urine. Cannot hold water on coughing or walking.

:rubric=rectum
Stools large and hard, with cutting in rectum and soreness of anus.

:rubric=extremities
Stiff, sore feeling in nape of neck. Feels as if back would break. Sacral backache. Lower extremities weak and heavy.

:rubric=modalities
M[Worse], lying on affected side, in warm room, evening and night. M[Better], in open air and resting.

