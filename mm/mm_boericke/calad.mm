:local-name=American Arum

:rubric=generals
This remedy has a marked action on the genital organs, and pruritus of this region. Coldness of single parts and inclination to lie down, with aggravation on lying on left side. Slightest noise startles from sleep. 2[Dread from motion]. Modifies craving for tobacco. Tobacco heart. Asthmatic complaints.

:rubric=head
Headaches and mental states of smokers. Very forgetful, does not know about the occurrences of things. Confused headache with pain in shoulder, pressure in eyes and forehead; extremely sensitive to noise, throbbing in ear.

:rubric=stomach
Gnawing in orifice of stomach, which prevents deep breathing and eructations. Eructations. 2[Stomach feels full of dry food; sensation of fluttering]. Acrid vomiting, thirstless and tolerates only warm drinks. Sighing respiration.

:rubric=male
2[Pruritus]. Glans very red. Organs seem larger, puffed, relaxed, cold, sweating; skin of scrotum thick. Erections when half-asleep; cease when fully awake. 2[Impotency]; relaxation of penis during excitement. No emission and no orgasm during embrace.

:rubric=female
2[Pruritus of vulva] (2[Ambr; Kreos]) and vagina during pregnancy (Hydrogen peroxyd 1: 12 locally). Voluptuousness. Cramp pains in uterus at night.

:rubric=skin
Sweet sweat-attracts flies. Insect bites burn and itch intensely. Itching rash alternates with asthma. 2[Burning sensation] and erysipelatous inflammation.

:rubric=respiratory
Larynx seems constricted. Breathing impeded. Catarrhal asthma; mucus not readily raised. Patient afraid to go to sleep.

:rubric=modalities
M[Better], after sweat, after sleeping in daytime. M[Worse], motion.

:rubric=relations
Incompatible: R[Arum triph].

:rubric2=complementary
R[Nit-ac].

:rubric2=compare
