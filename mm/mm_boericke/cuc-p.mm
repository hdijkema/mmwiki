R[Filix], R[Cuprum oxid nig].

:rubric=dose
Tincture. The seeds are a valuable remedy for tapeworm. Scald the seeds and peel off the outer skins when softened, the green inner pulp being the part used. Dose: two ounces of seed, yielding one of pulp. May be mixed with cream and taken like porridge. Take-in morning after twelve hours' fasting, and follow in two hours by castor oil.

