:rubric=generals
Diphtheria, post-nasal tumors, and epithelioma of the tongue have been benefited by this drug. Bloody, foul-smelling lochia. Symptoms come and go 2[suddenly], and return periodically; offensive discharges.

:rubric=nose
Ulcer and scabs in nose. Offensive smell. Corrosive pain. Ozaena (2[Aur]).

:rubric=throat
Diphtheria; sore throat. Tough mucus, with inclination to swallow it; worse, causing hawking. Post-nasal tumors.

:rubric=extremities
Uneasiness in limbs. Pain in shoulder-blades and back of neck. Pain in knees and balls of feet. Drawing pain in soles while walking.

:rubric=stool
Watery, frequent, copious, with nausea and vertigo. Haemorrhoids, internal and bleeding. Weakness in small of back.

:rubric=relations
:rubric2=compare
R[Kali bich], R[Rhus-t], R[Chromium Sulphate] (in locomotor ataxia, goitre, R[prostatic hypertrophy]. Herpes preputialis. Wry neck. Also exophthalmic, inhibits the vagus, relieving tachycardia. Acts like a nerve tonic where there is lack of nervous tone. Fibroid tumors. Infantile paralysis. Dose for adults, 3 to 5 grains after meals and at bedtime).

:rubric=dose
Homeopathically, third to sixth trituration.

