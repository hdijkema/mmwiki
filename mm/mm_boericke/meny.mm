:rubric=generals
A remedy for certain headaches, intermittent fever. Coldness of abdomen. Twitchings. Sensation of tension and compression. Fidgets and urinary difficulties in women. Diabetes.

:rubric=head
Pressing in vertex; 2[better, hard pressure with hand]. Pressing-together pain. Weight pressing on brain with every step on ascending. Pain from nape over whole brain; better, stooping, sitting; worse, going upstairs. Cracking in jaw and twitching of facial muscles.

:rubric=stomach
No thirst at any time. Revenous hunger; passing away after eating a little. Desire for meat. Sensation of coldness extending up to oesophagus.

:rubric=abdomen
Distended and full; increased by smoking tobacco. Coldness of abdomen.

:rubric=extremities
2[Icy coldness of hands and feet]. Cramp-like pain. As soon as patient lies down, 2[legs jerk and twitch].

:rubric=fever
Coldness predominates; felt most acutely in abdomen and legs and tip of nose.

:rubric=modalities
M[Worse], during rest, ascending M[Better], pressure on affected part, stooping, motion.

:rubric=relations
:rubric2=compare
R[Caps], R[Puls], R[Calc], R[Phos ac], R[Sang].

Antidote: R[Camph].

:rubric=dose
Third to thirtieth potency.

