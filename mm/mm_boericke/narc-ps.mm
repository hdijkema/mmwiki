Symptoms of nausea followed by violent vomiting and diarrhoea.

Daffodil bulbs contain an alkaloid the action of which, according to authorities, varies as to whether the alkaloid is extracted from the flowering bulb or from the bulb after flowering. Thus in the former case the alkaloid 2[produces dryness of the mouth, checks cutaneous secretions, dilates the pupil of the eye, quickens the pulse, and slows and weakens the heart contractions]. On the other hand, the alkaloid from the bulbs after flowering 2[produces copious salivation, increases cutaneous secretion, contracts the pupil of the eye, produces slight relaxation of the pulse, and slight faintness and nausea. - The Lancet].

