Difficult concentration of thought. Sensation as if mind were void. Though confused. "Woodeny" feeling in right side of head. Right side of scalp sensitive. Profuse, watery coryza, better indoors. Ozaena.

:rubric=respiratory
Hoarse cough, worse talking; posterior nares feel raw, inflamed, profuse, thick, yellowish discharge. Chronic laryngeal catarrh.

Back and extremities. - Weakness in the kidney region. Spinal paresis, especially for the aged and after disease. Pressing in groin and left thigh. Tension in both thighs, especially left. Dislocated feeling in left hip-joint and dull pain toward left gluteal region.

