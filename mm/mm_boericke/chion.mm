:rubric=generals
This remedy is often of service in many types of headaches, neurasthenic, 2[periodical sick], menstrual and bilious. Taken for several weeks, drop doses, will often break up the sick headache habit. The pain in the forehead, chiefly over eyes. Eyeballs very painful, with pressure over root of nose. Hepatic derangements. 2[Jaundice]. Enlarged spleen (2[Ceanoth]). Jaundice with arrest of menses. A prominent liver remedy. 2[Gallstones] (2[Berberis; Cholest; Calc]). 2[Diabetes mellitus]. Paroxysmal, abdominal pain.

:rubric=head
Listless, apathetic. Dull frontal headache, over root of nose, over eyes, through temples, worse stooping, motion, jar. 2[Yellow conjunctiva].

:rubric=tongue
Broad with thick yellow fur.

:rubric=mouth
Dry sensation not relieved by water, also profuse saliva.

Abdomen and Liver. - Aching in umbilical region, griping. Feels as if a string were tied in a "slip-knot" around intestines which was suddenly drawn tight and then gradually loosened. 2[Sore; enlarged, with jaundice] and constipation. Clay-colored stool, also soft, yellow and pasty. Tongue heavily coated. No appetite. Bilious colic. Hepatic region tender. Pancreatic disease and other glandular disorders.

:rubric=urine
Large amount of high specific gravity; frequent urination; bile and sugar in urine. Urine very dark.

:rubric=skin
Yellow; marked moisture of skin. Sallow, greenish, itching.

