:rubric=generals
Has a selective affinity for 2[bleeding mucous surfaces]. Tympanites and urinary symptoms very marked. Inflammation of kidneys, with haemorrhages-dark, passive, fetid. Bright's disease preceded by dropsy (Goullon). Drowsiness and strangury. Coma. 2[Unbroken chilblains].

:rubric=head
Dull pain like from a band around the head (2[Carb ac]). Vertigo, with vanishing of vision. Disturbed sense of equilibrium. Tired and difficult concentration of thoughts. Cold in head with sore nostrils with disposition to bleed.

:rubric=eyes
Ciliary neuralgia over right eye. Intense pain in eye and side of head. Amblyopia from alcohol.

:rubric=ears
Own voice sounds unnatural; humming as of a seashell, talking loudly is painful. Otalgia.

:rubric=mouth
Tongue 2[dry, red sore, shining]; burning in tip, with prominent papillae (2[Arg n; Bell; Kali b; Nux m]). Breath cold, foul. Choking sensation in throat. Stomatitis. Dentition.

:rubric=stomach
Nausea and vomiting; heat in epigastric region.

:rubric=abdomen
2[Enormous distention]. Diarrhoea; stools watery, greenish, fetid, bloody. Pain before flatus with and relief after stool. Haemorrhage from bowels. Worms; lumbrici. Abdominal dropsy; pelvic peritonitis. Fainting after every stool Entero-colitis, with haemorrhage and ulceration of bowels.

:rubric=urinary
2[Strangury, with bloody urine]. Scanty, suppressed, 2[odor of violets]. Urethritis, with painful erections (2[Canthar]). Inflamed kidneys following any acute disease. Constant tenesmus.

:rubric=female
Intense 2[burning in uterine region]. Metritis; puerperal peritonitis. Metrorrhagia with burning in uterus.

:rubric=respiratory
Difficult breathing; lungs feel distended; haemoptysis. Bloody expectoration.

:rubric=heart
Pulse rapid, small, thready, intermittent.

:rubric=back
2[Burning pain in region of kidneys]. Drawing in right kidney extending to hip.

:rubric=skin
Acne. Erythema; itching pustular, vesicular eruption; urticaria. Purpura, ecchymosis, dropsies. Scarlatina. Chilblains; with excessive itching and pulsative pains. Aching soreness of the muscles.

:rubric=fever
Heat, with violent thirst, dry tongue, profuse cold, clammy sweat. Typhoid with tympanites, haemorrhages, stupor, delirium. Prostration.

