:rubric=generals
Catarrhal symptoms, especially of the respiratory tract, and distinct eye symptoms of a paralytic type, are most characteristic. Circumscribed spots in chest left after inflammations.

:rubric=mind
Suddenly remembers unimportant regions which he saw long ago. Inclined to quarrel.

:rubric=head
Dullness, with pressure and weakness of eyes. Pain in temples. 2[Bursting] pain in forehead.

:rubric=eyes
Hyperphoria, better by bending head backwards. Acts on the rectus superior. Blepharitis; lids dry and crusty (2[Graph]). Dryness, with sensation 2[as if too large for orbits]. Starting. Lachrymation. Flickering; must wipe eyes frequently. Objects look shaded. Muscular asthenopia (2[Caust]). Double vision; better only by bending head backward. Opacities of the vitreous humor. Promotes absorption of fragments of lens, after operation.

:rubric=nose
Dry. Coryza; much watery mucus and sneezing. Nostrils feel peppery.

:rubric=face
Paralysis of left side of face. Heat in face. Burning vesicles in corners of mouth and lips.

:rubric=throat
Catarrhal inflammation of throat and fauces, with scraping hoarseness. Burning and rawness. Sensation as if membrane had been abraded.

:rubric=respiratory
Hoarseness. Hurts to talk. Bursting pain in back on coughing. Catarrh of larynx. Loss of voice. Hacking cough. Thorax feels too narrow. 2[Cough often ends in a sneeze. Rattling in chest] (2[Tart emet]). Chest oppressed on ascending. Bronchial catarrh, 2[with sore chest walls]; much mucus; sensation of oppression and weight of chest. 2[Difficult raising of tough, profuse mucus], in the aged. Asthenic bronchitis of old people with chronic interstitial nephritis or chronic emphysema. Old asthmatics with congestive attacks. 2[Exudations in Pleura]. Hydrothorax (2[Merc sulph]). Pressure on chest as though lungs were forced back to spine. Voice unsteady, vocal cords partially paralyzed.

:rubric=urinary
Greatly diminished; loaded with shreds and mucus; scalding before and after urinating. 2[Back], bursting distending pain in kidney region.

:rubric=modalities
M[Worse], walking in open air, during rest. M[Better], from sweat; 2[bending head backwards].

