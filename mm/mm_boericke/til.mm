:rubric=generals
Of value in muscular weakness of the eye; haemorrhage o thin, pale blood. Puerperal metritis. Diseases of the antrum (2[Kali hyd; Chelid]).

:rubric=head
2[Neuralgia] (first right, then left side), 2[with veil before eyes]. Confusion, with dimness of vision. Much sneezing, with fluent coryza. Bleeding from nose.

:rubric=eyes
Sensation as of gauze before eyes (2[Calc; Caust; Nat m]). Binocular vision imperfect.

:rubric=female
2[Intense sore feeling about uterus]; bearing-down, with hot sweat, but without relief. Much slimy leucorrhoea when walking (2[Bov; Carb an; Graph]). Soreness and redness of external genitals (2[Thuj; Sulph]). Pelvic inflammation, tympanites, abdominal tenderness and hot sweat which does not relieve.

:rubric=skin
Urticaria. Violent itching, and burning like fire after scratching. Eruption of small, red itching pimples. 2[Sweat warm and profuse] soon after falling asleep. Sweat increases as rheumatic pains increase.

:rubric=modalities
M[Worse], in afternoon and evening; in warm room, heat of bed. M[Better], cool room, motion.

