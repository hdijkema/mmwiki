:rubric=generals
Of use in intermittent fever, with weariness and inclination to stretch. Hamstrings feel too short (2[Ammon mur]). Flexors mostly affected. Sensation of retraction of arm tendons. Stretching.

:rubric=head
Violent headache, caused by drinking. Great rage; vehement at beginning of chilly stage. Would like to tear everything to pieces. Pain under right frontal bone.

:rubric=female
Shooting pain from vagina up towards left ovary.

:rubric=fever
Chilliness of whole body. Sensation as of wind blowing on knees. 2[Pains in all joints, as if tendons were too short], especially knee-joints. Chill; worse lying down. Thirst during apyrexia, but little during chilly stage; still less during hot stage, and none during sweating. Musty, offensive sweat.

:rubric=bowels
Constipation, feces dry and in small balls (2[Op; Plumb; Thuj]) and hard. Ulcer of rectum.

:rubric=dose
Sixth to two hundredth attenuation. 

