S{Sensation as if swimming in cold water.}

:rubric=stomach
S{Pressure like a stone.}

:rubric=respiratory
S{Fluent coryza; margins of nostrils feel sore.}
S{Sneezing; throat irritated; short, dry cough; must take a deep breath.}
S{2[Dyspnoea and stitches in chest], and painful contraction of abdominal muscles.}
S{2[Violent], furious, exhausting cough, with much mucus; profuse, salty, slimy expectoration, and with 2[involuntary spurting of urine and sneezing.}
