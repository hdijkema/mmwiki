:local-name=(BRASSICA NIGRA)

:rubric=generals
Is of use in hay-fever, coryza, and pharyngitis. Dry nares and pharynx, with thick, lumpy secretion. Small-pox.

:rubric=head
Scalp hot and itches. 2[Sweat on upper lip and forehead]. Tongue feels blistered.

:rubric=nose
Mucus from posterior nares feels cold. Scanty, 2[acrid] discharge. 2[Stoppage of left nostril all day], or in afternoon and evening. Dry, hot, with lachrymation, sneezing; hacking cough; better lying down. 2[Nostrils alternately stopped]. Dryness of anterior nares.

:rubric=respiratory
Cough is relieved by lying down.

:rubric=throat
Feels scalded, hot inflamed. Asthmatic breathing. Loud coughing-spells with barking expiration.

:rubric=stomach
2[Offensive breath], smelling like onions (2[Asaf; Armorac]). Burning in stomach, extending up oesophagus, throat, and mouth, which is full of canker sores. Hot sour eructations. 2[Colic; pains come on while bent forward; better, sitting up straight]. Sweat better when nausea comes on.

:rubric=urinary
Pain in bladder, frequent 2[copious] flow day and night.

:rubric=back
Rheumatic pain in intercostal and lumbar muscles; sleeplessness from pain in back and hips.

:rubric=relations
:rubric2=compare
R[Sulph], R[Capsic], R[Colocy], R[Sinapis alba]-White Mustard - (throat symptoms marked, especially R[pressure and burning], R[with obstruction in oesophagus]; sensation of a lump in oesophagus behind the Manubrium Sterni and with much eructation; similar symptoms in rectum). R[Mustard oil] by inhalation (acts on the sensory nerve endings of the trigeminal. Relieves pain in middle ear disease and in painful conditions of nose, nasal cavities, and tonsils).

:rubric=dose
Third potency.

