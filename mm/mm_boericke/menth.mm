:local-name=Peppermint

:rubric=generals
Stimulates the cold-perceiving nerves, so just after taking it, a current of air at the ordinary temperature seems cold. Marked action on respiratory organs and skin. Useful in gastrodynia, flatulent cold.

:rubric=abdomen
Bloated, disturbing sleep. Infantile colic. Bilious colic with great accumulation of gas.

:rubric=respiration
Voice husky. Tip of nose to touch. Throat dry and sore, as if pin crosswise in it. 2[Dry cough, worse from air into larynx, tobacco smoke, fog], talking; 2[with irritation in suprasternal fossa] (2[Rumex]). 2[Trachea painful to touch].

:rubric=skin
Every scratch becomes a sore. Itching of arm and hand when writing. Vaginal pruritus. Herpes zoster (2[Ars; Ran bulb]).

:rubric=relations
:rubric2=compare
R[Rumx], R[Laches], R[Mentha pulegium] - European pennyroyal - (pain in bones of forehead and extremities). R[Mentha viridis]-Spearmint - (scanty urine with frequent desire).

:rubric=dose
Tincture, 1 to 20 drops, to thirtieth potency. Locally, in pruritus vaginae.

