:local-name=Dog's Milk

:rubric=generals
This remedy is of undoubted value in certain form of sore throat and diphtheria, and rheumatism. Corresponds to a low-vitiated, non-feverish type of sickness. The keynote symptom is, 2[erratic pains, alternating sides]. Feels as if walking on air, or of not touching the bed when lying down. Great lassitude. Ozaena. Decided effect in drying up milk in women who cannot nurse the baby. 2[Great weakness and prostration]. Sinking spells every morning. Mastitis.

:rubric=mind
Very forgetful; in writing, makes mistakes. 2[Despondent]; thinks her disease incurable. Attacks of rage. 2[Visions of snakes]. Thinks himself of little consequence.

:rubric=head
Sensation of walking or floating in the air (2[Sticta]). Pain first one side, then the other. Blurred vision, nausea and vomiting at height of attack of headache. Occipital pain, with shooting extending to forehead. Sensation as if brain were alternately contracted and relaxed. Noises in ears. Reverberation of voice.

:rubric=nose
Coryza; one nostril stuffed up, the other free; alternate. Alae nasi and corners of mouth cracked. Bones of nose sore to pressure. Bloody pus discharged.

:rubric=mouth
Tongue coated white with bright red edges; profuse salivation. Drooling in diphtheria. 2[Cracking of jaw while eating] (R[Nit-ac]; R[Rhus-t]). Putrid taste increased by sweets.

:rubric=throat
Sensitive to touch. Painful swallowing; pain extends to ears. Sore throat and cough with menstruation. 2[Tonsillitis and diphtheria symptoms change repeatedly from side to side. Shining glazed] appearance of deposit, 2[pearly-white] or like pure white porcelain. 2[Stiffness of neck] and tongue. Throat feels burned raw. Tickling sensation causes constant cough. Sore throat beginning and ending with menses.

:rubric=female
Menses too early, profuse, 2[flow in gushes. Breasts swollen; painful before] (R[Calc]; R[Con]; R[Puls]) and better on appearance of menses. Mastitis; 2[worse, least jar]. 2[Helps to dry up milk]. Sinking at epigastrium. Sexual organs easily excited. Backache; spine very sensitive to touch or pressure. 2[Galactorrhoea].

:rubric=extremities
Sciatica, right side. Legs feel numb and stiff, cramps in feet. Rheumatic pains in extremities and back, from one side to the other. Pain in arms to fingers. Burning in palms and soles.

:rubric=sleep
Dreams of snakes.

:rubric=modalities
M[Worse], morning of one day and in the evening of next. M[Better], cold, cold drinks.

