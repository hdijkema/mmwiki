A remedy in liver affections with pain or soreness of the left lobe of the liver and extending downwards. Dumb ague. Soreness of external parts, as if skin were off; debility. Malaise, following intermittents. Dyspepsia with hepatic torpor. Jaundice. 2[Round and thread worms]. It is an enemy to every kind of worm infesting the human body.

:rubric=dose
Tincture, in one to five drop doses.

