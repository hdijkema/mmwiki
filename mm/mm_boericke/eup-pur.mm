:rubric=generals
Albuminuria, diabetes, strangury, 2[irritable bladder], enlarged prostate are a special field for this remedy. Excellent in renal dropsy. Chills and pains run upwards. Impotency and sterility. Homesickness.

:rubric=head
2[Left-sided headache with vertigo]. Pain from left shoulder to occiput. Sick headache beginning in morning, worse afternoon and evening, worse in cold air.

:rubric=urinary
Deep, dull pain in kidneys. Burning in bladder and urethra on urinating. Insufficient flow; milky. Strangury. Haematuria. Constant desire; bladder feels dull. Dysuria. 2[Vesical irritability in women. Diabetes insipidus].

:rubric=back
Weight and heaviness in loins and back.

:rubric=female
Pain around left ovary. Threatened abortion. External genitals feel as though wet.

:rubric=fever
No thirst during chill, but much frontal ache. 2[Chill commences in back]. Violent shaking, with comparatively little coldness. Bone-pains.

