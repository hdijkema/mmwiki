:rubric=generals
Used homeopathically, dynamized Glycerine seems to act

deeply and long, building up tissue, hence of great use in marasmus, debility, mental and physical, diabetes, etc. It disturbs nutrition in its primary action, and, secondarily, seems to improve the general state of nutrition (Dr. Wm. B. Griggs).

:rubric=head
Feels full, throbs; mentally confused. Severe headache two days be before menstruation. Occiput feels full.

:rubric=nose
Stopped up, sneezing, irritating coryza. Sensation of crawling on mucous membrane. Post-nasal dripping.

:rubric=chest
Hacking cough with sense of weakness. Chest seems full. Influenzal pneumonia.

:rubric=stomach
Fermentation, burning in stomach and oesophagus.

:rubric=urinary
Profuse and frequent urination. Increased specific gravity and sugar. Diabetes.

:rubric=female
Profuse, long-lasting flow with bearing down heaviness in uterus. General sense of exhaustion.

:rubric=extremities
Rheumatic pains of a remittent type. Feet painful and hot, feel enlarged.

