:rubric=generals
Lowered blood pressure. Dilated blood vessels but does not act on the centers in the medulla. Pulse is slow due to central irritation of the vagus.

The symptoms point especially to rheumatic and gouty complaints; neuralgia, especially sciatica. Epilepsy, 2[chorea], and metrorrhagia. 2[Rheumatic deafness. Asthma]. Spinal pains, due to uterine causes. Rheumatism with 2[tearing pains]. Hypertensive albuminuria. Valvular disease, with disturbances in sexual sphere. Symptoms like epileptic aura and petit mal.

:rubric=head
Feeling as if whole vault of skull were lifted up. Blue ring around eyes. Double vision. Buzzing and stopped-up feeling in ear. Deafness from cold. Facial muscles in constant agitation. Persistent vertigo.

:rubric=respiratory
Dyspnoea; 2[feeling of suffocation when lying on left side]. Spasmodic cough. 2[Asthma], if connected with gout or rheumatism. Stertorous breathing.

:rubric=female
Haemorrhage, with pain; blood partly clots and bright red. Climacteric complaints (2[Lach; Sulph]). Pain from sacrum into pelvis, with tearing, shooting pains from above downwards. Retained placenta (2[Secale]). Chronic endometritis. Metrorrhagia. Ovaralgia, especially left.

:rubric=heart
Hypertrophy with valvular insufficiency; pulse small and weak; unable to rest in a reclining position. Palpitation during coitus. Low tension. Failing compensation, dyspnoea worse lying on left side. Weight and oppression of heart; as if a hand were squeezing it; tickling sensation about heart.

:rubric=extremities
Pains alternate in the knee and ankle with shoulder and elbow. 2[Sciatica. Tearing], shooting pains in both thighs and upper extremities. A 2[glow] rises from the feet to the head; seems to be on fire. Periodic pains from sacrum into pelvis, 2[worse in bed], with pains into thighs and upper extremities. General tremor, as if all muscles were in state of fibrillary contraction. Dropsy of extremities. Sensation of a spider crawling over back of hand and foot. Itching all over. Compressing pain in feet.

:rubric=modalities
M[Worse], winter, cold, stormy weather; in bed. Movement; lying on left side.

