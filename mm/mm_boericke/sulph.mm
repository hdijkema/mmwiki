:rubric=dose
Acts in all potencies from the lowest to the highest. Some of the best results are obtained from the higher, and not too frequent doses. The twelfth potency is a good one to begin treatment with, going higher or lower according to the susceptibility of the patient. In chronic diseases, 200th and upward. In 2[torpid] eruptions the 2[lowest] potencies.