:local-name=Cowslip

:rubric=generals
Cerebral congestion, with neuralgia; migraine; rheumatic and gouty pains.

:rubric=head
Sensation of a band around head; cannot keen hat on (2[Carbol ac]). Skin of forehead tense. Fear of falling when standing up. Violent vertigo, as if everything turned around. Buzzing in ears; better in open air.

:rubric=respiratory
Cough, with burning and pricking in respiratory tracts. Weak voice.

:rubric=urinary
Urine smells strongly of violets (2[Terebinth]).

:rubric=extremities
Right axillary muscles painful. Weight and lassitude in limbs, especially the 2[shoulders]. Burning in hollow of right hand. Drawing pain in thumb and big toe.

:rubric=relations
:rubric2=compare
R[Cyclamen], R[Ranunc], R[Oenothera] - Evening Primrose - (exhausting, watery diarrhoea; cholera infantum; hydrocephaloid); R[Primula farinosa]-the wild Primrose - (dermatitis, especially on index fingers and thumbs).

:rubric=dose
Third potency.

