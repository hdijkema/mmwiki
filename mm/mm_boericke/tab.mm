:rubric=antidotes
Vinegar; sour apples. 2[Camphor] in the physiological antagonist. 2[Ars] (chewing tobacco); 2[Ign]; (smoking); 2[Sep] (neuralgia and dyspepsia); 2[Lycop] (impotency); 2[Nux] (bad taste due to tobacco); 2[Calad] and 2[Plantag] (cause aversion to tobacco); 2[Phosph] (tobacco heart, sexual weakness).

:rubric=dose
Third to thirtieth and higher potencies.

