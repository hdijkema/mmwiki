:rubric=generals
The symptomatology of Tabacum is exceedingly well marked. The nausea, giddiness, death-like pallor, vomiting, icy coldness, and sweat, with the intermittent pulse, are all most characteristic. Has marked antiseptic qualities, antidotal to cholera germs. Complete prostration of the entire muscular system. Collapse. Gastralgia, enteralgia, 2[seasickness], cholera infantum; cold, but 2[wants abdomen uncovered]. Vigorous peristaltic activity diarrhoea. Produces high tension and arteriosclerosis of the coronary arteries. Should prove the most homeopathic drug for angina pectoris, with coronaritis and high tension (Cartier). Constriction of throat, chest, bladder, rectum. Pallor, breathlessness, hard-cordlike pulse.

:rubric=mind
Sensation of excessive wretchedness. 2[Very despondent]. Forgetful. Discontented.

:rubric=head
Vertigo 2[on opening eyes]; sick headache, with deathly nausea; periodical. Tight feeling as from a band. Sudden pain, as if struck by a hammer. Nervous deafness. Secretion from eyes, nose and mouth increased.

:rubric=eyes
Dim sight; sees as through a veil; strabismus. 2[Amaurosis]; muscae volitantes. Central scotoma. Rapid blindness without lesion, followed by venous hyperaemia and atrophy of optic nerve.

:rubric=face
Pale, blue, pinched, sunken, collapsed, covered with cold sweat (2[Ars; Verat]). Freckles.

:rubric=throat
Nasopharyngitis and tracheitis, 2[hemming], morning cough, sometimes with vomiting. Hoarseness of public speakers.

:rubric=stomach
Incessant nausea; worse, smell of tobacco smoke (2[Phos]); vomiting on least motion, sometimes of fecal matter, 2[during pregnancy with much spitting. Seasickness; terrible faint, sinking feeling at pit of stomach]. Sense of relaxation of stomach, with nausea (2[Ipec]). Gastralgia; pain from cardiac end extending to left arm.

:rubric=abdomen
Cold. 2[Wants abdomen uncovered]. It lessens the nausea and vomiting. Painful distension. Incarcerated hernia.

:rubric=rectum
Constipation; rectum paralyzed, prolapsed. Diarrhoea, sudden, watery, with nausea and vomiting, prostration, and cold sweat; discharges look like sour milk, thick, curdled, watery. Rectal tenesmus.

:rubric=urinary
Renal colic; violent pain along ureter, left side.

:rubric=heart
Palpitation when lying on left side. Pulse intermits, feeble, imperceptible. Angina pectoris, pain in praecordial region. Pain radiates from center of sternum. Tachycardia. Bradycardia. 2[Acute dilatation] caused by shock or violent physical exertion (Royal).

:rubric=respiratory
Difficult, violent constriction of chest. Praecordial oppression, with palpitation and pain between shoulders. Cough followed by hiccough. Cough dry, teasing, must take a swallow of cold water (2[Caust; Phos]). Dyspnoea, with tingling down left arm when lying on left side.

:rubric=extremities
Legs and hands icy cold; limbs tremble. Paralysis following apoplexy (2[Plumb]). Gait shuffling, unsteady. Feebleness of arms.

:rubric=sleep
Insomnia with dilated heart, with cold, clammy skin and anxiety.

:rubric=fever
Chills, with 2[cold sweat].

:rubric=modalities
M[Worse], opening eyes; evening; extremes of heat and cold. M[Better], uncovering, open fresh air.

