:rubric=generals
Acts principally on kidneys, and genito-urinary tract; affects also lymphatic and mesenteric glands and female mammae. Plethoric young women with dysuria. Women with large breasts. Hepatic and renal dropsies; chronic alcoholics. Incipient and progressive cataracts.

One of the remedies whose symptoms point to its employment in bladder affections, notably catarrh, acute and chronic. 2[Scanty urine, and loaded with ropy, muco-purulent sediment. Prostatic enlargement].

:rubric=head
Pain in left frontal protuberance. Halo about the light. Itching of eyelids. Stabbing pain in left eye with lachrymation.

:rubric=mouth
Toothache, worse after eating and exertion, better cool water. Pain as if tooth was being gently pulled.

:rubric=urinary
Urging to urinate. Urine turbid, offensive, containing ropy or bloody mucus, and depositing a copious sediment. Burning and scalding during micturition, and straining afterwards. 2[Must strain] before flow comes. Scanty urine. Acute prostatitis, retention, and 2[feeling of a ball in perineum] (2[Cann ind]). Fluttering in region of kidney. 2[Sugar in urine]. Unable to urinate without standing with feet wide apart and body inclined forward.

:rubric=female
Labia inflamed, swollen. Pain in vagina. Hot flashes. Painful 2[tumor of mammae], not ulcerated, with undue secretion of milk. Rapid atrophy of breasts. Women with 2[very large breasts] and tumor in the mammary gland with 2[sharp] pain through it.

:rubric=male
Smarting in urethra from neck of bladder to meatus. Gleet. Loss of prostatic fluid. Prostatic enlargement and irritation.

:rubric=skin
Scrofulous ulcers. Glandular enlargements.

:rubric=extremities
Feeling of a band above left knee.

:rubric=modalities
M[Worse], in damp weather; from sitting on cold stones or pavements; left side.

