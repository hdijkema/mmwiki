:rubric=generals
Inflammatory rheumatism, pleurodynia, 2[sciatica], and other neuralgias, come within, the sphere of this remedy. Cystic and prostatic irritation, undue sexual excitement, and renal inflammation.

:rubric=head
Neuralgia of head and face.

:rubric=stomach
Acute gastritis, severe pain in epigastrium; 2[prolonged vomiting]. Uncontrollable appetite, notwithstanding irritable stomach. Gastralgia from nervous depression (Give five drops of 1x of Oil).

:rubric=skin
Smarting and burning. Intense erythema, worse, cold bathing; better, olive oil and cool air blowing on part.

:rubric=relations
:rubric2=compare
R[Spiraeea]. Gaultheria contains Arbutin. R[Salycyl] R[acid]. R[Methylium salicylicum] (an artificial Gaultheria oil for rheumatism, especially when the salicylates cannot be used. Pruritus and epididymitis, locally). After R[Cantharis] in burns.

