:rubric=generals
Chronic 2[rheumatism connected with heart lesions] and asthenopia offer a field for this remedy. Rheumatic nodes. Uric acid diathesis Whole body is 2[sore]. Gout and tophi.

:rubric=head
Tension, as if bound; better, sitting and going out. Externally sensitive. 2[Headache ceases while eating]. Trembling and throbbing. Pain in heart; extends to head. Dizzy states with ringing in ears. Both cheeks covered with dry, bran-like scales.

:rubric=eyes
Half vision; invisible right half. Photophobia. Pain over eyes. Dry lids. Eyes pain after reading.

:rubric=stomach
Acidity, nausea, 2[gnawing, relieved by eating] (2[Anacard]). Cannot endure slightest pressure of clothes (2[Laches]).

:rubric=urine
Tenesmus. Turbid urine, with mucus and red deposit. Pain in region of right kidney. Free and colorless. While urinating, pressure in heart. Cystitis, subacute and chronic.

:rubric=respiratory
Constriction of chest. Violent cough when lying down. Air feels cold when inspired. Pain in mammary glands, 2[which extend into the arms and fingers].

:rubric=heart
Rheumatic soreness in cardiac region. Sudden shock in heart. Throbbing, dull stitch in cardiac region. Pains in heart before menses, and associated with pains in bladder, and before urinating; better, after. Trembling and fluttering in heart, extending to back.

:rubric=urinary
Soreness of bladder; pain in right kidney and ureter. Turbid urine with mucus, scanty and dark, acrid; sandy deposit.

:rubric=extremities
Paralytic stiffness all over. Itching about joints. Rheumatic pains throughout shoulder-joint, arm, and fingers and small joints generally. Pain in hollow of foot, extending to knee. Swelling and tenderness of finger and toe joints; better, hot water. Nodular swellings in joints. Ankles pain when walking.

:rubric=skin
Scabby, tettery eruption on hands, head, and cheeks, preceded by red, raw skin. Dull stitch, ending in itching. 2[Barber's itch] (use high). Rough rash all over body, much loose epithelium, tough, dry, itchy skin.

:rubric=modalities
M[Worse], in morning, right side. M[Better], rising and moving about.

:rubric=relations
:rubric2=compare
R[Lyc], R[Am-p], R[Benz ac], R[Calc], R[Lithium chlor] (symptoms of cinchonism, viz.: R[Dizzy] head, full, R[bluring of vision] Ringing in ears; marked tremors; R[general weakness]; marked muscular and general prostration; no gastro-intestinal effects. Nose sore, heartburn, pain in teeth). R[Lithium lacticum] (rheumatism of shoulder, and small joints relieved by moving about; worse, resting). R[Lithium benzoicum] (deep-seated pains in loins; in small of back; uneasiness in bladder. Cystic irritation. Gallstones. Frequent desire. Diminishing uric acid deposit). R[Lithium bromatum] (cerebral congestion, threatened apoplexy, insomnia and epilepsy).

:rubric=dose
First to third trituration.

