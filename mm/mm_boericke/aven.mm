Has a selective action on brain and nervous system, favorably influencing their nutritive function.

Nervous exhaustion, sexual debility, and the morphine habit call for this remedy in rather material dosage. Best tonic for debility after exhausting diseases. Nerve tremors of the aged; chorea, paralysis agitans, epilepsy. Post-diphtheritic paralysis. Rheumatism of heart. 2[Colds]. Acute coryza (20 drop doses in hot water hourly for a few doses). Alcoholism. Sleeplessness, especially of alcoholics. Bad effects of 2[Morphine] habit. Nervous states of many female troubles.

