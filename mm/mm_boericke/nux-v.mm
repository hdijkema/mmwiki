S{Is the greatest of polychrests, because the bulk of its symptoms correspond in similarity with those of the commonest and most frequent of diseases.}
S{It is frequently the first remedy, indicated after much dosing, establishing a sort of equilibrium of forces and counteracting chronic effects.}
S{Nux is pre-eminently the remedy for many of the conditions incident to modern life.}
S{The typical Nux patient is rather thin, spare, quick, active, nervous, and irritable.}
S{He does a good deal of mental work; has mental strains and leads a sedentary life, found in prolonged office work, overstudy, and close application to business, with its cares and anxieties.}
S{This indoor life and mental strain seeks stimulants, coffee, wine, possibly in excess; or, again, he hopes to quiet his excitement, by indulging in the sedative effects of tobacco, if not really a victim, to the seductive drugs, like opium, etc.}
S{These things are associated with other indulgences; at table, he takes preferably rich and stimulating food; wine and women play their part to make him forget the close application of the day.}
S{Late hours are a consequence; a thick head, dyspepsia, and irritable temper are the next day's inheritance.}
S{Now he takes some cathartic, liver pills, or mineral water, and soon gets into the habit of taking these things, which still further complicate matters.}
S{Since these frailties are more yielded to by men than women.}
S{Nux is pre-eminently a male remedy.}
S{These conditions, produce an 2[irritable], nervous system, hypersensitive and over-impressionable, which Nux will do much to soothe and calm.}
S{Especially adapted to digestive disturbances, portal congestion, and hypochondrical states depending thereon.}
S{Convulsions, with consciousness; worse, touch, moving.}
S{2[Zealous fiery temperament].}
S{Nux patients are easily chilled, avoid open air, etc.}
S{Nux always seems to be out of tune; inharmonious spasmodic action.}

