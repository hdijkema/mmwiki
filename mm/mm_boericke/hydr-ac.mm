:rubric=generals
One of the most toxic agents known. Convulsions and paralysis express the action of this remedy. Spasmodic constriction in larynx, feeling of suffocation, pain and tightness in chest, palpitation; pulse weak, irregular. 2[Singing sensation at the epigastrium]. Hysterical and epileptic convulsions. Cyanosis. Collapse, due to some pulmonary condition not a cardiac collapse. Catalepsy. Cholera. Stage of collapse (2[Ars; Verat]). Coldness. Tetanus narcolepsy.

:rubric=mind
Unconscious. Wild delirium. Fear of imaginary troubles. 2[Fears] everything-horses, wagons, houses falling, etc.

:rubric=head
Violent stupefying headache. Brain feels on fire. Pupils motionless or dilated. Supra-orbital neuralgia, with flushing on same side of face.

:rubric=face
Jaws clenched in rigid spasm. Froths at mouth. Pale, bluish lips.

:rubric=stomach
Tongue cold. 2[Drink rumbles through throat and stomach]. Gastralgia; worse when stomach is empty. 2[Great sinking at pit of stomach]. Pulsative pain in praecordial region.

:rubric=respiratory
Noisy and agitated breathing. Dry, spasmodic, suffocative cough. Asthma, with contraction of throat. Whooping-cough. Paralysis of lungs (2[Aspidos]). Marked cyanosis; venously congested lung.

:rubric=heart
Violent palpitation. Pulse, 2[weak irregular]. Cold extremities. Torturing pain in chest. Angina pectoris (2[Spigel; Oxal ac]).

:rubric=sleep
Yawning with shivering. Irresistible drowsiness. Vivid, incoherent dreams.

