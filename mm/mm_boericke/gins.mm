:rubric=generals
Said to be a stimulant to the secretory glands, especially salivary. Acts on the lower part of the spinal cord. 2[Lumbago, sciatica, and rheumatism]. Paralytic weakness. 2[Hiccough]. Skin symptoms, itching pimples on neck and chest.

:rubric=head
Vertigo, with gray spots before eyes; semi-lateral headache; occipital; difficult opening of eyelids; objects appear double.

:rubric=throat
Tonsillitis, just like Bellad, but in dark-complexioned people.

:rubric=abdomen
Tense, painful, rumbling. Pain in right side. Loud gurgling in ileo-caecal region. Perityphlitis.

:rubric=male
Rheumatic pains after frequent emissions. Weakness of genital organs. Voluptuous tickling at end of urethra. Sexual excitement. Pressure in testicles.

:rubric=extremities
Hands feel swollen. Skin feels tight. Contraction. Coldness in back and spine. Bruised pain in small of back and thighs; nightly digging in right lower limb to toes. Burning heat in tips of fingers. Eruption on upper inner thighs. 2[Stiff, contracted joints], heaviness of lower limbs. Crackling in joints. Stiffness in back.

