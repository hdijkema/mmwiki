:rubric=generals
Has a pronounced action on the inferior maxillary branch of the fifth pair of the cranial nerves; on the ear; and respiratory tract and bladder. 2[Catarrhs], and colds, 2[with periodical prosopalgia]. Quiets nervous, and bronchial, and urinary irritation, and cough.

:rubric=face
Neuralgia affecting zygoma, temporo maxillary joint, and ear (2[Menyanth]), particularly of left side, with lachrymation, coryza, and sensation 2[as if parts were crushed with tongs]. Talking, sneezing, and change of temperature aggravate the pains; also, pressing teeth together. Pains seem to come in flashes, excited by least movement, occurring periodically at same hour in morning and afternoon each day.

:rubric=ears
Otalgia, with a sense of obstruction. Deafness. Dry, scaly condition of meatus (use locally).

:rubric=abdomen
Pain extends deep down, causing contraction of sphincter ani.

:rubric=rectum
Many movements a day, with twisting about navel. Haemorrhoids, with obstructed, hardened stool. Inflamed and painful piles.

:rubric=respiratory
2[Hoarse]; voice deep, harsh; sounds like a trumpet; "basso profundo". Cough; worse at night. Asthma. Soreness in pharynx, cough during sleep.

:rubric=urinary
Constant dribbling. 2[Enuresis]. Burning urination. Increase with pressure in bladder.

:rubric=extremities
Cramp-like pain in soles, right foot, and knee. Lower extremities feel heavy. Thumb feels numb. Neuralgic pain in left ankle. Stiffness 2[and soreness of joints of lower extremities].

:rubric=modalities
M[Worse], change of temperature, talking, sneezing, biting hard (inferior dental nerve); from 9 am to 4 pm.

:rubric=relations
:rubric2=compare
R[Rhus arom], R[Caust], R[Platin], R[Sphingurus] (pain in zygoma).

:rubric=dose
2[Mullein oil], (locally, for earache and dry, scaly condition of meatus. Also for teasing cough at night or lying down. Internally, tincture and lower potencies). 2[Enuresis], five-drop doses night and morning.

