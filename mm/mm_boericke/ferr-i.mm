:rubric=abdomen
Fullness, even after a little food; stuffed feeling, as if she could not lean forward.

:rubric=throat
Sore, as if of a splinter, shooting in different directions. Hoarse.

:rubric=respiratory
Coryza; discharge of mucus from nose, trachea, and larynx. Pressure beneath sternum. Scrofulous swelling of nose. Chest feels oppressed. Haemoptysis.

:rubric=urinary
Urine dark. Sweet smelling. 2[Crawling sensation in urethra and rectum]. Sensation as if urine were stopped at fossa navicularis. Difficulty in retaining urine. Incontinence in anaemic children.

:rubric=female
On sitting, 2[feeling as if something pressed upward in vagina]. Much bearing down. Retroversion and prolapse of uterus. Leucorrhoea like boiled starch. Menses suppressed or scanty. Itching and soreness of vulva and vagina.

:rubric=dose
Third trituration. Does not keep long.

