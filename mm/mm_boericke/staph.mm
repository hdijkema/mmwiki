:rubric=generals
Nervous affections with marked irritability, diseases of the genito-urinary tract and skin, most frequently give symptoms calling for this drug. Acts on teeth and alveolar periosteum. Ill effects of anger and insults. 2[Sexual sins and excesses. Very sensitive]. Lacerated tissues. Pain and nervousness after extraction of teeth. Sphincters lacerated or stretched.

:rubric=mind
Impetuous, 2[violent outbursts of passion], hypochondriacal, sad. 2[Very sensitive] as to what others say about her. Dwells on sexual matters; prefers solitude. Peevish. Child cries for many things, and refuses them when offered.

:rubric=head
Stupefying headache; passes off with yawning. Brain feels squeezed. Sensation of a ball of lead in forehead. Itching eruption above and behind ears (2[Oleand]).

:rubric=eyes
Heat in eyeballs, dims spectacles. 2[Recurrent styes]. 2[Chalazae] (2[Platanus]). Eyes sunken, with blue rings. Margin of lids itch. Affections of angles of eye, particularly the inner. Lacerated or incised wounds of cornea. Bursting pain in eyeballs of syphilitic iritis.

:rubric=throat
2[Stitches flying to the ear on swallowing, especially left].

:rubric=mouth
Toothache during menses. 2[Teeth black and crumbling]. Salivation, spongy gums, bleed easily (2[Merc; Kreos]). Submaxillary glands swollen. After eating feels sleepy pyorrhea (2[Plantago])

:rubric=stomach
Flabby and weak. Desire for stimulants. Stomach feels relaxed. 2[Craving for tobacco]. Canine hunger, even when stomach is full. Nausea after abdominal operations.

:rubric=abdomen
Colic after anger. Hot flatus. Swollen abdomen in children, with much flatus. Colic, with pelvic tenesmus. 2[Severe pain following an abdominal operation]. Incarcerated flatus. Diarrhoea after drinking cold water, with tenesmus. 2[Constipation] (2 drops tincture night and morning), haemorrhoids, with enlarged prostate.

:rubric=male
Especially after self-abuse; persistent dwelling on sexual subjects. Spermatorrhoea, with sunken features; guilty look; emissions, with backache and weakness and sexual neurasthenia. Dyspnoea after coition.

:rubric=female
2[Parts very sensitive], worse sitting down (2[Berb; Kreos]). 2[Irritable bladder in young married women]. Leucorrhoea. Prolapsus, with sinking in the abdomen; aching around the hips.

:rubric=urinary
2[Cystocele] (locally and internally). Cystitis in lying-in patients. Ineffectual urging to urinate in 2[newly married] women. Pressure upon bladder; feels as if it did not empty. 2[Sensation as if a drop of urine were rolling continuously along the channel]. Burning in urethra during micturition. Prostatic troubles; frequent urination, burning in urethra 2[when not urinating] (2[Thuja; Sabal; Ferr pic]). Urging and pain 2[after] urinating. Pain after lithotomy.

:rubric=skin
Eczema of head, ears, face, and body; thick scabs, dry, and itch violently; 2[scratching changes location of itching]. Fig-warts pedunculated (2[Thuja]). Arthritic nodes. Inflammation of phalanges. Night-sweats.

:rubric=extremities
Muscles, especially of calves, feel bruised. 2[Backache; worse in morning before rising]. Extremities feel beaten and painful. Joints stiff. 2[Crural neuralgia]. Dull aching of nates extending to hip-joint and small of back.

:rubric=modalities
M[Worse], anger, indignation, grief, mortification, loss of fluids, onanism, sexual excesses, tobacco; least touch on affected parts. M[Better], after breakfast, warmth, rest at night.

