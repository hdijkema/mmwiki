:rubric=generals
A powerful tissue remedy for hard, stony glands, varicose and enlarged veins, and malnutrition of bones. Hard knots in female breast. Goitre. Congenital hereditary syphilis. 2[Induration threatening suppuration]. Many cases of cataract have undoubtedly been influenced favorably by it. Congenital syphilis manifesting itself in ulcerating of mouth and throat, caries and necrosis with boring pains and heat in parts. Arterio-sclerosis; threatened apoplexy. Tuberculosis. Used after operations, the tendency to adhesions is reduced.

:rubric=mind
Great depression; groundless fears of financial ruin.

:rubric=head
Creaking noise in head. Blood-tumors of new-born infants. Hard excrescences on the scalp. Ulcers on the scalp with callous, hard edges.

:rubric=eyes
Flickering and sparks before the eyes, spots on the cornea; conjunctivitis; cataract. 2[Strumous phlyctemular keratitis. Subcutaneous palpebral cysts].

:rubric=ears
Calcareous deposits on tympanum; sclerosis of ossicula and petrous portion of temporal bone, with deafness, ringing and roaring. 2[Chronic suppuration of middle ear].

:rubric=nose
Cold in the head; stuffy cold; dry coryza; ozaena. Copious, offensive, thick, greenish, lumpy, yellow nasal catarrh. Atrophic rhinitis, especially if crusts are prominent.

:rubric=face
Hard swelling on the cheek, with pain or toothache, hard swelling on jaw-bone.

:rubric=mouth
Gum-boil, with hard swelling on the jaw. Cracked appearance of the tongue, with or without pain. Induration of the tongue, hardening after inflammation. Unnatural looseness of the teeth, with or without pain; teeth become loose in their sockets. Toothache, with pain if any food touches the tooth.

:rubric=throat
Follicular sore throat; plugs of mucus are continually forming in the crypts of the tonsils. Pain and burning in throat; better by warm drinks; worse, cold drinks. Hypertrophy of Luschka's tonsil. Relaxed uvula, tickling referred to larynx.

:rubric=stomach
Vomiting of infants. Vomiting of undigested food. Hiccough (2[Cajup; Sulph ac]). Flatulency. Weakness and daintiness of appetite, nausea and distress after eating in young children who are overtaxed by studies. 2[Acute indigestion from fatigue and brain-fag]; much flatulence.

Stool and Anus. - Diarrhoea in gouty subjects. Itching of anus. Fissure of the anus, and intensely sore crack near the lower end of the bowel. Bleeding haemorrhoids. Itching of anus as from pin-worms. Internal or blind piles frequently, with pain in back, generally far down on the sacrum, and constipation. Much wind in lower bowels. Worse, pregnancy.

:rubric=male
Hydrocele; indurations of the testicles.

Respiratory Organs. - Hoarseness. 2[Croup]. Cough with expectoration of tiny lumps of yellow mucus, with tickling sensation and irritation on lying down. Spasmodic cough. 2[Calc. Fluor] removes fibroid deposits about the endocardium and restores normal endocardial structure (Eli G. Jones, M. D).

Circulatory Organs. - Chief remedy for vascular tumors with dilated blood-vessels, and for 2[varicose or enlarged veins]. Aneurism. Valvular disease. When the tuberculous toxins attack the heart and blood-vessels.

Neck and Back. - Chronic 2[lumbago]; aggravated on beginning to move, and ameliorated on continued motion. Osseous tumors. 2[Rachitic enlargement of femur in infants]. Pain lower part of back, with burning.

:rubric=extremities
Ganglia or encysted tumors at the back of the wrist. Gouty enlargements of the joints of the fingers. Exostoses on fingers. Chronic synovitis of knee-joint.

:rubric=sleep
Vivid dreams, with sense of impending danger. Unrefreshing sleep.

:rubric=skin
Marked whiteness of skin. Scar tissue; adhesions after operations. Chaps and cracks. Fissures or cracks in the palms of the hands, or hard skin. Fissure of the anus. Suppurations with callous, hard edges. Whitlow. Indolent, fistulous ulcers, secreting thick, yellow pus. Hard, elevated edges of ulcer, surrounding skin purple and swollen. Knots, kernels, hardened glands in the female breast. 2[Swellings or indurated enlargements] having their seat in the 2[fasciae] and capsular ligaments of joints, or in the tendons. 2[Indurations of stony hardness].

