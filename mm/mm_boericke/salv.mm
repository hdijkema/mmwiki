:rubric=generals
Controls excessive sweating when circulation is enfeebled; of less use in phthisis 2[with night-sweats] and suffocating tickling cough. Galactorrhoea. Exerts a tonic influence on the skin.

:rubric=respiratory
Tickling cough, especially in consumption.

:rubric=skin
Soft, relaxed, with enfeebled circulation and cold extremities. 2[Colliquative perspiration].

:rubric=relations
:rubric2=compare
R[Chrysanhemum Leucanthemum] - Ox-eye Daisy. Has specific action on sudoriparous glands. Quiets nervous system like R[Cypripedium]. Right sided tearing pain in bones of jaw and temple. Pain in teeth and gums, worse touch, better warmth. Irritable and tearful. Here use 12x. R[Insomnia and night-sweats]. For colliquative sweating and hyperaesthesia of nervous system. Material doses of tincture. R[Phelland], R[Tub], R[Salvia sclerata] (tonic influence on nervous system; dose, teaspoonful to one pint hot water, as inhalent for sponging). R[Rubia tinctorum]-Madder-A remedy for the spleen (R[Ceanothus]). Chlorosis and amenorrhoea; tuberculosis. Anaemia; undernourished conditions; splenic anaemia. Dose, 10 drops of tincture.

:rubric=dose
Tincture, in twenty-drop doses, in a little water. The effects manifest themselves quickly two hours after taking a dose, and they persist for from two to six days.

