:local-name=Coral-snake

:rubric=generals
Similar to snake-poisons generally. Has very marked 2[black discharges. Cold things disagree]. Desire for sweetened buttermilk. Nausea and vomiting. Prostrating diarrhoea of consumption. Acidity of stomach, with faint feeling. Sudden pain in stomach. Spasm of oesophagus; pharynx constricted; food and liquids suddenly arrested, and then fall heavily into stomach. Spasms followed by paresis. 2[Cold feeling in stomach]. Fruits and ice-water lie very cold. 2[Right-sided paralysis]. Must have oscillatory motion. Rheumatic constitutions. Ear, nose and throat symptoms important.

:rubric=mind
Depressed; imagines he hears someone talking; dreads to be left alone. 2[Fear of rain]. Can speak, but cannot understand speech. Fears apoplexy.

:rubric=head
Violent headache, extending from forehead 2[to occiput]; first one eye, then the other. Pain in ears. Vertigo with tendency to fall forward. Weight and pain in forehead. Fullness in head.

:rubric=eyes
Aversion to light; letters run together when reading. Veil before eyes. Burning in lids. Bloated around the eyes in the morning. Large red fiery spot before eyes.

:rubric=ears
Cerumen 2[black] and hard, with difficult hearing, or serous 2[greenish] discharge, offensive; buzzing, and illusion of hearing. Sudden attack of nightly deafness, with roaring and crackling in ears, cracking in ears on swallowing. Intolerable itching in ear.

:rubric=nose
2[Chronic nasal catarrh, with fetid odor and greenish crusts]. Ozaena; yellowish-green discharge. Mucous membrane wrinkled; nostrils plugged up with dry mucus. Pains from nose to ears on swallowing. 2[Nostrils stopped up]. Nasal bleeding. Pain at root of nose. Eruption about nose.

:rubric=throat
Thick, very offensive, dry, greenish-yellow crusts upon the posterior pharyngeal wall and extremely foul breath. Spasmodic contraction of 2[oesophagus]; passage of fluids arrested.

:rubric=chest
2[Coldness in chest after drinking]. Haemorrhage from lungs black as ink and watery; stitches 2[in apex of right lung]. Fainting caused by stooping. Oppression in going upstairs. Peeling off of skin from palms and fingers. 2[Cough], with terrible pain through lungs. Worse right and expectoration of black blood. Sensation of a sponge in oesophagus.

:rubric=stomach
2[Feels cold]. Sensation as if food turned like a corkscrew on swallowing; desire for sweetened buttermilk. Acidity after every mouthful.

:rubric=female
Dysmenorrhoea, with black blood. Discharge of black blood between menses. Itching of vulva and vagina.

:rubric=sleep
Dreams about dead persons.

:rubric=skin
Glands and skin of axillae affected; itching with tetter. Tips of fingers peel off. Itching eruption in axillae.

:rubric=extremities
Icy cold feet. Vesicular eruptions on feet. Arms and hands swollen bluish. Knee-joints feel sprained. Pricking under the nails.

:rubric=fever
Cold perspiration all over. Typhoid when ulcers have eaten into tissues, and black blood is discharged.

:rubric=modalities
M[Worse] eating fruit; cold drinks; 2[wet weather].

