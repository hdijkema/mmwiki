:rubric=generals
The great drug for general sclerotic conditions. Lead paralysis is chiefly of extensors, forearm or upper limb, from center to periphery with partial anaesthesia or excessive hyperasthesia, preceded by pain. Localized neuralgic pains, neuritis. The blood, alimentary and nervous systems are the special seats of action of Plumbum. Hematosis is interfered with, rapid reduction in number of red corpuscles; hence pallor, icterus, anaemia. Constrictive sensation in internal organs.

Delirium, coma and convulsions. Hypertension and arteriosclerosis. 2[Progressive muscular atrophy]. Infantile paralysis. Locomotor ataxia. Excessive and rapid emaciation. Bulbar paralysis. Important in peripheral affections. The points of attack for Plumbum are the neuraxons and the anterior horns. Symptoms of multiple sclerosis, posterior spinal sclerosis. Contractions and boring pain. All the symptoms of acute. Nephritis with amaurosis and cerebral symptoms. 2[Gout] (Chronic).

:rubric=mind
2[Mental depression. Fear of being assassinated]. Quiet melancholy. Slow perception; loss of memory; amnesic aphasia. Hallucinations and delusions. Intellectual apathy. Memory impaired (2[Anac; Baryta]). Paretic dementia.

:rubric=head
Delirium alternating with colic. Pain as if a ball rose from throat to brain. Hair very dry. 2[Tinnitus] (2[Chin; Nat-salic; Carbon sulph]).

:rubric=eyes
Pupils contracted. Yellow. Optic nerve inflamed. Intraocular, suppurative inflammation. 2[Glaucoma], especially if secondary to spinal lesion. Optic neuritis, central scotoma. Sudden loss of sight after fainting.

:rubric=face
2[Pale and cachetic]. Yellow, corpse-like; cheeks sunken. Skin of face greasy, shiny. Tremor of naso-labial muscles.

:rubric=mouth
Gums swollen, pale; 2[distinct blue lines along margins of gums]. Tongue tremulous, red on margin. Cannot put it out, seems paralyzed.

:rubric=stomach
Contraction in oesophagus and stomach; pressure and tightness. 2[Gastralgia]. Constant vomiting. Solids cannot be swallowed.

:rubric=abdomen
Excessive colic, 2[radiating to all parts of body. Abdominal wall feels drawn by a string to spine]. Pain causes desire to stretch. Intussusception; strangulated hernia. 2[Abdomen retracted]. Obstructed flatus, with intense colic. Colic alternates with delirium and pain in atrophied limbs.

:rubric=rectum
Constipation; 2[stools hard, lumpy, black with urging and spasm of anus]. Obstructed evacuation from impaction of feces (2[Plat]). Neuralgia of rectum. 2[Anus drawn up with constriction].

:rubric=urinary
Frequent, ineffectual tenesmus. Albuminous; low specific gravity. 2[Chronic interstitial nephritis], with great pain in abdomen. Urine scanty. Tenesmus of bladder. Emission drop by drop.

:rubric=male
2[Loss of sexual power]. Testicles drawn up, feel constricted.

:rubric=female
2[Vaginismus], with emaciation and constipation. 2[Induration of mammary glands]. Vulva and vagina hypersensitive. Stitches and burning pains in breasts (2[Apis; Con; Carb an; Sil]). Tendency to abortion. Menorrhagia with sensation of string pulling from abdomen to back. Disposition to yawn and stretch.

:rubric=heart
Cardiac weakness. Pulse soft and small, dichrotic. Wiry pulse, camp-like constriction of peripheral arteries.

:rubric=back
Spinal cord sclerosed. Lightning-like pains; temporarily better by pressure. Paralysis of lower extremities.

:rubric=skin
Yellow, dark-brown liver spots. Jaundice. Dry. Dilated veins of forearms and legs.

:rubric=extremities
Paralysis of single muscles. Cannot raise or lift anything with the hand. Extension is difficult. Paralysis from overexertion of the extensor muscles in piano players (2[Curare]). Pains in muscles of thighs; 2[come in paroxysms]. 2[Wrist-drop]. Cramps in calves. Stinging and tearing in limbs, also twitching and tingling, numbness, pain or tremor. Paralysis. Feet swollen. Pain in atrophied limbs alternates with colic. Loss of patellar reflex. Hands and feet cold. Pain in 2[right big toe] at night, very sensitive to touch.

:rubric=modalities
M[Worse], at night, motion. M[Better], rubbing, hard pressure, physical exertion (2[Alumen]).

