:rubric=generals
Spasmodic affections, cramps</span>, convulsions, beginning in fingers and toes, violent, contractive, and intermittent pain, are some of the more marked expressions of the action of Cuprum; and its curative range therefore includes tonic and clonic spasms, convulsions, and epileptic attacks. Chorea brought on by fright. Nausea greater than in any other remedy. In epilepsy, aura begins at knees, ascends to hypogastrium; then unconsciousness, foaming, and falling. Symptoms disposed to appear periodically and in groups. Complaints begin in left side (2[Laches]). Tape worm (colloidal Cuprum 3x).

Where eruptions trike in, as in scarlet fever, complaints may result, such as excessive vomiting, stupor, convulsions, which come within the sphere of this remedy. The pains are increased by movement and touch.

:rubric=head
Fixed ideas, malicious and morose. Uses words not intended. Fearful. Empty feeling. Purple, red swelling of head, with convulsions. Bruised pain in brain and eyes on turning them. Meningitis. Sensation as if water were poured over head. Giddiness accompanies many ailments, head falls forward on chest.

:rubric=eyes
Aching over eyes. Fixed, stary, sunken, glistening, turned upward. Crossed. Quick rolling of eyeballs, with closed eyes.

:rubric=face
Distorted, pale 2[bluish], with blue lips. 2[Contraction of jaws], with foam at mouth.

:rubric=nose
Sensation of violent congestion of blood to nose (2[Melilot]).

:rubric=mouth
2[Strong metallic, slimy taste], with flow of saliva. Constant protrusion and retraction of the tongue, like a snake (2[Lach]). Paralysis of tongue. Stammering speech.

:rubric=stomach
Hiccough preceding the spasms. 2[Nausea]. Vomiting, relieved by drinking cold water; with colic, diarrhoea, spasms. Strong metallic taste (R[Rhus-t]). 2[When drinking, the fluid descends with gurgling sound] (2[Laur]). Craves cool drink.

:rubric=abdomen
Tense, hot and tender to touch; 2[contracted]. Neuralgia of abdominal viscera. 2[Colic], violent and intermittent. Intussusception.

:rubric=stool
Black, painful, bloody, with tenesmus and weakness. Cholera; with cramps in abdomen and calves.

:rubric=female
Menses too late, protracted. Cramps, extending into chest, before, during, or after suppression of menses. Also, from suppressed foot sweats (2[Sil]). Ebullition of blood; palpitation. Chlorosis. 2[After-pains].

:rubric=heart
Angina pectoris. Slow pulse; or hard, full and quick. Palpitation, praecordial anxiety and pain. Fatty degeneration (2[Phytol]).

:rubric=respiratory
Cough as a gurgling sound, better by drinking cold water. Suffocative attacks, worse 3 am (2[Am c]). 2[Spasm and constriction] of chest; spasmodic asthma, alternating with spasmodic vomiting. Whooping-cough, better, swallow water, with vomiting and spasms and purple face. Spasm of the glottis. Dyspnoea with epigastric uneasiness. Spasmodic dyspnoea before menstruation. Angina with asthmatic symptoms and cramps (Clarke).

:rubric=extremities
Jerking, twitching of muscles. Coldness of hands. Cramps in palms. Great weariness of limbs. 2[Cramps in calves and soles]. Epilepsy; auro begins in knees. Clenched thumbs. Clonic spasms, beginning in fingers and toes.

:rubric=skin
2[Bluish], marbled. Ulcers, itching spots, and pimples at the folds of joints. Chronic psoriasis and lepra (Hughes).

:rubric=sleep
Profound, with shocks in body. During sleep constant rumbling in abdomen.

:rubric=modalities
M[Worse], before menses; from vomiting, contact. M[Better], during perspiration, drinking cold water.

