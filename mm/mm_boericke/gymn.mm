Sore throat, dark livid redness of fauces, and erysipelatous swelling of face are most marked. Hives. Desire for heat and quiet. Headache, throbbing in forehead and temples and over eyes, with 2[bluish-white coating of tongue]. Burning in eyes.

:rubric=face
Sensation as of flies crawling over face. Erysipelas. Great sensibility of teeth.

:rubric=throat
Sore; dark livid redness of fauces and tonsils. Sticking pain. Mucus in throat and hawking. Tickling, with dry cough.

