:rubric=generals
Eucalyptus is a powerful antiseptic and destructive to low forms of life, a stimulating expectorant and an efficient diaphoretic. Atonic dyspepsia, gastric and intestinal catarrh. A remedy with marked effects on catarrhal processes, malaria, and intestinal disturbance. 2[Influenza]. Fevers of a 2[relapsing] character. Produces diuresis and great increase of urea. Haemorrhages internally and locally (2[Hamam]). 2[Typhoid]. Symptoms of exhaustion and toxaemia. Conditions of the mucous surfaces of the air passages, genito-urinary organs and gastro-intestinal tract. A gastro-intestinal irritant with pain in stomach and upper intestines several hours after eating.

:rubric=head
Exhilaration. Desire for exercise. Dull congestive headache. 2[Coryza]; sore throat. Eyes smart and burn.

:rubric=nose
2[Stuffed-up sensation]; thin, watery coryza; nose does not stop running; tightness across bridge. 2[Chronic catarrhal, purulent and fetid discharge]. Ethmoid and frontal sinus involved.

:rubric=throat
Relaxed, aphthous condition of mouth and throat. Excessive secretion of saliva. Burns, feels full. Constant sensation of phlegm in throat. Enlarged, ulcerated tonsils and inflamed throat (Use tincture locally).

:rubric=stomach
2[Slow digestion]. Much fetid gas. Beating and goneness with pulsation in epigastric arteries. Spleen hard and contracted. Pain in epigastrium and upper abdomen ameliorated by food. Malignant disease of stomach with vomiting of blood and sour fluid.

:rubric=abdomen
2[Acute diarrhoea]. Aching pains in bowels with feeling of impending diarrhoea. 2[Dysentery], with rectal heat; tenesmus; haemorrhage. Diarrhoea; stools thin, watery, preceded by sharp pains. Typhoid diarrhoea.

:rubric=urinary
2[Acute nephritis complicating influenza]. Haematuria. Suppurative inflammation of kidneys. Urine contains pus and is deficient in urea. Bladder feels loss of expulsive force. Burning and tenesmus; 2[catarrh of bladder]; diuresis; 2[urethral caruncle]. Spasmodic stricture; gonorrhoea.

:rubric=respiratory
2[Asthma], with great dyspnoea and palpitation. Moist asthma. Expectoration white, thick mucus. Bronchitis in the aged. Bronchorrhoea (2[Bals. Peru]). Profuse expectoration of offensive muco-pus. Irritative cough. Whooping-cough in rachitic children. Fetid form of bronchitis, bronchial dilatation and emphysema.

:rubric=female
Leucorrhoea, acrid, fetid. Ulcer around orifice of urethra.

:rubric=extremities
Rheumatic pains; worse at night, walking or carrying anything. Stiff, weary sensation. 2[Pricking sensation, followed by painful aching]. Nodular swellings over metacarpal and metatarsal joints.

:rubric=skin
Glandular enlargements and nodular swelling over joints. Foul and indolent ulcers. Herpetic eruptions.

:rubric=fever
Elevation of temperature. Continued and typhoid fevers. Scarlet fever (protective and curative). Discharges show a tendency to foulness, high temperature, accelerated but not strong pulse. Use the tincture.

:rubric=relations
:rubric2=compare
Oil of R[Eucalyptus]. - (Produces remarkable bodily exhaustion, no desire for any motion, unable to do any real mental work, study, etc. The volatile oil possesses, in common with other terpenes, the property of converting water, in presence of air and sunlight, into hydrogen peroxide, or to convert oxygen into ozone, which is the explanation usually given of its deodorizing and antiseptic properties (Merrel). Locally, in catarrhal affections, especially when of a suppurating or putrid nature). R[Eucalyptus tereticoris] (menstrual cough and prostration). R[Eucalyptol] (depresses temperature of healthy body more than R[Quinine]; acts on kidneys like R[Ter]); R[Anacard], R[Hydrast], R[Kali sulph. Eucalyptus] neutralizes ill effects of R[Strychnin]. R[Angophora]-Red Gum - (dysentery, pains, tenesmus; better lying flat on face; obstinate constipation). R[Eucalyptus rostrata], R[Kino].

:rubric=dose
Tincture in one to 20 drop doses, and lower potencies. Also Oil of Eucalyptus in five-drop doses.

