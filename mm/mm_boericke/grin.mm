:rubric=generals
Both Grindelia robusta and Grindelia squarrosa have been used for the symptoms here recorded. There is practically no difference in their action, although the G. Squarrosa is credited with more splenic symptoms, dull pains and fullness in left hypochondrium; chronic malaria; gastric pains associated with 2[splenic] congestion. Induces paralysis, beginning in extremities Its action is shown on the heart first quickening, then retarding it.

Acts on the cardio-pulmonary distribution of the pneumo-gastric in dry catarrh (Tart Emetic in muco-purulent). Produces a paresis of the pneumo-gastric, interfering with respiration. 2[Smothering after falling asleep]. Asthmatic conditions, chronic bronchitis. Bronchorrhoea with tough mucus, difficult to detach. Raises the blood pressure. Nausea and retching of gastric ulcer. Sugar in urine. An effective antidote to Rhus-poisoning, locally and internally; also for burns, blisters, vaginal catarrh and herpes zoster. Hyperchlorhydria when attended with asthmatic and other neurotic symptoms. Hyperaemia of gastric mucous membrane with difficult respiration.

:rubric=head
Feels full, as from quinine. Pain in 2[eyeballs], running back to brain; worse, moving eyes. Pupils dilated. Purulent ophthalmia and iritis.

:rubric=respiratory
An efficacious remedy for wheezing and oppression in bronchitic patients. The sibilant rales are disseminated with foamy mucus, very difficult to detach. Acts on the pulmonary circulation. Asthma, with profuse tenacious expectoration, which relieves. 2[Stops breathing when falling asleep]; wakes 2[with a star], and gasps for breath. Must sit up to breathe. 2[Cannot breathe when lying down]. Pertussis, with profuse mucous secretion (2[Coccus]). Bronchorrhoea, with tough, whitish, mucous expectoration. Sibilant rales. Weak heart and respiration. Cannot breathe lying down. Cheyne-Stokes respiration.

:rubric=spleen
Cutting pain in region of spleen, extending to hips. Spleen enlarged (2[Ceanoth; Carduus]).

:rubric=skin
Rash, like roseola, with severe burning and itching. Vesicular and papular eruptions. Herpes zoster. Itching and burning. 2[Poison oak] (locally as a wash). Ulcers, with swollen, purplish skin.

