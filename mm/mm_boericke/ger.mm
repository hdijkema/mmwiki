:rubric=generals
Habitual sick headaches. 2[Profuse, haemorrhages], pulmonary and from different organs. Vomiting of blood. 2[Ulceration of stomach]. 2[Atonic and foul ulcers]. Summer complaint.

:rubric=head
Giddiness, with diplopia; better, closing eyes. Ptosis and dilated pupils. Sick headache.

:rubric=mouth
Dry; tip of tongue burning. Pharyngitis.

:rubric=stomach
Catarrhal gastritis with profuse secretion, tendency to ulceration and passive haemorrhage. 2[Lessens the vomiting in gastric ulcer].

:rubric=stool
Constant desire to go to stool, with inability to pass anything for some time. Chronic diarrhoea, with offensive mucus. Constipation.

:rubric=female
Menses too profuse. Post-partum haemorrhage. Sore nipples (2[Eup arom]).

:rubric=relations
:rubric2=compare
R[Geranin] 1x. Constant hawking and spitting in elderly people. R[Erodium]-Hemlock-Stork's bill - (a popular haemostatic in Russia, and especially used for metrorrhagia and menorrhagia); R[Hydrastinin], R[Cinch], R[Sabin].

:rubric=dose
Tincture, half-dram doses in gastric ulcer. Tincture, to third attenuation, as a general rule. Locally, in ulcers, it will destroy the pyogenic membrane.

