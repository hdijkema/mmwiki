:local-name=Red Starfish

:rubric=generals
A remedy for the sycotic diathesis; flabby, lymphatic constitution, flabby with red face. Lancinating pains. Nervous disturbances, neuralgia, chorea, and hysteria come within the range of this remedy. Has been used for cancer of the breast, and has an unquestioned influence over cancer disease. Excitement in both sexes.

:rubric=head
Cannot bear contradiction. Shocks in brain; throbbing; 2[heat in head, as if surrounded by hot air].

:rubric=face
Red. Pimples on side of nose, chin and mouth. 2[Disposition to pimples at adolescence].

:rubric=female
Colic and other sufferings cease with appearance of flow. Breasts swell and pain in breasts; worse left. Ulceration with sharp pains, piercing to scapulae. Pains down left arm to fingers, worse motion. Excitement of sexual instinct with nervous agitation.

Nodes and indurations of mammary gland, dull aching, neuralgic pain in this region (2[Conium]).

:rubric=chest
Breasts swollen, indurated. Neuralgia of left breast and arm (2[Brom]). Pain under sternum and in muscles of praecordial region. 2[Left breast feels as if pulled inward], and pain extends over inner arm to end of little finger. Numbness of hand and fingers of left side. 2[Cancer mammae even in ulcerative stage. Acute, lancinating pain. Axillary glands swollen hard and knotted].

Nervous System. - Gait unsteady; muscles refuse to obey the will. Epilepsy; preceded by twitching over whole body.

:rubric=stool
Constipation. Ineffectual desire. Stool like olives. Diarrhoea, watery brown, gushing out in jet.

:rubric=skin
Destitute of pliability and elasticity. Itching spots. Ulcers, with fetid ichor. Acne. Psoriasis and herpes zoster worse left arm and chest. Enlarged axillary glands, worse, at night and in damp weather.

