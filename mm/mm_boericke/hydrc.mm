:rubric=generals
Curative in disorders that exhibit interstitial inflammation and cellular proliferation in any part. Hypertrophy and induration of connective tissue. Has considerable reputation in 2[leprosy] and 2[lupus], when there is no ulceration. The skin symptoms are very important. Of great use in ulceration of womb. Difficulty in maintaining the upright posture. Very copious perspiration. Pains of cervical cancer.

:rubric=face
Pain in left cheek-bones and about orbits.

:rubric=female
Pruritus of vagina. Inflammation of neck of bladder. 2[Heat within vagina]. 2[Granular ulceration of womb]. Profuse leucorrhoea. Dull pain in ovarian region. Cervical redness.

:rubric=skin
Dry eruptions. 2[Great thickening of epidermoid layer and exfoliation of scales]. Psoriasis 2[gyrate], on trunk and extremities, palms and soles. Pustules on chest. Circular spots, with scaly edges. 2[Intolerable itching, especially of soles]. Profuse sweat. Syphilitic affections. 2[Acne]. Leprosy. 2[Elephantiasis] (2[Ars]). Lupus non-exedens.

