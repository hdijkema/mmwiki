2[R[Mag-p].] is best known for its spasmodic conditions and neuralgias.



:rubric=The pains

2[] are very violent and may affect any nerve. A pain localizes itself in a nerve and becomes worse and worse, sometimes coming in paroxysms, but becoming so violent that the patient becomes frantic.

The pains are 2[ameliorated by heat and pressure.] The patient feels better in a warm place; and his neuralgias are also better, he is miserable, and his pains are brought on when he becomes cold or is in a cold place. Pains are brought on from riding in the cold, and in cold, damp weather. Exposure for a long time to cold winds causes neuralgia of the face,

The pains are felt everywhere. Pain in the bowels, enteralgias, cramps in the stomach and bowels, with the same modalities. Pains in the spinal cord under the same rule - amelioration from heat. There are times when a nerve, in which there is considerable pain, becomes sensitive to pressure, becomes sore. The spinal cord becomes sore.

Convulsions, with stiffness of the limbs. Convulsions in adults or children, followed by extreme sensitiveness to touch, to wind, to noise, to excitement, to everything. Such convulsions, as children have during dentition. Colic; three months'colic, cramps, bilious colic.

But the special feature is its power to debilitate, to cause irritation of the nerves and muscles. Cramps from prolonged exertion. Stiffness numbness, awkwardness and deadness of a nerve from prolonged exertion.



:rubric=Fingers

Thus it applies to long use of the hands and fingers in writing, and gives a fair sample of writer's cramp. It is especially useful in the cramps that come in the fingers, from writing, playing instruments and piano practice.

Pianists suddenly break down, with stiffness of the fingers, after several hours' labor every day for years. The fingers give out. In playing the harp a cramp comes on and the fingers cannot perform their use. Other parts are affected in the same way from prolonged exertion. A laborer's hand will sometimes cramp and become almost useless. As soon as he undertakes to do that particular thing his hand cramps and he clutches the implement or loses hold. The carpenter after prolonged use of a tool has a cramp. This is a strong feature of the remedy in all sorts of over-exertion.

Violent cramps in dysentery and cholera morbus, that make him scream out. Twitching of the muscles all over the body, as in cholera. It was 2[Schüessler's] main remedy for chorea, but we can only use it by its proving. 2[Schüessler] prescribed it in all nervous conditions, but its proving justifies its use in neuralgia ameliorated by heat and pressure, cramps and twitchings.

Shooting pains along the nerves, but these are not so common as violent pains in paroxysms - a tearing pain as if the nerve were inflamed and put on a stretch. Shaking as in paralysis agitans and complaints resembling it. Amelioration from heat and pressure, and aggravation from cold, cold bathing, cold winds, cold weather, lack of clothing. Pains all over, but more likely pain located in one part.



:rubric=Mind

The mental symptoms have not been brought out to any extent. It has been used clinically when diarrheas have ceased suddenly and brain troubles have come on. Congestion of the brain, but this is clinical.



:rubric=Head

Neuralgia and rheumatic headaches ameliorated by heat. Excruciating pains. Violent attacks of headache ameliorated by hard pressure, heat and in the dark.

I have seen this mitigation of the symptoms in chronic congestive headache, when the face was red and there was throbbing, almost like 2[R[Bell] .]; those headaches give way to 2[R[Mag-p].,] when there is relief by heat and pressure. He wants the head. bandaged with a tight-fitting cloth, a warm room, and he is aggravated by cold.



:rubric=Eyes

Spasms and jerking about the eyes, or prolonged tonic spasms producing a strabismus. Violent supra and infra-orbital pains with amelioration from heat and pressure. It has cured more face-aches than other pains.

Face Neuralgia of the face, worse on the right side, and ameliorated by heat and pressure, and aggravated by cold. Tic douloureux. Chronic jerkings of the face. It favors rheumatic and, gouty subjects who suffer with neuralgia. It is a wonderful remedy for spasmodic hiccoughing. I have sometimes given 2[R[Mag-p].] for hiccoughing when I could not get any other symptoms to describe on.



:rubric=Stomach and abdomen

Pain at the pit of the stomach. Spasms of the stomach with clean tongue. Colic ameliorated by doubling up, like 2[R[Coloc].,] and ameliorated by heat.

The colic is not so markedly relieved by heat in 2[R[Coloc].,] but is relieved by pressure. Distension of the abdomen and flatulence, with much pain. 2[Radiating pains in the abdomen.] Compelled to walk and groan from pain. Meteorism. It is said to cure cows of this condition. 2[R[Colch]] willI[] cure cows when they are distended with gas after being turned into clover patches.

Cutting, darting pains in hemorrhoids. If well proved we would probably have many liver symptoms, because both 2[Magnes.] and 2[R[Phos].] have liver symptoms.

Violent pains in acute rheumatism, ameliorated by heat. Neuralgic pains in the limbs. Rest relieves many complaints, and the least motion brings them on. Pains changing place.






