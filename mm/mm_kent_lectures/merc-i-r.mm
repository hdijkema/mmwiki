Again, if in a 2[R[Merc].] patient with diphtheria, tonsillitis, etc., the inflammation and pain begin on the 2[left] side, and incline to remain there or spread to the right, the 2[bin-iodide] is indicated.

These two iodides have more rapid and greater induration beneath ulcers and chancres than 2[R[Merc].,] and in old syphilitics the iodides are sometimes more useful.






