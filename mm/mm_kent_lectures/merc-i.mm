There are sore throats that especially call for the 2[proto-iodide.]

When in sore throats the inflammation and pain predominantly affect the 2[right] side, and there is a tendency to remain on the right side, or if the 2[R[Merc].] state is present and the sore throat goes from 2[right to I[left]]I[], it is the proto-iodide you want.

The patient that needs this remedy for constitutional troubles will be worse during rest and from a warm room and better in the open air.

This is especially true when the patient needs 2[R[Merc]. prot.] in neuritis of the right arm that comes on in writers.

The arm then is very painful when writing, from passive motion, from rubbing, from pressure, from both beat and cold, but better from walking in the open! air.

Complaints are nearly all worse on the right side of the body.






